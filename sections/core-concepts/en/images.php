<h5>Media Manager</h5>
<p align="justify">Joomla's Media Manager enables you to upload, store and delete images and other media files in a similar way to using an FTP program. If you are more used to using FTP, continue to use that, but you should also understand how the Media Manager works.</p>
<ol class="marked-list">
  <li>NAVIGATE to the <strong>Content </strong>menu and then <strong>Media</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/images-10.jpg" alt=""></p>
</figure>
</ol>
<h5>Navigation and Folder Management</h5>
<p align="justify">To move between folders, you can utilize the left <strong>sidebar folder tree</strong> in much the same way you do on a home computer. You can also click the <strong>icons for the folders</strong> in the center of the page. </p>
<figure class="img-polaroid">
    <p><img src="img/images-11.jpg" alt=""></p>
</figure>
<p align="justify">The icon with the <strong>up arrow</strong> will take you to the folder directly above your current location.</p>
<figure class="img-polaroid">
    <p><img src="img/images-12.jpg" alt=""></p>
</figure>
<p align="justify">The <strong>Create New Folder</strong> button  allows you to create a new
  folder based on your current location.  You can see the folder path just
above the folder icons in the middle of the page. </p>
<figure class="img-polaroid">
    <p><img src="img/images-13.jpg" alt=""></p></figure>
    <p align="justify">Clicking the <strong>checkbox</strong> next to a file and then clicking the <strong>Delete</strong> button will remove those files from the server. This action will work as well with <strong>folders</strong>; however, the folder must be empty before it is deleted. You can remove multiple files or empty folders at once using this method. The <strong>X</strong> in the top right of the file or folder icon can be used to delete one at a time.</p>
<figure class="img-polaroid">
    <p><img src="img/images-14.jpg" alt=""></p></figure>
    <h5>File Uploads</h5>
    <p align="justify">The Media Manager also allows single and multiple file <strong>uploads</strong>. Clicking the green <strong>Upload</strong> button will toggle the <strong>Browse</strong> and <strong>Start Upload </strong>buttons that enable you to navigate your computer files to select single or multiple files for direct upload to your server. As with folder creation, file uploads are dependent upon your current location, so make sure you are in the folder where you want to place your images on upload. Once your files have been selected through the <strong>Browse button</strong>, simply click <strong>Start Upload</strong> to begin the upload process.</p>
