<h3>Images
</h3>
<p align="justify">The Joomla Media Manager is used to add images to <strong>Articles</strong> and <strong>Modules</strong>, and can be accessed in different ways. In this section we will go over how to add images to your content, the importance of resizing them and how to upload them through the <strong>Media Manager</strong>.</p>
<h6>Adding Images to an Article or Module</h6>
<p align="justify">After deciding where to place your image and adjusting it for file size and pixel size, follow these steps to include your image in your content. Make sure you are signed in to Administrator.</p>
<ol class="marked-list">
  <li>NAVIGATE to the <strong>Content </strong>menu and then <strong>Articles</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/images-1.jpg" alt=""></p>
</figure>

<li>FIND the Article previously created by searching its Title in Article Manager.</li>
<figure class="img-polaroid">
    <p><img src="img/images-2.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">CLICK the Article's title to edit it and POSITION your cursor on an empty line, which may require you to enter a new line.</div>
</li>
<figure class="img-polaroid">
    <p><img src="img/images-3.jpg" alt=""></p>
</figure>
 <li>CLICK the<strong> Image</strong> button.</li>
 <figure class="img-polaroid">
    <p><img src="img/images-4.jpg" alt=""></p>
</figure>
<li>CLICK the<strong> Browse</strong> button and navigate to an image on your computer to upload to your website. </li>
 <figure class="img-polaroid">
    <p><img src="img/images-5.jpg" alt=""></p>
</figure>
<li>CLICK the<strong> Start Upload</strong> button once you have selected your image to upload.</li>

<li>CLICK the newly uploaded image in the directory window and notice that its filename is added to the<strong> Image URL </strong>field.</li>
<figure class="img-polaroid">
    <p><img src="img/images-6.jpg" alt=""></p>
</figure>
<li>CLICK the<strong> Insert </strong>button.</li>
<figure class="img-polaroid">
    <p><img src="img/images-7.jpg" alt=""></p>
</figure>
 
 <li>CLICK <strong>Save &amp; Close</strong> after checking your Article's content to make sure the image was placed correctly.</li>
 <li>CHECK the frontend of your website to ensure that your Article is displaying properly.</li>
</ol>
