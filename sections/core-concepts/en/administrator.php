<strong>Help Menu</strong>


<p align="justify">The <strong>Help</strong> menu provides links to various Joomla help sites, such as the Official Joomla Support Forum, the Joomla Extensions Directory and others.</p><figure class="img-polaroid">
<p><img src="img/administrator-11.jpg" alt=""></p> </figure>

<strong>Current User Menu</strong>
<p align="justify">In the top right corner of the menu bar is an avatar icon that provides links to manage aspects of the currently logged in user. You can edit your own account from here as well as log out of the system.</p><figure class="img-polaroid">
