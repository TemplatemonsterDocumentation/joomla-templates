<h5>Review Messages</h5>
<p align="justify">When you first access your Control Panel in Joomla, you may see a notification that you have messages to review, and possibly a message that an update is available. In order to dismiss the message notification, you need to access and hide those messages.</p>
<ol class="marked-list">
<li>CLICK <strong>Review Messages</strong> button.</li>
<li>CLICK <strong>Hide Message</strong> button once you have reviewed each message.</li>
</ol>
