<h3>Templates</h3>
<p align="justify">One great aspect of Joomla is that there is a wide range of Templates available, and if you are a designer or developer, you can create your own. It's also very possible to find someone to create one for you since Joomla uses widely accepted technologies in its construction.</p>
<h5>The Template Manager</h5>

<p align="justify">Joomla <strong>Templates</strong> are technically a type of Extension, and they are managed in the<strong> Template Manager</strong> in Administrator underneath the <strong>Extensions Menu</strong>.</p>
<p align="justify">You can manage a specific Template by clicking on its name in the list. You can also set the <strong>Default </strong>template, or the Template that Joomla will use for displaying your site, by clicking the star icon in the <strong>Default </strong>column. The current <strong>Default Template</strong> is shown with a gold star.</p>
