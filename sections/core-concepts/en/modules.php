<h3>Modules</h3>

<h6>Understanding Modules</h6>
<p align="justify">As you've learned in previous sections, website content can be broken up into two parts: primary content and secondary content. You learned how to create primary content in the <strong>Articles</strong> section and how to link to that content in the <strong>Menus</strong> one. Other content on the page, such as navigation menus, newsletter signup forms or footer text is secondary content. In Joomla, this secondary content is normally controlled through <strong>Modules</strong>. Modules are one of the five types of Joomla <strong>Extensions</strong>. The other types are more fully explained in next sections.</p>
<h6>Creating a New Module</h6>

<p align="justify">Using  the simple instructions, you should
  be able to create almost any module type available to you. Here is a recap of the general
steps to creating a new Module:</p>
<ol class="marked-list">
<li>NAVIGATE to the <strong>Extensions </strong>menu and then <strong>Modules</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/modules-1.jpg" alt=""></p>
</figure>

<li>CLICK <strong>New</strong> button.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-2.jpg" alt=""></p>
</figure>
<li>CHOOSE the <strong>Module type</strong> from the list box.</li>

<figure class="img-polaroid">
    <p><img src="img/modules-7.jpg" alt=""></p>
</figure>
 <li>ENTER a<strong> Title</strong> for the new Module.</li>
 <figure class="img-polaroid">
    <p><img src="img/modules-3.jpg" alt=""></p>
</figure>
<li>SELECT a <strong>Template Position </strong>to display the new Module.</li>
 <figure class="img-polaroid">
    <p><img src="img/modules-4.jpg" alt=""></p>
</figure>
 <li>ENTER any other applicable <strong>settings</strong> or <strong>content</strong> for the Module.</li>

 <li>SAVE the Module and CHECK the frontend by refreshing your browser.</li>
</ol>
<h6>Assigning Modules to Menu Items </h6>
<p align="justify">Modules can be set to display on all pages throughout the site, or just pages that you specify. First, as always, sign in to the Administrator.</p>
<ol class="marked-list">
<li>NAVIGATE to the <strong>Extensions </strong>menu and then <strong>Modules</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/modules-1.jpg" alt=""></p>
</figure>

<li>CLICK the <strong>Title</strong> of the Module you wish to work with.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-8.jpg" alt=""></p>
</figure>
<li>CLICK the<strong> Menu Assignment </strong>tab.</li>
 <li>SELECT <strong>Only on the pages selected</strong> from the Module Assignment dropdown.</li>
 <figure class="img-polaroid">
    <p><img src="img/modules-9.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">CLICK the <strong>None link</strong> next to<strong> Select</strong> at the top of the <strong>Menu Selection </strong>screen in order to clear the pages that will be displayed. This will cause the Module to not be displayed on any pages, but it will also make selecting the page or pages where you want to display the Module easier to manage.</div>
</li>
 <figure class="img-polaroid">
    <p><img src="img/modules-10.jpg" alt=""></p>
</figure>
<li>CLICK the <strong>plus</strong> sign next to <strong>Main Menu</strong> in order to view the options under that Menu.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-11.jpg" alt=""></p>
</figure>
<li>SELECT the <strong>Menu Item</strong> &quot;pages&quot; that you would like your Module to display on.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-12.jpg" alt=""></p>
</figure><li>CLICK the <strong>plus</strong> sign next to <strong>Main Menu</strong> in order to view the options under that Menu.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-11.jpg" alt=""></p>
</figure>
 
 <li>CLICK <strong>Save &amp; Close</strong> and CHECK the frontend to ensure that your Module is displaying properly.</li>
</ol>
<h5>Module Order</h5>
<p align="justify">The last issue that we will consider in this section is the <strong>ordering</strong> of Modules on the page. We can change the order that Modules display by setting the<strong> Ordering </strong>column in the Module Manager. We will go through the required steps to change the ordering. As always, begin by signing in to Administrator.</p>
<ol class="marked-list">
  <li>NAVIGATE to the <strong>Extensions </strong>menu and then <strong>Modules</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/modules-1.jpg" alt=""></p>
</figure>

<li>FILTER the Modules by <strong>Position</strong> with the<strong> Select Position</strong> dropdown.We're working with the <strong>mainbottom-2</strong>.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-13.jpg" alt=""></p>
</figure>
<li>GRAB the <strong>ordering icon</strong> to the left of the Module that you would like to reorder and drag it to the new location in the list.</li>
<figure class="img-polaroid">
    <p><img src="img/modules-14.jpg" alt=""></p>
</figure>
 <li>CHECK the frontend of your site to ensure that the Module has been reordered.</li>


</ol>