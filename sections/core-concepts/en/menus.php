<h3>Menus</h3>
<p align="justify">The displaying of content in Joomla is a bit different than traditional web design. In a Joomla website, <strong>Menus</strong> are used to create pages and specify how the content appears on the page.</p>

<h6>Adding a Menu Item to an Existing Menu</h6>
<p align="justify">We will add a <strong>Menu Item</strong> to the <strong>Main Menu</strong> on the  Jumerix site. The new link will be displayed on the main menu. </p>
