<h6>Search and Filter</h6>

<p align="justify">You can control how many Articles are shown in the list by selecting a number from the <strong>Display dropdown</strong> in the top right of the page as shown. By default, 20 Articles are shown.</p>
<figure class="img-polaroid">
           <p><img src="img/articles-8.jpg" alt=""></p>
</figure>
<div align="justify">As your site grows and you have more Articles to manage, you will need to
  utilize some of the other filtering features provided by Joomla. At the top of the
  Article Manager page is a <strong>Search box</strong>. You can search through all of the Articles by
  entering Titles or even parts of Titles in the box. Clear the box by clicking the Clear
  button when you are done filtering.</div>
<figure class="img-polaroid">
  <p><img src="img/articles-9.jpg" alt=""></p>
</figure>
  <div align="justify">There are also various other filtering options, and these can be shown or hidden
    by clicking the<strong> Search Tools</strong> button as shown below. Once they are visible, you can
    select several options such as the <strong>Status</strong> of an Article, the <strong>Author</strong> of an Article or the
    <strong>Category</strong> that the Article is stored in etc. </div>
