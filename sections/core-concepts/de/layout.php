<h3>Layout</h3>

<h6>Mit den Layout Optionen können Sie die Haupteinstellungen vom Thema-Layout verändern:</h6>

<p><b>Theme layout</b> - wählen Sie, ob das Thema fixiert (Breite in Pixeln) oder angepasst (Breite in Prozent) sein sollte;</p>

<p><b>Logo</b> - wählen Sie hier das Bild des Logos aus;</p>

<p><b>Logo Width (Logo Breite)</b> - wählen Sie hier die Breite vom Logo- Container aus (basierend auf dem Bootstrap- Raster);</p>

<p><b>Sidebar Width (Sidebar Breite)</b> -  legen Sie die Breite der Sidebars fest.</p>

