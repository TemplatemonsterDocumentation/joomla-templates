<h3>Шаблоны
</h3>
<p align="justify">Еще один большой плюс Joomla состоит в том, что для нее существует   огромное количество шаблонов, а если же вы дизайнер или разработчик, вы можете создать свой собственный шаблон самостоятельно. </p>
<h5>Менеджер шаблонов</h5>
<p align="justify"><strong>Шаблоны</strong> Joomla,  технически, являются еще одним типом дополнений,  управление ими происходит через <strong>Template Manager</strong>, который находится в меню в <strong>Extension</strong>.</p>
<p align="justify">Вы можете перейти к настройке конкретного шаблона, щелкнув по его имени в списке всех шаблонов в <strong>Template Manager</strong>. Также можно  задать шаблон <strong>по умолчанию</strong>, или шаблон, который будет использовать Joomla для  вашего сайта, нажав на значок звездочки в столбце <strong>Default Template</strong>. <strong>Текущий шаблон</strong> по умолчанию  помеченный золотой звездой.</p>
