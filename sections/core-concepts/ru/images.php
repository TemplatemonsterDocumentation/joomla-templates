    <p><img src="img/images-2.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">ПЕРЕЙДИТЕ по ссылке заголовка статьи,  что бы продолжить работу с ней. СОЗДАЙТЕ новую строку в тексте статьи, это и будет место, где будет отображаться изображение.</div>
</li>
