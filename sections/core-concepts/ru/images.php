<h5>Media Manager</h5>
<p align="justify"><strong>Media Manager</strong> в Joomla позволяет загружать, сохранять и удалять изображения и другие мультимедийные файлы аналогично тому, как это происходить с использованием FTP-программ. Если же вы больше привыкли к работе с  FTP, вы можете продолжать использовать этот метод, но, все же, не помешает  понимание принципа работы <strong>Media Manager</strong>.</p>
<ol class="marked-list">
  <li>ПЕРЕЙДИТЕ к меню <strong>Content</strong>, а затем к <strong>Media</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/images-10.jpg" alt=""></p>
</figure>
</ol>
<h5>Навигация и управление</h5>
<p align="justify">Для перемещения между папками, вы можете использовать <strong>левую боковую панель дерева каталогов</strong>, так же как вы делаете это на домашнем компьютере. Вы также можете кликать  на<strong> иконки  папок</strong> в основном окне менеджера.</p>
<figure class="img-polaroid">
  <p><img src="img/images-11.jpg" alt=""></p>
</figure>
<p align="justify"> Значок со <strong>стрелкой вверх</strong> приведет вас к родительскому каталогу  данной папки, в соответствии с текущим положением.</p>
<figure class="img-polaroid">
  <p><img src="img/images-12.jpg" alt=""></p>
</figure>
<p align="justify">Кнопка <strong>Create New Folder</strong> позволяет создать новую папку. Путь к папке можно увидеть в поле чуть выше иконки самой папки.</p>
<figure class="img-polaroid">
<p><img src="img/images-13.jpg" alt=""></p></figure>
<p align="justify"><strong>Отмечая флажком</strong> файл в поле рядом с иконкой файла и нажимая кнопку<strong> Delete</strong>, вы удаляете данный файл с сервера. Это действие будет работать аналогичным образом с <strong>папками</strong>. Тем не менее перед  удалением, папка должна быть пустой.   С помощью этого метода, вы можете удалять несколько файлов или пустых папки сразу. Значок &quot;X&quot; в правом верхнем углу иконки файла или папки используется для единичного удаления.</p>
<figure class="img-polaroid">
<p><img src="img/images-14.jpg" alt=""></p></figure>
    <h5>Загрузка файлов    </h5>
    <p align="justify"> Media Manager также позволяет <strong>загрузку</strong> как одного файла, так и нескольких файлов сразу. При нажатии зеленой кнопки <strong>Upload</strong>, автоматически появятся кнопки <strong>Browse </strong>и <strong>Start Upload</strong>, которые позволят вам выбрать один или несколько файлов для прямой загрузки с вашего компьютера. Как и в случае создания папки, загрузка файлов происходит с учетом вашего текущего положения, поэтому убедитесь в том, что вы находитесь в той папке, в которую вы хотите поместить загружаемые изображения. После того, как файлы для загрузки были выбраны,  нажмите кнопку <strong>Start Upload</strong>,  начав процесс загрузки.</p>
