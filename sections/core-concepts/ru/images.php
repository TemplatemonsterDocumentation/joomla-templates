<h3>Изображения

</h3>
<p align="justify"> Joomla Media Manager используется для добавления изображений к <strong>Articles</strong> и<strong> Modules</strong>, и это может быть реализовано различными способами. В этом разделе мы рассмотрим, как прикреплять изображения к  контенту вашего сайта и, о том как загружать изображения через <strong>Media Manager</strong>.</p>
<h6>Прикрекление изображений к  Article [статье] или Module [модулю]</h6>
<p align="justify">После того, как вы определились с тем, к какому модулю или статье вы ходите прикрепить изображение и убедились в размере изображения, следуйте следующей инструкции для реализации задуманного действия. </p>
<ol class="marked-list">
  <li>ПЕРЕЙДИТЕ к меню <strong>Content</strong>, а затем к<strong> Articles</strong>.</li>

