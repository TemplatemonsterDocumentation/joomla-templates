<h3>Меню
</h3>
<p align="justify"> Способ отображения контента в Joomla немного отличается от традиционного способа, используемого в сфере веб-дизайна. На веб-сайте Joomla, меню используется для создания страниц и указания правил отображения контента.</p>

<h6>Добавление нового пункта меню к существующему</h6>
<p align="justify">Выполнив, указанные ниже инструкции, вы добавите <strong>новый пункт меню</strong> <strong>Menu Item</strong> к существующему<strong> главному меню</strong> сайта. </p>
<ol class="marked-list">
<li>ПЕРЕЙДИТЕ к меню  <strong>Menus</strong>, а затем к <strong>Main Menu</strong>.</li>

<figure class="img-polaroid">
    <p><img src="img/menu-1.jpg" alt=""></p>
</figure>

<li>КЛИКНИКЕ кнопку  <strong>New</strong>.</li>
<figure class="img-polaroid">
    <p align="justify"><img src="img/menu-2.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">ВВЕДИТЕ <strong>заголовок </strong>[Menu Title] для нового пункта меню. Этот заголовок и будет  наименованием нового пункта меню на вашем сайте.</div>
</li>

 <li>НАЖМИТЕ кнопку  <strong>Select button</strong> возле поля опции Menu Item Type.</li>
 <figure class="img-polaroid">
    <p align="justify"><img src="img/modules-3.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">ВЫБЕРИТЕ <strong>Articles</strong>, затем, в появившемся окне, выберите <strong>Single Article</strong>.</div>
</li>
 <figure class="img-polaroid">
    <p align="justify"><img src="img/menu-3.jpg" alt=""></p>
</figure>
 <li>НАЖМИТЕ кнопку <strong>Select button</strong> возле поля опции Select Article.</li>
  <figure class="img-polaroid">
    <p><img src="img/menu-4.jpg" alt=""></p>
</figure>
<li>
  <div align="justify">КЛИКНИТЕ по ссылке заголовка статьи,  которую вы хотели бы прикрепить к этому пункту меню. Обратите внимание,  вы можете использовать фильтры поиска.</div>
</li>
<figure class="img-polaroid">
    <p align="justify"><img src="img/menu-5.jpg" alt=""></p>
</figure>

 <li>НАЖМИТЕ на кнопку <strong>Save &amp; Close</strong>.</li>
 <li>ОБНОВИТЕ страницу вашего сайта и убедитесь в корректности отображения ссылки меню.</li>
 <figure class="img-polaroid">
    <p align="justify"><img src="img/menu-6.jpg" alt=""></p>
</figure>
</ol>
