<h6>Привязка модуля к  элементам меню сайта</h6>
<p align="justify">Модули могут быть настроены различным образом, они могут  отображения на всех страницах  сайта, или только на тек страницах, которые вы укажете. Сначала, как всегда, войдите в систему администратора.</p>
<ol class="marked-list">
  <li>ПЕРЕЙДИТЕ к меню <strong>Extensions</strong>,  а затем к <strong>Modules</strong>.</li>

