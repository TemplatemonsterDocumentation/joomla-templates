<h3>Модули</h3>

<h6>Базовое понимание модулей</h6>
<p align="justify">Как вам стало известно из предыдущих разделов, содержание веб-сайта можно разбить на две части: главный (первичный) и вспомогательный (вторичный)  контент. Вы научились создавать главный (первичный) контент в разделе <strong>Статьи</strong>, а так же тому, как создавать ссылку на этот контент в разделе <strong>Меню</strong>. Другой  контент страницы, такой как меню навигации, подписка на рассылку или текст колонтитула является вспомогательным (вторичным) контентом. В Joomla, этот вспомогательный (вторичный) контент, как правило, управляется посредством модулей. Модули являются одним из пяти типов расширений (дополнений) Joomla. Другие типы расширений (дополнений) более подробно описаны в последующих разделах.</p>
