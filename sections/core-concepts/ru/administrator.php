</figure>
<h5>Сообщение об уведомлении</h5>
<p align="justify">Вы можете увидеть сообщение, уведомляющее о необходимости загрузки обновлений. Для того, чтобы отклонить уведомление, вам необходимо кликнуть на область сообщения, а затем скрыть его.</p>
<ol class="marked-list">
<li>КЛИКНИТЕ кнопку <strong>Review Messages</strong>.</li>
<li>НАЖМИТЕ кнопку <strong>Hide Message</strong>  как только вы ознакомитесь с содержанием сообщения.</li>
</ol>
