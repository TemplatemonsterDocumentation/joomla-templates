<h3>Панель Администрирования</h3>
<h5>Доступ к панели  Администрирования сайтом</h5>
<p align="justify">Теперь, когда разница между  frontend и backend уже очевидна, рассмотрим способы доступа к панели  Администрирования Joomla.</p>
<ol class="marked-list">
<li>В адресной строке браузера, после фактического адреса вашего сайта, добавьте <strong>/administrator</strong>.</li>
<li>
  <div align="justify">Произведите вход в панель  Администрирования сайтом, используя корректные параметры входа, указанные вами при установке.</div>
</li>
</ol>

