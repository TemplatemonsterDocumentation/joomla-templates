<h5> Разделы главного Меню панели  Администрирования</h5>
<strong>System Menu</strong>
<p align="justify">Наиболее важными ссылками в  <strong>System menu</strong> являются  <strong>Control Panel, </strong>возвращающая вас назад к начальной странице, на которую вы попадаете при  входе в админ-панель, и <strong>Global Configuration</strong>, перейдя по которой вы получите доступ к основным настройкам вашего сайта.</p>
<figure class="img-polaroid">
<p><img src="img/administrator-5.jpg" alt=""></p> </figure>
<strong>Users Menu</strong>
<p align="justify">Меню  <strong>User</strong>. Здесь вы сможете создавать учетные записи пользователей  вашего сайта и управлять списками контроля доступа. Правила доступа можно устанавливать как для  просмотра, так и для администрирования.</p>
<figure class="img-polaroid">
<p><img src="img/administrator-6.jpg" alt=""></p> </figure>
<strong>
Menus Menu
</strong>

<p align="justify">Меню <strong>Menus</strong> позволяет создавать страницы для вашего сайта на основе контента, который вы создаете посредством  Article Manager и других дополнений.</p><figure class="img-polaroid">
<p><img src="img/administrator-7.jpg" alt=""></p> </figure>
<strong>
Content Menu

</strong>

<p align="justify"><strong>Content</strong> включает в себя меню которые позволяют управлять контентом  вашего сайта. Управление Article Manager и Category Manager осуществляется также с помощью этого меню. С него же, вы получаете доступ к Media Manager, через который происходит управление изображениями и видео сайта.</p>
<figure class="img-polaroid">
<p><img src="img/administrator-8.jpg" alt=""></p> </figure>
<strong> Меню Components и Extensions</strong>
<p align="justify">Меню <strong>Components</strong> содержит в себе ссылки на установленные сегменты программного обеспечения, которые обеспечивают функциональные возможности   сайта. Здесь вы также найдете  перечень базовых компонентов Joomla, таких, как Banners and Joomla Update, a также сторонних компонентов [дополнительных], таких, как  SLogin и JoomGallery. </p>
<figure class="img-polaroid">
<p><img src="img/administrator-9.jpg" alt=""></p> </figure>
<p align="justify">Меню <strong>Extensions</strong> содержит ссылки на все другие типы  дополнений Joomla, таких, как модули, плагины, шаблоны и языки.</p>
<figure class="img-polaroid">
<p><img src="img/administrator-10.jpg" alt=""></p> </figure>
<strong>Help Menu</strong>


<p align="justify">Меню <strong>Help </strong>содержит ссылки на различные сайты по предоставлению  технической помощи Joomla, таких, как Official Joomla Support Forum,  Joomla Extensions Directory и другие.</p>
<figure class="img-polaroid">
<p><img src="img/administrator-11.jpg" alt=""></p> </figure>

<strong>Current User Menu</strong>
<p align="justify">В правом верхнем углу панели главного меню панели находится значок, после нажатия на который, появится список, содержащий ссылки необходимые для управления и настройки  профиля текущего авторизованного пользователя.  Здесь, вы можете редактировать свой собственный профиль, а также осуществлять выход из системы админ-панели.</p>
