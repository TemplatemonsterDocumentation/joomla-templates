<h3>Articles [Статьи]
</h3>
<p align="justify">В этом разделе будет описан, второй шаг  создания основного контента сайта — создание Joomla статей [<strong>Articles</strong>]. Вы также научитесь быстро находить нужную статью  и применять фильтры для поиска статей.</p>

