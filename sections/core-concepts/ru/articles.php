<h6>Поиск и фильтры</h6>

<p align="justify">Вы можете контролировать количество отображаемых в менеджере статей на странице. Для этого выберите число отображаемых статей в списке <strong>Display dropdown</strong> в правом верхнем углу, как показано на изображении. По умолчанию отображается  20 статей.</p>
<figure class="img-polaroid">
           <p><img src="img/articles-8.jpg" alt=""></p>
</figure>
<div align="justify">
  <p>По мере роста количества статей на вашем сайте, появится острая необходимость эффективного их организации и управления, необходимым будет использование функции фильтров, предоставленные в Joomla опцией <strong>Search box</strong>.  Вы можете осуществлять поиск  путем ввода названия или даже части названия в поле поиска. Очистите поле, нажав кнопку Очистить.</p>
</div>
<figure class="img-polaroid">
  <p><img src="img/articles-9.jpg" alt=""></p>
</figure>
  <div align="justify">
    <p>Существуют также другие параметры  поиска,  они могут быть показаны или скрыты, нажимая  кнопку Инструменты поиска, как показано ниже. После того, как вы нажали кнопку, вы можете воспользоваться несколькими вариантами фильтров, такими как  <strong>Status</strong> статьи, <strong>Author</strong> статьи или <strong>Category</strong>, в которой  статья хранится и т.д.</p>
</div>
