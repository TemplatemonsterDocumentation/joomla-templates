<h3>Categories [Категории]
</h3>
<p align="justify">Это раздел, где вы начинаете создавать  фактическое содержание веб-сайта. Существует два шага на пути к созданию  нового контента в Joomla. Сначала нужно создать одну или несколько категорий [<strong>Categories</strong>], что подробно описано в данном разделе, а затем вам нужно будет создать статьи [<strong>Articles</strong>], создание которых мы обсудим в следующем разделе.</p>
<p align="justify">Joomla обеспечивает очень гибкий способ категоризации и хранения контента. Система хранения данных можно сравнить, в некоторой степени, со способом хранения файлов в папках. С Joomla вы создаете категории, которые действуют  по принципу папок для хранения статей, которые, в свою очередь, являются файлами в этой аналогии. Вы можете создавать подкатегории в Joomla  так же, как вы можете создавать вложенные папки на вашем компьютере. Будьте осторожны, не усложните  структуру категории, ведь система хранения призвана упростить процесс, а не наоборот.</p>

