<h3>Categories</h3>

<p align="justify">This is the section  where you start to create your actual website content. There are two steps to creating most new content in Joomla. First you create one or more <strong>Categories</strong>, which is described in this section, and then you create<strong> Articles</strong> which we will discuss in the next section.</p>
<p align="justify">Joomla provides a very flexible method of categorizing and storing content. The storage system can be compared somewhat to folders and the files that go inside. With Joomla you are creating Categories that act like folders to store the Articles that you create, which are like the files in this analogy. You can also nest Categories in Joomla just like you can nest folders on your computer. Be careful not to create an overly complicated Category structure however. Keep things simple where you can.</p>

