<h3>Disposizione</h3>

<h6>Opzioni di layout consentono di modificare le impostazioni di base del layout del tema:</h6>

<p><b>Theme layout (layout del tema)</b> - esso consente di effettuare le impostazioni di larghezza fissa per il tema (larghezza
    in pixel) o relativa (percentuale di larghezza);</p>

<p><b>Logo (Logo)</b> - esso consente di selezionare l'immagine del logo;</p>

<p><b>Logo Width (la larghezza del logo)</b> - larghezza Logo del contenitore
    (basato sulla griglia Bootstrap);</p>

<p><b>Sidebar Width (La larghezza della barra laterale)</b> - imposta la larghezza dei pannelli laterali.</p>

