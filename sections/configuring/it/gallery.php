    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Aggiunta di immagini</h4>

<p>Per aggiungere altre immagini alla galleria, è sufficiente aggiungere un nuovo articolo nella sottocategoria 'Gallery'. In ogni
   articolo ci deve essere l'immagine principale e introduttiva. Entrambe le immagini sono di solito collocati in una cartella 'images/gallery'.</p>

<p>L'immagine di apertura viene visualizzata sulle pagine della galleria. L'immagine principale viene visualizzata dopo aver fatto clic sull'immagine introduttiva:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Personalizzazione dell'aspetto</h4>

<p>La pagina galleria può essere personalizzata con un design diverso, così come qualsiasi altra pagina del display Joomla categorie. In Joomla sezione 
   pannello di amministrazione vai nella sezione <b>Menus (Menu) > Main Menu (Menu principale)</b> e apri la voce di menu "Galleria". Fai clic sulla scheda
   avanzata bella sezione <b>Advanced Options (Impostazioni Avanzate)</b>, per entrare nella pagina delle impostazioni della galleria.</p>

<p>Per esempio, se è necessario modificare il numero di colonne sulla pagina della galleria, utilizza l'impostazione <b>Columns</b> (Colonne)
   nella sezione <b>Blog Layout Options</b> (Impostazioni Layout Blog):</p>

