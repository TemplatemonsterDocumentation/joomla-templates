<h3>Galleria</h3>

<p>La galleria mostra i post dalla categoria 'Galleria' e sottocategorie. La Galleria ha
    un filtronell’angolo in alto a destra della paginache consente di filtrare le
    immagini per categorie.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Aggiungere Immagini</h4><p>Per aggiungere ulteriori immagini alla galleria
    dovrete semplicemente aggiungere dei nuovi articoli allesottocategorie della
    'Galleria'. Ogni articolo dovrà avere caricato un’immagine complete e una intro.
    Entrambe le immagini si trovano di solito nella directory 'images/gallery'.</p>


<p>L’anteprima immagine è visibile all’interno della galleria ee nelle vetrine delle
    immagini. L’immagine completaverrà visualizzata dopo aver cliccato sull’anteprima
    immagine.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Organizza layout</h4><p>Il layout della galleriapuò essere modificato come in
    qualsiasi altra categoria Joomla. Aprite la sezione '<b>'Menù > Menù Principale'
        (Menus > Main Menu)</b>' dalpannello di amministrazione Joomla e modificate gli
    elementi del menù Galleria. Passate alla tab <b>Opzioni Avanzate (Advanced
        Options)</b> per avere accesso alle impostazioni della Galleria. </p>

<p>Se per esempio si vorrà variare il numero delle colonne della Galleria, basterà
    modificare le impostazioni dell’opzione <b>Colonne (Columns)</b> dall’area <b>Opzioni
        Layout del Blog (Blog Layout Options)</b>.</p>

