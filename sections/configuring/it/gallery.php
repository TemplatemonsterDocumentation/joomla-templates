<h3>Galleria</h3>

<p>La pagina della galleria mostra gli articoli della categoria "Gallery" (Galleria) e le sue sottocategorie. Nella galleria sulla pagina superiore
   a destra è presente un filtro per filtrare le immagini per la categoria.</p>

