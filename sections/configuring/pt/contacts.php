<h3>Página de Contatos</h3>

<p>A página de contatos exibe o formulário de contato, endereço e o Google Mapas. Para
    alterar os detalhes do contato (
    <i>e-mail de contato, endereço, etc</i>
    ) vá para
    Componentes &gt; Contatos &gt; Contatos &gt; Dono do Site (em Inglês, &quot;<b>Components
        > Contacts > Contacts > Website Owner</b>&quot;.) Usando as abas no topo você
    pode obter acesso a várias opções de contato.
</p>

<p>Para configurar o que deve ser prestado na página de contatos, vá para a seção
    Sistema &gt; Configuração Global &gt; Contatos (em Inglês, &quot;<b>System > Global
        Configuration > Contacts&quot;</b>.)</p>

<p><b>Mapa do Google</b> na página de contatos que é criada usando o plug-in do Google
    Mapas. Ele pode ser configurado através de Extensões &gt; Gerenciador de Plug-in
    &gt; Google Mapas (em Inglês, &quot;<b>Extensions > Plug-in Manager > Google
        Maps</b>&quot;.)</p>

