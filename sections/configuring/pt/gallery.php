<h3>Página da Galeria</h3>

<p>A página Galeria mostra posts da categoria 'Galeria' e suas sub-categorias. A página
    Galeria tem um filtro no canto superior direito para filtrar imagens por
    categorias.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Adicionando Imagens</h4><p>
    Para adicionar mais imagens para a galeria, você precisa simplesmente adicionar
    novos artigos para as sub-categorias da 'Galeria'. Cada artigo deve ter imagem cheia
    e imagem de introdução enviada. Ambas as imagens são normalmente colocadas no
    diretório 'images/gallery'.</p>

<p>A imagem de introdução é exibida dentro da galeria e das visualizações do item da
    galeria. A imagem cheia é exibida uma vez que a imagem de introdução for
    clicada.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>


<h4>Layout de Ajuste</h4><p>
    A página da galeria pode ser ajustada como qualquer página da categoria Joomla. Abra
    a seção '<b>Menus > Menu Principal (em Inglês,&quot;Menus > Menu
        Principal&quot;) </b>do painel de administração do Joomla e edite o item do menu
    Galeria. Alterne para a aba Opções Avançadas (em Inglês, &quot;<b>Advanced Options&quot;)</b>
    e você terá acesso as configurações da página Galeria. </p>

<p>Por exemplo, se você quiser alterar o número de colunas para a página Galeria, altere
    a opção Colunas (em Inglês, &quot;<b>Columns&quot;</b>) da área de configurações
    &quot;Opções do Layout do Blog (em Inglês, &quot;<b>Blog Layout Option&quot;</b>).
</p>


