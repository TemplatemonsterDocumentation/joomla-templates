<h3>Strona Galerii</h3>

<p>Strona Galerii pokazuje informacje z kategorii 'Galeria' i jej podkategorii. Strona
    Galerii ma wbudowany filtr w prawym górnym rogu który pozwala filtrować grafiki
    według kategorii.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Dodawanie grafik</h4><p>By dodać więcej grafik do galerii wystarczy dodać nowe
    hasła do podkategorii 'Galeria'. Każde hasło powinno mieć załadowany pełen obraz i
    obraz wstępny. Oba obrazy są zazwyczaj umieszczone w folderze 'obrazy/galeria'
    (ang.'images/gallery').</p>

<p> Obraz wstępny jest wyświetlany w galerii i wyświetlaniu przedmiotów galerii. Pełen
    obraz jest wyświetlany po kliknięciu na obraz wstępny. </p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>


<h4>Dostosowanie układu</h4><p>Układ strony galerii może być dostosowany jak każda
    strona Joomla. Otwórz sekcję '<b>Menu > Główne Menu</b>' z panelu administracyjnego
    Joomla i edytuj opcję menu Galerii. Przełącz do <b>Zaawansowane Opcje (ang.Advanced
        Options)</b> i uzyskasz dostęp do ustawień strony Galerii. </p>

<p>Na przykład jeżeli chcesz zmienić ilość kolumn dla strony galerii, zmień <b>Kolumny
        (ang. Columns)</b> z <b>Opcje Układu Bloga (ang. Blog Layout Options)</b> w obszarze
    ustawień. </p>

