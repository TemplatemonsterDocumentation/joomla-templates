<h3>Galeria</h3>

<p>Strona Galeria wyświetla wpisy z kategorii "Galeria" oraz jej podkategorii. Na stronie Galeria w prawym górnym rogu znajduje się filtr, który umożliwia filtrowanie obrazów według kategorii:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Dodawanie obrazów</h4>

<p>Aby dodać więcej zdjęć do galerii, po prostu trzeba dodać nowe artykuły do podkategorii "Galeria". Każdy artykuł powinien mieścić przesłane pełny obraz oraz obraz wprowadzający. Oba obrazy są zwykle wprowadzane do katalogu 'images/gallery'.</p>

<p>Wprowadzający obraz jest wyświetlany w  galerii i w poglądach elementów galerii. Pełny obraz jest wyświetlany po kliknięciu na wprowadzającym obrazie:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Regulowanie układu</h4>

<p>Układ strony Galeria można wyregulować jak każdą inną stronę kategorii Joomla. W panelu administracyjnym należy otworzyć kartę <b>Menu > Główne Menu</b>, zatem otworzyć element "Galerii". Należy przejść na kartę  
    <b>Opcje Zaawansowane</b>, aby uzyskać dostęp do ustawień strony Galerii. </p>

<p>Na przykład, jeśli Państwo chcą zmienić liczbę kolumn na stronie Galerii, należy zmienić liczbę <b>Kolumn</b> w sekcji <b>Opcje układu bloga</b>: </p>

