<h3>Strona Kontaktów</h3>

<p> Strona kontaktów wyświetla formę kontaktu, adres i mapę Google. By zmienić treść
    kontaktu (
    <i>email, adres itp.</i>
    ) idź do <b>Komponenty > Kontakty > Właściciel
        Strony (ang.Components > Contacts > Contacts > Website Owner)</b>. Używając
    strzałek na górze uzyskasz dostęp do różnych opcji kontaktu.
</p>

<p>By skonfigurować co powinno się znajdować na stronie kontaktów idź do <b>System >
        Konfiguracja Globalna > Kontakty (ang.System > Global Configuration > Contacts</b>)
</p>

<p><b>Google Map</b> Mapa Google na stronie kontaktów jest tworzona za pomocą pluginu
    Google Maps. Może być konfigurowana poprzez <b>Rozszerzenia > Menadżer Pluginów >
        Mapy Google (ang. Extensions > Plug-in Manager > Google Maps)</b></p>

