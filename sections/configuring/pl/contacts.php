<h3>Kontakty</h3>

<p>Strona Kontakty wyświetla formularz kontaktowy, adres i mapę Google. Aby zmienić dane kontaktowe
 (<i>adres e-mail, adres, itp.</i>), należy przejść na kartę <b>Komponenty > Kontakty > Kontakty > Właściciel strony</b>. Można uzyskać dostęp do różnych ustawień, poprzez korzystanie z różnych zakładek.
</p>

<p>Aby skonfigurować formularz kontaktowy, należy przejść do sekcji <b>System > Ogólna
        Konfiguracja > Kontakty</b>.</p>

<p><b>Mapa Google</b> na stronie "Kontakty" jest wyświetlana za pomocą plugin'u Google Maps. Ten plugin można skonfigurować w sekcji <b>Rozszerzenia > Plugin Manager >
        Google Maps</b>.</p>

