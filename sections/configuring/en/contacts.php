<h3>Contacts</h3>

<p align="justify">Contacts page displays contact form, address and Google map. In order to change contact details<i>, </i>please navigate to <b>Components > Contacts > Contacts > Website Owner</b> tab. Using  the top tabs, you can get access to various settings.
</p>
