<h3>Contacts Page</h3>

<p>Contacts page displays the contact form, address and Google Map. To change the
    contact details (
    <i>contact email, address, etc</i>
    ) go to <b>Components > Contacts
        > Contacts > Website Owner</b>. Using the tabs at the top you can get access to
    various contact options.
</p>

<p>To configure what should be rendered at the contacts page go to the <b>System >
        Global Configuration > Contacts</b> section.</p>

<p><b>Google Map</b> at the contacts page is created using the Google Maps plugin. It
    can be configured through <b>Extensions > Plug-in Manager > Google Maps.</b></p>

<h6><strong>Tip:</strong>
    You can use the filter tool above the
    plugins list to narrow your search.
</h6>

