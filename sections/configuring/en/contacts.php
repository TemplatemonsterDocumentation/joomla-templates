<h3>Contacts</h3>

<p>Contacts page displays contact form, address and Google map. In order to change contact details
 (<i>e-mail address, address, etc.</i>), please navigate to <b>Components > Contacts > Contacts > Website Owner</b> tab. Using  the top tabs, you can get access to various settings.
</p>

<p>To configure the contact form, please navigate to <b>System > Global
        Configuration > Contacts</b> section.</p>

<p><b>Google Map</b> on the “Contacts” page is displayed with the help of Google Maps plugin. This plugin can be configured in <b>Extensions > Plugin Manager >
        Google Maps</b> section.</p>

