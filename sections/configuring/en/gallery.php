<h3>Gallery</h3>

<p>Gallery page displays posts from the "Gallery" category and its subcategories. The Gallery page has a filter at the top right, which allows to filter images by categories:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Adding images</h4>

<p>In order to add more images to the gallery, you just need to add new articles to 'Gallery' subcategories. Each article should have a full image and an intro image uploaded. Both images are usually placed into the 'images/gallery' directory.</p>

<p>An intro image is displayed within the gallery and gallery item views. A full image is displayed once the intro image is clicked on:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Adjusting layout</h4>

<p>The Gallery page layout can be adjusted as any Joomla category page. Open <b>Menus > Main Menu</b> tab in the admin panel and open the "Gallery" item. Navigate to 
    <b>Advanced Options</b> tab to access Gallery page settings. </p>

<p>For example, if you want to change the number of columns on the Gallery page, change the number of <b>Columns</b> in <b>Blog Layout Options</b> section: </p>

