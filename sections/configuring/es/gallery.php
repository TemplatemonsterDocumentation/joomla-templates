<h3>Galería</h3>

<p>Página de la galería muestra los elementos de la "Galería" (Gallery) y sus subcategorías. La página de la galería tiene un filtro en la parte superior derecha, que puede filtrar las imágenes por categoría:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Adición de Imágenes</h4>

<p>Para añadir imágenes a la galería, sólo tiene que añadir nuevos elementos a las subcategorías de 'Galería' ('Gallery'). Cada artículo debe tener una imagen completa y una imagen de introducción disponible. Las dos imágenes se encuentran en el directorio 'images/gallery'.</p>

<p>La imagen de introducción se muestra en las páginas de galería. La imagen completa se muestra una vez hace clic en la imagen de introducción:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Ajustando el diseño</h4>

<p>El diseño de la página de galería se puede añadir como cualquier página de categorías de Joomla. Abra la pestaña en su panel de Admin <b> Menus (Menus) > Menú principal (Main Menu)</b> y abrir el elemento de menú "Gallery". A continuación, vaya a la pestaña 
    <b>Opciones Avanzadas (Advanced Options)</b> y usted tendrá acceso a los parámetros de la página de galería. </p>

<p>Por ejemplo, si desea cambiar el número de columnas de la página de galería, utilice el <b>Columnas (Columns)</b> en <b>Opciones del Diseño del Blog (Blog Layout Options)</b>: </p>

