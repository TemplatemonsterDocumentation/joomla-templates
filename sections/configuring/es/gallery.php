<h3>Página de Galería </h3>

<p>La página de galería muestra las entradas de la categoría "Galería" y sus
    subcategorías. La página de galería tiene un filtro en la esquina superior a la
    derecha que permite filtrar imágenes por categorías.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Agregar Imágenes</h4>
<p> Para añadir más imágenes a la galería sólo hay que
    añadir nuevos artículos a las subcategorías de "Galería". Cada artículo debe tener
    la imagen completa y la imagen de introducción. Ambas imágenes se encuentran en el
    directorio 'images/gallery'.</p>



<p>La imagen de introducción es mostrada en la galería y vistas de elementos de galería.
    La imagen completa se muestra al hacer clic en la imagen de introducción.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Ajustar diseño </h4>
<p>Se puede ajustar el diseño de la página de galería de la
    misma manera como cualquier otra página de categorías de Joomla. Abra la sección
    'Menus > Main Menu' (Menús > Menú Principal) del panel de administración de Joomla y
    modifique el elemento de Galería del menú. Abra la pestaña de Advanced Options
    (Opciones avanzadas) y obtendrá acceso a la configuración de página de la galería.
</p>

<p>Por ejemplo, si Usted desea cambiar el número de columnas de la página de Galería,
    modifique la opción Columns (Columnas) del área de configuración de Blog Layout
    Options (Opciones de diseño de blog).</p>


