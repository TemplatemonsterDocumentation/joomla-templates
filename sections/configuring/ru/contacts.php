<h3>Контакты</h3>

<p align="justify">Контактная страница отображает контактную форму, адрес и карту Google Map. Чтобы
    изменить контактную информацию [<i>email, адрес и т.п</i>], перейдите в меню <b>Components
        [Компоненты] > Contacts [Контакты] > Contacts [Контакты] > Website Owner [Владелец
        сайта]</b>. Используя вкладки вверху можно открывать различные дополнительные настройки.
</p>
