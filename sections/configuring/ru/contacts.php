<p align="justify">Для настройки страницы контактов перейдите в раздел <b>System [Система] > Global
        Configuration [Глобальные настройки] > Contacts [Контакты]</b>.</p>
        <figure class="img-polaroid">
    <img src="img/plugin-manager.jpg" alt="">
</figure>

<p align="justify"> Карта<b> Google Map</b> на странице контактов задается через плагин Google Maps. Его
    можно настроить в <b>Extensions [Расширения] > Plugins [Менеджер плагинов] >
        Google Maps</b>.</p>

