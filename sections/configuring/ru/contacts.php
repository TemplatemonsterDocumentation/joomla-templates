<h3>Контакты</h3>

<p>Контактная страница отображает контактную форму, адрес и карту Google Map. Чтобы
    изменить контактные детали (
    <i>email, адрес и т.п</i>
    ), перейдите в меню <b>Components
        (Компоненты)> Contacts (Контакты)> Contacts (Контакты)> Website Owner (Владелец
        сайта)</b>. При помощи вкладок вверху можно открывать различные настройки.
</p>

<p>Для настройки страницы контактов перейдите в раздел <b>System (Система) > Global
        Configuration (Глобальные настройки) > Contacts (Контакты)</b></p>

<p> Карта<b> Google Map</b> на странице контактов задается через плагин Google Maps. Его
    можно настроить в <b>Extensions (Расширения) > Plug-in Manager (Менеджер плагинов) >
        Google Maps</b></p>

