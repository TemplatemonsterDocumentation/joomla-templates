<h3>Галерея</h3>

<p>Страница галереи отображает статьи из категории 'Gallery (Галерея)' и ее подкатегорий. На странице галереи в верхнем
    правом углу находится фильтр, позволяющий фильтровать картинки по категориям.</p>

