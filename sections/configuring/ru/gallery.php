    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Добавление изображений</h4>

<p>  Чтобы добавить больше картинок в галерею, нужно просто добавить новые статьи в подкатегорию 'Gallery'. В каждой
    статье должно быть главное и вводное изображение. Обе картинки обычно размещают в папке 'images/gallery'.</p>

<p>Вводное изображение отображается на страницах галереи. Главное изображение появляется после нажатия на вводное.</p>

