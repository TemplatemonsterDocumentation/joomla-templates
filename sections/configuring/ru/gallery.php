    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Настройка оформления</h4>

<p>Оформление страницы галереи можно настраивать, как и любую страницу категорий Joomla. В панели управления Joomla
    перейдите в '<b>Menus (Меню) > Main Menu (Главное меню)</b>' и откройте элемент меню галереи. Перейдите во вкладку
    <b>Advanced Options (Расширенные настройки), </b>чтобы попасть в настройки страницы галереи. </p>

<p>Например, если нужно изменить количество колонок на странице галереи, воспользуйтесь опцией <b>Columns (Колонки)</b>
    в секции <b>Blog Layout Options</b> (настройки оформления блога). </p>

