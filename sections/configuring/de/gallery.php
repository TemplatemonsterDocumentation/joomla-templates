    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Bilder hinzufügen</h4>

<p>Um mehr Bilder zur Galerie anzuhängen, fügen Sie einfach neue Artikel zu Unterkategorien der 'Galerie' (Gallery) hinzu. Jeder Artikel muss ein hochgeladenes Vollbild und Intro Bild haben.  Die beiden Bilder werden normalerweise im Verzeichnis 'images/gallery' gespeichert. </p>

<p>Intro Bild wird innerhalb der Vorschau von Gallerie und vom Galerieelement angezeigt. Das Vollbild wird dargestellt, wenn das Intro Bild angeklickt wird. </p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Layout anpassen</h4>

<p>Das Layout der Galerie-Seite kann man wie jede Joomla Kategorieseite anpassen.  Im Joomla Adminbereich öffnen Sie  <b>Menüs > Hauptmenü  (Menus > Main Menu)</b> und öffnen Sie dort den „Galerie“ Menüpunkt. Gehen Sie zu 
    <b>Erweiterten Optionen (Advanced Options)</b> um die Einstallungen der Galerie-Seite aufzurufen. </p>

<p>Wenn Sie, zum Beispiel, die Anzahl den Spalten für die Galerie-Seite verändern möchten, ändern Sie die Anzahl der <b>Spalten (Columns)</b> in der <b>Blog Layout Options</b> Sektion: </p>

