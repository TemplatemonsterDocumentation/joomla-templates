<h3>Galerie</h3>

<p>Galerie Seite zeigt Artikel aus der Kategorie 'Galerie' (Gallery) und ihre Unterkategorien. In der oberen rechten Ecke hat die Galerie Seite einen Filter, damit man Bilder nach Kategorien filtern kann.</p>

