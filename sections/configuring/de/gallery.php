<h3> Galerie Seite</h3>

<p> Galerie Seite zeigt Artikel aus der Kategorie 'Galerie' (Gallery) und ihre
    Unterkategorien. In der oberen rechten Ecke hat die Galerie Seite einen Filter,
    damit man Bilder nach Kategorien filtern kann. </p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Bilder hinzufügen</h4>
<p>Um mehr Bilder zur Galerie anzuhängen, fügen Sie
    einfach neue Artikel zu Unterkategorien der 'Galerie' (Gallery). Jeder Artikel muss
    ein hochgeladenes Vollbild unf Intro Bild haben. Die beide Bilder werden
    normalerweise im Verzeichnis 'images/gallery' platziert. </p>

<p>Intro Bild wird innerhalb der Vorschau von Gallerie und vom Galerieelement angezeigt.
    Das Vollbild wird dargestellt, wenn das Intro Bild angeklickt wird. </p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Layout anpassen</h4>
<p>Das Layout der Galerie-Seite kann man als eine Joomla
    Kategorieseite anpassen. Im Joomla Adminbereich öffnen Sie '<b>Menüs > Hauptmenü</b>'
    (Menus > Main Menu) und bearbeiten Sie dort das „Galerie“ Menüpunkt. Mit dem Druck
    auf den Tab <b> Erweiterten Optionen </b> (Advanced Options) können Sie die
    Einstallungen der Galerie-Seite anrufen. </p>

<p>Wenn Sie, zum Beispiel, die Anzahl den Spalten für die Galerie-Seite verändern
    möchten, verwandeln Sie die Option <b>Spalten </b> (Columns) vom Einstellungsbereich
    <b>Blog Layout Options</b>. </p>


