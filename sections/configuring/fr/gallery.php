<h3>Galerie</h3>

<p>La page de Galerie affiche les articles dans la catégorie « Galerie » (« Gallery »)
    et ses sous-catégories. La page de Galerie a un filtre en haut à droite qui permet
    de filtrer les images par catégories.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Ajout d'images</h4><p>Pour ajouter des images à la galerie, il vous suffit
    d'ajouter des nouveaux articles aux sous-catégories « Galerie » (« Gallery »).
    Chaque article devrait avoir une image complète et une image d'introduction
    disponibles. Les deux images sont typiquement placées dans le répertoire
    « images/gallery. »</p>

<p>L'image d'introduction est affichée dans les vues de galerie et d'élément de galerie.
    L'image complète est affichée une fois l'image d'introduction cliquée.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Ajuster l'agencement</h4><p>L'agencement de la page de galerie peut être ajusté
    comme n'importe quelle page de catégorie de Joomla. Ouvrez la section « <b>Menus >
        Menu principal</b> » (« <b>Menus > Main Menu</b> ») dans le panneau
    d'administration de Joomla et éditez l'élément de menu. Puis allez dans l'onglet
    « <b>Options avancées</b> » (« <b>Advanced Options</b> ») et vous aurez accès aux
    paramètres de la page de galerie. </p>

<p>Par exemple, si vous voulez changer le nombre de colonnes de la page de galerie,
    changez l'option « <b>Colonnes</b> » (« <b>Columns</b> ») option dans la section de
    réglages « <b>Options d'agencement de blog</b> » (« <b>Blog Layout Options</b> »).
</p>

