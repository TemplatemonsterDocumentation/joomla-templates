<h3>Galerie</h3>

<p>La page de Galerie affiche les articles dans la catégorie "Gallery" (Galerie) et ses sous-catégories. La page de Galerie a un filtre en haut à droite, qui permet de filtrer les images par catégories:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Ajout d'images</h4>

<p>Pour ajouter des images à la galerie, il vous suffit d'ajouter des nouveaux articles aux sous-catégories 'Galerie' ('Gallery').  Chaque article devrait avoir une image complète et une image d'introduction disponibles. Les deux images sont placées dans le répertoire 'images/gallery'.</p>

<p>L'image d'introduction est affichée sur les pages de galerie. L'image complète est affichée une fois l'image d'introduction cliquée:</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Ajuster la mise en page</h4>

<p>L'agencement de la page de galerie peut être ajusté comme n'importe quelle page de catégorie de Joomla. Dans le panneau d'administration de Joomla
    ouvrez l'onglet <b>Menus (Menus) > Main Menu (Menu principal)</b> et ouvrez l'élément de menu "Galerie". Ensuite allez dans l'onglet 
    <b>Advanced Options (Options avancées)</b> et vous aurez accès aux paramètres de la page de galerie. </p>

<p>Par exemple, si vous voulez changer le nombre de colonnes de la page de galerie, utilisez l'option <b>Columns (Colonnes)</b> dans la section <b>Blog Layout Options (Options d'agencement de blog)</b>: </p>

