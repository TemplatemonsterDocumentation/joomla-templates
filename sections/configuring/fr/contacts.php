<h3>Contacts</h3>

<p>La page de contact affiche le formulaire de contact, une adresse et une carte Google
    Maps. Pour changer les renseignements de contact (
    <i>adresse e-mail, adresse
        postale, etc.
    </i>
    ) allez dans « <b>Composants > Contacts > Contacts >
        Propriétaire du site web</b> » (« <b>Components > Contacts > Contacts > Website
        Owner</b> »). En utilisant les onglets en haut, vous avez accès à diverses
    options de contact.
</p>

<p>Pour configurer ce qui devrait être affiché sur la page de contact, allez dans la
    section « <b>Système > Configuration globale > Contacts</b> » (« <b>System > Global
        Configuration > Contacts</b> »).</p>

<p><b>La carte Google Maps</b> sur la page de contact est créée en utilisant le plugin
    de Google Maps. Il peut être configuré dans « <b>Extensions > Gestionnaire de
        plug-ins > Google Maps</b> » (« <b>Extensions > Plug-in Manager > Google
        Maps</b> »).</p>

