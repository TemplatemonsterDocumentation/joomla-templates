<h3>Contacts</h3>

<p>La page de contact affiche le formulaire de contact, une adresse et une carte Google Maps.  Pour changer les renseignements de contact
 (<i>adresse e-mail, adresse postale, etc.</i>), allez dans <b>Components (Composants) > Contacts (Contacts) > Contacts (Contacts) > Website Owner (Propriétaire du site web)</b>. En utilisant les onglets en haut, vous avez accès à diverses options de contact.
</p>

<p>Pour configurer la page de contact, allez dans <b>System (Système) > Global
        Configuration (Configuration globale) > Contacts (Contacts)</b>.</p>

<p><b>La carte Google Map</b> sur la page de contact est créée en utilisant le plugin de Google Maps. Il peut être configuré dans  <b>Extensions (Extensions) > Plugin Manager (Gestionnaire de plug-ins) >
        Google Maps</b>.</p>

