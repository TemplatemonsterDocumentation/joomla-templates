<h3>İletişim Sayfası</h3>

<p>İletişim Sayfası iletişim formu, adres ve google map görüntülerini, ilerişim
    bilgilerini değiştirebilmeniz için (
    <i>e-posta, adres vb temasları</i>
    ) <b>Components
        > Contacts > Contacts > Website Owner</b> izlemelisiniz. Üst kısımdaki sekmeleri
    kullanarak çeşitli seçemeklere ulaşabilirsiniz.
</p>

<p>İletşim sayfasını yapılandırabilmek için <b>System > Global Configuration >
        Contacts</b> bölümüne gidin.</p>

<p><b>Google Map</b>'ta iletişim sayfası Google Maps eklentisi kullanılarak oluşturulur.
    Bu yapılandırılabılır <b>Extensions > Plug-in Manager > Google Maps</b></p>


