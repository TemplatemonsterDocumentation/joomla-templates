<h3>Galeri Sayfası</h3>

<p>Galeri sayfası 'Galeri' geleri görüntülerinin altındaki mesajları görüntüler. galeri
    sayfa kategorilere göre görüntülere filtre saplar, sağ üst köşesinde bir filtre
    vardır.</p>

