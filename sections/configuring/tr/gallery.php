    <img src="img/pages-gallery-preview.jpg" alt="">
</figure>

<h4>Görüntü Eklemek</h4>
<p> Galeriye daha fazla resim ekleyebilek için, sadece
    'Galeri' alt makaleler eklemeniz gerekir. Her yüklenen tüm görüntüler İntro görünümü
    almaktadır. Her iki görüntüde genellikle 'images/gallery' dizinine yerleştirilir.
</p>

<p>Giriş görüntü galerisi ve galeri öğesi görünümler içinde gösterilir. Intro görüntü
    tıklandığında kez tam görüntü gösterilir.</p>

<figure class="img-polaroid">
    <img src="img/pages-gallery-1.jpg" alt="">
</figure>

<h4>Düzen Ayarlama</h4>
<p>Galeri sayfa düzeni herhangi bir joomla kategori sayfası
    olarak ayarlanabilir . '<b>Menus > Main Menu</b>' açın Joomla yönetim paneli ve öğe
    düzenleme bölümü.<b>Advanced Options</b> sekmesine gidin ve Galeri sayfasından
    ayarlarına erişebilirsiniz. </p>

<p>Örnek olarak siz sayfanızın sütun sayısını değiştirmek istiyorsanız Örneğin Blog
    Düzen Seçenekleri ayarları alanından Sütünlar seçeneğini değiştirin.</p>

