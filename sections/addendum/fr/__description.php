<h4>Validation</h4>

<p class="alert alert-info">
    Les deux codes, le balisage HTML (Langage hypertexte) et les styles CSS, utilisés dans nos modèles, sont
    sémantiquement corrects et valides. Néanmoins, certaines erreurs W3C peuvent toujours avoir lieu. Rendre valide
    le code 100% W3C élimine l'utilisation des technologies modernes de site web comme les fonctions de CSS3 et des
    balises HTML5. Notre objectif est de fournir une riche expérience de l'utilisateur avec des modèles de haute
    qualité et parfois nous devons enfreindre certaines règles.</p>