<h4>Validação</h4>

<p class="alert alert-info">
    Ambos marcação do HTML e estilos CSS usados em nossos temas são semânticamente corretos e válidos. Entretanto,
    alguns erros no validador do W3C ainda podem ocorrer. Criar código 100% válido pelas diretrizes do W3C elimina o
    uso de tecnologias web modernas, como o CSS3 e marcação HTML5. Nossa proposta é prover experiências ricas ao
    usuário com temas de alta qualidade e, para isso, temos que quebrar algumas regras.</p>