<h4>Validation</h4>

<p class="alert alert-info">
    Zarówno HTML jak i style CSS używane w naszych szablonach są semantycznie poprawne. Jednakże, niektóre błędy W3C
    mogą się pojawić. Sprawienie, że kod jest w 100% poprawny W3C eliminuje możliwość użycia nowoczesnych
    technologii jak CSS3 oraz HTML5. Naszym celem jest dostarczenie przyjaznych dla użytkowników oraz wysokiej
    jakości szablonów, jednocześnie czasami jesteśmy zmuszeni ominąć niektóre zasady.</p>