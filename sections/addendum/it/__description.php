<h4>Validation</h4>

<p class="alert alert-info">
    I nostri template usano sia il markup HTML sia gli stili CSS entrambi in maniera semanticamente corretta e
    valida. È però possibile che si verifichino comunque degli errori W3C. Compilare un codice valido in WC3 elimina
    l'utilizzo di tecnologie web moderne, come CSS3 e HTML5 markup. Per raggiungere il nostro obiettivo di fornire
    un'esperienza ricca con template di alta qualità è necessario a volte infrangere alcune regole.</p>