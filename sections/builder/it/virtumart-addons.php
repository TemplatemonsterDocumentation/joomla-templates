  <li><strong>Nav</strong> - you can use the navigation arrows for your carousel. </li>
  <li><strong>Dots</strong> - enable to show the navigation dots. </li>
  <li><strong>Autoplay</strong> -  sets whether you want the carousel to start automatically when the page loads.</li>
  <li><strong>Autoplay Timeout</strong> -  allows to set the autoplay timeout.</li>
