<h4><strong>Virtuemart Custom Block</strong></h4>
<p>You can use this addon to display Banners with all the essential information on it. You can add the link to any page or category, you can set the custom link, as well. </p>
<figure class="img-polaroid">
  <p><img src="img/builder-25.jpg" alt="">
  </p></figure>
<p>Let's see the addon settings:</p>
<h6><strong>General tab</strong></h6>
<ul class="marked-list">
  <li><strong>Admin Label</strong> - enter the addon title (for internal reference only).</li>
  <li><strong>Title</strong> - add some description to the addon (for internal reference only). </li>

<p><strong>Addon Options</strong></p>

  <li><strong>Select Layout</strong> - choose the layout for the addon.</li>
  <li><strong>Choice column custom block</strong> - set the number of columns to display in the addon area.</li>

<figure class="img-polaroid">
  <p><img src="img/builder-26.jpg" alt="">
</p></figure>

  <li><strong>Header Text</strong> - specify the text you want to display as the heading.</li>
  <li><strong>Footer Text</strong> - enter the text to display at the bottom of the addon area.</li>
  <li><strong>CSS Class</strong> - you can apply a particular  CSS style  to the addon. You need to add the CSS class name.</li>
</ul>
<p><strong>Repeatable Items</strong></p>
<p>To add new item press the <strong>Add Item </strong>button.</p>
<p>Access the item settings by clicking the <strong>&quot;Gears&quot;</strong> button.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-24.jpg" alt="">
  </p></figure>
<ul class="marked-list">

  <li><strong>Enable Background Options</strong> -  customize the background of your addon with various styling options. You can add the background color and background image, text content and button.</li>
  <li><strong>Background Color</strong> - choose the  color you want to use as a background color for the addon. </li>
  <li><strong>Background Image</strong> - you can add a  background image.</li>
  <li><strong>Image width</strong> - set the image width (e.g. 695, 400).</li>
  <li><strong>Button Icon</strong> - select the  icon for the button.</li>
  <li><strong>Item title, Sub Title, Content</strong>  - specify the text you want do show with the help of this addon. </li>
</ul>
<figure class="img-polaroid">
  <p><img src="img/builder-27.jpg" alt="">
</p></figure>
<ul class="marked-list">

  <li><strong>Btn Link</strong> - you can add the button with the link to a specific category or page. In case you do not wan to use the button (the option set to disabled) the whole addon area will be linkable.</li>
  <li><strong>Select  Link Type</strong> - choose the link type. You can add the link to the joomla pages, virtuemart categories or any custom link.</li>
  <li><strong>Select menu item, Select Category</strong> - select the page or products category you want to link to.</li>
  <li><strong>Button Link</strong> - enter the URL for the button.</li>
  <li><strong>Button Text</strong> - enter the text for the button. Leave the field empty  if no button is required. </li>
  <li><strong>Button position</strong> - choose the button position.</li>
  <li><strong>Button Style</strong> - select the button style.</li>
</ul>
<figure class="img-polaroid">
  <p><img src="img/builder-28.jpg" alt="">
</p></figure>
<p>Remember to  <strong>save</strong> the settings.</p>
