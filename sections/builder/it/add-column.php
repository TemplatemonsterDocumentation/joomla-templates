<h3>Add New Column</h3>
<p><strong>Backend Editor</strong></p>
<p>The rows and addons can be organized in columns.</p>
<p>Click <strong>Add/Manage Columns</strong>.</p>
<p>To add <strong>3</strong> columns, type <strong>4+4+4</strong> and press <strong>Generate</strong>.</p>
<p>The screen width equals to <strong>14</strong>, so if you want to add <strong>2</strong> columns, you need to enter <strong>6+6</strong>.</p>
