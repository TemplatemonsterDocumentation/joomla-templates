<h3>Add New Addon</h3>
<h5><strong>Backend Editor</strong></h5>
<p>You can add  Addons to each row. </p>
<p>Press the<strong> Addon</strong> button.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-8.jpg" alt="">
  </p></figure>
<p>Choose the extension you want to add.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-18.jpg" alt="">
  </p></figure>
<p>Configure the selected addon as you want it to display and <strong>save </strong>the setting.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-19.jpg" alt="">
  </p></figure>
<p>Change the Addon position. </p>
<figure class="img-polaroid">
  <p><img src="img/builder-20.jpg" alt="">
  </p></figure>
<p>To access the addon display setting tap<strong> Edit Addon</strong>.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-21.jpg" alt="">
  </p></figure>
<h5><strong>Frontend Editor</strong></h5>
<p>To add the new addon just select the extension you want to display  and pit it into the right position on the page using the Drag&amp;Drop functionality of the Frontend Editor.</p>
<p>Then set the addon display settings.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-22.jpg" alt="">
  </p></figure>
  <p>To edit the addon tap the extension area (the addon area will be highlighted) or click the<strong> &quot;Edit Addon&quot;</strong> button.</p>

