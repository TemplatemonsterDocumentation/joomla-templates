<h5><strong>Frontend Editor</strong></h5>
<p> There are  several ways to open the <strong>Frontend Editor</strong>:</p>
<p><strong>1.</strong>  Press on the <strong>Frontend Editor</strong> button at the page you want to edit.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-4.jpg" alt="">
  </p></figure>
<p><strong>2.</strong> Open the page you want to edit.</p>
<p>Tab the <strong>Frontend Editor</strong> button at the top.</p>
<p>Enter the <strong>Username</strong> and <strong>Password</strong> you use to access your website admin &gt; click <strong>LOG IN</strong>.</p>
<p>Now you can edit the page.</p>
<h5><strong>Edit Header or Footer</strong></h5>
