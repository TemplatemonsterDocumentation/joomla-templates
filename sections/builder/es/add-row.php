<h3>Add New Row</h3>
<h5><strong>Backend Editor</strong></h5>
<p>Open the Page you want to add the row to and scroll down to the bottom of the page. </p>
<p>There you'll see the <strong>Add New Row</strong> button.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-8.jpg" alt="">
  </p></figure>
<p>Click the button and select column structure.</p>
<p>Press the <strong>Generate </strong>button.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-9.jpg" alt="">
  </p></figure>
<p>To add the new row, click the &quot;<strong>Column Options</strong>&quot; button and tab <strong>Add New Row</strong>.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-10.jpg" alt="">
  </p></figure>
<p>Select <strong>Row Options</strong> to configure the row. Add the <strong>Section Title</strong>, set the <strong>Padding</strong>, <strong>Text Color</strong> and <strong>Background Image</strong>.</p>
<figure class="img-polaroid">
  <p><img src="img/builder-14.jpg" alt=""></p>
</figure>
<h5><strong>Frontend Editor</strong></h5>
<p>Open the page in the <strong>Frontent Editor</strong>.</p>
<p>Move to the bottom of the page.</p>
<p>Press the <strong>&quot;+&quot; </strong>button.</p>
