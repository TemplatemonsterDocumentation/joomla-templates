<h3>Mega Menu Builder</h3>
<p>A Mega Menu is the name given to a large panel of content which is displayed below a menu item when the user hovers over the menu item. Mega Menus can contain sub menu items and modules. We all know how it helps a site be more navigable.</p>
<p>With <strong>Helix Ultimate</strong> you are able to build such mega menus on your site easily. Helix Ultimate mega menu builder offers you different layouts to give diverse looks of your menus. Styling menus has been made easier for your convenience. Add as many menu items as you need.</p>
<p>Inside each menu item you can find new tab <strong>"= Mega Menu"</strong> - it came from Helix Ultimate Framework. Inside this tab, you can find several switchers, responsible for additional features.</p>

