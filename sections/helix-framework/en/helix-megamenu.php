<h4>Show Menu Title</h4>
<p>By default enabled, it allows you to hide Menu Title from menu, but alias in system will stay. It's good method to create hidden menu item for short url.  This may be useful also when you want to show only icon instead full menu item name, for Home or Search position.</p>

<h4>Dropdown position</h4>
<p>It allows you to change dropdown submenu position, to the right or to the left side.</p>

<h4>Menu Icon</h4>
<p>Mega Menu allows you to set an icon for each menu item in your menu. The use of icons beside menu items titles is a fantastic alternative that favors a quick visual option identification; and even the use of icons without text to accompany is viable. You can use font icons based on Font Awesome v4.7, it means that you have huge collection of icons to choose from. By default the icon will be displayed to the left of the menu item text. To remove the icon, select the “Select” name, then save settings.</p>

<h4>Custom Class</h4>
<p>This field is needed in the case when your template has a particular menu location and it needs adjustment. You can place here any necessary custom class name which is already defined inside your Custom CSS code section. You can add more than one class name hee, but they have to be separated with space. Notice! No dots, comma or hash mark here.</p>

<h4>Badge in menu items</h4>
<p>Want to highlight your most important menu item(s) redirecting to any special page, service, product or announcement? With Helix Ultimate, we give you the system to highlight your menu item(s) with name badges (for example with word “sale” or "new"). Draw your visitors’ attention to any menu item you want.</p>
