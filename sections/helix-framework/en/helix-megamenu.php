<h4>Mega Menu</h4>

<p>To enable/disable advanced Mega Menu options. It is simple way to create highly customized and creative mega menu configurations. You can add and use row and column based menus, also drag and drop the modules to the menu positions from the modules list.</p>

<ol class="marked-list">
	<li><strong>Width</strong> - allows you to choose total width of submenu area.</li>
	<li><strong>Alignment</strong> - choose submenu area alignment to the right, left, center, or set full width.</li>
</ol>

<div class="alert alert-info">
	<p><strong>General tip</strong>: building a two or three columns sub-menu is easy process - all starts from Menu Manager, where you have to add all those sub menu items. For main submenu headers you can use "Menu Heading" or "External Links" menu type.</p>
</div>

<p>How to use Mega Menu to build columns for menu items:</p>

<ul class="index-list">
	<li>Click “Add New Row” button</li>
	<li>Select Menu layout: single row or rows with columns, you have 9 options to choose from.</li>
	<li>Then click “Save” button from top bar to refresh/rebuild content.</li>
	<li>After refreshing you should be able to see submenu items, if were added before. Using drag and drop function in Mega Menu, you can easily arrange columns, move elements or modules into another position, from one column into another.</li>
	<li>In addition, you can also select existing module(s) from the right sidebar and put into column.</li>
	<li>Trash icon - allows you to remove undesired row.</li>
	<li>Close “X” icon - allows you to remove undesired element.</li>
	<li>Remember to save setting after all changes (“Save” or “Save &amp; Close”).</li>
</ul>

