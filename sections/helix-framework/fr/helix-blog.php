<h4>Images</h4>
<p>Used to set image size and resolution (“Blog Option” tab only) used inside different article views.</p>

<h4>Crop Quality</h4>
<p>It allows you set image quality for better compressions for images size resolution. Applicable for JPG images only.</p>

<h4>Blog List Image</h4>
<p>Select an image size for Category Article Blog view. By default, if a featured image will be shown if available.</p>

<h4>Leading Article Image</h4>
<p>Select an image size for leading article. By default, if a featured image will be shown if available.</p>

<h4>Blog Details Image</h4>
<p>Select an image size for single article details view. By default, if a featured image will be shown if available.</p>

<h4>Social Share</h4>
<p>Show or Hide social share buttons (Facebook, Twitter, LinkedIn, Google+) above content of single article. By default in top right corner.</p>

<h4>Author Info</h4>
<p>This option will show user bio as author information along with author profile image taken from Gravatar if the profile plugin is enabled. By default, images are presented at 64px by 64px. Relation is based on Joomla user email. What happens when an email address has no matching Gravatar image? By default, you would get blue box with gravatar logo inside.</p>

<h4>Comments</h4>
<p>This build-in option allows you to add IntenseDebate Comments system, Facebook comments and / or Disqus Comments to your site. Feature-rich comment system complete with social network integration allows your readers to log in via a preferred social network and add comment under article. All you have to do is choose and set correct Facebook Application ID, IntenseDebate Account or Disqus Username/Subdomain. You can use only one selected social comments system or disable it and use your own based on extra comments component or plugin.</p>

<div class="alert alert-info">
	<p><strong>Important!</strong>Disqus Comments have three membership plans, and in basic one Ads (advertising) and sponsored comments are (or may be) displayed.</p>
</div>

<ol class="marked-list">
	<li><strong>Select Categories</strong> - select single or multiple categories from the list to where comments will be applied. Comments option will be available for subcategories if parent category is selected.</li>
	<li><strong>Comments Width</strong> - choose submenu area alignment to the right, left, center, or set full width.</li>
</ol>