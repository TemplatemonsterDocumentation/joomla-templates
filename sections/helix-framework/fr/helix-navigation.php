<h4><strong>Select</strong></h4>

<p>It allows you to choose menu from available in your Joomla.</p>

<h4><strong>Menu type</strong></h4>

<p>By default there are enabled two separate menus: Mega Menu and Off Canvas Menu [=] - sometimes called Hamburger Menu, because of used shape. You can use both or only of them - as you want or need.</p>

<h4><strong>How to disable OffCanvas Menu</strong></h4>
<p>It pretty easily to disable Off Canvas menu. Just select "Mega Menu" only.</p>

<h4><strong>Dropdown Width</strong></h4>

<p>Dropdown Menu Width value would be to set it to the width of your submenu size content area. By default 240px.</p>

<h4><strong>Dropdown Animation</strong></h4>
<p>MegaMenu offer serveral transition options for your dropdown submenus:</p>
<ol class="marked-list">
    <li>No Animation</li>
    <li>Fade In</li>
    <li>Fade In Up</li>
    <li>Fade In Down</li>
    <li>Zoom In</li>
    <li>Rotate In</li>
    <li>Pulse</li>       
</ol>
<p>This is the transition that occurs when the sub-menu is opened. To have the sub-menu appear with no fancy effect, set to None (No Animation).</p>





























