<h3>Navigation</h3>
<p>There you can find general settings for MegaMenu. Mega Menu is an easy to use menu tool for creating beautiful, customized menus for your site, also with modules inside.  From here you have access to 4 options, more advanced settings are "hidden" in Menu Manager inside each menu item.</p>
