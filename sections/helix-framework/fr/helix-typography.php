<h3>Typography Settings</h3>
<p>Typography in websites is arguably more important than other design elements since type is the one thing that is consistently rendered across different browsers.</p>

<h4><strong>Google Fonts</strong></h4>

<p>In general typography in Helix Ultimate templates is mainly based on Google Fonts collection (850+ fonts). With so many unique fonts to use, you're bound to find something you like! This settings area allows you to easily choose font(s) from the Google Font Directory to your Joomla! template.</p>
<p>The Google Fonts  is compatible with popular (and updated) browsers. They works reliably on the vast majority of modern mobile operating systems, including Android browsers and iOS 4.2+ (iPhone, iPad, iPod). Support for earlier iOS versions is limited.</p>
<p>All available Google Fonts are released under open source licenses. You can use them in any non-commercial or commercial project.</p>
<p>Only some of the fonts in the Google Font Directory support full range of subset - multiple scripts (like Latin, Latin Ext, Cyrillic, and Greek for example). Notice, not all fonts are fully support your native language diacritical marks (signs). Check it before complains.</p>

<h4><strong>GDPR/EU-DSGVO/Privacy Policy and Google Fonts</strong></h4>

<p>By new law is not allowed, to collect private data from your website visitors without permission.</p>

<p>Google Fonts is a service provided by Google Inc. from USA. There is no doubt that Google Fonts relies on the generic Google Privacy Policy. The Google Fonts API (used inside Helix Ultimate) is designed to limit the collection, storage, and use of end-user data to what is needed to serve fonts efficiently. Use of Google Fonts is unauthenticated. No cookies are sent by website visitors to the Google Fonts API. Requests to the Google Fonts API are made to resource-specific domains, such as fonts.googleapis.com or fonts.gstatic.com, so that your requests for fonts are separate from and do not contain any credentials you send to google.com while using other Google services that are authenticated, such as Gmail. The use of Google Web Fonts follows from interest in a uniform and appealing online presentation of services. This represents a legitimate interest within the scope of Article 6 (1) 1 lit. f GDPR. With regard to the transfer of personal data to the US, Google has submitted to the EU-US Privacy Shield, https://www.privacyshield.gov/EU-US-Framework. More information about Google Fonts and privacy can be found here: https://developers.google.com/fonts/faq#what_does_using_the_google_fonts_api_mean_for_the_privacy_of_my_users.</p>

<p>GDPR is very fresh law, and as I know, no EU court can have made any judgement about "illegal" use online version of Google Fonts on website(s). </p>

<p>In general Google claims GDPR compliance. But if you feel that Google's explanations are not enough for law regulation in your country - anytime you can decide to:</p>

    Not use Google Fonts on your site, disable them from Typography settings, and use classic fonts (tip below).
    Replace Google Fonts loaded from google servers to loaded from your server (tip about it you will find in FAQ section). Read about google-webfonts-helper service which makes it easy to self-host any font from Google Fonts. If you embed them from your own server, everything in legal way would be OK again.

<h4><strong>Arial, Verdana, Georgia..</strong></h4>
<p>Beside Google Fonts you can choose widely-supported cross-platform fonts: Arial, Tahoma, Verdana, Helvetica, Times New Roman, Trebuchet MS and Georgia. “Web Safe” fonts mean that they will look perfect no matter the browser you are viewing them from. Also, these fonts are very popular and pre-installed on operating system: Windows, iOS or Linux. They are not loaded during loading a website, no delays because of that. You can save bandwidth and keep fast loading pages.</p>

