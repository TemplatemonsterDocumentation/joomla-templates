<h3>Layout Builder</h3>


<p>Layout Manager allows you to build flexible layout based on module positions. Layout Builder is one of the unique features of Helix Ultimate which allows anyone to customize the existing template in almost any shape without code customization.</p>

<h4><strong>Layout Grid</strong></h4>

<p>Helix Ultimate Layout is based on 12 column layout from Bootstrap 4. It includes a powerful mobile-first flexbox grid system for building layouts of all shapes and sizes.</p>

<p>You can choose columns preset or set your own grid. The grid system is responsive, and the columns will re-arrange depending on the screen size.</p>
<ol class="marked-list">
	<li>In current version of Helix Ultimate you can use Predefined Headers.</li>
	<li>Each Row can have its own column structure and unique settings.</li>
	<li>Icon with arrows allows you to change positions of rows by moving them up or down.</li>
	<li>Use plus icon to add new Row, then choose column structure and select module position(s) used there. We also suggest to insert short ID name for the new Row.</li>
</ol>

<p> Change the module position by clicking on the configuration icon on the top right.</p>
