<h4><strong>How to use module below/above component area</strong>?</h4>

<p>Here you can publish any module below or/and above component area. For this purpose we created two built-in module positions: Content Top &amp; Content Bottom
- just choose them from the list during edition of any module. They are not visible in visual Layout grid, but you can use them in all templates based on Helix Ultimate framework.</p>

<h4><strong>Row Options</strong></h4>

<figure class="img-polaroid">
  <p><img src="img/helix-framework/layout-02.png" alt="" /> </p>
</figure>

<p>In Row Options you can:</p>
<ol class="marked-list">
    <li>Insert section title (should be short, without extra languge native marks).</li>
    <li>Set Fluid Width.</li>
    <li>Add Custom CSS Class Name.</li>
    <li>Define Row Style: margin, padding, text color, link color, link hover color, background color and choose background image.</li>
    <li>Responsive settings - allows to control of visibility of rows.</li>       
</ol>
<p>You can hide Row on selected devices, related with screen resolution:</p>
<ol class="marked-list">
    <li>Hidden on Smartphone - screen width less than 576px.</li>
    <li>Hidden on Largen Smartphone - screen width equal to or greater than 576px.</li>
    <li>Hidden on Tablet - screen width equal to or greater than 768px.</li>
    <li>Hidden on Smaller Desktop - screen width equal to or greater than 992px.</li>
    <li>Hidden on Desktop - screen width equal to or greater than 1200px.</li>
</ol> 

