<h3>Modules positions map</h3>

<p>Available positions may vary in any particular template but general positions map can
    be seen below:</p>

<p>As you can see, the content area is marked with a different color. That's because the
    content area can't handle any modules by default. It displays only the page
    content. </p>

<p>All positions are turned off if no modules are assigned, so they won't hamper
    you.</p>

