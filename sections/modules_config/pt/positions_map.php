<h3>Mapa de posições dos Módulos</h3>

<p>As posições disponíveis podem variar em qualquer template particular, mas o mapa de
    posições geral pode ser visto abaixo:</p>

<p>Como você pode ver, a área do conteúdo está marcada em diferentes cores. Isso foi
    feito pois a área do conteúdo não aguenta quaisquer módulos por padrão. Ele exibe
    somente o conteúdo da página. </p>

<p>Todas as posições são desligadas se nenhum módulo for atribuído, assim eles não lhe
    dificultam.</p>

