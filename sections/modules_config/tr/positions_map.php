<h3>Modül Pozisyon Haritası</h3>

<p>Mevcut pozisyonlar herhangi bir şablon için değişebilir ama genel olarak pozisyon
    haritası aşşağıdaki gibidir.</p>

<p>Görmiş olduğunuz gibi içerik alanları farklı renklerle işaretlenmiştir. İçerik
    alanları herhangi bir modüle müdahale etmeyecek şekilde yapılandırılmıştır. Sadece
    sayfa içeriğini görüntülemektedir.</p>

<p>Size engel olmayacak şekilde hiçbir modül atanmamışsa bütün pozisyonları
    kapatılır.</p>

