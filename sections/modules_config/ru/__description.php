<h2 class="item4">Настройка модулей</h2>

<h6>Веб-сайт Joomla состоит из двух основных частей - раздел с содержимым сайта и позиции модулей (для получения
    более детальной информации, ознакомьтесь с картой позиций модулей). Модули используются в
    Joomla, для того чтобы отобразить содержимое в подготовленных позициях.</h6>