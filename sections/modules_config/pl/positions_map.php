
<h3>Mapa pozycji modułów</h3>

<p>Dostępne pozycje mogą się różnić w poszczególnych szablonach, ale generalne pozycje
    mogą być sprawdzone poniżej:</p>

<p>Jak widzisz, pole treści jest zaznaczone różnymi kolorami. Zrobiono tak, ponieważ
    pole treści nie może obsługiwać każdego modułu jako domyślny. Wyświetla tylko
    zawartość strony. </p>

<p>Wszystkie pozycje są wyłączone, jeśli brak przypisanych modułów, aby uniknąć
    problemów.</p>

