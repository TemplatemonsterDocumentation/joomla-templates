
<h3>Plan des positions</h3>

<p>Les positions disponibles peuvent varier d'un modèle à l'autre mais voici un plan de
    positions générales :</p>

<p>Comme vous le voyez, la zone de contenu est marquée d'une couleur différente. C'est
    parce que la zone de contenu ne peut pas recevoir de modules par défaut. Elle
    n'affiche que le contenu de la page. </p>

<p>Toutes les positions sont désactivées si aucun module ne leur est associé, et donc
    elles ne vous gêneront pas.</p>

