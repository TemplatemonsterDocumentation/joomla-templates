<h3>Positionskarte der Module</h3>

<p>Je nach Template können sich die Positionen variieren, aber Sie können sich unten die allgemeine Positionskarte schauen:</p>

<p> Wie Sie sehen, wird der Inhaltbereich mit einer anderen Farbe markiert. Es wird
    getan, weil der Inhaltsbereich standardmäßig keine Module enthalten kann. Es
    zeigt nur den Seiteninhalt an</p>
<p>Ohne zugeweisene Module werden die Positionen nicht angezeigt,sodass sie Sie nicht stören:</p>

