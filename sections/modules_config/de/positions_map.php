<h3> Positionskarte von Module</h3>

<p> Je nach Template können sich die Positionen variieren, aber Sie können unten die
    allgemeine Positionskarte schauen:</p>

<p> Wie Sie sehen, wird der Inhalt Bereich in verschiedene Farben markiert. Es wird
    getan, weil der Inhaltsbereich standardmäßig keine Module verarbeiten kann. Dies
    zeigt nun den Seiteninhalt an</p>

<p>Ohne zugeweisene Module werden alle Positionen ausgeschaltet, deswegen hemmen sie
    Ihnen nicht.</p>

