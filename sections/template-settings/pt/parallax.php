<h3>Usando o efeito de Parallax</h3>

<p>
    O efeito de parallax pode ser adicionado ao template com a ajuda do plugin de jQuery
    <a href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
       rel="nofollow">Stellar</a>.
</p>

<p>Para adicionar o efeito na sua página, navegue para "Template manager -> Templates",
    clique no nome do template que você usa (ThemeXXXX arquivos e detalhes) e abra o
    arquivo "index.php". Selecione o bloco em que deseja adicionar o efeito de parallax,
    "folio-row" por exemplo, insira o atributo</p>
<code><code>data-stellar-background-ratio="[valor]"</code></code>

<p>onde [valor] é a proporção que será relativa a rolagem em velocidade natural, você
    pode procurar informações detalhadas na página de <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">documentação</a> do plugin.</p>

<p>Depois de você realizar todos os passos, você verá o efeito parallax no bloco
    específico.</p>

