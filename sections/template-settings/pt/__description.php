<h2 class="item7">Configurações do Template</h2>

<p>Cada template do Joomla possui suas opções de configurações. Elas podem ser ajustadas
    para configurar as classes do layout do tema, as tags de tipografia, algumas funções
    adicionais, etc. As opções do template podem ser acessadas através do painel de controle
    do Joomla na seção &quot;Extensões &gt; Gerenciador de Template (em Inglês, &quot;<b>Extensions
        > Template Manager</b>&quot;.)</p>

<p>Quando você estiver seção Gerenciador de Template, selecione o tema que você quer
    modificar. </p>
