<h3>Tipografia</h3>

<p>As opções de tipografia permite selecionar tags para artigo para várias
    visualizações: artigos em destaque, visualização de categoria, visualização de blog,
    visualização de artigo único, etc. Há várias tags HTML que podem ser usadas para
    definir o cabeçalho (h1, h2, h3, h3 etc). As tags são diferentes pela importância
    lógica e estilo. <a href="http://www.w3schools.com/tags/tag_hn.asp" target="_blank">Saiba
        mais</a></p>

