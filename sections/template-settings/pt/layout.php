<h3>Layout</h3>

<h6>As opções do Layout lhe permite modificar as configurações principais de layout do
    tema:</h6>

<p><b>Layout do Tema</b> - selecione se você quer que o tema seja fixo (largura em
    pixels) ou fluído (largura em porcentagens);</p>

<p><b>Logo</b> - permite selecionar a imagem da logo;</p>

<p><b>Largura da Logo</b> - permite selecionar a largura do conteúdo da logo (de acordo
    com a grade Bootstrap);</p>

<p><b>Largura da Sidebar</b> - permite colocar a largura para as sidebars;</p>

