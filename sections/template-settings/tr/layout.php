<h3>Layout (Düzen)</h3>

<h6>Düzen seçeneklerinin ana teması düzen ayarlarını değitirmek için izin sağlar:</h6>

<p><b>Theme layout</b> - tema (yüzde genişliği) sabit (Piksel olarak genişlik) veya sıvı
    olmasını istiyorsanız şeçmelisiniz;</p>

<p><b>Logo</b> - logo resmi seçmenizi sağlar;</p>

<p><b>Logo Width</b> - logo container genişliği seçmenizi sağlar (Bootstrap tablo göre);
</p>

<p><b>Sidebar Width</b> - Kenar çubuğu için genişliği ayarlamanızı sağlar;</p>
