<h3>Parallax efektini kullanmak</h3>

<p>Parallax efektini, bir jQuery-eklentisi olan <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a> yardımıyla yükleyebilirsiniz.</p>

<p>Bir sayfaya Parallax efekti uygulamak için "Görünüm -> Temalar" bölümüne girin,
    kullandığınız temayı seçin (TemaXXXX Detaylar ve Dosyalar) ardından "index.php"
    dosyasını açın. Parallax'ı ekleyeceğiniz alanı seçin (örn : "folio-row"). Bu alana</p>
<code><code>data-stellar-background-ratio="[değer]"</code></code>

<p>Yukarıdaki [değer] bölümü - kayma hızını belirler. Daha detaylı bilgi için,
    eklentinin <a href="http://markdalgleish.com/projects/stellar.js/docs/"
                  target="_blank" rel="nofollow">Dokümantasyon</a> sayfasından yardım
    alabilirsiniz.</p>

<p>Tüm adımları tamamladıktan sonra, belirlediğiniz alana Parallax efektinin uygulanmış
    olduğunu göreceksiniz.</p>


