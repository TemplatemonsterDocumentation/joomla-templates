<h3>Diseño</h3>

<h6>Opciones de diseño le permiten modificar la configuración de diseño del tema:</h6>

<p><b>Diseño del tema (Theme layout)</b> - seleccione si Usted desea que el tema sea fijado (anchura en píxeles) o líquido (anchura en porcentaje);;</p>

<p><b>Logotipo (Logo)</b> -  seleccione una imagen de logotipo;</p>

<p><b>Ancho de Logo (Logo Width)</b> - permite seleccionar el ancho de contenedor de logotipo (de acuerdo a grid de Bootstrap);</p>

<p><b>Ancho de barra lateral (Sidebar Width)</b> - permite configurar el ancho de barras laterales.</p>

