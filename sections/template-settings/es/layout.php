<h3>Diseño </h3>

<h6>Opciones de diseño le permiten modificar la configuración de diseño del tema:</h6>


<p><b>Theme layout/Diseño de tema</b> - hay que elegir si Usted desea que el tema sea
    fijado (anchura en píxeles) o líquido (anchura en porcentaje);</p>

<p><b>Logo/Logotipo </b> - permite seleccionar una imagen de logotipo;</p>

<p><b>Logo Width/Ancho de Logotipo </b> - permite seleccionar el ancho de contenedor de
    logotipo (de acuerdo a grid de Bootstrap); </p>

<p><b>Sidebar Width/Ancho de barra lateral </b> - permite configurar el ancho de barras
    laterales.</p>

