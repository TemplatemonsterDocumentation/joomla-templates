<h3>Usando el efecto Parallax</h3>

<p>El efecto Parallax puede ser agregado a la plantillas con la ayuda del plugin <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a> de jQuery. </p>

<p>Con el fin de añadir el efecto Parallax a la página, ve a “Administrador de
    Plantillas --> Plantillas”, haz clic en el nombre de la plantilla que utilizas
    (TemaXXXX Detalles y Archivos) y abre el archivo “index.php”. Selecciona el bloque
    al que necesites agregarle Parallax, por ejemplo “folio-row”, agrega la propiedad</p>
<code><code>data-stellar-background-ratio="[valor]"</code></code>

<p>donde [valor] – es la proporción que es relativa a la velocidad natural de
    desplazamiento, puedes checar por información detallada en la página de <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">Documentación</a> del plugin. </p>

<p>Después de que realices todos los pasos, verás el efecto Parallax en el bloque
    específico. </p>

