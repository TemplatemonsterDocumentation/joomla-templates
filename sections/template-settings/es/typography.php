<h3>Tipografía</h3>

<p>Las opciones de tipografía le permiten elegir etiquetas para títulos de las entradas
    para varios puntos de vista: artículos destacados, vista por categorías, en el blog,
    etc. Hay varias etiquetas HTML que Usted puede utilizar para definir cabeceras (h1,
    h2, h3, h3, etc.) Las etiquetas son diferentes por la importancia y el estilo <a
        href="http://www.w3schools.com/tags/tag_hn.asp" target="_blank">Más
        información.</a></p>


