<h2 class="item7">Configuración de Plantilla</h2>

<p>Cada plantilla de Joomla tiene sus valores de configuración. Con su ayuda se puede configurar el diseño del tema, tipografía, algunas características adicionales, etc. A las Opciones de la plantilla se puede acceder a través del panel de administración de Joomla en la pestaña <b>Extensiones> Gestor de plantillas (Extensions > Template Manager)</b>.</p>

<p>Por favor seleccione la plantilla necesaria en Administrador de plantillas:</p>

