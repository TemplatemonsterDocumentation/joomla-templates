<h2 class="item7">Configuración de Plantilla</h2>

<p>Cada plantilla Joomla tiene sus opciones de configuración. Se puede ajustarlas para
    configurar clases de diseño del tema, etiquetas de tipografía, algunas características
    adicionales, etc. Se puede encontrar las opciones de plantilla en el panel de
    administración de Joomla en la sección Extensions > Template Manager (Extensiones >
    Gestor de plantillas).</p>

<p>En la sección de Template Manager seleccione el tema que desea modificar. </p>

