<h3>Using Parallax effect</h3>

<p>Parallax effect can be added to the template with the help of jQuery-plugin <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a>.</p>

<p>In order to add Parallax effect to the page, navigate to "Template manager ->
    Templates", click on the template name that you use (ThemeXXXX Details and Files)
    and open "index.php" file. Select the block that you need to add parallax to, for
    example "folio-row", add the</p>
<code><code>data-stellar-background-ratio="[value]"</code></code>

<p>where [value] - the ratio that is relative to the natural scroll speed, you can check
    detailed information on a plugin's <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">Documentation</a> page.</p>

<p>After you perform all the steps, you will see the Parallax effect on the specific
    block.</p>

