<h2 class="item7">Template Settings</h2>

<p>Each Joomla template has its configuration options. They can be adjusted to set up theme
    layout classes, typography tags, some additional features, etc. Template options can be
    accessed through the Joomla administration panel in the <b>Extensions > Template
        Manager</b> section.</p>

<p>When you are in the Template Manager section select the theme you want to modify. </p>

