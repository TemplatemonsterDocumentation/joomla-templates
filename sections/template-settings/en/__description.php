<h2 class="item7">Template settings</h2>

<p>Each Joomla template has its configuration settings. With their help you can set up theme layout, typography, some additional features, etc. Template options can be accessed through the Joomla admin panel in <b>Extensions > Template Manager</b>
tab.</p>

