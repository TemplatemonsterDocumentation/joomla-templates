<h3>Layout</h3>

<h6>Layout options allow you to modify the main theme layout settings:</h6>


<p><b>Theme layout</b> - select if you want the theme to be fixed (width in pixels) or
    fluid (width in percent);</p>

<p><b>Logo</b> - allows to select the logo image;</p>

<p><b>Logo Width</b> - allows to select logo container width (according to the Bootstrap
    grid);</p>

<p><b>Sidebar Width</b> - allows to set the width for the sidebars.</p>

