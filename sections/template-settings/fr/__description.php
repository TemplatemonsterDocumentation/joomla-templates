<h2 class="item7">Réglages du modèle</h2>

<p>Chaque modèle pour Joomla a ses propres options de configuration. Elles peuvent être
    ajustées pour configurer des classes d'agencement de thème, des balises typographiques,
    certaines fonctionnalités additionnelles, etc. Les options du modèle sont accessibles
    dans la section « <b>Extensions > Gestionnaire de modèles Manager</b> » (« <b>Extensions
        > Template Manager</b> » du panneau d'administration de Joomla.</p>


<p>Lorsque vous êtes dans la section du gestionnaire de modèles (« Template Manager »),
    sélectionnez le thème que vous souhaitez modifier. </p>
