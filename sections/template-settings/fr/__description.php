<h2 class="item7">Réglages du modèle</h2>

<p>Chaque modèle pour Joomla a ses propres options de configuration. Elles peuvent être ajustées pour configurer
    d'agencement de thème, des balises typographiques, certaines fonctionnalités additionnelles, etc. TLes options du modèle sont accessibles dans la section <b>Extensions (Extensions) > Template Manager (Gestionnaire de modèles)</b>
du panneau d'administration de Joomla.</p>

<p>Dans le menu Template Manager (Gestionnaire de modèles) sélectionnez le thème que vous souhaitez modifier:</p>

