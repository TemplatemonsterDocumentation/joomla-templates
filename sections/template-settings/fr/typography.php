<h3>Typographie</h3>

<p>Les options de typographie permettent de sélectionner des tags pour les titres des
    articles pour différentes vues : articles présentés, vue par catégories, vue
    d'articles individuels, etc. Il y a plusieurs balises HTML qui peuvent être
    utilisées pour définir les en-têtes (h1, h2, h3, h3, etc.). Les balises différent en
    importance logique et en style. <a href="http://www.w3schools.com/tags/tag_hn.asp"
                                       target="_blank">En apprendre plus</a>.</p>

