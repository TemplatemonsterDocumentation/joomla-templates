<h3>Agencement</h3>

<h6>Les options d'agencement vous permettent de modifier les paramètres d'agencement du
    thème principal :</h6>

<p><b>Theme Layout (agencement du thème)</b> - sélectionnez si vous voulez que le thème
    soit fixe (largeur en pixels) ou fluide (largeur en pourcentages) ;</p>

<p><b>Logo</b> - permet de sélectionner une image de logo ;</p>

<p><b>Logo Width (largeur du logo)</b> - permet de sélectionner la largeur du conteneur
    du logo (en fonction de la grille Bootstrap) ;</p>

<p><b>Sidebar Width (largeur de la sidebar)</b> - permet de déterminer la largeur des
    sidebars.</p>

