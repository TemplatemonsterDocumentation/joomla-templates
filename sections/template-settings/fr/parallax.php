<h3>Utiliser l'effet Parallax</h3>

<p>L'effet Parallax peut être ajouté au template à l'aide du plugin jQuery <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a>.</p>

<p>Pour ajouter l'effet Parallax à la page, allez dans "Template manager -> Templates",
    cliquez sur le nom du template que vous utilisez (ThemeXXXX Details and Files) et
    ouvrez le fichier "index.php". Sélectionnez le bloc auquel vous devez ajouter
    Parallax, par exemple "folio-row", ajoutez la propriété</p>
<code><code>data-stellar-background-ratio="[valeur]"</code></code>

<p>avec [valeur] - le ratio relatif à la vitesse de défilement naturelle, vous pouvez
    obtenir des information détaillées sur la page de <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">Documentation</a> du plugin.</p>

<p>Après avoir effectué toutes les étapes, vous verrez l'effet Parallax dans le bloc
    spécifique.</p>

