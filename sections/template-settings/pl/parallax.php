<h3>Używanie efektu Parallax</h3>

<p>Efekt Parallax może zostać dodany do szablonu przy użyciu jQuery-plugin <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a>.</p>

<p>Aby dodać efekt Parallax, przejdź do "Template manager -> Templates", kliknij na
    nazwę szablonu używanego przez Ciebie (ThemeXXXX Details and Files) i otwórz plik
    "index.php". Wybierz element, do którego chcesz dodać parallax, na przykład
    "folio-row", dodaj wartość</p>
<code><code>data-stellar-background-ratio="[wartość]"</code></code>

<p>gdzie [wartość] - stosunek odnoszący się do normalnej prędkości scrollowania, możesz
    sprawdzić szczegółowe informacje na stronie pluginu z <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">Dokumentacją</a>.</p>

<p>Po wykonaniu wszystkich kroków, zobaczysz efekt Parallax na wybranym elemencie.</p>

