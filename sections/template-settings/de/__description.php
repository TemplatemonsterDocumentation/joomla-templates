<h2 class="item7">Template Einstellungen</h2>

<p>Jede Vorlage hat ihre eigenen Konfigurationsoptionen. Man kann die anpassen, um Layout
    Klassen, Typografie Tags, zusätzliche Funktionen usw. der Theme zu einstellen. Template
    Optionenn kann man durch den Admin-Bereich via <b>Erweiterungen > Vorlagenverwaltung</b>
    (<b>Extensions > Template Manager</b>) zugreifen</p>
<figure class="img-polaroid">
    <img src="img/template-settings-1.jpg" alt="">
</figure>

<p> Im Bereich „Vorlagenverwaltungsind“ wöhlen Sie die Theme aus, sie Sie bearbeiten
    möchten. </p>

