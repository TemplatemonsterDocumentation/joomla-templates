<h3>Layout</h3>

<h6>Mit den Lazout Optionen können Sie die Haupteinstellungen vom Theme-Layout
    verändern:</h6>

<p><b>Theme Layout</b> - wählen Sie, ob die Theme fixiert (Breite in Pixeln) oder
    zirkuliert (Breite in Prozent) sein sollte;</p>

<p><b>Logo</b> - wählen Sie hier das Bild des Logos aus;</p>

<p><b>Logo Width</b> (Logo Breite) – wählen Sie hier die Breite vom Logo- Container aus
    (nach dem Bootstrap- Raster);</p>

<p><b>Sidebar Width</b> (Sidebar Breite)- legen Sie die Breite den Sidebars fest;</p>

