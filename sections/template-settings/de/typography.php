<h3>Typografie</h3>

<p> Dank der Optionen von Typografie können Sie Tags für Artikeltitels aus verschiedenen
    Ansichten (besonderen Artikel, Kategorieansicht, Blog-Ansicht, Ansicht vom einzigen
    Artikel usw) auswählen. Um die Überschrift zu definieren, kann man verschiedene HTML
    Tags verwenden (h1, h2, h3, h3 etc). Tags sind verschieden wegen der logischen
    Bedeutung und des Stils. <a href="http://www.w3schools.com/tags/tag_hn.asp"
                                target="_blank">Mehr erfahren</a></p>

