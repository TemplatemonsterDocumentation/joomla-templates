<h3>Usare l'effetto Parallax</h3>

<p>L'effetto Parallax può essere aggiunto al template grazie al plugin jQuery <a
        href="http://markdalgleish.com/projects/stellar.js/" target="_blank"
        rel="nofollow">Stellar</a>.</p>

<p>Per aggiungere l'effetto Parallax alla pagina, naviga su "Template manager ->
    Templates", clicca sul nome del template che stai usando (ThemeXXXX Details and
    Files) e apri il file "index.php". Seleziona il blocco a cui vuoi aggiungere
    Parallax, ad esempio "folio-row", aggiungi la proprietà</p>
<code><code>data-stellar-background-ratio="[valore]"</code></code>

<p>dove [valore] è il rapporto relativo alla velocità di scorrimento naturale. Puoi
    trovare ulteriori informazioni sulla pagina <a
        href="http://markdalgleish.com/projects/stellar.js/docs/" target="_blank"
        rel="nofollow">Documentation</a> del plugin.</p>

<p>Dopo aver completato tutte le fasi, vedrai l'effetto Parallax sul blocco
    specificato.</p>

