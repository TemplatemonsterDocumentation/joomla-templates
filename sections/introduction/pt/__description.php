<h2 class="item1">Introdução</h2>

<h5>Obrigado por comprar o template Joomla. Essa documentação consiste em várias partes e lhe mostra o processo inteiro
    de como configurar e administrar o site em Joomla do zero. Nós demos o nosso melhor para fazer esse manual o mais
    claro e fácil de se seguir possível. </h5>


<h3>O que é Joomla CMS?</h3>

<p>Joomla CMS é uma solução open source para criação de conteúdo rico para websites. Ele lhe permite construir sites e
    aplicativos online poderosos e há praticamente nenhuma necessidade de possuir habilidades técnicas ou conhecimento
    para gerenciar. Em muitos aspectos, incluindo sua facilidade de manuseio e extensibilidade, têm feito o Joomla ser o
    software para website mais popular disponível.
    <a href="http://www.joomla.org/about-joomla.html" target="_blank">Leia Mais</a>
</p>

<h3>O que é template Joomla</h3>

<p>Template Joomla é um skin para a sua plataforma CMS do Joomla. Em outras palavras, você pode facilmente alterar a
    aparência do seu site Joomla instalando um novo template em poucas e fáceis etapas. Com toda sua simplicidade, o
    template Joomla é fornecido com todos os arquivos fontes necessários e você pode editá-lo ou extendê-lo do jeito que
    você precisar.</p>

