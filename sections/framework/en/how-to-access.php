  <p><img src="img/t3-1.jpg" alt="">
  </p></figure>
  <p>Go to the <strong>General</strong> tab.</p>
  <figure class="img-polaroid">
  <p><img src="img/t3-2.jpg" alt="">
  </p></figure>


<p>The following settings will be applied for all styles, themes and layouts</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Development Mode</strong>: when you are developing your site, you should enable this option. Once it's turned on, your site directly runs on original files - LESS files. Enabling this option will decrease your site speed so when you finish developing, turn the option off. When you are not developing your site, you should disable the option. This will increase your site speed. In this mode, your site runs on CSS, not LESS. The CSS files are compiled from LESS files.</div>
 </li>
 <li><strong>ThemeMagic</strong>: enable the option &quot;Enable ThemeMagic .</li>
</ol>
<div class="alert alert-info">
<p><strong>Note</strong>: This option should be enabled only when you customize your theme using ThemeMagic, after you finish your customization, you should turn it off.</p></div>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Legacy Compatible</strong>: this option will make your site compatible with Bootstrap 2. For example, you use some extensions that integrates Bootstrap 2. To make the extensions work in your site, it will load some CSS of Bootstrap 2. It will load font awesome version 3 as well..</div>
  </li>
  <li>
    <div align="justify"><strong>Responsive</strong>: when the option is disabled, your site is a non-responsive site and you can set the fixed width size for your site. Once it's disabled, all responsive files will not be loaded.</div>
  </li>
  <li> 
    <div align="justify"><strong>Build RTL CSS</strong>: when compile LESS to CSS, you can select to build CSS for RTL language or not. To active this option, please enable option and run the Compile LESS to CSS.</div>
  </li>
  <li>
    <div align="justify"><strong>T3 Assets Folder</strong>: when enabled, the optimized and joined CSS files will be located in the defined T3 Assets Folder folder. By default, this folder is t3-assets..</div>
  </li>
  <li>
    <div align="justify"><strong>Show T3 Logo</strong>: 	enable the option to show the T3 Logo in the footer.</div>
  </li>
  <li>
    <div align="justify"><strong>Optimize CSS</strong>: when you enable this option, compressed CSS files will be used (.min.css files).</div>
  </li>
  <li>
    <div align="justify"><strong>Optimize JS:</strong> combined and compress Javascript files. </div>
  </li>
  <li>
    <div align="justify"><strong>Link Title for Article View</strong>: override setting for Link Title in Article View. This setting only applies for Single Article view.</div>
  </li>
</ol>