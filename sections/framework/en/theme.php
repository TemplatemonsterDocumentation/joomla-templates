<h3>Theme Settings</h3>
<p>Go to the<strong> Theme </strong>tab.</p>
<figure class="img-polaroid">
  <p><img src="img/t3-3.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Theme</strong>: select a theme for current style. Each style goes with one theme.</div>
  </li>
  <li><strong>Logo Type</strong>: choose Logo type (Text\Image).</li>

  <li> <strong>Site Name</strong>: specify your site name.</li>
  <li><strong>Slogan</strong>: enter a slogan for your site.
  </li>
  <li><strong>Logo Image</strong>: upload the image for Logo. </li>
  <li><strong>Enable Small Logo</strong>: enable this option to allow select a new version logo for small screen. </li>
  <li><strong>Display footer logo</strong>: 	enable the option to show the Logo in the footer. </li>
  <li><strong>Footer logo width</strong>: set the footer Logo width.
  </li>
  <li><strong>Footer logo image:</strong> upload the image file for the footer Logo. </li>
  <li><strong>Footer text logo</strong>: enter the text for Logo. Works with  text logo type.</li>
  <li><strong>Footer slogan</strong>: enter the slogan for your site.</li>
  <li><strong>Footer after logo text</strong>: specify the text which will be displayed after the logo.</li>
</ol>
