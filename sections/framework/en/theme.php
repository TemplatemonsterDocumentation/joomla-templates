</figure>
<ol class="marked-list">
  <li><strong>Privacy Policy Link</strong>: enable the Privacy Policy link.</li>
  <li><strong>Privacy Policy Item</strong>: specify the Privacy Policy link.</li>
  <li><strong>Privacy Policy Label</strong>: enter the label for the Privacy Policy item.</li>
</ol>
