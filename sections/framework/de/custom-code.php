<h3>Custom Code Tab</h3>
<p>Go to the<strong> Custom Code </strong>tab.</p>
<p>Add custom code to some special positions of webpage. Those markup will not filter. Please be careful when copy code from other websites.</p>
<figure class="img-polaroid">
  <p><img src="img/t3-13.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong>After &lt;head&gt;</strong>: add custom code right after open &lt;head&gt; tag.</li>
  <li><strong>Before &lt;/head&gt;</strong>:  add custom code before closing &lt;/head&gt; tag.</li>

  <li><strong>After &lt;body&gt;</strong>: add custom code right after open &lt;body&gt; tag.</li>
  <li><strong>Before &lt;/body&gt;</strong>: add custom code before closing &lt;/body&gt; tag. </li>
  <li><strong>Show debug module position</strong>: add modules in debug position before closing &lt;/body&gt; tag. </li>
</ol>
