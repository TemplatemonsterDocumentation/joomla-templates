<h3>Theme Magic</h3>
<p>You have to enable ThemeMagic first, then click on the ThemeMagic button to open ThemeMagic panel.</p>
<figure class="img-polaroid">
  <p><img src="img/t3-23.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong></strong> Select theme to customize.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-25.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li>Customize theme by changing parameter values.    </li>
</ol>
<p><strong>Typo</strong><span> (not available in all themes)</span></p>
<ol class="marked-list">
  <li><strong>Base font size</strong>: set the main text font size in pixels only. </li>
  <li><strong>Base line height</strong>: set the base line height.</li>
  <li><strong>Font Family base/Font Family secondary</strong>: choose a font family.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-27.jpg" alt="" /> </p>
</figure>
<p><strong>Color</strong></p>
<ol class="marked-list">
  <li><strong>Brand Primary Color</strong>: the color must be specified in hexadecimal format (for example, #aabbcc). Also, it is possible to select a color from a palette provided.</li>
  <li><strong>Brand secondary color</strong>: the color must be specified in hexadecimal format (for example, #aabbcc). Also, it is possible to select a color from a palette provided.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-28.jpg" alt="" /> </p>
  
</figure>
<p><strong>Grid</strong></p>
<ol class="marked-list">
  <li><strong>Large Desktop Width</strong>: set the large desktop width in pixels only. </li>
  <li><strong>Gutter Width</strong>: set the gutter width in pixels only.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-28.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li>Click on preview to see the changes.    </li>
  <li>Save or save as copy the customized theme.</li>
</ol>
