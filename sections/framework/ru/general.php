<p>Следующие настройки будут применяться ко всем стилям, темам и макетам.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Development Mode</strong>: опцию следует активировать при разработке сайта. После активации опции будут использоваться LESS  файлы, что уменьшит скорость загрузки сайта,но увеличит скорость разработки. После окончания процесса разработки следует отключить опцию. И ваш сайт будет снова использовать  CSS, вместо LESS. Файлы LESS компилируются в CSS.</div>
  </li>
  <li><strong>ThemeMagic</strong>: включите функцию ThemeMagic.</li>
</ol>
<div class="alert alert-info">
  <p><strong>Note</strong>: данная функция должна быть отключена, после того как вы завершите редактирование темы.</p>
</div>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Legacy Compatible</strong>: включите опцию, для совместимости с Bootstrap 2. Необходимо использовать, если установленный модуль использует Bootstrap 2.</div>
  </li>
  <li>
    <div align="justify"><strong>Responsive</strong>: включите опцию для возможности использовать (настраивать) мобильную версию.</div>
  </li>
  <li>
    <div align="justify"><strong>Build RTL CSS</strong>: при компиляции LESS в CSS, вы можете выбрать стоит ли создавать CSS для языка RTL. Для использование данной опции следует активировать опцию  Compile LESS to CSS.</div>
  </li>
  <li>
    <div align="justify"><strong>T3 Assets Folder</strong>: если опция включена, оптимизированные и присоединенные CSS файлы будут располагаться в обозначенной T3 Assets Folder папке. По умолчанию  это папка — t3-assets.</div>
  </li>
  <li>
    <div align="justify"><strong>Show T3 Logo</strong>: 	вы можете отключить отображения лого &quot;T3 Framework&quot; в футере вашего сайта.</div>
  </li>
  <li>
    <div align="justify"><strong>Optimize CSS</strong>: включите опцию для использования сжатых CSS файлов (.min.css файлы).</div>
  </li>
  <li>
    <div align="justify"><strong>Optimize JS:</strong> опция поможет использовать сжатые Javascript файлы. </div>
  </li>
</ol>
