<h3>Theme Magic</h3>
<p>Вы сможете воспользоваться возможностями функции ThemeMagic, только после ее активации для вашей темы. </p>
<p>Для того чтобы перейти к окну настроек ThemeMagic кликните на одноименную кнопку расположенную на панели инструментов.</p>
<figure class="img-polaroid">
  <p><img src="img/t3-23.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong></strong> Выберите тему, в которой хотите произвести изменения, из списка опции.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-25.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li>Настраивайте тему, изменя параметры следующих опций.    </li>
</ol>
<p><strong>Typo</strong></p>
<ol class="marked-list">
  <li><strong>Base font size</strong>: задайте размер шрифта для базового текста сайта. </li>
  <li><strong>Base line height</strong>: задайте высоту строки базового текста.</li>
  <li><strong>Font Family base/Font Family secondary</strong>: выберите тип шрифта.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-27.jpg" alt="" /> </p>
</figure>
<p><strong>Color</strong></p>
<ol class="marked-list">
  <li><strong>Brand Primary Color</strong> (основной цвет): цвет должен быть определен в шестнадцатеричном формате (напр., #aabbcc). Также, возможно выбрать нужный цвет из предоставленной цветовой палитры.</li>
  <li><strong>Brand secondary color</strong> (вспомогательный цвет):  цвет должен быть определен в шестнадцатиричном формате (напр., #aabbcc). Также, возможно выбрать нужный цвет из предоставленой цветовой палитры.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-28.jpg" alt="" /> </p>
  
</figure>

<ol class="marked-list">
  <li>Нажмите на кнопку  &quot;Рreview&quot;, чтобы увидеть изменения.    </li>
  <li>Сохраните изменения. Вы также можете сохранить копию измененной темы.</li>
</ol>
