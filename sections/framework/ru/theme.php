<h3>Настройки темы (Theme Settings)</h3>
<p>Перейдите во вкладку <strong> Theme</strong>.</p>
<figure class="img-polaroid">
  <p><img src="img/t3-3.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Theme</strong>: выберите стиль для текущей темы.</div>
  </li>
  <li><strong>Logo Type</strong>: выберите тип логотипа (текстовый лого\изображение).</li>

  <li> <strong>Site Name</strong>: укажите название сайта.</li>
  <li><strong>Slogan</strong>: введите слоган для вашего сайта.
  </li>
  <li><strong>Logo Image</strong>: загрузите изображение для логотипа сайта. </li>
  <li><strong>Enable Small Logo</strong>: активируйте опцию для того, чтобы отображать уменьшенный вариант логотипа для маленьких экранов. </li>
  <li><strong>Display footer logo</strong>: 	активируйте опцию для отображения логотипа в футере сайта. </li>
  <li><strong>Footer logo width</strong>: задайте значение ширины логотипа в футере.
  </li>
  <li><strong>Footer logo image:</strong> загрузите файл изображения логотипа для футера. </li>
  <li><strong>Footer text logo</strong>: введите текст логотипа в футере. Только при выборе типа &quot;текстовый логотип&quot;.</li>
  <li><strong>Footer slogan</strong>: введите слоган вашего сайта.</li>
  <li><strong>Footer after logo text</strong>: введите текст, который будет отображаться после основного лого.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-11.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong>Privacy Policy Link</strong>: активируйте ссылку Privacy Policy (&quot;Все права защищены&quot;) в футере сайта.</li>
  <li><strong>Privacy Policy Item</strong>: определите объект на который будет указывать ссылка Privacy Policy.</li>
  <li><strong>Privacy Policy Label</strong>: введите заголовок для ссылки Privacy Policy.</li>
</ol>
<figure class="img-polaroid">
  <p><img src="img/t3-12.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong>Left Sidebar Width</strong>: укажите ширину левого сайдбара.</li>
  <li> <strong>Right Sidebar width</strong>: укажите ширину правого сайдбара.</li>
  
</ol>
