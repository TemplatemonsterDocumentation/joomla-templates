<h3> Вкладка Features </h3>
 <p>Перейдите во вкладку <strong> Features</strong>.</p>
  <figure class="img-polaroid">
  <p><img src="img/t3-18.jpg" alt="">
  </p></figure>
<ol class="marked-list">
  <li><strong>To Top button</strong>: включите для отображения кнопки <strong>To Top</strong>.</li>
 <li><strong>Totop text</strong>:  введите текст кнопки.</li>

  <li><strong>To Desktop button</strong>: активируйте опцию для отображения кнопку <strong>To Desktop</strong> на мобильных устройствах.</li>
  <li>
    <div align="justify"><strong>Todesktop text</strong>: введите текст для кнопки <strong>To Deskptop</strong>.</div>
  </li>
  <li><strong>Tomobile text: </strong>введите текст, который будет отображаться на кнопке  <strong>To Mobile</strong>. </li>
  <li><strong>jQuery Black&amp;White plugin</strong>: вы можете включить jQuery Black&amp;White плагин (принцип действия плагина: при наведении курсора на изображение, цветовая схема последнего меняется на черно-белую). </li>
  <li><strong>Invert hover effect</strong>: при включении опции эффект плагина jQuery Black&amp;White будет срабатывать наоборот. </li>
  <li><strong>Animation in speed</strong>: установите скорость  анимации.</li>
  <li>
    <div align="justify"><strong>Animation out speed:</strong> установите скорость возвращения к нормальному состоянию.</div>
  </li>
  <li><strong>Offline Countdown Time</strong>: установите конечную дату для таймера.</li>
</ol>
