<h3>Вкладка Custom Code </h3>
<p>Перейдите во вкладку <strong> Custom Code</strong>.</p>
<p>Эта функция поможет добавить пользовательский код в код вашего сайта. </p>
<figure class="img-polaroid">
  <p><img src="img/t3-13.jpg" alt="" /> </p>
</figure>
<ol class="marked-list">
  <li><strong>After &lt;head&gt;</strong>: добавьте нужный код после &lt;head&gt;.</li>
  <li><strong>Before &lt;/head&gt;</strong>:  добавьте нужный код перед &lt;/head&gt;.</li>

  <li><strong>After &lt;body&gt;</strong>: добавьте нужный код сразу после &lt;body&gt;.</li>
  <li><strong>Before &lt;/body&gt;</strong>: добавьте нужный код перед &lt;/body&gt; tag. </li>
</ol>
