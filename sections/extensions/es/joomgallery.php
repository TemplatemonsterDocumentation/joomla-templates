<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> es un componente que permite crear una galería de fotos completa basada en Joomla! No dude en consultar más detalles en la <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">página web oficial</a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Creación de Categoría</p></h5>

<p> Vaya a <strong>Componentes</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Gestionar Categoría</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Pulse en el boton <strong>Nuevo</strong> en la esquina superior izquierda: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
  <p align="justify">Categoría de entrada <strong>Título</strong> y <strong>Descripción</strong> con imágenes y ajustes <strong>Guardar</strong> . También puede especificar <strong> Meta Descripción</strong> para esta categoría y establecer restricciones de acceso si es necesario.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-9.jpg" alt="">
  </figure>
    
</p>
<p>Al configurar, puede hacer frente a los ajustes en una tabla a continuación:</p>
<table width="980"  class="options-table">
    <tr>
        <td width="154" class="col-1"> <strong>Alias </strong></td>
        <td width="29">-</td>
        <td width="781"><div align="justify">Deje esto en blanco y JoomGallery rellenará un valor predeterminado, que es la ruta de la categoría en minúsculas y con guiones en lugar de espacios. Puede introducir el Alias manualmente. Si lo hace, use letras minúsculas y guiones [-]. No se permiten espacios ni subrayados. El Alias se utilizará en la URL SEF. </div></td>
  </tr>
    <tr>
        <td class="col-1"><strong>Oculto</strong></td>
