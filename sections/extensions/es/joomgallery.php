<h5><p>Intentemos<strong>Subida de Imagen</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Pulse el botón <strong>Navegar</strong> en la sección <strong>Selección de imágenes</strong> y seleccione archivos multimedia en su ordenador. </div>
  </li>
  <li>
    <div align="justify">Especifique <strong>Categoría</strong> para estas imágenes [puedes subir un montón de imágenes para cada categoría de esta manera], <strong>Título Genérico</strong> para estas imágenes [por ejemplo, si subes 3 archivos con el título genérico &quot;test&quot;, aparecerán en tu Administrador de imágenes como test_1, test_2 y test_3]. </div>
  </li>
  <li>
    <div align="justify">Especificar <strong>Descripción genérica</strong> y <strong>Autor</strong> si es necesario. Selecciona el tipo de <strong>Acceso</strong> : publico, super usuario, registrado, especial. </div>
  </li>
  <li>
    <div align="justify">Utilizar la opción <strong>Archivos de imagen especiales</strong>. Esta opción permite la carga de archivos de imagen especiales. Sólo elija esta opción en caso de que su carga se compone exclusivamente de archivos .png o .gif animados o transparentes. Tenga en cuenta que los archivos no se vuelven a clasificar según el tamaño y, por lo tanto, se muestran en tamaño completo en la vista de detalle.</div>
  </li>
  <li>
    <div align="justify"><strong>Modo Debug</strong>. Con activado <strong>modo debug</strong> Se mostrarán todos los pasos de procesamiento. En caso de errores se muestran siempre.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Por favor, haga clic en el botón <strong> Subir</strong> en la parte inferior después de completar el proceso de configuración.</p>
</div></div>
<h5><p>Edición de un elemento de Galería existente</p></h5>
<div align="justify">Vaya a la sección <strong>Componentes</strong> > <strong>JoomGallery</strong> > <strong>Administrador de imágenes</strong>. Aquí puede encontrar la lista de imágenes que ya se cargaron, para que pueda editar las existentes: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p>En caso, de que necesite <strong> modificarlos</strong>, debe realizar lo siguiente: </p>
<ol class="marked-list">
  <li>Abra cualquier elemento de la lista. </li>
  <li>Cambie <strong> Título</strong>, <strong>Categoría</strong>, <strong>Descripción</strong>, <strong>Accesos</strong>, etc. en la pestaña <strong> Imagen</strong> . Desplácese hacia abajo para <strong>Vista previa de miniaturas</strong> y <strong>Vista previa de la imagen</strong>. </li>
  <li>
    <div align="justify">Use la opción <strong>Ocultar</strong>. Con la ocultación de esta imagen no se mostrará en la vista de categoría ni se reconocerá por la búsqueda o las listas superiores más, pero todavía estará disponible a través de la vinculación directa. Si no desea que esté disponible, por favor, deshaga la publicación en su lugar. Al ocultar las imágenes es posible utilizarlas como miniaturas de categorías sin mostrarlas en la categoría correspondiente, por ejemplo.</div>
  </li>
  <li>
    <div align="justify"><strong>Destacada </strong>opción. Aquí puede marcar una imagen como "Destacados". Por ejemplo, el módulo JoomImages puede configurarse para mostrar sólo imágenes destacadas en una posición de módulo.</div>
  </li>

<p>
  <figure class="img-polaroid">
