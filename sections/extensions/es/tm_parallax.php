<h3>TM Parallax Module</h3>

<p>TM Parallax es un módulo que usa secuencia RD Parallax script para implementar el efecto de paralaje en la plantilla.</p>

<h6>Todas las opciones de los módulos necesarios están disponibles en configuraciones del módulo:</h6>

<ol class="index-list">
    <li>Ajustes de imagen;</li>
    <li>Ajustes de la velocidad del efecto de paralaje;</li>
    <li>Parámetros de la dirección del efecto de paralaje.</li>
</ol>
