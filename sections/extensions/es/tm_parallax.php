<div class="alert alert-warning">
    <div align="justify">En nuestras plantillas <strong>TM Parallax Module</strong> el contenido se presenta cargando un determinado módulo, por ejemplo, "{loadmodule articles_single, Home parallax article}". Para evitar el Error 404 en su sitio web, asegúrese de que el nombre del módulo cargado idéntico al especificado para la carga. En este caso, el módulo "<strong>Home parallax article</strong>" debe ser publicado. El nombre del módulo no debe contener más de un espacio. </div>
          Puede obtener más información sobre loadmodule/loadposition en la <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">página web oficial</a>.
