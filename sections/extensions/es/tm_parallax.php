  <p align="justify">Para editar el módulo <strong>Home parallax article</strong>, vaya a la pestaña <strong>Extensiones</strong> &gt; <strong>Modulos</strong>. Busque utilizando el campo de búsqueda <strong>Home parallax article</strong>. Para abrir los ajustes del módulo <strong>Home parallax article</strong>, haga clic en su título.</p>
