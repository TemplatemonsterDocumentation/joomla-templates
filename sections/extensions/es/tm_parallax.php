      <li>Para abrir cualquier configuración de modificación necesaria, haga clic en su título. [Editemos el módulo <strong>Home parallax</strong>.]</li>
<li>
  <div align="justify">El módulo <strong> TM Parallax </strong>la interfaz de edición se abre delante de usted.            
  </div>
