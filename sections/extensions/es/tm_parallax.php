<h3>TM Parallax Module</h3>

<p><strong>TM Parallax</strong> es un módulo que utiliza la secuencia de comandos Parallax RD para implementar la funcionalidad de paralaje en la plantilla. </p>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-20.jpg" alt=""></p></figure>
<p align="justify">El módulo estará ahora disponible en el Administrador de Módulos <strong>Extensions</strong> en el menú superior de la interfaz de administración. Puede crear módulos adicionales haciendo clic en el botón <strong>Nuevo</strong>. Seleccione los módulos que ya ha realizado para configurarlos. No olvide publicarlos en la posición correcta y en las páginas correctas.</p>

<h5><p>Con el fin de configurar el módulo <strong>TM Parallax </strong>, realice los siguientes pasos:</p></h5>
<li><p>Vaya a la pestaña <strong>Extensiones</strong> &gt; <strong>Módulos</strong>:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>A continuación, seleccione el módulo <strong> TM Parallax </strong>para editar.</p>
      </li>
