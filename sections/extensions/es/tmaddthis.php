<h3>TM Addthis</h3>

<p>TM Addthis - plugins que permiten mostrar los botones sociales para "Compartir".</p>

<figure class="img-polaroid"><img src="img/addthis-1.jpg" alt=""></figure>

<p>Aqu&iacute; puedes ver las configuraciones del plugin TM Addthis. Introduzca el ID de
    su perfil en el campo ID de perfil. Para obtener el ID de perfil, es necesario crear
    una cuenta y un perfil en la siguiente p&aacute;gina <a
        href="https://www.addthis.com/settings/publisher" target="_blank"
        rel="nofollow">https://www.addthis.com/settings/publisher</a>.</p>
