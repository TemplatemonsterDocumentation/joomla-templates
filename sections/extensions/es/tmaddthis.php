<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> es la plataforma de intercambio de contenido más grande del mundo. Ofrece herramientas que facilitan el intercambio de contenido a través de la web social, y proporciona a los editores un mayor tráfico y parálisis en profundidad. <Strong> AddThis</strong> puede integrarse en un sitio web con el uso de un widget web. Una vez que se añade el widget, los visitantes del sitio web pueden marcar un elemento con una variedad de servicios, como Facebook, MySpace, Google Bookmarks, Pinterest, and Twitter.</p>
<p><strong>TM Addthis </strong>es un plugin para mostrar los botones sociales de "compartir":</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br />
Añadir nuestros botones de acciones para Joomla a su sitio web de Joomla es una gran manera de obtener más de su contenido distribuido en línea. Cuanto más se comparta su contenido, más tráfico volverá a su sitio. AddThis botones de compartir son fáciles de agregar, y también proporcionan análisis detallado que le ayudarán a convertirse en un editor más eficaz. </p>
<p><strong>CARACTERISTICAS</strong></p>
<ul>
  <li>Elija dónde aparecerán los botones compartidos en su página;</li>
  <li>Controla cuántos botones desea mostrar;</li>
  <li>Incluir cuenta  de las acciones de compartir para animar;</li>
  <li>Personalizar los botones de uso compartido para cada usuario o recogelos usted mismo.</li>
</ul>
<p align="justify">Para personalizar el plugin, vaya a <strong>Extensiones</strong> &gt; <strong>Plugins</strong>. Esto le llevará a <strong>Administrador de Plugins</strong>. Tipo &quot;Añadir&quot; en el campo de búsqueda y verá el <strong>Content - TM AddThis</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>Primero tiene que <strong>activar</strong> el plugin. Pulse el boton rojo &quot;X&quot; para activarlo. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Una vez que el plugin está <strong>activado</strong>, verá un mensaje de confirmación.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Para obtener la ventana principal de configuración del plugin, presione el enlace de título <strong>Content - TM AddThis</strong> y usted será llevado a la siguiente ventana:</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Aquí puede ver los principales <strong>Ajustes de TM Addthis</strong>. Selecciona las<strong> categorías a excluir</strong> en el campo correspondiente. En el campo <strong>ID de Perfil</strong> ingrese su ID de perfil que puede obtener después de registrarse y crear una cuenta en la siguiente página: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>Básicamente, usted debe obtener un nuevo <strong>ID de Perfil</strong> para cambiar<strong>AddThis</strong> los íconos sociales:</p>
<p>Después de registrarse, debe añadir botones <strong>compartir</strong>. </p>
<p>Para ello, pulse el botón &quot;<strong>AddThis</strong>&quot; en la barra de herramientas de perfil.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Entonces debe presionar el botón &quot;<strong>Empezar</strong>&quot;  en la sección Botones de Compartir.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Active <strong>En línea </strong>Botones de Compartir.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">Debe elegir tipo <strong>Contador de Acciones</strong>. También puede seleccionar sus propias redes sociales y personalizar el pedido utilizando la opciónn <strong>Seleccione su propio</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Cambiar el diseño de los iconos en la sección <strong>Diseño</strong>. Personalizar <strong>Estilo</strong>, <strong>Tamaño del Botón</strong> y <strong>Colores</strong> de los íconos. Establecer <strong>Esquinas Redondeadas</strong> parámetro y <strong>ocultar nombres de red</strong> si es necesario.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Cuando termines con personalización de <strong>Botones Compartir</strong>, pulse <strong>Guardar&amp;Continuar</strong> vaya al siguiente paso.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Copie su ID de perfil en la sección &quot;<strong>1</strong>&quot; . </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>Debe insertarlo en el campo de  <strong>ID de Perfil</strong> en ventana de configuración <strong>Contenido - TM AddThis</strong> para que aparezcan los nuevos <strong>Botones de Comparir</strong> en su sitio.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Tiene y tiene la opción de excluir categorías [páginas] en las que no desea colocar el plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Asegúrate de tener <strong>activado</strong>el plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Guarde los cambios y actualize la página <strong>Botones Compartir</strong>. </p>
