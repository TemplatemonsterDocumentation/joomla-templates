<h3>Komento Comments</h3>

<p>Komento es un componente de comentarios para Joomla. Le permite añadir comentarios a
    las entradas de Joomla</p>

<figure class="img-polaroid">
    <img src="img/komento-1.jpg" alt="">
</figure>

<p>Se puede encontrar el componente Komento a través del panel de administración de
    Joomla en la sección Components > Komento (Componentes > Komento).</p>

<p>Para encontrar los ajustes de configuración, por favor haga clic en Components >
    Komento > Integrations (Componentes > Komento > Integraciones) y elija Articles.</p>

<figure class="img-polaroid">
    <img src="img/komento-2.jpg" alt="">
</figure>

<p>Con la ayuda de la configuración del componente Usted puede activar comentarios para
    categorías específicas, añadir captcha, perfiles de usuario etc. Usted puede
    aprender más sobre Komento en <a href="http://stackideas.com/docs/komento.html"
                                     target="_blank">el sitio web oficial</a></p>

<p>Nuestras plantillas tienen sólo la versión estándar de Komento incluida. Para obtener
    acceso a características adicionales y obtener servicios de soporte completos para
    la extensión Komento nosotros recomendamos comprar <a
        href="http://stackideas.com/komento/plans">Komento Profesional</a>. Lista
    completa de características disponibles <a
        href="http://stackideas.com/komento?ref=kmfeature">aquí</a>.</p>