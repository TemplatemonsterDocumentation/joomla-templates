<h3>Komento Comments</h3>

<p><strong>Komento</strong> es una extensión avanzada de comentarios para Joomla que permite a los visitantes de su sitio web comentar artículos y blogs.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Comments зueden evaluarse en la pestaña <b>Componentes > Komento</b> de su panel de Administración de Joomla.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Cómo administrar las integraciones de componentes de Komento</p></h5>
<ol class="marked-list">
  <li>
    <p>Inicie sesión en su panel de administración de Joomla, vaya a <strong>Componentes</strong> &gt; <strong>Komento</strong> &gt; <strong>Integraciones</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Haga clic en <strong>Joomla Article</strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">En la pestaña General puede <strong>Activar</strong> o <strong>Desactivar</strong> comentarios. También puede cambiar <strong>Moderación</strong> y otros ajustes. </p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li><p>Hemos <strong>desactivado</strong> [No] comentarios [Mira la captura de pantalla abajo]. </p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>El bloque de comentarios se desactiva en el sitio web.</p></li>
  <p><strong>Activado</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Desactivado</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Hemos <strong>activado </strong> comentarios de nuevo. Puede asignar Komento a todas las categorías o seleccionar categorías particulares. </p>
  </li>
  <li>Hemos asignado comentarios a &ldquo;<strong>Companía</strong>&rdquo;.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Usted también puede <strong>desactivar</strong> comentarios para un artículo en particular. Abra el artículo en el <strong>Article Manager [Contenido &gt; Artículos]</strong> e inserte el siguiente código en el cuerpo del artículo:</p>
    <pre>{KomentoDisable}</pre>
