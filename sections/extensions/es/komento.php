<h3>Komento Comments</h3>

<p>Komento es un componente de comentarios para Joomla. Le permite añadir comentarios a las 
entradas de Joomla:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Se puede acceder a Komento Comments desde la pestaña <b>Componentes > Komento (Components > Komento)</b> de su panel de Administración de Joomla.</p>

<p>Para acceder a los ajustes de configuración, abra la pestaña <b>Componentes > Komento > Integraciones (Components > Komento >
        Integrations)</b> y haga clic en <b>Artículos (Articles)</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>Mediante la configuración del componente Usted puede activar comentarios para categorías específicas, añadir captcha, perfiles de usuario etc. Usted puede aprender más sobre Komento en la <a
        href="http://stackideas.com/docs/komento.html" target="_blank">página oficial</a>.</p>

<p>Nuestras plantillas tienen sólo la versión estándar de Komento incluida. Para obtener acceso a características adicionales y obtener servicios de soporte completo para la extensión Komento nosotros recomendamos comprar <a href="http://stackideas.com/komento/plans">Komento Professional</a>. Puede comprobar la lista completa de características en el siguiente sitio web: <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
   Con Komento Professional también recibirá soporte técnico completo.</p>
