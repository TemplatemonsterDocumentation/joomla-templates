</ol>
<h5><p>Como editar Komento &quot;Términos y Condiciones&quot;</p></h5>
  <p align="justify">Komento <strong>Términos y Condiciones</strong> texto se muestra en páginas de un solo artículo. Los visitantes del sitio deben estar de acuerdo con estos términos para poder enviar sus comentarios si esta opción está activada. Consulte la siguiente captura de pantalla: </p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Realice los pasos siguientes para cambiar el texto:</p>
<ol class="marked-list">
  <li>
    <p>Abra su panel de Administración de Joomla y navegue hasta el menu <strong>Componentes</strong> &gt; <strong>Komento</strong> &gt; <strong>Integraciones</strong>:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Abra componente &ldquo;<strong>Joomla Article</strong>&rdquo;. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p>Luego, cambie a sección  <strong>Diseño</strong> y vaya a la pestaña <strong> Formulario de Comentario</strong>:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>En primer lugar, seleccione el grupo de usuarios necesario en la parte inferior de la página, en la sección Activar Términos y Condiciones. </p>
  De forma predeterminada, se establece en Invitado, puede elegir cualquier otro grupo de usuarios. 
  <p>Activamos <strong>Términos y Condiciones</strong> A todos los usuarios 'tipo.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>A continuación, puede cambiar el texto del campo <strong>Términos y Condiciones</strong>. </p>
  Introduzca el texto deseado aquí y guarde los cambios pulsando el botón<strong> Guardar </strong> en la esquina superior izquierda.
  </li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

<p>El texto se cambió correctamente:</p>
<figure class="img-polaroid"><img src="img/komento-15.jpg" alt=""></figure>
</ol>
