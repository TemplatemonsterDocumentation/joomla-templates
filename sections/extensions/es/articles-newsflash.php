<h3>Articles Newsflash [Advanced]</h3>

<p align="justify">Se trata de una versión del valor predeterminado majorada del <strong>Módulo Joomla Articles Newsflash</strong>. Además de las funciones predeterminadas del módulo, tiene opciones de configuración adicional y módulos basados de plantilla en Bootstrap. Para añadir una gran cantidad de pulido de su diseño de sitio web basado en Joomla, este es un módulo de calidad dramáticamente superior para dar paso a su contenido. Fácil de usar, fácil de actualizar, con algunas opciones de personalizaciones grandes y potentes.</p>

