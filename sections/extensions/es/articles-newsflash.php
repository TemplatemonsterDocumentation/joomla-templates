<h4>Opciones Adicionales</h4>

<p><b>Number of columns/Número de columnas</b> - permite mostrar artículos en varias
    columnas.</p>

<p><b>Custom Link/Enlace personalizado</b> - le permite mostrar un vínculo personalizado
    bajo el contenido del módulo. El enlace puede estar relacionado con la dirección URL
    externa o con algún elemento del menú de Joomla.</p>

<p><b>Pretext</b> -permite mostrar un texto personalizado antes del contenido del
    modulo.</p>

<p><b>Published Date/Fecha de publicación </b> - muestra la fecha de publicación de la
    entrada. </p>

<p><b>Created by/Escrito por </b> - muestra el autor de la entrada.</p>

<p><b>Show intro images/Mostrar imágenes de intro </b> - permite mostrar la imagen de
    introducción de la entrada.</p>

<p><b>Intro image align/Alineación de imágenes de intro </b> - permite alinear las
    imágenes de introducción.</p>

