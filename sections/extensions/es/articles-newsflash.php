<h3>Artículos Newsflash</h3>

<p>El módulo Artículos Newsflash (Avanzado) es una versión mejorada del módulo
    predeterminado de Joomla Artículos Newflash. Además de las características
    predeterminadas del módulo, esta versión ofrece otras opciones de configuración y
    plantillas de módulos basados ​​en Bootstrap.</p>

