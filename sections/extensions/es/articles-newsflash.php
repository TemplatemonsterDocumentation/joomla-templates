<h3>Articles Newsflash (Advanced)</h3>

<p>The Articles Newsflash (Advanced) module es una versión mejorada del módulo predeterminado de Joomla Artículos Newflash. Además de las características predeterminadas del módulo, esta versión ofrece otras opciones de configuración y plantillas de módulos basados ​​en Bootstrap.:</p>

