    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Opciones Adicionales</h4>

<p><b>Número de columnas (Number of columns)</b> - permite mostrar artículos en varias columnas.</p>

<p><b>Enlace personalizado (Custom Link)</b> -  le permite mostrar un vínculo personalizado bajo el contenido del módulo. El enlace puede estar relacionado con la dirección URL externa o con algún elemento del menú de Joomla.</p>

<p><b>Pretexto (Pretext)</b> - permite mostrar un texto personalizado antes del contenido del modulo.</p>

<p><b>Fecha de publicación (Published Date)</b> - muestra la fecha de publicación de la entrada. </p>

<p><b>Escrito por (Created by (Автор))</b> - muestra el autor de la entrada.</p>

<p><b>Mostrar imágenes de intro (Show intro images)</b> - permite mostrar la imagen de introducción de la entrada.</p>

<p><b>Alineación de imágenes de intro (Intro image align)</b> - permite alinear las imágenes de introducción.</p>

