<p>Aqu&iacute; puedes ver los ajustes principales de TM Instagram. Introduzca el nombre
    del perfil de Instagram en el campo nombre de usuario, introduzca el número deseado
    de im&aacute;genes en el campo Número de fotos, configure el ID de la aplicaci&oacute;n
    de Instagram en el campo Tu Instagram ID de CLIENTE. Para de obtener el ID, siga al
    enlace <a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
              target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    donde usted necesita registrar una cuenta. Despu&eacute;s, siga este enlace <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> y cree la
    aplicaci&oacute;n. A continuaci&oacute;n obtendr&aacute; los datos de sus
    aplicaciones, donde podr&aacute;s ver el ID de CLIENTE.</p>

