<h3>TM Instagram</h3>
 
<p>TM Instagram es un módulo que permite mostrar el Widget de Instagram:</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Aquí puedes ver los ajustes principales de TM Instagram. Introduzca el nombre del perfil de Instagram en el campo nombre del usuario, introduzca el número deseado de imágenes en el campo Número de fotos, configure el ID de la Aplicación de Instagram en el campo Tu ID de CLIENTE de Instagram. Para de obtener el ID, siga al enlace 
<a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                 target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    y registrese en una cuenta. A continuación, siga el enlace
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a>  y cree la aplicación. Después de esto usted conseguirá los datos de la aplicación, donde se encuentra el 
ID del CLIENTE.:</p>
 
