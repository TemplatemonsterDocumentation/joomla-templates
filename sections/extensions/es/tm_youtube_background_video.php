<p class="alert alert-warning">
En nuestras plantillas el contenido de TM Youtube background video se presenta mediante la carga del módulo determinado o posición, por ejemplo, "{loadmodule articles_single, About us}". Con el fin de evitar que un error 404 en su sitio web, por favor asegúrese de que el nombre del módulo cargado coincide con el definido para cargar. En este caso, el módulo "About us" debe ser publicado y no debe contener más de un espacio en el título del módulo.
    Usted puede aprender más acerca de loadmodule/loadposition en la <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">página oficial</a>.
</p>
