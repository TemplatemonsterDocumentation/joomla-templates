<h3>Módulo de TM YouTube background video</h3>

<p><strong>TM YouTube background video</strong> es un módulo para agregar un video de fondo de youtube a su sitio web.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>Para configurar el módulo de vídeo de fondo de TM YouTube, realice los siguientes pasos:</p></h5>
<ol class="marked-list">
<li><p>Vaya a la pestaña <strong>Extensiones</strong> &gt; <strong>Modulos</strong>:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>A continuación, seleccione el módulo <strong>TM YouTube background video</strong> para editar.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Consejo:</strong> si quiere encontrar todos los módulos <strong>TM YouTube background video</strong>, utilizar los filtros para simplificar el proceso. Pulse el botón &quot;<strong>Buscar herramienta</strong>&quot; y seleccione &quot;<strong>TM YouTube background video</strong>&quot; escriba en la &quot;<strong>- Seleccionar Tipo -</strong>&quot; lista desplegable de filtros. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>Tiene que hacer click en &quot;<strong>Nuevo</strong>&quot; Botón para crear un nuevo módulo.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>To open any edit required module settings, click on  its title. [Vamos a editar el módulo <strong>Home youtube</strong> .]</li>
<li>
  <div align="justify">El módulo <strong>Home youtube </strong> la interfaz de edición se abre delante de usted. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Pulse el botón <strong>Artículo</strong> pra abrir la ventana modal de selección de artículos, como se muestra a continuación. Puede utilizar el filtro para simplificar el proceso de búsqueda.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>Para añadir un nuevo artículo <strong>Contenido</strong> &gt; <strong>Artículos</strong> &gt; <strong>Añadir nuevo Artículo</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Aquí puede encontrar <strong>Opciones de la pestaña</strong>, donde usted podrá cambiar:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Preparar Contenido</strong> – opcionalmente preparar el contenido con Joomla Contents Plugins. </p>
      </li>
      <li>
        <p><strong>Ingrese una URL de video de YouTube *</strong> – permite especificar la URL personalizada para el vídeo de YouTube que tiene que ser visible en la página.</p>
      </li>
      <li>
        <p><strong>Iniciar vídeo</strong> – el video empieza desde los segundos especificados.</p>
      </li>
      <li>
        <p><strong>Mudo</strong> – cambiar el video a modo silencioso o alto.</p>
      </li>
      <li><strong>Auto reproducción</strong> – el video se inicia automáticamente, una vez que se carga la página web.</li>
      <li><strong>Mostrar el botón de reproducción/pausa</strong> – puede mostrar u ocultar los botones.</li>
      <li> <strong>Reproducir/Pausar texto del botón</strong> – especifique el texto de los botones.</li>
      <li>
        <p><strong>Imagen del Móvil</strong> – Mostrar esta imagen en dispositivos móviles en lugar del video. [Para subir imágenes, vaya a sección <strong>Contenido </strong>&gt; <strong>Media</strong> .]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>Para cambiar el video, simplemente hay que <strong>reemplazar la URL del video</strong> con lo copiado previamente<strong>enlace de video de youtube</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Desplácese hacia arriba y haga clic en el botón verde. <strong>Guardar</strong> en la parte superior izquierda de la pantalla para aplicar los cambios:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Vuelva a su sitio web y actualice la página con el texto que ha cambiado. Ahora podemos ver que la edición fue exitosa. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">En nuestras plantillas <strong>TM Youtube background video</strong> el contenido se presenta cargando cierto módulo o posición, por ejemplo, "{loadmodule articles_single, About us}". Para evitar el Error 404 en su sitio web, asegúrese de que el nombre del módulo cargado es idéntico al especificado para la carga. En este caso, debe publicarse el módulo "Acerca de nosotros". El nombre del módulo no debe contener más de un espacio.
    Puede obtener más información sobre loadmodule/loadposition en la <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">página web oficial</a>.
</p>
