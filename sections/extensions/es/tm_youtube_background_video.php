<h3>Módulo TM Youtube background video</h3>

<p>TM Youtube background video es un módulo que permite añadir fondo de vídeo YouTube en el sitio.</p>

<h6>Todas las opciones necesarias están disponibles en la configuración del módulo:</h6>

<ol class="index-list">
    <li>Enlace del video de Youtube;</li>
    <li>Ajustes de reproducción de vídeo;</li>
    <li>Ajustes de la pantalla en los dispositivos móviles;</li>
    <li>Ajustes de los botones ”Play/Stop”.</li>
</ol>
