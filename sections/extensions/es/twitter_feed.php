<h5>Encuentra tu ID de widget de Twitter</h5>
<p align="justify">Si el widget de Twitter que intenta activar ya solicita una ID de widget de Twitter, deberá crear un nuevo widget <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">ajustes de página de widget de Twitter</a>.</p>
<p align="justify">Para acceder a la configuración del widget de Twitter, inicie sesión en la cuenta de Twitter que desee utilizar con su widget. A continuación, haga clic en el engranaje en la esquina superior derecha y haga clic en <strong>Ajustes</strong>. En la barra lateral de los ajustes, pestaña <strong>Widgets</strong> en el fondo.</p>
<p align="justify">En esta página, tiene que localizar el botón <strong>Crear nuevo</strong> y pulsar para crear un nuevo widget. A partir de aquí, se le ofrecerán opciones de configuración y una vista previa del widget. Puede cambiar las opciones de configuración del widget más adelante.</p>
<p align="justify">Después de crear el widget, el botón <strong>Crear nuevo</strong> será sustituido por un botón <strong>Guardar cambios</strong>. Ahora que su widget se crea, puede tomar el ID del widget de la URL. Mire la barra de direcciones de su navegador. Debe tener una URL que se vea algo como esto:</p>
 <blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p>Su ID de widget es el número de 18 dígitos. En este caso, <strong>512253083833733120</strong>. Recuerde mantener este número privado, por lo que su widget no será utilizado por otros sin su permiso.</p>
<p>Copie su ID y péguelo en el campo de texto que le pide su ID de widget. Después de guardar la configuración, el nuevo widget de Twitter debe funcionar.</p>
