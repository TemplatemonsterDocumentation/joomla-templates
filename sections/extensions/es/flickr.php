<p>At the <strong>Module</strong> tab you may set up the following options: </p>
  <ol class="marked-list">
    <li><strong>Pretext</strong> — allows displaying custom text before the module content;</li>
    <li><strong> Use user id</strong> — whether to use user id;</li>
    <li><strong>User id</strong> — create  Flockr account. Get the user id and insert here. You can get the user ID by the following link: <a href="http://idgettr.com/">http://idgettr.com/.</a></li>
    <li><strong> Use item tag</strong> —  whether to use tag to sort images;</li>
    <li><strong>Tag</strong> — set the tag the items will be displayed by.</li>
  </ol>
  <h5>Items settings </h5>
  <ol class="marked-list">
    <li><strong> Number of items</strong> — the number of items to display within this module;</li>
    <li> <strong>Columns</strong> — set the number of columns;</li>
    <li> <strong>Year format</strong> — set a year format (display full year format/display 2 last digits of the year);</li>
    <li><strong>Month format</strong> —  set a month format (displays full month name/displays month number);</li>
    <li><strong>Number of post comments</strong> — set the number of post comments to display in each item;</li>
    <li><strong> Item image size</strong> — set the gallery image size.</li>
  </ol>
<h5> Display settings &nbsp;</h5>
  <ol class="marked-list">
    <li><strong>Display item link</strong> — whether to show the photo link;</li>
    <li><strong>Link format</strong> — choose the link format [Link title like url/ Link title like custom text];</li>
    <li><strong>Custom link title</strong> — specify the custom link title;</li>
    <li><strong>Display  item date </strong> — whether to show the picture publishing  date;</li>
    <li><strong> Display profile</strong> — whether to show the profile;</li>
    <li><strong> Post item title</strong> — whether to show the pictures' title;</li>
    <li><strong>Display  description</strong> — whether to show the picture description;</li>
    <li><strong> Display author name</strong> — whether to show the author name;</li>
    <li><strong> Post link text </strong>— whether to show the link text;</li>
    <li><strong> Display author name with email</strong> — whether to show the author name with email;</li>
    <li><strong>Display  tags </strong>— whether to show the  tags.</li>
  </ol>
