<h3>Flickr Gallery</h3>

<p align="justify"><strong>Flickr Gallery</strong> is a lightweight Image Gallery Module for Joomla. This module will allow you to quickly and easily incorporate your Flickr photos into your website. It shows thumbnail view of all images from selected Flickr.com album. You can use your own Flickr ID and Flickr Album ID to publish photo albums on your website. When user will click on thumbs he will be redirected into full size photo page.</p>

