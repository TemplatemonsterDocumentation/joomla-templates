<h3>CarouFredSel</h3>

<p>CarouFredSel es un módulo de carrusel de imágenes que fue creado como una combinación
    del módulo Articles Newsflash de Joomla y el plugin CarouFredSel de jQuery. </p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>

<p>Todas las opciones de carrusel necesarias están disponibles en la configuración del
    módulo.</p>

<ol class="index-list">
    <li>Dirección de carrusel</li>
    <li>Ancho del elemento</li>
    <li>Elementos visibles</li>
    <li>Opciones de animación</li>
    <li>Navegación y controles</li>
</ol>

<p>El plugin CarouFredSel tiene otras opciones de <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">configuración</a>
    . Si desea agregar sus opciones, Usted puede modificar el fichero
    \modules\mod_caroufredsel\tmpl\default.php. </p>

