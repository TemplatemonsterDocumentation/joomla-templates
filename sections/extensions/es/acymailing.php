<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> Es el componente de suscripción para Joomla! AcyMailing es un Newsletter confiable y una Extensión de email marketing para Joomla. AcyMailing le permite gestionar eficientemente un número ilimitado de suscriptores, organizarlos en listas de correo y enviar boletines personalizados. </p>
<p>En su sitio web verá el siguiente formulario de suscripción:</p>
<figure class="img-polaroid">
    <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Vaya a <strong>Componentes</strong> &gt; <strong>AcyMailing</strong> y se le dirigirá al tablero de AcyMailing.</p>
<figure class="img-polaroid">
    <img src="img/acymailing-4.jpg" alt="">
</figure>


<p align="justify"> Para ver los ajustes principales de esta extensión, vaya a <strong>Extensiones</strong> &gt; <strong>Modulos</strong>. Haga clic en el botón <strong>Nuevo</strong> .</p>
<figure class="img-polaroid">
  <img src="img/acymailing-6.jpg" alt="">
</figure>
<p align="justify">Seleccione <strong>Módulo de Suscripción AcyMailing</strong> de la lista de nuevos módulos.</p>
<figure class="img-polaroid">
  <p><img src="img/acymailing-7.jpg" alt=""></p>
</figure>
