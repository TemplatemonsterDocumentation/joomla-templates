<h5>Cómo agregar un nuevo campo en el módulo de formulario TM AJAX </h5>
<p align="justify">Cada plantilla de Joomla 3 tiene una página de contactos con el formulario de contacto incluido. Generalmente, el formulario de contacto tiene campos estándar, por ejemplo,. &quot;<strong>Nombre</strong>&quot;, &quot;<strong>E-mail</strong>&quot; y &quot;<strong>Mensaje</strong>&quot;:</p>
 <figure class="img-polaroid">
<p><img src="img/contact-form-6.png" alt=""></p></figure>
