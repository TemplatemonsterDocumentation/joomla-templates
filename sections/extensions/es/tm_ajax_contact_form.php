<h3>Módulo TM Ajax Contact Form</h3>

<p>El TM Ajax Contact Form es un módulo de formulario de contacto para el motor Joomla.
    Este módulo permite añadir formularios de contacto en cualquier página de Joomla.</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>
<p> El envío de datos se realiza utilizando requests de AJAX, porque lo que la página no
    tiene que volver a cargarse. Las configuraciones del módulo permiten añadir
    protección contra bots (reCaptcha), crear campos de formulario de contactos, sus
    etiquetas, configurar la dirección de email del destinatario, agregar la dirección
    CC (Con Copia) y los mensajes de error y éxito. Puedes ver la lista completa de las
    configuraciones debajo. </p>

<figure class="img-polaroid">
    <img src="img/contact-form-2.png" alt="">
</figure>
<p>En la configuración de este módulo, se pueden encontrar todas las características del Diseñador de formularios, usted tiene la posibilidad de añadir campos de cualquier tipo (texto, correo electrónico, casilla de verificación, área de texto). Debido a esto, la forma puede ser personalizada de acuerdo con cualquiera de sus necesidades.</p>
