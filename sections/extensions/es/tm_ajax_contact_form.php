<h3>TM Ajax Contact Form Module</h3>

<p align="justify"><strong>TM Ajax Contact Form Module</strong> es un módulo de formulario de contacto para el motor de Joomla.
Este módulo permite añadir formulario de contacto a cualquier página de su sitio web Joomla.</p>
<p align="justify"><strong>Formulario de Contacto </strong>es básicamente un conjunto de preguntas completadas en la página del sitio web por su visitante que se envía automáticamente a su correo electrónico cuando se rellena. Por lo general, un formulario de contacto incluye campos para rellenar el nombre, dirección, teléfono, correo electrónico y tipo de comentario. Una vez que los visitantes de su sitio web hayan llenado los campos del formulario de contacto, harán clic en el botón Enviar [Enviar] para enviar la información.</p>
