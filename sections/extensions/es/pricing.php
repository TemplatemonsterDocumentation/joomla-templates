<h3>Pricing Tables</h3>
 
<p align="justify">The<strong> Pricing Tables</strong> allows you to showcase the prices for your product, services, or packages in a beautiful and responsive pricing table. </p>

<figure class="img-polaroid">
    <p><img src="img/price-30.jpg" alt="">
</p></figure>
    <h5>How output the Pricing Tables to the Frontend</h5>
    <p> Navigate to<strong> Component</strong> &gt; <strong>Pricing Tables</strong>.</p>
    <figure class="img-polaroid">
      <p><img src="img/price-33.jpg" alt="" /> </p>
</figure>
    <h5><strong>How to create new category</strong></h5>
    <p>For adding new price table item you have to create category first.</p>
    <p>Go to the <strong>Categories</strong> tab and press the<strong> New</strong> button.</p>
<figure class="img-polaroid">
      <p><img src="img/price-31.jpg" alt="" /> </p>
    </figure>
    <p align="justify">Enter the category title in the <strong>Title</strong> option field. You can add the category description. Save the category, pressing the <strong>Save</strong> button.</p>
<figure class="img-polaroid">
  <p><img src="img/price-34.jpg" alt="" /> </p>
    </figure>
<h5><strong>How to create new price table item</strong></h5>
<p>After creating category you have to create price table item.</p>
    <p>Go to the <strong>Pricing Tables</strong> tab and press the<strong> New</strong> button.</p>
    <figure class="img-polaroid">
      <p><img src="img/price-35.jpg" alt="" /> </p>
</figure>
    <p>Enter the <strong>price table item</strong> title in the <strong>Title</strong> option field. You can add the Item pretext. Save the price table item, pressing the <strong>Save</strong> button.</p>
    <figure class="img-polaroid">
      <p><img src="img/price-36.jpg" alt="" /></p>
</figure>
<p>Event settings:</p>
    <ol class="marked-list">
      <li><strong>Item title </strong> — enter the title of the item (the title will be displayed on the frontent);</li>
      <li><strong>Price</strong> — set the item price;</li>
      <li><strong>Currency</strong> — enter the currency sign;</li>
      <li><strong>Period</strong> — set the period (day, month, year, etc);</li>
      <li><strong> Item image </strong> — select an image for the item (press select button);</li>
      <li><strong>Event label </strong>— enter the item label;</li>
      <li><strong> Item pretext</strong> — enter the item pretext;</li>
      <li><strong> List</strong> — set the pricing table list;</li>
      <li><strong>Show button</strong> — whether to display the &quot;Show&quot; button;</li>
      <li><strong> Button text </strong>— specify the  text of the button;</li>
      <li><strong> Button link type</strong> —  select whether you wish to set a link to the menu item or URL;</li>
      <li><strong> Menu</strong> * — choose the menu item you want to set a link to.</li>
      <li><strong>Url</strong> — specify the URL link.</li>
    </ol>
    <p>Select the category for the price table item. Note the<strong> Category</strong> field is required.</p>
    <figure class="img-polaroid">
      <p><img src="img/price-37.jpg" alt="" /></p>
</figure>
<p>Save the price table item, pressing the <strong>Save</strong> button.</p>

