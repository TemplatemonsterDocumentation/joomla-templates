<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> es un foro de discusión para el sitio de Joomla. Es flexible y fácil de trabajar. El diseño predeterminado del foro para los usuarios no registrados y registrados es diferente, así como las opciones disponibles. </p>
<p><strong>Kunena</strong> es un componente de foro para Joomla. Permite implementar un foro totalmente funcional basado en Joomla! </p>
<p>Esta función se encuentra en elementos de menu '<strong>Página</strong>' en la parte delantera del sitio. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p> Ajustes generales de <strong>Kunena</strong> y su contenido puede ser controlado a través del panel de administración de su sitio. </p>
<p>Desplácese por la barra lateral hasta Extensiones y seleccione alli <strong>Kunena Forum</strong> :</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p> Habrá varias pestañas disponibles para trabajar con:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Category manager</strong>. En el centro habrá todas las categorías actuales disponibles en tu foro. Seleccione cualquiera para modificar si es necesario. Puede editar su sección principal, el nombre, el alias, la descripción, el encabezado y otras configuraciones:  </p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify"> Éste será el área que contiene la discusión de su miembro. Puede crear tantas discusiones como sea necesario. Cada discusión debe ser asignada a la sección general [se utiliza como encabezado para los temas del foro]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Permisos de Categoría</strong> ficha especifica el nivel de acceso de la categoría específica - grupos de usuarios para verlos y editarlos. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Configuración de Categoría </strong> permite controlar la configuración general de los mensajes de categoría. </p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify"><strong>Sección de Usuarios</strong>. Aquí puedes encontrar usuarios del foro actual y administrar la configuración de sus cuentas, los permisos de edición y ver foro: </p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify"> Pulse el <strong> enlace de nombre de usuario</strong> y verá la ventana, donde podrá seleccionar el <strong>avatar</strong> y <strong>texto de la firma</strong> si es necesario bajo los <strong>ajustes Básicos</strong>. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify"><strong>Moderación</strong> esta pestaña permite especificar si este usuario es moderador o no, seleccionando categorías para moderar también. Haga click en botón <strong>Guardar &amp; Cerrar</strong> en la parte superior una vez que haya terminado con la configuración del usuario.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Archivos adjuntos</strong>. Aquí puede trabajar con archivos cargados.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emociones</strong>. Todas las emociones disponibles se encuentran aquí. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Use area <strong>Nueva sonrisa</strong> para obtener algunos nuevos si es necesario: </p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Rangos</strong>&rsquo;. Aquí puede ver todos los rangos disponibles para el usuario. Puede agregar nuevo rango, si lo desea. Cada fila se puede editar manualmente así como: </p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Plantillas</strong>. Seleccione la plantilla para aplicar Foro a: </p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Configuración</strong>. Esta pestaña permite controlar los ajustes del Foro en detalle. Los títulos, dirección de correo electrónico, SEO, caché y otras configuraciones pueden ser manejadas en la pestaña <strong>General</strong>. Con la sección <strong>Interfaz</strong> puede modificar el diseño del foro. Los ajustes generales para los usuarios se gestionan con la sección <strong>Usuarios</strong>. <strong>Seguridad</strong> controla los permisos para invitados y usuarios registrados que usan el foro. <strong>Avatars</strong> Es la que permite/prohíbe que los usuarios carguen avatares propios y especifique la configuración de la imagen. Usando <strong>Cargas</strong> Puede permitir/prohibir la carga de archivos adjuntos en los mensajes, especificar la configuración de estos archivos.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
<p align="justify">
Haga Click en botón <strong>Guardar &amp; Cerrar</strong> en la parte superior una vez que haya terminado de configurar la configuración del foro. </p>
<p><strong>Herramientas</strong>. Aquí puede gestionar opciones específicas como informes, sincronización, IP&rsquo;s y asi: </p>
<p>
    <figure class="img-polaroid"><img src="img/forum-17.jpg" alt=""></figure>
</p>
<p>Al actualizar la página del foro en el sitio verá los cambios implementados.
</p>
