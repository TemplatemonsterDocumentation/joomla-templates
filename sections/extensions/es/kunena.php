<h5>Crear Categorías</h5>
<ol class="marked-list">
  <li>
    <p>Vaya a <strong>Componentes</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Categorias</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Haga clic en el botón <strong>Nueva Categoría</strong> en la esquina superior izquierda.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Introduzca la configuración general de categorías la pestaña <strong>Nombre y Descripción de la Categoría </strong>:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">Para crear una sección, elija <strong>Nivel superior</strong> como padre. </p>
       </li>
       <li>
         <p>Ingrese <strong>Nombre</strong> y <strong>Alias</strong> de la categoría.</p>
       </li>
       <li>
         <p>Asegúrese de que la categoría esté <strong>publicada</strong>.</p>
       </li>
       <li>
         <p>Rellene la <strong>descripción</strong> y <strong>título</strong> a los campos correspondientes de categoría.</p>
       </li>
       <li>
         <p>Puede añadir <strong>Clase css personalizada</strong> para su categoría. </p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Haga clic en la pestaña <strong> Permisos de Categoría</strong> defina permisos para categorías:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Puedes ver dos opciones disponibles aquí:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Tipo de Control de Acceso</strong> para ser usado en esta categoría.</p>
      </li>
      <li>
        <p align="justify">El <strong>Grupo de Nivel de Acceso</strong> que se permite ver esta categoría. Puede definir sus propios niveles de acceso en<strong> Usuarios</strong> &gt; <strong>Niveles de Acceso</strong>. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p><strong>Pastaña Configuración de Categoría</strong>:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Bloqueado</strong>. Establezca en Sí si desea bloquear la categoría. Los moderadores y administradores del sitio son los únicos que pueden crear nuevas respuestas a la categoría bloqueada.</p>
      </li>
      <li>
        <p align="justify"><strong>Opinión de Artículo</strong>. Establezca en Sí si desea que los moderadores revisen las publicaciones antes de publicarlas en esta categoría.</p>
      </li>
      <li>
        <p align="justify"><strong>Permitir mensajes anónimos </strong>le permite activar/deshabilitar mensajes anónimos para la categoría.</p>
      </li>
      <li>
        <p align="justify"><strong>De forma predeterminada, enviar respuestas como opción</strong>. Si se han activado mensajes anónimos, esta opción selecciona la opción predeterminada para el usuario. Los usuarios regulares pueden posteriormente editar sus propias publicaciones para que sean anónimas, pero sólo los moderadores tienen la capacidad de editar publicaciones anónimas.</p>
      </li>
      <li>
        <p>Usted puede <strong>Permitir Encuestas</strong> para la categoría que cree.</p></li></ol>
