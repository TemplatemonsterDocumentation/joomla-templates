<h3>Foro Kunena</h3>

<p>Kunena es un compnente para Joomla. Le permite crear un foro funcional a la base de
    Joomla! CMS.</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>El componente Kunena esta disponible en el panel de administracion de Joomla en el
    menu <b>Components -> Kunena</b>.</p>

<p>Mas informacion sobre instalacion y uso del componente Kunena en el <a
        href="http://www.kunena.org/docs/Main_Page" target="_blank">sitio web
        oficial</a>.</p>

