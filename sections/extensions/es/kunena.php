<h3>Kunena Forum</h3>

<p>Kunena es un compnente para Joomla. Le permite crear un foro funcional a la base de Joomla! CMS:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Puede acceder al componente Kunena desde su panel de administración de Joomla en la pestaña <b> Componentes -> Kunena (Components -> Kunena)</b>.</p>

<p>Mas informacion sobre instalacion y uso del componente Kunena puede encontrar en la <a href="http://www.kunena.org/docs/Main_Page" target="_blank">página oficial</a>:</p>

