<h3>Articles Single</h3>

<p><strong>Articles Single</strong> es un módulo que le permite mostrar un solo artículo en la posición deseada: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">El único ajuste inusual del módulo es <b>Enlace Personalizado</b>. Permite mostrar un enlace personalizado bajo el contenido del módulo. Puede vincularlo a una URL externa o a un elemento de menú de su sitio web Joomla:</p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">Si desea cambiar el diseño de la sección &quot;<strong>HOW WE WORK</strong>&quot; , vaya a <strong>Extensiones</strong> &gt; <strong>Módulos</strong>. Entonces escoja el módulo &quot;<strong>How we work</strong>&quot;, que pertenece al tipo de <strong>Articulos - Single</strong>. Puede utilizar Ctrl + F para encontrarlo. Presione la combinación de teclas Ctrl + F y escriba <strong>220</strong> [es el ID del módulo &quot;How we work&quot;]. Haga clic en su enlace de tema y verá la ventana de configuración de uno.</p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
 </p>
 <p>El <strong> Módulo Articles Single</strong> tiene las siguientes configuraciones:</p>
 <p align="justify"><strong>Seleccione Artículo.</strong> Este campo contiene el artículo que se mostrará en este diseño. Pulse el botón <strong>Seleccionar/Cambiar</strong> Para abrir la ventana de selección de artículos, como se muestra a continuación. </p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify"> Esta pantalla es similar a <strong>Gestor de Artículos</strong>. Puede utilizar el <strong>Filtro de campos</strong> para encontrar el artículo deseado y luego haga clic en el <strong>Título</strong> del artículo para seleccionarlo. En ese punto, la ventana modal se cerrará y el título se mostrará en el <strong>Campo seleccionado de Artículo</strong>.     </p>
     <p align="justify">Ahora tiene la oportunidad de editar el artículo. Para editar el artículo, haga clic en el botón<strong> Editar</strong> y verá la ventana de <strong>Editar Artículo </strong>. Aquí tiene la opción para editar el contenido del artículo, su imagen y enlaces.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify">El <strong>Articles Single</strong>  tiene el siguiente <strong>Opciones del artículo</strong> [Options Tab], como se muestra abajo. Estas opciones determinan cómo se mostrará el artículo en el diseño de Front-end del sitio. Tenga en cuenta que estas opciones incluyen &quot;<strong>Utilizar Global</strong>&quot;. Si se selecciona esta opción, se utilizará el ajuste de las Opciones del Administrador de artículos.</p>
    <figure class="img-polaroid">
     <p><img src="img/articles-single-8.jpg" alt="">
     </p></figure>
