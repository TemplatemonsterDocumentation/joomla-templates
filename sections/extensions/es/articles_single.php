<h3>Articles Single</h3>

<p>Articles single es un módulo que permite agregar un solo artículo a la posición
    deseada.</p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>


<p>La única opción inusual en este módulo es Custom Link/Enlace personalizado. Le
    permite mostrar su enlace personalizado bajo del contenido del módulo. Se puede usar
    la dirección URL externa o enlace a algún elemento de menú de Joomla. </p>
