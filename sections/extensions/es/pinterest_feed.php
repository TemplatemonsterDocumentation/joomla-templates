<h3>SW Pinterest Display</h3>


<p align="justify"><strong>SW Pinterest</strong> es un módulo para mostrar "Pinterest Board Widget". Este Módulo le puede ayudar a mostrar Pinterest Profile Pins o Board Pins. Anchura y Altura pueden ser escalables.</p>
<figure class="img-polaroid">
    <img src="img/pinterest_feed-1.jpg" alt="">
</figure>

<p align="justify"> Vaya a <strong>Administrador de módulos</strong>. Verá un módulo disponible en la lista denominada &ldquo;<strong>SW Pinterest Display</strong>&rdquo;. Haga clic en él para editar o puede hacer clic en el botón &ldquo;<strong>Nuevo</strong>&rdquo; Botón para crear un nuevo módulo.</p>
