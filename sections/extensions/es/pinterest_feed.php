<p>En <strong>Opciones Avanzadas</strong> – Usted puede asignar al módulo <strong>un sufijo de clase</strong> si lo desea.</p>
<p align="justify">A continuación, elige la página, donde colocar el módulo. <strong>Asignación del Menú </strong>pestaña para este propósito. </p>
<p align="justify">En esta pestaña hay una lista de elementos del menú. Puede elegir entre En todas las páginas, Sin páginas, Sólo en las páginas seleccionadas y En todas las páginas, excepto las seleccionadas en la Lista. Si se seleccionan las dos últimas opciones, aparecerá una lista con todos los elementos del menú encendidos, como se muestra en la captura de pantalla anterior. Esto le permite asignar módulos a algunas pero no a todas las páginas y al seleccionar los enlaces de menú que desea que el módulo asociado con usted pueda personalizar en qué páginas aparecen o no aparecen los módulos.</p>
