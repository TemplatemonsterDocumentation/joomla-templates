<h3>Plugin Joomla Cookie Directive</h3>

<p><strong>Joomla Cookie Directive Plugin</strong> capaz de mostrar un banner de aceptación de cookies en la parte superior del sitio para advertir sobre el sitio mediante cookies.</p>

<figure class="img-polaroid">
    <p><img src="img/cookies-1.jpg" alt="">
    </p></figure>
    <p align="justify">Para configurar el plugin<strong>Joomla Cookie Directive</strong> vaya a <strong>Extensiones</strong> &gt; <strong>Plugins</strong>. Para encontrar el plugin necesario entre los demás, escriba "cook /.../& quot; En el campo de búsqueda.</p>
    <p align="justify">Haga Click en el enlace de título &quot;<strong>system - EUCookieDirectiveLite</strong>&quot; y verá los principales ajustes del plugin <strong>Joomla Cookie Directive</strong>.</p>

<p>Esta pantalla le permite editar detalles y opciones del plugin. </p>

