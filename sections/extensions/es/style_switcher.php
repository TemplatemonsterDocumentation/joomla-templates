<h3>TM Ajax Style Switcher Module</h3>

<p align="justify"><strong>TM Ajax Style Switcher Module</strong> es un módulo para conmutar esquemas de color vía front-end. El módulo aplica automáticamente esquemas de color desde la carpeta templates\themexxx\css\ del tema actual.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Nota</strong>: en la plantilla Live Demo la plantilla funciona en modo de vista previa usando cookies. En su sitio web, el módulo funcionará en modo completo y estará disponible para usuarios autorizados con derechos de categoría de usuario SuperAdmin.</p>
<h5> Cómo administrar el módulo TM ajax style switcher </h5>
<p align="justify">Para configurar el Módulo <strong>TM Ajax Style Switcher</strong> vaya a <strong>Extensiones </strong>&gt;<strong>Modulos</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify"> Pulse el botón &quot;<strong>Search Tool</strong>&quot; y seleccione <strong>Modulo TM Ajax Style Switcher</strong> escriba el título &quot;- Seleccione Tipo -&quot; lista desplegable de filtros. Usted verá el <strong> Módulo Ajax Style Switcher</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Haga clic en el enlace del título del módulo para que se lleve a la ventana de configuración del módulo. No hay ajustes específicos para el módulo en el Panel de Administración, sólo los valores estándar para todos los módulos:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Asegúrese de que el módulo esté <strong>publicado</strong>.</p>
  <h5><p>Para activar el selector de estilos, siga las instrucciones que se enumeran a continuación:  </p></h5>
  <ol class="marked-list">
    <li>
      <p>Añada un nuevo usuario de Joomla a través de <strong>Usuarios</strong> &gt; <strong>Gestionar</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Activar</strong> y <strong>active</strong> nuevos usuarios.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Presione El nuevo vínculo de inicio de sesión de usuario para llevarlo a la ventana de configuración. </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Asegúrese de asignar a su usuario a grupo de &lsquo;<strong>Super Usuarios</strong>&rsquo;. Vaya a la pestaña <strong>Usuarios Asignados</strong> y marque &quot;<strong>Super Usuarios</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Inicie sesión en su sitio web a través del módulo <strong>Inicie sesión o Regístrese</strong> en la parte de interfaz:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>El selector de estilos ha sido activado con éxito. Actualizar su sitio web para ver la caja de herramientas de personalización: </p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
