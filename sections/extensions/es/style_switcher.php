<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> es el módulo que permite cambiar los esquemas de color a través de front-end. El módulo aplica automáticamente las esquemas de color de la carpeta color_schemes\css\ del tema actual.</p>

<p>No hay configuraciones específicas del módulo en panel de Administración, que incluye la configuración estándar del módulo:</p>

