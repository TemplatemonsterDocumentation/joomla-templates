    <img src="img/style_switcher_1.jpg" alt="">
</figure>

<p class="alert alert-warning">Por favor, tenga en cuenta que en la demo en vivo de la plantilla el módulo funciona en modo de vista previa, usando cookies. En el sitio web real el módulo funciona en un modo completo y está disponible sólo para los usuarios autorizados con los SuperAdmin derechos..</p>
