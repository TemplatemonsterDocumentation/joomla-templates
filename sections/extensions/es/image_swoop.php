<h3>Image Swoop</h3>

<p>El módulo "Image Swoop" es un slider adaptable desarrollado como una combinación del
    módulo “Articles Newsflash” predeterminado de Joomla y el plugin gratuito de código
    abierto “Camera Slideshow” de jQuery. Funciona perfectamente en dispositivos móviles
    Android y iOS o computadoras. Es compatible con todos los navegadores modernos
    incluyendo IE8.</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>El módulo "Image Swoop" tiene varias opciones de configuración, se puede encontrarlas
    si abre el módulo Image Swoop en la sección Extensions > Module Manager (Extensiones
    > Gestor de módulos) de su panel de administración de Joomla. Se puede configurar
    todas las opciones principales del plugin "Camera Slideshow" de jQuery en el área de
    configuración del módulo.</p>

<p>Mantenga el cursor del ratón sobre el título de la opción para ver su descripción.
    También se puede obtener más información acerca de las opciones del plugin de jQuery
    en <a href="http://www.pixedelic.com/plugins/camera/" target="_blank">el sitio web
        oficial</a>.</p>

