<h3>Swiper</h3>

<p>Swiper Module es un maravilloso sldier adaptivo que es una integración estándar del módulo Joomla Articles Newsflash y plugin gratuito jQuery Swiper. Creado para iOS, este plugin funciona perfectamente en el último Android, Windows Phone 8 y en los navegadores modernos para PC:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Swiper Module contiene configuraciones diferentes y se pueden encontrar en la pestaña <b>Estensiones (Extensions)
        > Gestor de módulos (Module Manager)</b> de su panel de administración de Joomla. Todas las opciones principales del plugin jQuery Swiper 
se pueden configurar en la configuración del módulo. </p>

<p>Arrastrar el cursor del ratón sobre el título de una opción para ver su descripción. También puede obtener más información sobre el plugin jQuery Swiper en la <a
        href="http://idangero.us/swiper/" target="_blank">página oficial</a>.</p>
<p>Todos los parámetros necesarios están disponibles en el carrusel de los parámetros del módulo:</p>

<ol class="index-list">
    <li>Ajustes de reproducción;</li>
    <li>Ajustes de altura;</li>
    <li>Elementos visibles;</li>
    <li>Configuración de la animación;</li>
    <li>Navegación y controles;</li>
    <li>Ajustes de miniaturas</li>
</ol>

