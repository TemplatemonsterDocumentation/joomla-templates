<h3>Swiper</h3>

<p align="justify"><strong>Swiper Module</strong> es un slider adaptativo maravilloso que es una integración de los artículos estándar del módulo Joomla Articles Newsflash y plugin gratuito jQuery Swiper. Creado para IOS, este plugin funciona perfectamente en los últimos Android, Windows Phone 8 y los Navegadores de PC de escritorio.</p>
<p align="justify">Vamos a comprobar qué tipo de diapositivas podemos agregar al slider del sitio utilizando este módulo. En primer lugar, podría ser una diapositiva animada regular con una imagen y un título en él: </p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">Pero también puede ser una diapositiva con el vídeo de fondo o una diapositiva configurable con el fondo de la imagen de paralaje. Hay una gran variedad de opciones para elegir. Vamos a aprender a usarlos. </p>

