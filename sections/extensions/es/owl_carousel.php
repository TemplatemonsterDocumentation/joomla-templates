<h3>Owl Carousel</h3>
<p><strong>Owl Carousel </strong>módulo es una combinación predeterminada del módulo Artículos de Joomla de Newsflash y Owl Carousel jQuery plugin.</p>
<figure class="img-polaroid">
  <img src="img/owl-carousel-1.jpg" alt="">
</figure>
<h5>Cómo Acceder</h5>
<p>Para "añadir" un nuevo módulo <strong>Owl Carousel</strong>, vaya al Administrador de Módulos:</p>
<p>Seleccione <strong>Extensiones</strong> &gt; <strong>Modulos</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Haga click en el botón '<strong>New</strong>' y haga clic en <strong>Owl Carousel</strong> en la ventana emergente modal.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Detalles</h5>
<ol class="marked-list">
  <li><strong>Título:</strong> el módulo debe tener un título [en nuestro caso es <strong>Carousel</strong>].</li>
</ol>
<h5>Módulo</h5>
<p>El Módulo <strong>Owl Carousel </strong> mostrará un número fijo de artículos de una categoría específica o un conjunto de categorías.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Categoría</strong>: Seleccione Artículos de una categoría específica o un conjunto de categorías. Si no hay ninguna categoría seleccionada, la selección muestra todas las categorías como predeterminadas. </div>
  </li>
  <li><strong>Imagenes</strong>: [Ocultar/Mostrar]. Mostrar imágenes del artículo.</li>
  <li><strong>Título del Artículo</strong>: Mostrar/ocultar el título del artículo.</li>
  <li><strong>Títulos Vinculados</strong>: [Usar Global/No/Sí]. Vincular los títulos de artículos a los artículos.</li>
  <li> <strong>Fecha de publicación</strong>: Mostrar la fecha de publicación de un elemento.</li>
  <li><strong>Nivel de encabezado</strong>: [h1/h2/h3/h4/h5]. Seleccione el nivel de encabezado HTML deseado para los títulos de artículo.</li>
  <li><strong>Mostrar imágenes de introducción</strong>: Seleccione si desea mostrar las imágenes de introducción del artículo.</li>
  <li><strong> Alineación de la imagen de Introducción</strong>: [Ninguno/izquierda/derecha] Seleccione la alineación de la imagen de introducción. Anula la configuración del artículo.</li>
  <li>
    <div align="justify"><strong>'Leer Más...'Enlace:</strong> [Mostrar/Ocultar]. Si se establece en Mostrar, el enlace "Leer más ..." mostrará si el texto principal se ha proporcionado para un artículo.</div>
  </li>
  <li><strong>Número de Artículos</strong>: El número de artículos que se mostrarán en este módulo.</li>
  <li>
    <div align="justify"><strong>Ordenar resultados</strong>: [Fecha de publicación/Fecha de creación/Pedido/Aleatorio]. Seleccione el orden en el que desea que se presenten los resultados de la consulta.</div>
  </li>
  <li><strong>Pretexto</strong>: Puede introducir texto que desee mostrar antes del contenido del módulo. No admite ninguna etiqueta HTML.</li>
</ol>
<h5>Detalles comunes</h5>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Mostrar título
</strong>. [Mostrar/Ocultar] Elija si desea mostrar u ocultar el título de los módulos en la parte frontal. El título será el del campo de formulario anterior.</div>
  </li>
  <li>
    <div align="justify"><strong>Posición del Módulo</strong>. Elija la posición del módulo en la que desee que se muestre este módulo. Puede introducir una posición de módulo personalizada para utilizarla con el complemento de posición de carga o puede presionar el botón de posición para seleccionar una posición de módulo de la plantilla.</div>
  </li>
  <li><strong>Status</strong>. [Publicado/No publicado/Borrado] El estado publicado del artículo.</li>

  <li><strong>Acceso</strong>. El nivel de acceso de visualización para este elemento.</li>

  <li>
    <div align="justify"><strong>Ordenación del Módulo</strong>. Esto muestra un desplegable de cada módulo en la posición en la que se encuentra el módulo actual. Este es el orden en el que los módulos se mostrarán cuando se visualice en la parte frontal así como en el Administrador de módulos.</div>
  </li>

  <li>
    <div align="justify"><strong>Empezar a publicar</strong>. Fecha y hora para comenzar a publicar. Utilice este campo si desea introducir el contenido antes de tiempo y luego hacer que se publique automáticamente en un futuro.</div>
  </li>

  <li>
    <div align="justify"><strong>Finalizar la Publicación</strong>. Fecha y hora de finalización de la publicación. Utilice este campo si desea que el contenido cambie automáticamente a Estado no publicado en un momento futuro [por ejemplo, cuando ya no sea aplicable].</div>
  </li>

  <li><strong>Idioma</strong>. Idioma del artículo.</li>

  <li>
    <div align="justify"><strong>Nota</strong>. Nota del artículo. Esto es normalmente para uso del administrador del sitio [por ejemplo, para documentar información acerca de este elemento] y no se muestra en la parte frontal del sitio.</div>
  </li>
</ol>
<p>Ahora debemos establecer los parámetros del módulo como se muestra en la imagen de abajo [Usted debe elegir la posición <strong> característica </strong>escriba en el campo Posición]:</p>
<figure class="img-polaroid">
    <p><img src="img/owl-carousel-6.jpg" alt="" /></p></figure>
    <p align="justify">A continuación, elija la página, donde colocar el módulo. <strong>Asignar el menu </strong>para este propósito. </p>
  <p align="justify">En esta pestaña hay una lista de elementos del menú. Puede elegir entre En todas las páginas, Sin páginas, Sólo en las páginas seleccionadas y En todas las páginas, excepto las seleccionadas en la Lista. Si se seleccionan las dos últimas opciones, aparecerá una lista con todos los elementos del menú encendidos, como se muestra en la captura de pantalla anterior. Esto le permite asignar módulos a algunas pero no a todas las páginas y al seleccionar los enlaces de menú que desea que el módulo asociado con usted pueda personalizar en qué páginas aparecen o no aparecen los módulos. </p>
    <p align="justify">Seleccione la página <strong>Blog</strong> y el módulo sólo aparece en esa página del sitio web.  </p>
    <p align="justify">No olvide pulsar el botón <strong>Guardar </strong>de la barra de herramientas.  </p>
