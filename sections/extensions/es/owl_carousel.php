<h3>Owl Carousel</h3>

<p>Owl Carousel es un módulo que muestra carrusel de imágenes, creado a base de la norma del módulo
 Joomla Articles Newsflash y el plugin jQuery Owl Carousel:</p>

<figure class="img-polaroid">
    <img src="img/owl-carousel.png" alt="">
</figure>

<h6>Todos los ajustes necesarios del carrusel están disponibles en la configuración del módulo: </h6>

<ol class="index-list">
    <li>Anchura del elemento (Element width);</li>
    <li>Elementos visibles (Visible elements);</li>
    <li>Configuración de la animación (Animation settings);</li>
    <li>Navegación y control (Navigation and control).</li>
</ol>


