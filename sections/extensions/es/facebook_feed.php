      <li> <strong>Clase de Cabecera</strong>. La clase CSS para el encabezado/título del módulo.</li>
      <li> <strong>Estilo del Módulo</strong>. Utilice esta opción para sustituir el estilo de plantilla por su posición.</li>
    </ol>
   