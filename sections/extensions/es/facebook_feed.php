<h3>TM Facebook Page Plugin</h3>
 
<p><strong>TM Facebook Page</strong> es un módulo que permite mostrar su página de Facebook en el sitio web:</p>

<figure class="img-polaroid">
    <p><img src="img/facebook_feed-1.jpg" alt="">
    </p></figure>
    <p>Para configurar el módulo <strong>TM Facebook Page Plugin</strong> vaya a <strong>Extensiones</strong> &gt; <strong>Módulos</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Pulse el botón &quot;<strong>Buscar herramienta</strong>&quot; y seleccione <strong>TM Facebook Page Plugin </strong>en el filtro de la lista desplegable &quot;- Seleccione Tipo -&quot;. Usted verá el Módulo <strong>Facebook Box</strong>.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-4.jpg" alt="">
</p></figure>
    <p>Haga clic en el enlace del título del módulo para que lleve a la ventana de configuración del módulo. </p>
 <figure class="img-polaroid">
    <p><img src="img/facebook_feed-5.jpg" alt=""></p></figure>
   <p>En la pestaña del <strong>Módulo</strong> puede configurar las siguientes opciones:</p>
    <ol class="marked-list">
      <li> <strong>ID de la Aplicación de Facebook</strong> *.</li>
      <li> <strong>URL de Página de Facebook</strong>.</li>
      <li> <strong>El Nombre de su Compañía</strong>.</li>
      <li> <strong>Width</strong>. El ancho de píxeles del widget.</li>
      <li> <strong>Height</strong>. La altura de píxel del widget. </li>
      <li> <strong>Mostrar caras de Amigos</strong>. Activando esta opción permite que las fotos de perfil se muestren en el módulo.</li>
      <li> <strong>Mostrar los Mensajes de la Página</strong>. Mostrar publicaciones de la línea de tiempo de la página.</li>
      <li> <strong>Ocultar Foto de Portada</strong>. Ocultar foto de portada en el Encabezado.</li>
    </ol>
<p align="justify">El Campo <strong>Facebook App ID</strong> es requerido. Es un Identificador de Aplicación de Facebook. Para obtener el identificador, abra el sitio web: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a> y creae una aplicación para su sitio web. </p>
<p align="justify">Simplemente siga las instrucciones sencillas, y tendrá el identificador al instante. </p>
<h5>Guia Paso a Paso</h5>
        <h5>1. Inicie sesión en Facebook</h5>
        <p>Antes de crear una cuenta de Desarrollador, inicie sesión en su cuenta de Facebook.</p>
<a href="https://www.facebook.com/login.php">Inicie sesión en Facebook</a>
        <h5>2. Cuenta del Desarrollador</h5>
        <p align="justify">Para empezar, necesitará la cuenta de Desarrollador de Facebook. Si no tiene una actualización de su cuenta personal de Facebook a una cuenta de desarrollador de Facebook ahora. Salte este paso, si ya tiene una cuenta de Desarrollador.</p>
<a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Crear Cuenta de Desarrollador</a>
<h5>3. Crea una nueva Aplicación de Facebook</h5>
        <p>Elija <a href="https://developers.facebook.com/apps/"><strong>Aplicación</strong></a> en la navegación del encabezado y seleccione <strong>Añadir una Nueva Aplicación</strong> o utilice el botón de abajo.</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Crea una nueva aplicación de Facebook</a>
   <h5>4. Elegir plataforma</h5>
        <p align="justify">Elija la plataforma que desea agregar. Si tu aplicación está en varias plataformas, podrá agregarlas a tu aplicación de Facebook más tarde.  </p>
        <figure class="img-polaroid">
    <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
    <h5>5. Escoja un nombre</h5>
    <p align="justify">Elija un nombre para tu aplicación y selecciona <strong>Crear un Nuevo ID de Aplicación de Facebook</strong>. Elija la <strong>categoría</strong> para su aplicación. Si está creando una versión de prueba de una aplicación de Facebook existente, seleccione la aplicación que está utilizando.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
    <h5>6. Siga los pasos de &quot;Quick Start&quot; </h5>
    <p align="justify">El quick start wizard le ayudará a configurar su aplicación de Facebook para la plataforma seleccionada. Puede volver al asistente de quick star en un momento posterior para continuar la configuración o agregar una nueva plataforma a su Aplicación.</p>

<h5>7. ID de Aplicación</h5>
    <p align="justify">Su aplicación tendrá un ID de Aplicación único. Utilizará este ID siempre que utilice uno de nuestros <a href="https://developers.facebook.com/docs/apis-and-sdks">SDKs</a> o <a href="https://developers.facebook.com/docs/sharing/webmasters/">Abra Etiquetas de gráfico para compartir</a>. Puede encontrar su ID de Aplicación en su <a href="https://developers.facebook.com/apps/">tablero de aplicación</a>.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
    <h5>8. Proteja su Secreto de la Aplicación</h5>
    <p align="justify">Facebook genera automáticamente una aplicación secreta asociada a su Aplicación. El secreto auténtico de la aplicación de las solicitudes hechas por su aplicación a los servidores de Facebook y debe tratarse como una contraseña.</p>
<p align="justify">No extraiga su secreto de Aplicación de Facebook en su marcado de página web; Tenga cuidado al enviar archivos que contengan este valor en el software de control de código fuente tal como un repositorio Git o Subversion público.</p>
<h5>9. Email de Contacto</h5>
    <p align="justify">Debe agregar un correo electrónico de contacto para cada aplicación. Vaya a su <a href="https://developers.facebook.com/apps/">tablero de aplicación</a> y seleccione el elemento de navegación <strong>Ajustes</strong> en la barra lateral izquierda y proporcione un correo electrónico de contacto válido.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
    <h5>10. Detalles de Aplicación</h5>
    <p>Seleccione Detalles de la Aplicación en la barra lateral izquierda para ver y agregar información adicional sobre su aplicación.</p>
    <p align="justify">Si desea que su Aplicación aparezca en el centro de aplicaciones, active la configuración desde la Plataforma listada en App Center para su sitio web o sitio para dispositivos móviles. Agrega iconos, imágenes promocionales y videos. Asegúrese de seguir el <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">App Center Guidelines</a> será aprobado para su inclusión en App Center.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11. Desactivar el Modo de Desarrollo</h5>
    <p align="justify">Las nuevas aplicaciones de Facebook existen en modo de desarrollo de forma predeterminada. Estos límites publican acciones y objetos relacionados, como el etiquetado de amigos en Facebook Timelines propiedad de un desarrollador de la aplicación.   </p>
<p align="justify">Una vez que su aplicación esté lista para uso público, puede cambiar del modo de Desarrollo al modo Público para permitir que su aplicación interactúe con cuentas de Facebook no asociadas con su aplicación.</p>
   <figure class="img-polaroid">
    <p><img src="img/facebook_feed-12.jpg" alt="">
</p></figure>
  
    <p>También puede configurar las siguientes opciones para el módulo en la pestaña <strong>Módulo</strong> :</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-13.jpg" alt="">
</p></figure>
<ol class="marked-list">
      <li> <strong>Mostrar Título</strong>. Puede mostrar u ocultar el título del módulo en la pantalla. El efecto dependerá del estilo cromado en la plantilla.</li>
      <li>
        <div align="justify"> <strong>Posición</strong>. Puede seleccionar una posición de módulo de la lista de posiciones predefinidas o introducir su propia posición de módulo escribiendo el nombre en el campo y presionando enter.</div>
      </li>
      <li><strong> Status</strong>. Si se publica, este módulo se mostrará en su sitio Frontend o Backend dependiendo del módulo.</li>
      <li> Fecha de publicación [<strong>Empezar a Publicar</strong>/<strong>Finalizar la Publicación</strong>] una fecha opcional para iniciar/finalizar Publicar el módulo.</li>
      <li> <strong>Acceso</strong>. El grupo de acceso a nivel que tiene permiso para ver este elemento.</li>
      <li> <strong>Ordenación</strong>. Seleccione la ordenación.</li>
      <li> <strong>Idioma</strong>. Asignar un idioma a este módulo.</li>
    </ol>
