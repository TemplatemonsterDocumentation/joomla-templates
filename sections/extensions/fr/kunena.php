<h3>Forum Kunena</h3>

<p>Kunena est un composant de forum pour Joomla. Il vous permet de mettre en œuvre un
    forum entièrement fonctionnel basé sur le CMS Joomla.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Composant Kunena peut être accessible via le panneau d'administration Joomla sous les
    <b>Composants -> Kunena</b> menu.</p>

<p>Vous pouvez en apprendre plus sur Kunena utilisation de composants et de
    configuration à la <a href="http://www.kunena.org/docs/Main_Page" target="_blank">site
        officiel</a>.</p>

