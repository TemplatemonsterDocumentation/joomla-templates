<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> est un forum de discussion pour le site Joomla. Il est flexible et facile à utiliser. 
La disposition par défaut du forum pour les utilisateurs non enregistrés et enregistrés est différente, ainsi que les options disponibles. </p>
<p><strong>Kunena</strong> est un composant de forum pour Joomla. Il permet la mise en place d'un forum pleinement fonctionnel basé sur Joomla! </p>
<p>Cette fonctionnalité se trouve sous l'élément de menu '<strong>Pages</strong>' à l'avant du site. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p>Les paramètres généraux du <strong>Kunena</strong> et son contenu peuvent être contrôlés via le panneau d'administration de votre site. </p>
<p>Faites défiler vers le bas les extensions et sélectionnez <strong>Kunena Forum</strong> la:</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p> Plusieurs onglets seront disponibles là pour travailler:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Gestionnaire de Catégorie</strong>. Au milieu, toutes les catégories actuelles sont disponibles dans votre forum. Sélectionnez un élément à modifier si nécessaire. Vous pouvez modifier sa section parent, le nom, l'alias, la description, l'en-tête et d'autres paramètres:  </p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify">La zone qui contient la discussion de vos membres sera là. Vous pouvez créer autant de discussions que nécessaire. Chaque discussion devrait être attribuée à la section générale. [c'est utilisé comme en-tête pour les sujets du forum]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Autorisations de Catégorie</strong> cet onglet spécifie le niveau d'accès de la catégorie spécifique - groupes d'utilisateurs pour les afficher et les modifier. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Paramètres de Catégorie</strong> permet de contrôler les paramètres généraux pour les messages de catégorie. </p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify">La section <strong>Utilisateurs</strong>.Ici, vous pouvez trouver des utilisateurs du forum et gérer leurs paramètres de compte, les permissions  d'édition et de visualisation forum: </p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify">Appuyez sur le <strong>lien de nom d'utilisateur</strong> et vous verrez la fenêtre, où vous pouvez sélectionner <strong>avatar</strong> et <strong>texte de signature</strong> si nécessaire dans les<strong>Paramètres de base</strong>. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify">L'onglet <strong>Modération</strong> permet de spécifier si cet utilisateur est modérateur ou non, en sélectionnant les catégories à modérer ainsi. Cliquez sur le bouton <strong>Sauvegarder &amp; Fermer</strong> une fois que vous avez terminé avec les paramètres utilisateur.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Pièces Jointes</strong>. Ici vous pouvez travailler avec les fichiers téléchargés.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Émotions</strong>. Toutes les émotions disponibles sont ici. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Utilisez la zone <strong>Nouveau Sourire</strong> pour en obtenir de nouveaux si nécessaire: </p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Rangs</strong>&rsquo;. Vous voyez tous les rangs disponibles pour l'utilisateur ici. Vous pouvez ajouter un nouveau rang, si vous le souhaitez. Chaque rang peut être édité manuellement aussi: </p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Modèles</strong>. Sélectionnez le modèle à appliquer Forum à:</p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Configuration</strong>. Cet onglet permet de contrôler les paramètres du Forum en détail. Les titres, l'adresse e-mail, SEO, cache et autres paramètres peuvent être gérés sous l'onglet <strong>Général</strong>. Avec la section <strong>Frontend</strong> vous pouvez modifier la disposition du Forum. Les paramètres généraux pour les utilisateurs sont gérés avec la section <strong>Utilisateurs.</strong>. L'onglet <strong>Sécurité</strong> contrôle les autorisations pour les invités et les utilisateurs enregistrés utilisant le Forum. L'onglet <strong>Avatars</strong> autorise / interdit aux utilisateurs de télécharger leurs propres avatars et spécifie les paramètres de l'image. En utilisant des <strong>Téléchargements</strong> vous pouvez autoriser ou interdire de télécharger des pièces jointes aux messages, spécifiez les paramètres de ces fichiers.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
<p align="justify">Cliquez sur le bouton <strong>Sauvegarder &amp; Fermer</strong une fois que vous avez terminé de configurer les paramètres du forum. </p>
<p><strong>Outils</strong>. Ici, vous pouvez gérer des options spécifiques telles que les rapports, la synchronisation, IPs, etc: </p>
<p>
    <figure class="img-polaroid"><img src="img/forum-17.jpg" alt=""></figure>
</p>
<p>En actualisant la page Forum du site, vous verrez les modifications apportées.
</p>
<p>Si vous avez besoin de fonctionnalités supplémentaires pour Kunena, vous pouvez choisir parmi un grand nombre de <strong>plugins</strong> conçus spécialement pour Kunena.</p>
