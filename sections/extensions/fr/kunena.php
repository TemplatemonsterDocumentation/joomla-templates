<h3>Kunena Forum</h3>

<p>Kunena est un composant de forum pour Joomla. Il vous permet de mettre en œuvre un forum entièrement fonctionnel basé sur le CMS Joomla:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Composant Kunena peut être accessible via le panneau d'administration Joomla dans le menu <b>Components (Composants) -> Kunena</b>.</p>

<p>Plus d'informations sur l'utilisation et la configuration de composant Kunena vous pouvez trouver sur <a href="http://www.kunena.org/docs/Main_Page" target="_blank">le site officiel</a>:</p>

