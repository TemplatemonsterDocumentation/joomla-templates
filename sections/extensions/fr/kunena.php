<h5>Créer des Catégories</h5>
<ol class="marked-list">
  <li>
    <p>Allez aux <strong>Composants</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Catégories</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Cliquez sur le bouton <strong>Nouvelle Catégorie</strong> dans le coin supérieur gauche.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Saisissez les paramètres généraux des catégories sous l'onglet <strong>Nom et Description de Catégorie </strong>:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">Pour créer une section, choisissez <strong>Haut Niveau</strong> comme parent. </p>
       </li>
       <li>
         <p>Entrez <strong>Nom</strong> et <strong>Alias</strong> de la catégorie.</p>
       </li>
       <li>
         <p>Assurez-vous que la catégorie est <strong>publiée</strong>.</p>
       </li>
       <li>
         <p>Remplissez la <strong>description</strong> et le <strong>titre</strong> de la catégorie aux champs correspondants.</p>
       </li>
       <li>
         <p>Vous pouvez ajouter <strong>Classe css personnalisée</strong> pour votre catégorie.</p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Cliquez sur l'onglet <strong>Autorisations de Catégorie</strong> pour définir les permissions pour les catégories:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Vous pouvez voir deux options disponibles ici:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Type de Contrôle d'accès</strong> à utiliser dans cette catégorie.</p>
      </li>
      <li>
        <p align="justify">Le <strong>Groupe de Niveau d'accès</strong> qui est autorisé à voir cette catégorie. Vous pouvez définir vos propres niveaux dans <strong> Utilisateurs</strong> &gt; <strong>Niveaux d'accès</strong>. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p>L'onglet <strong>Paramètres de Catégorie</strong>:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Verrouillé</strong>. Sélectionnez "Oui" si vous voulez verrouiller la catégorie. Les modérateurs de site et les administrateurs sont les seuls à pouvoir créer de nouvelles réponses à la catégorie verrouillée.</p>
      </li>
      <li>
        <p align="justify"><strong>Revoir les Postes</strong>. Sélectionnez "Oui" si vous voulez que les postes soient examinés par les modérateurs avant de les publier dans cette catégorie.</p>
      </li>
      <li>
        <p align="justify"><strong>Autoriser les Messages Anonymes</strong> l'option vous permet d'activer / désactiver les messages anonymes pour la catégorie.</p>
      </li>
      <li>
        <p align="justify"><strong>Par défaut, réponses en option</strong>. Si des messages anonymes ont été activés, cette option sélectionne le choix par défaut de l'utilisateur.  Les utilisateurs réguliers peuvent ensuite modifier leurs propres messages pour être anonymes, mais seuls les modérateurs ont la possibilité de modifier les messages anonymes.</p>
      </li>
      <li>
        <p>Vous pouvez <strong>Autoriser Sondages</strong> pour la catégorie.</p>
	  </li>
	</ol>
<h5>Ajouter la page du forum au menu</h5>
<ol class="marked-list">
  <li>
    <p>Ouvrez les <strong>Menus</strong> &gt; <strong>Menu Principal</strong> &gt; <strong>Ajouter Nouvel Elément de Menu</strong>:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-24.jpg" alt=""></figure>
  <li>
    <p><strong>Sélectionnez</strong> le type d'élément de menu sur l'écran suivant.</p>
  </li>
   <figure class="img-polaroid"><img src="img/forum-25.jpg" alt=""></figure>
  <li>
    <p>Sélectionnez le type de menu Kunena Forum dans la fenêtre popup et vous verrez:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-26.jpg" alt=""></figure>
  <li>
    <p>Modifiez les paramètres de l'élément de menu, enregistrez le menu et  vérifiez votre site.</p>
  </li>
</ol>
