<h5><p>Essayons <strong>Chargement de l'image</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Appuyez sur le bouton <strong>Parcourir</strong> dans la section <strong>Sélection d'images</strong> et sélectionnez des fichiers multimédias sur votre ordinateur. </div>
  </li>
  <li>
    <div align="justify">Précisez <strong>Catégorie</strong> pour ces images [vous pouvez télécharger une série d'images pour chaque catégorie de cette façon], <strong>Titre Génériquee</strong> pour ces images [fPar exemple, si vous téléchargez 3 fichiers avec le titre générique &quot;test&quot;, elles apparaîtront dans votre Gestionnaire d'images comme test_1, test_2 and test_3]. </div>
  </li>
  <li>
    <div align="justify">Précisez <strong>Description Générique</strong> et <strong>Auteur</strong> si nécessaire. Définissez <strong>Type d'accès</strong>: public, super-utilisateurs, enregistré, spécial. </div>
  </li>
  <li>
    <div align="justify">Utilisez l'option <strong>Fichiers d'Images Spéciaux</strong>. Cette option permet de télécharger des fichiers d'images spéciaux. Choisissez cette option uniquement si votre téléchargement consiste exclusivement en fichiers .png ou .gif animés ou transparents. N'oubliez pas que les fichiers ne sont pas redimensionnés et sont donc affichés en taille réelle dans la vue détaillée.</div>
  </li>
  <li>
    <div align="justify"><strong>Mode Débogage</strong>. Avec <strong>Mode Débogage</strong> activé, toutes les étapes de traitement seront affichés. En cas d'erreurs, elles sont toujours affichées.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Cliquez sur le bouton <strong>Télécharger</strong> en bas s'il vous plaît, après la fin du processus de configuration</p>
</div></div>
<h5><p>Modification d'Elément de Galerie Existant</p></h5>
<div align="justify">Allez à la section <strong>Composants</strong> > <strong>JoomGallery</strong> > <strong>Gestionnaire d'images</strong>. Vous trouverez ici la liste des images qui ont déjà été téléchargées, afin que vous puissiez les éditer: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p>Dans le cas où vous devez les <strong>modifier</strong>, il faut effectuer les étapes suivantes: </p>
<ol class="marked-list">
  <li>Ouvrez un élément de la liste.
  </li>
  <li>Changez <strong> Titre</strong>, <strong>Catégorie</strong>, <strong>Description</strong>, <strong>Hits</strong>, etc. 
  dans l'onglet<strong> Image</strong>. Faites défiler vers le bas pour vérifier la <strong>Aperçu de miniature</strong> et <strong>Aperçu de l'image</strong>. </li>
  <li>
    <div align="justify">Utilisez l'option <strong>Caché</strong>. En masquant cette image, elle ne sera pas affichée dans la vue de catégorie ou reconnu par la recherche ou dans les topsites, mais elle sera toujours disponible par un lien directe. Si vous ne souhaitez pas qu'il soit disponible, veuillez le dépublier. Par example, en cachant des images, il est possible de les utiliser comme vignettes de catégorie sans les montrer dans la catégorie correspondante.</div>
  </li>
  <li>
    <div align="justify">Option <strong>En vedette</strong>. Ici, vous pouvez marquer une image comme "En vedette". Par exemple, le module JoomImages peut être configuré pour afficher uniquement des images en vedette sur une position de module.</div>
  </li>

<p>
  <figure class="img-polaroid">
    <img src="img/gallery-17.jpg" alt="">
    
  </figure>
</p>

  <li>Accédez à l'onglet <strong>Paramètres</strong> pour chnager <strong>Propriétaire</strong> et <strong>Auteur</strong> de cet objet de galerie.</li>

