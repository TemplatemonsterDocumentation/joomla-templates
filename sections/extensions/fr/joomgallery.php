    <div align="justify">Dans l'onglet <strong>Remplacer les Fichiers Image</strong> vous pouvez sélectionner des fichiers image de votre disque dur afin de remplacer les fichiers actuels correspondants dans la galerie.
	Téléchargez de nouvelles images à l'aide du bouton Parcourir.</div>
  </li>

