<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> est un composant qui permet de créer une galerie de photos complète sur Joomla.
 N'hésitez pas à verifier plus de détails sur le  <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">site officiel</a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Création de la Catégorie</p></h5>

<p>Allez à <strong>Composants</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Gestionnaire de Catégorie</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Appuyez sur le bouton  <strong>Nouveau</strong> dans le coin supérieur gauche: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
  <p align="justify">Entrez le <strong>Titre</strong> de catégorie et la <strong>Description</strong> avec les images et les paramètres <strong>Enregistrer</strong>. Vous pouvez spécifier aussi la <strong>Description Meta</strong> pour cette catégorie et définirdes restrictions d'accès si nécessaire.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-9.jpg" alt="">
