<h3>Affichage SW Pinterest</h3>


<p align="justify"><strong>SW Pinterest</strong> est un module pour afficher "Pinterest Board Widget". Ce module peut vous aider à afficher les épingles ou pinces de profil Pinterest. Largeur et Hauteur peuvent être dimensionnables.</p>
<figure class="img-polaroid">
    <img src="img/pinterest_feed-1.jpg" alt="">
</figure>

<p align="justify"> Allez au  <strong>Gestionnaire de Modules</strong>. Vous verrez un module sur la liste nommée &ldquo;<strong>Affichage SW Pinterest</strong>&rdquo;. Cliquez sur le titre pour le modifier ou cliquez sur le bouton &ldquo;<strong>Nouveau</strong>&rdquo; pour créer un nouveau module.</p>
