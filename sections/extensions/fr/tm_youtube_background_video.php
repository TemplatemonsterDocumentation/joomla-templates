<h3>TM Youtube background video module</h3>

<p>TM Youtube background video est un module qui permet d'ajouter vidéo de fond youtube sur le site.</p>

<h6>Toutes les options nécessaires sont disponibles dans la configuration de module:</h6>

<ol class="index-list">
    <li>Lien vidéo Youtube;</li>
    <li>Paramètres de lecture de la vidéo;</li>
    <li>Paramètres d'affichage sur les appareils mobiles;</li>
    <li>Paramètres des boutons ”Play/Stop”.</li>
</ol>
