<h3>Module Vidéo de Fond TM YouTube</h3>

<p><strong>Module Vidéo de Fond TM YouTube</strong> est un module pour ajouter une vidéo de fond youtube à votre site Web.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>Afin de configurer le module Vidéo de Fond TM YouTube, effectuez les étapes suivantes:</p></h5>
<ol class="marked-list">
<li><p>Accédez à l'onglet <strong>Extensions</strong> &gt; <strong>Modules</strong> :</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Ensuite, vous devez sélectionner le module <strong>c</strong> pour éditer.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Conseil:</strong> si vous voulez trouver tous les modules <strong>Vidéo de Fond TM YouTube</strong>, utilisez les filtres pour simplifier le processus. Appuyez sur le bouton &quot;<strong>Outil de Recherche</strong>&quot; et sélectionnez le type &quot;<strong>Vidéo de Fond TM YouTube</strong>&quot; dans la liste déroulante du filtre&quot;<strong>- Sélectionner le Type -</strong>&quot;. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>Vous pouvez cliquer sur le bouton &quot;<strong>Nouveau</strong>&quot; pour créer un nouveau module.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>Pour ouvrir les paramètres de module requis, cliquez sur son titre. [Modifions le module <strong>Home youtube</strong>.]</li>
<li>
  <div align="justify">L'interface d'édition du module <strong>Home youtube</strong> s'ouvre devant vous. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Appuyez sur le bouton <strong>Article</strong> pour ouvrir la fenêtre de sélection de l'article, comme montré ci-dessous. Vous pouvez utiliser le filtre pour simplifier le processus de recherche.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>Pour ajouter un nouvel article, utilisez <strong>Contenu</strong> &gt; <strong>Articles</strong> &gt; <strong>Ajouter un Nouvel Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Ici vous pouvez trouver  <strong>Onglet d'Options</strong>, où vous pourrez changer:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Préparer le Contenu</strong> – facultativement, prépare le contenu avec les plugins du Contenu Joomla. </p>
      </li>
      <li>
        <p><strong>Entrez URL de vidéo YouTube *</strong> – permet de spécifier l'URL personnalisée de la vidéo YouTube que doit être visible sur la page.</p>
      </li>
      <li>
        <p><strong>Commencer la Video</strong> – la vidéo commence à partir des secondes spécifiées.</p>
      </li>
      <li>
        <p><strong>Muet</strong> – change la vidéo en mode silencieux ou fort.</p>
      </li>
      <li><strong>Lecture Automatique</strong> – la vidéo démarre automatiquement, une fois que la page Web est chargée.</li>
      <li><strong>Afficher le bouton Lecture / Pause</strong> – vous pouvez afficher ou masquer les boutons.</li>
      <li> <strong>Texte du bouton Lecture / Pause</strong> – spécifie le texte des boutons.</li>
      <li>
        <p><strong>Image Mobile</strong> – pour afficher cette image sur des appareils mobiles au lieu d'une vidéo. [Pour télécharger des images, allez à la section <strong>Contenu </strong>&gt; <strong>Médias</strong>.]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>Pour changer de vidéo, il suffit de <strong>remplacer URL de la vidéo</strong> avec le <strong>lien de la vidéo youtube</strong> précédemment copié.</p>
  </li>
  <li> </p>
<div align="justify">Faites défiler la page vers le haut et cliquez sur le bouton vert <strong>Sauvegarder</strong> pour appliquer les modifications:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Revenez à votre site Web et actualisez la page avec le texte que vous avez modifié. Maintenant, on peut voir que l'édition a réussi. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">Dans nos modèles, le contenu <strong>Vidéo de Fond TM YouTube</strong> est présenté en chargeant un certain module ou position, par exemple, "{loadmodule articles_single, About us}". Afin d'éviter l'erreur 404 sur votre site Web, veuillez vous assurer que le nom du module chargé est identique à celui spécifié pour le chargement. Dans ce cas, le module "About us" devrait être publié. Le nom du module ne doit pas contenir plus d'un espace. 
Vous pouvez en apprendre davantage sur le module de charge/position de chargement au <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">site officiel</a>.
</p>
