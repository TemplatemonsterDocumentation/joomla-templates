</figure>

<p class="alert alert-warning">
Dans la plupart de nos modèles le contenu du module TM Parallax est présenté sous la forme d'un module spécifique ou de la position, par exemple, "{loadmodule articles_single, About us}". Pour éviter l'erreur 404, assurez-vous, que le nom du module coïncide avec celui indiqué lors de la connexion. Dans le cas présent, le module "About us" doit être dans l'état published et ne contenir qu'un seul espace dans le titre.
    Vous pouvez obtenir des informations détaillées sur loadmodule/loadposition sur <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">le site officiel</a>.
</p>
