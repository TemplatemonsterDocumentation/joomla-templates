<h3>Module TM Parallax</h3>

<p><strong>TM Parallax</strong> est un module qui utilise le script Parallax RD pour implémenter la fonctionnalité de parallaxe dans le modèle. </p>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-20.jpg" alt=""></p></figure>
<p align="justify">Le module sera maintenant disponible à partir du Gestionnaire de Modules, qui se trouve sous l'onglet <strong>Extensions</strong> dans le menu supérieur de l'administration. Vous pouvez créer des modules supplémentaires en cliquant sur le bouton <strong>Nouveau</strong>. Sélectionnez les modules que vous avez déjà créés pour les configurer. N'oubliez pas de les publier dans le bon positionnement et sur les bonnes pages.</p>

<h5><p>Afin de configurer le module <strong>TM Parallax </strong>, effectuez les étapes suivantes::</p></h5>
<li><p>Accédez à l'onglet <strong>Extensions</strong> &gt; <strong>Modules</strong>:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Ensuite, vous devez sélectionner le module <strong> TM Parallax</strong> pour éditer.</p>
      </li>
    <div class="alert alert-warning">
 <div align="justify"><strong>Conseil:</strong> si vous voulez trouver tous les modules <strong>TM Parallax </strong>, utilisez les filtres pour simplifier la recherche. Appuyez sur le bouton &quot;<strong>Outil de recherche</strong>&quot; et sélectionnez le type  &quot;<strong>Articles - Single</strong>&quot; dans la liste déroulante du filtre &quot;<strong>- Sélectionner le type-</strong>&quot; </div>
  <figure class="img-polaroid">
<p> <img src="img/tm-parallax-1.jpg" alt=""></p></figure></div>
<ol class="marked-list">
      <li>Pour ouvrir les paramètres du module requis, cliquez sur son titre.[Modifions le module <strong>Home parallax</strong>.]</li>
<li>
  <div align="justify">L'interface d'édition du module <strong> TM Parallax </strong> s'ouvre devant vous.            
  </div>
  <figure class="img-polaroid">
    <p> <img src="img/tm-parallax-2.jpg" alt=""></p></figure>
<li>Appuyez sur le bouton <strong>Article</strong> pour ouvrir la fenêtre de sélection de l'article, comme montré ci-dessous. </li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>
<div class="alert alert-warning">
    <div align="justify">Dans nos modèles, le contenu du <strong>Module TM Parallax</strong> est présenté en chargeant un certain module, par exemple, "{loadmodule articles_single, Home parallax article}". Afin d'éviter l'erreur 404 sur votre site Web, veuillez vous assurer que le nom du module chargé est identique à celui spécifié pour le chargement. Dans ce cas, le module "<strong>Home parallax article</strong>" devrait être publié. Le nom du module ne doit pas contenir plus d'un espace. </div>
	Vous pouvez en apprendre davantage sur le module de charge/position de chargement au <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">site officiel</a>.
  </p>
    </div>
  <p align="justify">Pour modifier le module <strong>Home parallax</strong>, accédez à l'onglet <strong>Extensions</strong> &gt; <strong>Modules</strong>. 
  Recherchez <strong>Home parallaxe Article</strong> en utilisant le champ de recherche. 
  Pour ouvrir les paramètres du module <strong>Home parallaxe Article</strong>, cliquez sur son titre.</p>
 <figure class="img-polaroid"> <p> <img src="img/tm-parallax-4.jpg" alt=""></p></figure>
  <p align="justify">Maintenant, vous avez la possibilité de modifier l'article. Pour modifier l'article, cliquez sur le bouton <strong>Éditer</strong> Et vous allez voir la fenêtre Modifier Article. Ici vous avez une option pour modifier le contenu de l'article et son image et ces liens.</p>
  <div class="alert alert-warning">
  <p>Pour ajouter un nouvel article, utilisez <strong>Contenu</strong> &gt; <strong>Articles</strong> &gt; <strong>Ajouter un Nouvel Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
  </div>
<p></p>
<li>Ouvrez l'onglet <strong>Avancé</strong>.</li>
