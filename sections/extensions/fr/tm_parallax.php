<h3>Module TM Parallax</h3>

<p>TM Parallax est un module qui utilise RD Parallax script pour mettre en œuvre l'effet de parallaxe dans le modèle.</p>

<h6>Toutes les options nécessaires sont disponibles dans la configuration de module:</h6>

<ol class="index-list">
    <li>Paramètres d'image;</li>
    <li>Paramètres de vitesse de l'effet parallaxe;</li>
    <li>Paramètres de la direction de l'effet parallaxe.</li>
</ol>
