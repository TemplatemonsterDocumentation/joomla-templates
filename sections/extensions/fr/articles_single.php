<h3>Articles Individuels</h3>

<p><strong>Articles Individuels</strong> est un module qui vous permet d'afficher un seul article dans la position désirée: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">Le seul paramètre inhabituel du module est <b>Lien Personnalisé</b>. Il vous permet d'afficher un lien personnalisé sous le contenu du module. Vous pouvez le lier à une URL externe ou à un élément de menu de votre site Joomla:</p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">Si vous souhaitez modifier la section &quot;<strong>HOW WE WORK</strong>&quot; , naviguez jusqu'à <strong>Extensions</strong> &gt; <strong>Modules</strong>. 
Ensuite, choisissez le module &quot;<strong>How we work</strong>&quot;, qui appartiennent à la catégorie <strong>Articles - Single</strong>.
</p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
 </p>
 <p>Le module  <strong>Articles Individuels</strong>  a les paramètres suivants:</p>
 <p align="justify"><strong>Sélectionner un Article.</strong> Ce champ contient l'article à afficher dans cette disposition.  Appuyez sur le bouton <strong>Sélectionner / Modifier</strong> pour ouvrir la fenêtre de sélection d'article, comme indiqué ci-dessous. </p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify"> Cet écran est similaire au <strong> Gestionnaire d'article.</strong>. Vous pouvez utiliser les <strong>Champs du filtre.</strong> pour trouver l'article désiré, puis cliquez sur le <strong>Titre</strong> de l'article pour le sélectionner. À ce moment, la fenêtre du module se ferme et le titre affiche dans le <strong>Champ de Sélectionner un Article</strong>.</p>
     <p align="justify">Maintenant, vous avez la possibilité de modifier l'article. Pour modifier l'article, cliquez sur le bouton <strong>Editer</strong> et vous verrez la fenêtre <strong> Modifier l'article </strong>. Ici vous avez une option pour modifier le contenu de l'article et son image et les liens.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify">Le module <strong>Articles Individuels</strong>  dispose des <strong>Options de l'article</strong>  suivants [Onglet d'Options], comme indiqué ci-dessous. Ces options déterminent la façon dont l'article s'affichera sur la mise en page  du site.Notez que ces options incluent &quot;<strong>Utiliser Global</strong>&quot;. Si cette option est sélectionnée, le paramètre des Options du Gestionnaire d'Article sera utilisé.</p>
    <figure class="img-polaroid">
     <p><img src="img/articles-single-8.jpg" alt="">
