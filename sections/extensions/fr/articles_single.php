<h3>Articles Single</h3>

<p>Articles single est un module qui vous permet d'afficher un seul article à l'endroit
    désiré. </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p>La seule option inhabituelle de ce module est « <b>Lien personnalisé</b> » (« <b>Custom
        Link</b> »). Celle-ci permet d'afficher un lien personnalisé en-dessous du contenu
    du module. Cela peut être un lien externe ou un élément de menu de Joomla.</p>
