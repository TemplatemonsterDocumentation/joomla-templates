<h3>Articles Single</h3>

<p>Articles single est un module qui vous permet d'afficher une article dans la position désirée: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p>Vous disposez de l'option <b>Custom Link (Lien personnalisé)</b>. Elle vous permet d'afficher un lien personnalisé en-dessous du contenu du module. Cela peut être un lien externe ou le lien vers la page de votre site Joomla:</p>

