<h3>Articles Newsflash (Advanced)</h3>

<p>Le module Articles Newsflash (Advanced) est une version améliorée du module Articles Newsflash fourni par défaut avec Joomla. En plus des fonctionnalités du module par défaut il offre plus d'options de configuration pour les modèles basés sur le Bootstrap:</p>

<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Options supplémentaires</h4>

<p><b>Number of columns (Nombre de colonnes)</b> - permet d'afficher les articles dans plusieurs colonnes.</p>

<p><b>Custom Link (Lien personnalisé)</b> - permet d'afficher un lien personnalisé en-dessous du contenu du module. Cela peut être un lien externe ou un élément de menu Joomla.</p>

<p><b>Pretext (Prétexte)</b> - permet d'afficher un texte personnalisé avant le contenu du module.</p>

<p><b>Published Date (Date de publication)</b> - affiche la date de publication de l'article. </p>

<p><b>Created by (Créé par)</b> - affiche l'auteur des articles.</p>

<p><b>Show intro images (Montrer l'image d'intro)</b> - permet d'afficher l'image d'introduction de l'article.</p>

<p><b>Intro image align (Alignement des images d'intro)</b> - permet d'aligner les images d'introduction.</p>

