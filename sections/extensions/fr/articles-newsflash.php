<p align="justify">Si vous souhaitez modifier la section &quot;<strong>WHAT WE OFFER</strong>&quot; sur votre site, sélectionnez-le dans la liste des modules. Allez vers <strong>Extensions</strong> &gt; <strong>Modules</strong>.</p>
