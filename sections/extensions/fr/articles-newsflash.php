<h3>Articles Newsflash [Advanced]</h3>

<p align="justify">C'est une version améliorée du module par défaut <strong> Articles Newsflash Joomla</strong>. Outre les fonctionnalités du module par défaut, il dispose d'options de configuration supplémentaires et de modèles basés sur Bootstrap. Il s'agit d'un module de qualité plus élevé pour céder la place  à afficher sur votre contenu. Facile à utiliser, facile à mettre à jour, avec quelques options de personnalisation grandes et puissantes.</p>

