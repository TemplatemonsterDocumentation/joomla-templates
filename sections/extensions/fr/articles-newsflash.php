<h3>Articles Newsflash</h3>

<p>Le module Articles Newsflash (avancé) est une version améliorée du module Articles
    Newsflash fourni par défaut avec Joomla. En plus des fonctionnalités du module par
    défaut, il offre plus d'options de configuration et des modèles de modules basés sur
    le Bootstrap.</p>

<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Options supplémentaires</h4>

<p><b>Number of columns (nombre de colonnes)</b> - permet d'afficher les articles dans
    plusieurs colonnes.</p>

<p><b>Custom Link (lien personnalisé)</b> - vous permet d'afficher un lien personnalisé
    en-dessous du contenu du module. Cela peut être un lien externe ou un élément de
    menu Joomla.</p>

<p><b>Pretext</b> - permet d'afficher un texte personnalisé avant le contenu du module.
</p>

<p><b>Published Date (date de publication)</b> - affiche la date de publication des
    articles.</p>

<p><b>Created by (créé par)</b> - afficher l'auteur des articles.</p>

<p><b>Show intro image (montrer l'image d'intro)</b>s - permet d'afficher l'image
    d'introduction des articles.</p>

<p><b>Intro image align (alignement des images d'intro)</b> - permet d'aligner les
    images d'introduction.</p>

