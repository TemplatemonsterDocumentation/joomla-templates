<h3>Plugin de Directive Cookie Joomla</h3>

<p><strong>Plugin de Directive Cookie Joomla</strong> sert à afficher une bannière d'acceptation des cookies en haut du site pour avertir le site en utilisant des cookies.</p>

<figure class="img-polaroid">
    <p><img src="img/cookies-1.jpg" alt="">
    </p></figure>
    <p align="justify">Pour configurer le plugin <strong>Joomla Cookie Directive</strong> naviguez vers <strong>Extensions</strong> &gt; <strong>Plugins</strong>. Pour trouver le plugin nécessaire parmi les autres, tapez &quot;cook/.../&quot; dans le champ de recherche.</p>
    <p align="justify">Cliquez sur le lien du titre &quot;<strong>system - EUCookieDirectiveLite</strong>&quot; et vous verrez les paramètres principaux du plugin <strong>Joomla Cookie Directive</strong>.</p>

<p>Cet écran vous permet de modifier les détails et les options des plugins.. </p>

<p>
  <figure class="img-polaroid">
    <img src="img/cookies-2.jpg" alt="">
  </figure>
</p>
<p>Lors de la configuration, vous pourriez avoir à traiter avec les paramètres suivants:</p>
