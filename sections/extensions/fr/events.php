    <p><img src="img/events-30.jpg" alt="">
    </p></figure>
    <h5>How output the Events Calendar to the Frontend</h5>
<p> Navigate to<strong> Component</strong> &gt; <strong>Events</strong>.</p>
<figure class="img-polaroid">
    <p><img src="img/events-33.jpg" alt="">
    </p></figure>
    <h5><strong>How to create new category</strong></h5>
    <p>Go to the <strong>Category</strong> tab and press the<strong> New</strong> button.</p>
    <figure class="img-polaroid">
    <p><img src="img/events-31.jpg" alt="">
    </p> </figure>

    <p>Enter the category title in the <strong>Title</strong> option field. You can add the category description. Save the category, pressing the <strong>Save</strong> button.</p>
  <figure class="img-polaroid">
    <p><img src="img/events-34.jpg" alt="">
</p>  </figure>
  <h5><strong>How to create new event</strong></h5>
  <p>Go to the <strong>Events </strong>tab and press the<strong> New</strong> button.</p>
  <figure class="img-polaroid">
    <p><img src="img/events-35.jpg" alt="">
    </p> </figure>
  <p>Enter the event title in the <strong>Title</strong> option field. You can add the event description. </p>
  <figure class="img-polaroid">
    <p><img src="img/events-36.jpg" alt="" /> </p> </figure>
    <p>Event settings:</p>
 
  <ol class="marked-list">
     
      <li><strong>Event title </strong> — enter the title of the event (the title will be displayed on the calendar);</li>
      <li><strong> Event image </strong> — select an image for the event (press select button);</li>
      <li><strong>Event time </strong>— set the event time;</li>
      <li> <strong>Event background color </strong>— choose the color of the event day calendar cell;</li>
      <li><strong> Event text color</strong> — choose the event main text color;</li>
      <li><strong>Event link color</strong> — choose the color of the link;</li>
      <li><strong>Event link hover color</strong> — choose the link hover color;</li>
      <li><strong>Event date background</strong> — choose the date section background color;</li>
      <li><strong>Event date border</strong> — choose the date  border color;</li>
      <li><strong>Event date color </strong>— choose the date color;</li>
      <li><strong> Description </strong> — enter the event description.</li>
</ol>
  <p>Select the category for the event. Note the<strong> Category</strong> field is required.</p>
  <figure class="img-polaroid">
    <p><img src="img/events-37.jpg" alt="" /> </p></figure>
    <p>Save the event, pressing the <strong>Save</strong> button.</p>
