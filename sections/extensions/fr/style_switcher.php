<h3>Module TM Ajax Style Switcher</h3>

<p align="justify"><strong>TM Ajax Style Switcher</strong> est un module permettant de passer des schémas de couleurs via l'interface.
Le module applique automatiquement des couleurs à partir du dossier templates\themexxx\css\ du thème actuel.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Remarque</strong>: sur la Démonstration en Direct, le modèle fonctionne en mode prévisualisation à l'aide de cookies. Sur votre site Web, le module fonctionnera en mode plein et sera disponible pour les utilisateurs autorisés ayant des droits d'utilisateurs SuperAdmin.</p>
<h5> Comment gérer le module TM ajax style switcher</h5>
<p align="justify">Pour configurer le module <strong>TM Ajax Style Switcher</strong> accédez à <strong>Extensions </strong>&gt;<strong>Modules</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify">Appuyez sur le bouton &quot;<strong>Outil de Recherche</strong>&quot; et sélectionnez le type <strong>TM Ajax Style Switcher Module</strong> dans la liste déroulante du filtre &quot;- Sélectionner le type -&quot;. Vous verrez le module <strong>Ajax Style Switcher</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Cliquez sur le lien du titre du module pour être apporté à la fenêtre de configuration du module.
  Il n'y a pas de paramètres spécifiques pour le module dans le panneau d'administration, juste les paramètres standard pour tous les modules:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Assurez-vous que le module est <strong>publié</strong>.</p>
  <h5><p>Pour activer le changeur de style, suivez les instructions ci-dessous:  </p></h5>
  <ol class="marked-list">
    <li>
      <p>Ajouter un nouvel utilisateur Joomla via <strong>Utilisateurs</strong> &gt; <strong>Gérer</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Activer</strong> un nouvel utilisateur.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Appuyez sur le Nouveau lien de connexion de l'utilisateur à apporter à la fenêtre de configuration. </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Assurez-vous d'assigner votre utilisateur au groupe &lsquo;<strong>Super Users</strong>&rsquo;. Accédez à l'onglet <strong>Utilisateurs Affectés</strong> et cochez &quot;<strong>Super Users</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Connectez-vous à votre site Web via le module <strong>Se connecter ou S'inscrire</strong> à l'avant:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>Le changeur de style a été activé avec succès. Actualisez votre site Web pour voir la boîte à outils de personnalisation: </p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
<p>Pour modifier la couleur de votre thème, suivez les étapes ci-dessous:</p>
  <ol class="marked-list">
    <li>
      <p>Sélectionnez le schéma de couleurs souhaité.</p>
    </li>
    <li>
      <p>Cliquez sur le bouton <strong>Sauvegarder</strong> pour enregistrer vos modifications et mettre à jour le jeu de couleurs.</p>
      
    </li>
     <p>
       <figure class="img-polaroid">
         <img src="img/style_switcher_14.jpg" alt="">
       </figure>
     </p>
     <div class="alert alert-warning">
    <div align="justify">
      <p>Si le schéma des couleurs  a été enregistré, cela signifie que tous les futurs visiteurs verront celui sur le site. </p>
      <p><strong>Notez!!!</strong>Personne sauf les utilisateurs affectés à un groupe <strong> Super Users</strong> peut changer le schéma de couleur du site Web.</p>
    </div></div>

  </ol>
