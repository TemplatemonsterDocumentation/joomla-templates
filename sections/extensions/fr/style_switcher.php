<h3>TM Ajax Style Switcher Module</h3>

<p><strong>Module TM Ajax Style Switcher</strong>  – le module qui permet de basculer entre les schémas de couleur via front-end. Le module applique automatiquement les palettes de couleurs depuis le dossier color_schemes\css\ du thème actuel.</p>

<p>Il n'y a aucuns paramètres spécifiques du module dans le panneau d'administration, il inclut la configuration standard du module:</p>

<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>

<p class="alert alert-warning">Veuillez noter que sur live demo du template le module fonctionne en mode de prévisualisation, en utilisant des cookies. Sur le site Web réel le module fonctionne dans un mode complet et il est disponible uniquement 
	pour les utilisateurs autorisés avec les droits de SuperAdmin.</p>
