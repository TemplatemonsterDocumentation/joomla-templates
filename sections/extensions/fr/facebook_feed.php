<h3>TM Facebook Page Plugin</h3>
 
<p>TM Facebook Page affiche votre page Facebook sur votre site:</p>

<figure class="img-polaroid">
    <img src="img/facebook_feed-1.jpg" alt="">
</figure>

<p>Ici vous pouvez voir les principaux paramètres du module TM Facebook Page. Le champ Facebook App ID est réquis à remplir. C'est un identificateur de l'application Facebook. Pour l'obtenir veuillez suivre ce lien: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a>
    et créer une application pour votre site. Vous pouvez obtenir des informations détaillées sur les autres paramètres sur <a href="https://developers.facebook.com/docs/plugins/page-plugin"
                       rel="nofollow" target="_blank">le site officiel</a>:</p>

