<h3>TM Facebook Page Plugin</h3>
 
<p><strong>TM Facebook Page</strong> est un module qui permet d'afficher votre page Facebook sur le site:</p>

<figure class="img-polaroid">
    <p><img src="img/facebook_feed-1.jpg" alt="">
    </p></figure>
    <p>Pour configurer le module <strong>TM Facebook Page Plugin</strong> naviguez à <strong>Extensions</strong> &gt; <strong>Modules</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Appuyez sur le bouton &quot;<strong>Outil de Recherche</strong>&quot; et sélectionnez le plugin <strong>TM Facebook Page</strong> dans le champ &quot;- Sélectionnez le type -&quot; de liste déroulante de filtre. Vous verrez le module <strong>Facebook Box</strong> .</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-4.jpg" alt="">
</p></figure>
    <p>Cliquez sur le lien du titre du module pour accéder à la fenêtre de configuration du module. </p>
 <figure class="img-polaroid">
    <p><img src="img/facebook_feed-5.jpg" alt=""></p></figure>
   <p>À l'onglet <strong>Module</strong> vous pouvez configurer les options suivantes:</p>
    <ol class="marked-list">
      <li> <strong>ID d'App Facebook</strong> *.</li>
      <li> <strong>URL de la Page Facebook</strong>.</li>
      <li> <strong>Nom de Votre Société</strong>.</li>
      <li> <strong>Largeu</strong>. La largeur du pixel du widget.</li>
      <li> <strong>Hauteur</strong>. Hauteur du pixel du widget. </li>
      <li> <strong>Afficher Amis&rsquo;s Visages</strong>. L'activation de cette option permet d'afficher les photos de profil dans le module.</li>
      <li> <strong>Afficher les Messages de Page</strong>. Afficher les Messages de la chronologie de le page.</li>
      <li> <strong>Cacher Photo de Couverture</strong>. Masquer la photo de couverture dans l'en-tête.</li>
    </ol>
<p align="justify">Ce champ <strong>ID d'App Facebook</strong> est requis. C'est un identifiant d'application Facebook. Pour obtenir l'identifiant, veuillez ouvrir le site: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a> and create an app for your website. </p>
<p align="justify">Il suffit de suivre les instructions simples, et vous obtiendrez l'identifiant en un rien de temps. </p>
<h5>Guide pas à pas</h5>
        <h5>1. Se connecter à Facebook</h5>
        <p>Avant de créer un compte de développeur, connectez-vous à votre compte Facebook.</p>
<a href="https://www.facebook.com/login.php">Login to Facebook</a>
        <h5>2. Compte de Développeur</h5>
        <p align="justify">Vous aurez besoin du compte développeur Facebook pour commencer.  
		Si vous n'avez pas àctualiser votre compte Facebook personnel à un compte développeur Facebook maintenant, ignorez cette étape si vous possédez déjà un compte développeur.</p>
<a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Créer un Compte de Développeur</a>
<h5>3. Créer une Nouvelle Application Facebook</h5>
        <p>Choisissez <a href="https://developers.facebook.com/apps/"><strong>Apps</strong></a> dans la navigation de l'en-tête et sélectionnez <strong>Ajouter Nouvelle Application</strong> ou utilisez le bouton ci-dessous.</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Create new Facebook App</a>
   <h5>4. Choisissez Plateforme</h5>
        <p align="justify">Choisissez la plateforme que vous souhaitez ajouter. Si votre application se trouve sur plusieurs plateformes, vous pourrez les ajouter à votre application Facebook plus tard. </p>
        <figure class="img-polaroid">
    <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
    <h5>5. Choisissez Nom</h5>
    <p align="justify">Choisissez un nom pour votre application et sélectionnez <strong>Créer un nouvel ID d'App Facebook</strong>. Choisissez une <strong>catégorie</strong> pour votre application. Si vous créez une version de test d'une application Facebook existante, sélectionnez l'application que vous testez.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
    <h5>6. Suivez les étapes &quot;Début Rapide&quot; </h5>
    <p align="justify">Démarrage rapide wizard vous aidera à configurer votre application Facebook pour la plate-forme sélectionnée. Vous pouvez revenir au démarrage rapide wizard  plus tard pour poursuivre votre configuration ou ajouter une nouvelle plate-forme à votre application.</p>

<h5>7. App ID</h5>
    <p align="justify">Votre application disposera un ID d'application unique. 
	Vous utiliserez cet identifiant chaque fois que vous utilisez l'un de nos <a href="https://developers.facebook.com/docs/apis-and-sdks">SDKs</a> ou <a href="https://developers.facebook.com/docs/sharing/webmasters/">Balises Open Graph pour le partage</a>. 
	Vous pouvez trouver votre ID d'application dans <a href="https://developers.facebook.com/apps/">le tableau de bord</a>.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
    <h5>8. Protégez Votre Secret d'Application</h5>
    <p align="justify">Facebook génère automatiquement un secret d'application associé à votre application.
	Le secret d'application authentifie les demandes faites par votre application sur les serveurs Facebook et doit être traité comme un mot de passe.</p>
<p align="justify">Ne publiez pas votre secret d'application Facebook dans votre balisage de page Web; soyez prudent lors de la saisie de fichiers contenant cette valeur dans un logiciel de contrôle de source tel qu'un dépôt Git ou Subversion public.</p>
<h5>9. Email du Contact</h5>
    <p align="justify">Vous devez ajouter un e-mail de contact pour chaque application. Accédez au <a href="https://developers.facebook.com/apps/">tableau de bord des applications</a> et sélectionnez l'élément de navigation<strong>Paramètres</strong> dans la barre latérale gauche et fournissez un email de contact valide.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
    <h5>10. Détails d'App</h5>
    <p>Sélectionnez les détails de l'application sur la barre latérale gauche pour afficher et ajouter des informations supplémentaires sur votre application.</p>
    <p align="justify">Si vous souhaitez que votre application être répertorié dans le centre d'App,  activez le paramètre "App Center Listed Platform" pour votre site web ou mobile. Ajoutez des icônes, des images promotionnelles et des vidéos. Assurez-vous de suivre <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">Règles d'applications</a>  pour être approuvé pour l'inscription dans App Center.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11. Désactiver le Mode de Développemente</h5>
    <p align="justify">Les nouvelles applications Facebook existent en mode de développement par défaut. 
	Cela limite les actions publiées et les objets connexes tels que l'étiquetage d'amis dans Facebook Timelines appartenant à un développeur de l'application.   </p>
<p align="justify">Une fois que votre application est prête pour un usage public, vous pouvez passer du mode de développement au mode public pour permettre à votre application d'interagir avec des comptes Facebook non associés à votre application</p>
   <figure class="img-polaroid">
    <p><img src="img/facebook_feed-12.jpg" alt="">
</p></figure>
  
    <p>Vous pouvez également configurer les options suivantes pour le module à l'onglet <strong>Module</strong> :</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-13.jpg" alt="">
</p></figure>
<ol class="marked-list">
      <li> <strong>Montrer le titre</strong>. Vous pouvez afficher ou masquer le titre du module sur l'écran. L'effet dépendra du style chrome dans le modèle.</li>
      <li>
        <div align="justify"> <strong>Position</strong>. Vous pouvez sélectionner une position de module dans la liste des positions prédéfinies ou entrez votre propre position de module en tapant le nom dans le champ et en appuyant sur Enter.</div>
      </li>
      <li><strong> Etat</strong>. Si publié, ce module affichera sur votre site Frontend ou Backend en fonction du module.</li>
      <li> Date de publication [<strong> Commencer la publication</strong>/<strong>Terminer la publication</strong>] Une date facultative pour Démarrer / Terminer Publication du module.</li>
      <li> <strong>Accès</strong>. Le groupe de niveau d'accès qui est autorisé à voir cet objet.</li>
      <li> <strong>Tri</strong>. Sélectionnez le tri..</li>
      <li> <strong>Langue</strong>. Attribuez une langue à ce module..</li>
    </ol>
<p align="justify">Les onglets <strong>Affectation des Menus</strong> &amp; <strong>Autorisations du module</strong> sont standard pour les modules Joomla. Vous pouvez configurer des éléments de menu pour que le module apparaisse et définir l'autorisation.</p>
    <p align="justify">Onglet <strong>Affectation des Menus</strong> :</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-14.jpg" alt="">
</p></figure>
    <p align="justify">Onglet <strong>Autorisations du module</strong> :</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-15.jpg" alt="">
</p></figure>
<p>À l'onglet <strong>Avancé</strong> vous verrez les options suivantes:</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-16.jpg" alt="">
</p></figure>
    <ol class="marked-list">
      <li> 
        <div align="justify"><strong>Suffixe de Classe de Module</strong>. Un suffixe à appliquer à la classe CSS du module. Cela permet un style de module individuel.</div>
      </li>
      <li> <strong>Tag du Module</strong>. Le tag HTML pour module.</li>
      <li> <strong>Taille Bootstrap</strong>. Une option pour spécifier combien de colonnes le module utilisera.</li>
      <li> <strong>Colonnes de Décalage</strong>. Augmenter la marge gauche d'une colonne par le nombre sélectionné de colonnes.</li>
      <li> <strong>Tag d'en-tête</strong>. Le tag HTML pour l'en-tête / titre du module.</li>
      <li> <strong>Classe d'en-tête</strong>. La classe CSS pour l'en-tête / titre du module..</li>
      <li> <strong>Style de Module</strong>. Utiliser cette option pour remplacer le style de modèle de sa position.</li>
    </ol>
