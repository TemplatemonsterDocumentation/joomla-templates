<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> est le composant d'abonnement pour Joomla. AcyMailing est un  bulletin électronique et une extension de marketing par e-mail pour Joomla. AcyMailing vous permet de gérer efficacement un nombre illimité d'abonnés, les organiser en listes de diffusion et envoyer des bulletins d'information personnalisés.</p>
<p>Sur votre site Web, vous verrez le formulaire d'abonnement suivant:</p>
<figure class="img-polaroid">
    <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Accédez à <strong>Components &gt; AcyMailing</strong> et vous serez redirigé vers au tableau de bord AcyMailing.</p>
<figure class="img-polaroid">
    <img src="img/acymailing-4.jpg" alt="">
</figure>


<p align="justify"> Pour voir les paramètres principaux de cette extension, aller à <strong>Extensions</strong> &gt; <strong>Modules</strong>. 
Cliquez sur le bouton  <strong>Nouveau</strong> .</p>
<figure class="img-polaroid">
  <img src="img/acymailing-6.jpg" alt="">
</figure>
<p align="justify">Sélectionnez le module <strong>Module Abonner AcyMailing</strong> dans la liste des nouveaux modules.</p>
