<h3> Module TM Ajax Contact Form</h3>

<p>TM Ajax Contact Form Module - est un module de formulaire de contact pour Joomla.
Ce module permet d'ajouter un formulaire de contact sur n'importe quelle page de votre site Joomla:</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>

<p>L'envoi de données se fait à l'aide de requêtes AJAX, la page n'est donc pas rechargée.
Les paramètres permettent d'ajouter de la protection contre les robots (reCaptcha), d'ajouter les champs obligatoires, ses titres et de définir l'adresse e-mail du destinataire,
   ainsi qu des messages d'erreur et de succès. Vous pouvez consulter la liste complète des paramètres ci-dessous:</p>

<figure class="img-polaroid">
    <img src="img/contact-form-2.png" alt="">
</figure>
<p>Les paramètres de ce module vous permet de gérer toutes les fonctions du formulaire de contact. Vous pouvez activer des champs de tout type (texte, email, case à cocher, textarea). Pour cette raison, le formulaire peut être personnalisé à votre guise:</p>
<figure class="img-polaroid">
