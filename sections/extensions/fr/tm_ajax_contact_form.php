<h3>Module Formulaire de Contact TM Ajax</h3>

<p align="justify"><strong>Module Formulaire de Contact TM Ajax</strong> est un module de formulaire de contact pour Joomla. Ce module permet d'ajouter un formulaire de contact à n'importe quelle page de votre site Web.</p>
<p align="justify"><strong>Formulaire de contact </strong> est essentiellement un ensemble de questions sur la page du site, remplies par votre visiteur qui est automatiquement envoyé à votre email. Habituellement, un formulaire de contact comprend des champs pour remplir le nom, l'adresse, le téléphone, l'adresse électronique et le type de commentaire. Une fois que les visiteurs ont rempli les champs de formulaire de contact, ils cliquent sur le bouton Soumettre [Envoyer] pour soumettre les informations.</p>
