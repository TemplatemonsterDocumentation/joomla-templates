<h3>Module de Formulaire de Contact en Ajax TM</h3>

<p>Le Module de Formulaire de Contact en Ajax TM est un module de formulaire de contact
    en ajax pour Joomla. Ce module permet d'ajouter un formulaire de contact sur
    n'importe quelle page Joomla</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>

<p> L'envoi de données se fait à l'aide de requêtes AJAX,
    la page n'est donc pas rechargée. Les paramètres du module permettent d'ajouter de
    la protection contre les robots (reCaptcha), de définir les champs du formulaire de
    contact, leurs entêtes, de définir l'adresse e-mail du destinataire, d'ajouter
    l'adresse CC, ainsi qu des messages d'erreur et de succès. Vous pouvez consulter la
    liste complète des paramètres ci-dessous.</p>

<figure class="img-polaroid">
    <img src="img/contact-form-2.png" alt="">
</figure>
<p>Dans les réglages de ce module vous pouvez trouver toutes les fonctionnalités du constructeur des formulaires, vous avez la possibilité d'ajouter des champs de tout type (texte, email, case à cocher, textarea). Pour cette raison, le formulaire peut être personnalisé à votre guise.</p>
