<h3>CarouFredSel</h3>

<p>CarouFredSel est un module de carrousel d'images développé comme combinaison du
    module par défaut de Joomla « Articles Newsflash » et du plugin jQuery
    CarouFredSel. </p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>
<p>Toutes les options nécessaires au carrousel sont disponibles dans les réglages du
    module. </p>

<ol class="index-list">
    <li>Direction du carrousel</li>
    <li>Largeur des éléments</li>
    <li>Éléments visibles</li>
    <li>Options d'animation</li>
    <li>Navigation et contrôles</li>
</ol>


<p>Le plugin CarouFredSel a bien plus d'<a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">options
        de configuration</a>. Si vous voulez ajouter vos options, vous pouvez éditer le
    fichier « <b>\modules\mod_caroufredsel\tmpl\default.php</b>. » </p>
