<h3>TM Addthis</h3>

<p>TM Addthis - les plugins qui permettent d'afficher les boutons de partage
    social(«partager»).</p>

<figure class="img-polaroid"><img src="img/addthis-1.jpg" alt=""></figure>

<p>Ici vous pouvez voir les principaux paramètres du plugin TM Addthis. Entrez l'ID de
    votre profil dans le champ de profil ID. Afin d'obtenir l'ID de profil, vous devez
    vous inscrire et créer le profil à la page suivante <a
        href="https://www.addthis.com/settings/publisher" target="_blank"
        rel="nofollow">https://www.addthis.com/settings/publisher</a>.</p>

