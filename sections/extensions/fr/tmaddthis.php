<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> est la plus grande plateforme de partage de contenu du monde. Il fournit des outils qui facilitent le partage de contenu sur Internet, et aide les éditeurs à promouvoir l'information.
<strong> AddThis</strong> Peut être intégré dans un site Web avec l'utilisation de widget. Une fois que le widget est ajouté, les visiteurs du site Web peuvent partager un élément en utilisant une variété de services, tels que Facebook, MySpace, Google Bookmarks, Pinterest et Twitter. </p>
<p><strong>TM Addthis </strong> est un plugin pour afficher des boutons sociaux:</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br/>L'ajout de boutons de partage sur votre site Joomla est un excellent moyen d'obtenir plus de votre contenu distribué en ligne. Plus votre contenu est partagé, plus le trafic ramènera à votre site. Les boutons d'actions AddThis sont faciles à ajouter, qui vous aidera à devenir un éditeur plus efficace.. </p>
<p><strong>CARACTÉRISTIQUES</strong></p>
<ul>
  <li>On choisit où les boutons de partage apparaissent sur votre page;</li>
  <li>On contrôle le nombre de boutons que vous souhaitez afficher;</li>
  <li>On inclut le nombre pour encourager le partage;</li>
  <li>On peut personnaliser les boutons de partage à chaque utilisateur ou les choisir lui-même.</li>
</ul>
<p align="justify">Pour personnaliser le plugin, allez à <strong>Extensions</strong> &gt; <strong>Plugins</strong>. Cela vous amènera au <strong>Gestionnaire de plugins</strong>. Tapez &quot;Add&quot; dans le champ de recherche et vous verrez <strong>Content - TM AddThis</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>Tout d'abord, vous devez <strong>activer</strong> le plugin. Appuyez sur le bouton rouge &quot;X&quot; pour l'activer. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Une fois le plugin est <strong>activé</strong>, vous verrez un message de confirmation.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Pour obtenir la fenêtre de configuration principale du plugin, appuyez sur le lien du titre <strong>Content - TM AddThis</strong> et vous serez alors redirigé vers  la fenêtre suivante:</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Ici vous pouvez voir les <strong>paramètres TM Addthis </strong> principaux. Définissez les <strong>catégories à exclure</strong> 
dans le champ correspondant. Dans le champ <strong>ID de Profil</strong> veuillez entrer votre ID de profil que vous pouvez obtenir après l'enregistrement et la création d'un compte à la page suivante: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>Fondamentalement, vous devriez obtenir un nouvel  <strong>ID de Profil</strong> pour changer les icônes sociales de partage <strong>AddThis</strong>:</p>
<p>Une fois que vous avez enregistré, vous devez ajouter des boutons de <strong>partage</strong>. </p>
<p>Pour ce faire, appuyez sur le bouton &quot;<strong>AddThis</strong>&quot; sur la barre d'outils de profil.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Ensuite, vous devez appuyer sur &quot;<strong>Commencer</strong>&quot;  dans la section Boutons de Partage.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Activer Boutons de Partage<strong>En ligne </strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">Vous pouvez choisir le type <strong>Compteurs d'actions</strong>. Vous pouvez également sélectionner vos propres réseaux sociaux et personnaliser leur ordre en utilisant en utilisant l'option <strong>Sélectionner votre propre</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Modifiez la conception des icônes dans la section  <strong>Conception</strong>. Personnalisez le <strong>Style</strong>, <strong>Taille du bouton</strong> et <strong>Couleurs</strong>  des icônes. Réglez le paramètre <strong>Angles arrondis</strong> et <strong>Masquer les noms des réseaux</strong> au besoin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Lorsque vous avez terminé avec la personnalisation des  <strong>Boutons de Partage</strong>, appuyez sur le bouton <strong>Sauvegarder&amp;Continuer</strong>  pour passer à l'étape suivante.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Copiez votre ID de profil dans la section &quot;<strong>1</strong>&quot;. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>Vous devez l'insérer dans le champ <strong>ID de Profil</strong> dans la fenêtre des paramètres <strong>Content - TM AddThis</strong> pour que les nouveaux <strong>Boutons de Partage</strong> apparaissent sur votre site Web.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Vous avez une option pour exclure les catégories [pages] où vous ne souhaitez pas placer le plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Assurez-vous d'avoir <strong>activé</strong> le plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Enregistrez les modifications et actualisez la page <strong>Boutons de Partage</strong>. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-18.jpg" alt=""></figure>
</p>