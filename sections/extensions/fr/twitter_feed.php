<h5>Trouvez votre ID de widget Twitter</h5>
<p align="justify">Si le Widget Twitter, que vous essayez d'activer, demande un ID de Widget Twitter, vous devrez créer un nouveau widget à partir de votre page <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">Paramètres du Widget Twitter</a>.</p>
<p align="justify">Pour accéder aux paramètres du widget Twitter, connectez-vous au compte Twitter que vous souhaitez utiliser avec votre widget. Cliquez ensuite sur la roulette située dans le coin supérieur droit et cliquez <strong>Paramètres</strong>. Dans la barre latérale des paramètres, sélectionnez l'onglet  <strong>Widgets</strong> tout en bas.</p>

<p align="justify">Sur cette page, recherchez le bouton <strong>Créer un nouveau</strong> et appuyez-le pour créer un nouveau widget. À partir de là, des options de configuration et un aperçu du widget vous seront proposés. Vous pouvez toujours modifier les options de configuration des widgets plus tard.</p>
<p align="justify">Après avoir créé le widget, le bouton <strong>Créer un nouveau</strong>  sera remplacé par un bouton <strong>Sauvegarder Modifications</strong>. Une fois que votre widget est créé, vous pouvez saisir l'ID du widget à partir de l'URL. Regardez la barre d'adresse de votre navigateur.Il devrait avoir une URL qui ressemble à quelque chose comme ca:</p>
 <blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p>Votre ID de widget est  numéro en 18 chiffres. Dans ce cas, <strong>512253083833733120</strong>. N'oubliez pas ce numéro privé, de sorte que votre widget ne sera pas utilisé par d'autres sans votre permission.</p>
<p>Copiez votre ID et collez-le dans le champ qui demande votre ID de widget. Après avoir enregistré les paramètres, votre nouveau widget Twitter doit être fonctionnel.</p>
