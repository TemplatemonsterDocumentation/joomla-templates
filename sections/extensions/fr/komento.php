<h3>Commentaires de Komento</h3>

<p>Komento est un composant de commentaires pour Joomla. Il permet d'ajouter des
    commentaires aux articles Joomla. </p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Le composant Komento est accessible sur le panneau d'administration de Joomla, dans
    la section « <b>Composants > Komento</b> » (« <b>Components > Komento</b> »).</p>

<p>Pour accéder à la configuration des réglages, allez dans « <b>Composants > Komento >
        Intégrations</b> » (« <b>Components > Komento > Integrations</b> ») et cliquez sur
    « <b>Articles</b>. »</p>

