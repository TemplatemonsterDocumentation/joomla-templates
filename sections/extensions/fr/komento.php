<h3>Commentaires de Komento</h3>

<p>Komento - est un composant de commentaires pour Joomla. Il permet d'ajouter des commentaires 
   aux articles Joomla:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Le composant Komento est accessible dans le panneau d'administration de Joomla dans le menu <b>Components (Composants) > Komento</b>.</p>

<p>Pour accéder à la configuration du module allez dans <b>Components (Composants) > Komento >
        Integrations (Intégrations)</b> et cliquez sur <b>Articles (Articles)</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>En utilisant les réglages de composant, vous pouvez activer les commentaires pour des catégories et des articles, ajouter captcha, des profils d'utilisateurs, etc. Vous pouvez en apprendre plus à propos de Komento sur <a
        href="http://stackideas.com/docs/komento.html" target="_blank">le site officiel</a>.</p>

<p>Nos templates n'incluent que la version standard de Komento. Pour accéder aux fonctionnalités additionnelles de ce module,  nous recommandons utiliser <a href="http://stackideas.com/komento/plans">Komento Professional</a>. La liste complète des fonctionnalités est disponible sur le site: <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
   Avec Komento Professional vous recevrez également un support technique professionnel.</p>
