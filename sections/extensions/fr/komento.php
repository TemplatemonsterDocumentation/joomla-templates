<p>En utilisant les réglages des composants, vous pouvez activer les commentaires pour
    des catégories spécifiques, ajouter captcha, des profils d'utilisateurs, ainsi
    qu'aux articles Joomla, etc. Vous pouvez en apprendre plus à propos de Komento sur
    le <a href="http://stackideas.com/docs/komento.html" target="_blank">site
        officiel</a>.</p>

