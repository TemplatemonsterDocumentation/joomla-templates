<h3>Commentaires Komento</h3>

<p><strong>Komento</strong> Eet une extension de commentaire avancée pour Joomla qui permet aux visiteurs de commenter sur les articles et les blogs.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Commentaires Komento sont accessibles sous onglet <b>Composants > Komento</b> de votre panneau d'administration Joomla.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Comment gérer les intégrations de composants Komento</p></h5>
<ol class="marked-list">
  <li>
    <p>Connectez-vous à votre panneau d'administration Joomla, allez à <strong>Composants</strong> &gt; <strong>Komento</strong> &gt; <strong>Intégrations</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Cliquez sur <strong>Joomla Article</strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">Dans l'onglet Général, vous pouvez <strong>Activer </strong> ou <strong>Désactiver</strong> les commentaires.
	Vous pouvez également modifier <strong>Modération</strong> et autres paramètres. </p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li><p>Nous avons <strong>désactivé</strong> [No] commentaires [vérifiez la capture d'écran ci-dessous]. </p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>Le bloc des commentaires sera désactivé sur le site Web.</p></li>
  <p><strong>Activé</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Désactivé</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Nous avons <strong> activé </strong>les commentaires. Vous pouvez attribuer Komento à toutes les catégories ou sélectionner des catégories particulières. </p>
  </li>
  <li>Nous avons assigné des commentaires à &ldquo;<strong>Company</strong>&rdquo;.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Vous pouvez également <strong>désactiver</strong> les commentaires d'un article particulier.
	Ouvrez l'article dans le <strong>Gestionnaire d'articles [Contenu &gt; Articles]</strong> et insérez le code suivant dans le corps de l'article:</p>
    <pre>{KomentoDisable}</pre>
  </li>
  <figure class="img-polaroid"><img src="img/komento-10.jpg" alt=""></figure>
  <li>
    <p>Sauvegarder les modifications. Les commentaires n'apparaissent pas sur cet article.</p>
  </li>
</ol>
<h5><p>Comment éditer Komento &quot;Termes et conditions&quot;</p></h5>
  <p align="justify">Le texte de Komento <strong>Termes et Conditions</strong> est affiché sur des pages d'article unique. Les visiteurs du site doivent accepter ces conditions afin de pouvoir soumettre leurs commentaires si cette option est activée. Veuillez vous référer à la capture d'écran ci-dessous:: </p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Suivez les étapes suivantes s'il vous plaît pour modifier le texte:</p>
<ol class="marked-list">
  <li>
    <p>Ouvrez votre panneau d'administration Joomla et accédez au menu <strong>Composants</strong> &gt; <strong>Komento</strong> &gt; <strong>Intégrations</strong>:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Ouvrez le composant &ldquo;<strong>Joomla Article</strong>&rdquo;. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p>Passez ensuite à la section <strong>Disposition</strong> et allez à l'onglet <strong> Formulaire de commentaire</strong>:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>Tout d'abord, sélectionnez le groupe d'utilisateurs requis au bas de la page, à la section Activer les Termes et Conditions. </p>
 Par défaut, il est défini camme Visiteur, vous pouvez choisir un autre groupe d'utilisateurs.
  <p>We enable <strong>Terms and Conditions</strong> to all the users' type.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>Ensuite, vous pouvez modifier le texte de case à cocher  dans le <strong>champ de saisie des Termes et Conditions</strong>. </p>
 Veuillez saisir le texte souhaité ici et enregistrer les modifications en appuyant sur le bouton <strong> auvegarder</strong> dans le coin supérieur gauche.
  </li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

