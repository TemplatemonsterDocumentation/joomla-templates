<h3>TM Instagram</h3>

<p>TM Instagram - le module qui permet d'afficher "Instagram Widget"</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Ici vous pouvez voir les principaux paramètres de TM Instagram. Entrez le nom du
    profil d'Instagram dans le champ Nom d'utilisateur d'Instagram, entrez le numéro
    désiré d'images dans le champ Nombre de photos, définissez l'ID de votre application
    d'Instagram dans le champ appelé Votre d'Instagram CLIENT ID. Afin d'obtenir l'ID,
    suivez le lien <a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                      target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    où vous devez créer un compte. Ensuite, suivez ce lien <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> et créez
    l'application. Après cela vous obtiendrez vos données d'applications où vous verrez
    l'ID client.</p>

