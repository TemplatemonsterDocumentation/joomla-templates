<h3>TM Instagram</h3>
 
<p>TM Instagram - le module qui permet d'afficher widget Instagram:</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Ici vous pouvez voir les principaux paramètres du module TM Instagram. Entrez le nom du profil Instagram dans le champ Instagram username (Nom d'utilisateur d'Instagram), dans le champ Number of photos (Nombre de photos) entrez le numéro désiré d'images, affichées dans un widget, dans un champ Your Instagram CLIENT ID (Votre d'Instagram CLIENT ID) définissez l'ID de votre application d'Instagram. Afin d'obtenir l'ID, suivez le lien
<a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                 target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    où vous devez créer un compte. Ensuite, suivez ce lien
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> et créez l'application. Après cela vous obtiendrez vos données d'applications où vous verrez l'ID client 
		(CLIENT ID):</p>
 
