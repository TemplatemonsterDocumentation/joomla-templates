<h3>Swiper</h3>

<p>Swiper Module - un diaporama responsive, une combinaison du module par défaut de Joomla Articles Newsflash et du plugin jQuery open source Swiper. Conçu principalement pour IOS, ce plugin fonctionne parfaitement sur les derniers Android, Windows Phone 8 et dans les navigateurs modernes de PC:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Swiper Module a diverses options de configuration et il est placé dans le menu <b>Extensions ( Extensions)
        > Module Manager (Gestionnaire de modules)</b> de votre panneau d'administration de Joomla. Toutes les options principales du plugin jQuery Swiper 
		peuvent être configurées dans les réglages du module. </p>

<p>Faites glisser le curseur de la souris sur le titre d'une option pour en voir sa description. Vous pouvez également en apprendre plus à propos du plugin jQuery Swiper sur <a
        href="http://idangero.us/swiper/" target="_blank">le site officiel</a>.</p>
<p>Tous les paramètres requis de carrousel sont disponibles dans les paramètres du module:</p>

<ol class="index-list">
    <li>Paramètres de lecture;</li>
    <li>Paramètres de la hauteur;</li>
    <li>Éléments visibles;</li>
    <li>Paramètres d'animation;</li>
    <li>Navigation et contrôles;</li>
    <li>Paramètres de miniatures.</li>
</ol>

