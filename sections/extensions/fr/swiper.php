<h3>Swiper</h3>

<p align="justify"><strong>Swiper Module</strong> est un curseur adaptatif merveilleux, basé sur le module standard Newsflash de Joomla Articles et le plugin gratuit jQuery Swiper. Créé pour IOS, ce plugin fonctionne parfaitement dans les derniers navigateurs Android, Windows Phone 8, etc..</p>
<p align="justify">Vérifions quels types de diapositives nous pouvons ajouter au site en utilisant ce module. Tout d'abord, il pourrait être une diapositive animée régulière avec une image et une légende: </p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">Sinon, le curseur pourrait être une diapositive avec la vidéo d'arrière-plan ou d'une diapositive configurable avec le fond d'image de parallaxe. Il ya en fait une vaste gamme d'options à choisir. Apprenons à les utiliser.</p>

<ol class="marked-list">
