<h3>Owl Carousel</h3>

<p>Owl Carousel est un module de carrousel d'images développé comme combinaison du module par défaut de Joomla Articles Newsflash,
     et du plugin jQuery Owl Carousel:</p>

<figure class="img-polaroid">
    <img src="img/owl-carousel.png" alt="">
</figure>

<h6>Toutes les options nécessaires sont disponibles dans les réglages du module: </h6>

<ol class="index-list">
    <li>Largeur de l'élément;</li>
    <li>Éléments visibles;</li>
    <li>Options d'animation;</li>
    <li>Navigation et contrôles.</li>
</ol>


