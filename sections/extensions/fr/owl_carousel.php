<h3>Carrousel Owl</h3>
<p><strong>Carrousel Owl</strong> est une combinaison de module par défaut Joomla Articles Newsflash et le plugin Owl Carousel jQuery.</p>
<figure class="img-polaroid">
  <img src="img/owl-carousel-1.jpg" alt="">
</figure>
<h5>Comment accéder</h5>
<p>Pour 'ajouter' un nouveau module<strong>Carrousel Owl</strong> accédez au Gestionnaire de modules:</p>
<p>Sélectionnez <strong>Extensions</strong> &gt; <strong>Modules</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Cliquez sur le bouton '<strong>Nouveau</strong>' et appuyez sur <strong>Carrousel Owl</strong> dans la fenêtre contextuelle du module.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Détails</h5>
<ol class="marked-list">
  <li><strong>Titre:</strong> Le module doit avoir un titre [dans notre cas, il est <strong>Carrousel</strong>].</li>
</ol>
<h5>Module</h5>
<p>Le module <strong>Carrousel Owl</strong> affichera un nombre fixe d'articles d'une catégorie spécifique ou d'un ensemble de catégories.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Catégorie</strong>: Sélectionnez les articles d'une catégorie spécifique ou un ensemble de catégories. Si aucune sélection, toutes les catégories par défaut seront affichées. </div>
  </li>
  <li><strong>Images</strong>: [Masquer/Afficher]. Afficher les images de l'article.</li>
  <li><strong>Titre de l'article</strong>: Afficher/Masquer le titre de l'article.</li>
  <li><strong>Titres Liés</strong>: [Utiliser Global/Non/Oui]. Lier les titres d'article aux articles.</li>
  <li> <strong>Date de Publication</strong>: Afficher la date de publication d'un article.</li>
  <li><strong>Police d'en-tête</strong>: [h1/h2/h3/h4/h5]. Sélectionnez la police HTML souhaitée pour les titres d'article.</li>
  <li><strong>Afficher Images d'introduction</strong>: Sélectionnez cette option si vous souhaitez afficher des images d'introduction d'article.</li>
  <li><strong> Image d'introduction Alignée</strong>: [Aucun/Gauche/Droite] Sélectionnez l'alignement de l'image d'introduction. On ignore les paramètres de l'article.</li>
  <li>
    <div align="justify"><strong>Lien 'Lire la suite...' </strong> [Afficher/Masquer]. Si vous choisissez Afficher, le Lien 'Lire la suite...'  affichera si le texte principal a été prévu un article.</div>
  </li>
  <li><strong>Nombre d'articles</strong>: Le nombre d'articles à afficher dans ce module.</li>
  <li>
    <div align="justify"><strong>Résultats de Ordre</strong>: [date de publication/Date de création/Ordre/Aléatoire]. 
	Sélectionnez l'ordre dans lequel les résultats de l'ordre doivent être présentés.</div>
  </li>
  <li><strong>Prétexte</strong>: Vous pouvez saisir le texte que vous souhaitez afficher avant le contenu du module. Ne supporte pas les balises HTML.</li>
</ol>
<h5>Détails Communs</h5>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Montrer le Titre</strong>. [Afficher/Masquer] Choisissez d'afficher ou de masquer le titre des modules à l'avant. Le titre sera celui dans le Champ de Formulaire ci-dessus.</div>
  </li>
  <li>
    <div align="justify"><strong>Position du Module</strong>. Choisissez la position du module dans laquelle vous souhaitez que ce module soit affiché. La position du module personnalisé peut être saisie pour une utilisation avec la position de charge ou appuyez sur le bouton de position pour sélectionner une position de module à partir du modèle.</div>
  </li>
  <li><strong>Etat</strong>. [Publié/Non publié/Poubelle] L'état publié de l'article.</li>

  <li><strong>Accès</strong>. Le niveau d'accès de visualisation pour cet élément.</li>

  <li>
    <div align="justify"><strong>Ordre du Module</strong>. Cela montre un menu déroulant de chaque module dans la position que le module actuel se trouve. Il s'agit de l'ordre dans lequel les modules s'affichent lorsqu'ils sont affichés à l'avant ainsi que dans le Gestionnaire de modules.</div>
  </li>

  <li>
    <div align="justify"><strong>Commencer à publier</strong>. La date et l'heure de publication. Utilisez ce champ si vous souhaitez saisir du contenu à l'avance (à venir) et le faire publier automatiquement à une date ultérieure.</div>
  </li>

  <li>
    <div align="justify"><strong>Finir à publier</strong>. La date et l'heure de fin de publication. Utilisez ce champ si vous souhaitez que le contenu soit automatiquement changé en état non publié à une date ultérieure [par exemple, lorsqu'on n'est plus applicable].</div>
  </li>

  <li><strong>Langue</strong>.Langue de l'article.</li>

  <li>
    <div align="justify"><strong>Note</strong>. La note d'article. C'est normalement pour les administrateurs du site [par exemple, pour documenter des informations sur cet article]  et ne s'affiche pas à l'extrémité avant du site.</div>
  </li>
</ol>
<p>Maintenant, il fuat définir les paramètres du module comme indiqué dans l'image ci-dessous 
[Vous devez choisir le type de position <strong>feature</strong> dans le champ Position]:</p>
<figure class="img-polaroid">
    <p><img src="img/owl-carousel-6.jpg" alt="" /></p></figure>
    <p align="justify">Ensuite, vous choisissez la page, où placer le module. Vous devez utiliser l'onglet <strong>Affectation des Menus</strong> à cette fin. </p>
  <p align="justify">Il existe une liste d'éléments de menu dans cet onglet. Vous pouvez choisir entre 'Sur Toutes les Pages', 'Pas de Pages', 'Seulement sur les Pages Sélectionnées' et 'Sur Toutes les Pages sauf Sélectionnées dans la Liste'. Si les deux dernières options sont sélectionnées, une liste illustrée avec tous les éléments de menu activés comme indiqué dans la capture d'écran ci-dessus. Cela vous permet d'affecter des modules à certaines pages, mais pas à toutes, et en sélectionnant les liens de menu que vous souhaitez associer au module vous pouvez personnaliser sur quelles pages les modules apparaissent/n'apparaissent pas. </p>
    <p align="justify">Sélectionnez la page <strong>Blog</strong> et le module apparaît uniquement sur cette page du site Web. </p>
    <p align="justify">N'oubliez pas d'appuyer sur le bouton <strong>Sauvegarder</strong>.</p>
