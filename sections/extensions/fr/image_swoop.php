<h3>Image Swoop</h3>

<p>Le module « Image Swoop » est une glissière réceptive exceptionnelle développée comme
    une combinaison du module par défaut de Joomla « Artiles Newsflash » et du plugin
    jQuery open source « Camera Slideshow. » Fonctionne parfaitement sur les ordinateurs
    de bureau et sur les appareils mobiles iOS et Android. Compatible avec tous les
    navigateurs modernes à partir d'Internet Explorer 8.</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>Le module « Image Swoop » a diverses options de configuration. Vous pouvez y accéder
    en ouvrant le module Image Swoop dans la section « <b>Extensions > Gestionnaire de
        modules</b> » (« <b>Extensions > Module Manager</b> ») de votre panneau
    d'administration de Joomla. Toutes les options principales du plugin jQuery « Camera
    Slideshow » peuvent être configurées dans la section des réglages du module. </p>

<p>Faites glisser le curseur de la souris sur le titre d'une option pour en voir une
    description. Vous pouvez également en apprendre plus à propos des options du plugin
    jQuery Camera Slideshow sur le <a href="http://www.pixedelic.com/plugins/camera/"
                                      target="_blank">site officiel</a>.</p>

