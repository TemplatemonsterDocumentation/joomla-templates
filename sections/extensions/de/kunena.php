<h3>Kunena Forum</h3>

<p>Kunena ist eine Komponente des Forums für Joomla. Sie ermöglicht ein völlig funktionelles Forum zu realisieren, das auf Joomla! CMS erstellt wurde.:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Komponente Kunena ist erreichbar aus der Admin Panel von Joomla im  <b>Components -> Kunena</b> Tab.</p>

<p>Sie konnen mehr über die Verwendung und Einstellungen der Kunena Komponente auf der <a href="http://www.kunena.org/docs/Main_Page" target="_blank">offiziellen Webseite</a> erfahren:</p>

