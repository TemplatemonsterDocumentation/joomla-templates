<h3>Forum Kunena</h3>

<p>Kunena ist eine Komponente des Forums zur Joomla. Sie ermoglicht vollig funktionelles
    Forum zu realisieren, das auf Joomla! CMS gegrundet wurde.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Komponente Kunena ist erreichbar aus dem Control Panel von Joomla im Menu <b>Components
        -> Kunena</b>.</p>

<p>Sie konnen mehr uber Verwendung und Einstellung der Komponente Kunena auf <a
        href="http://www.kunena.org/docs/Main_Page" target="_blank">der offiziellen
        Webseite erfahren</a>.</p>

