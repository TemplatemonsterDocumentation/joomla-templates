    <p><strong>Wählen Sie </strong>Menu Item Type auf dem nächsten Bildschirm.</p>
  </li>
   <figure class="img-polaroid"><img src="img/forum-25.jpg" alt=""></figure>
  <li>
    <p>Wählen Sie im Popup-Fenster den Kunena Forum-Menütyp aus:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-26.jpg" alt=""></figure>
  <li>
    <p>Bearbeiten Sie die Menüpunkteinstellungen, speichern Sie das Menü und überprüfen Sie Ihre Website.</p>
  </li>
</ol>
