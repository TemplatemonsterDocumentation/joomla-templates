<h5>Kategorien erstellen</h5>
<ol class="marked-list">
  <li>
    <p>Gehen Sie zu <strong>Components</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Categories</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Klicken Sie auf den<strong>New Category</strong> Knopf oben links.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Geben Sie die Kategorien Haupteinstellungen im <strong>Category Name  und Description </strong>Tab ein:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">Um eine Sektion zu erstellen, wählen Sie <strong>Top Level</strong> als Überelement. </p>
       </li>
       <li>
         <p>Geben Sie <strong>Kategoriennamen </strong> und <strong>Alias</strong> ein.</p>
       </li>
       <li>
         <p>Stellen Sie sicher, dass die Kategorie <strong>veröffentlicht wurde</strong>.</p>
       </li>
       <li>
         <p>Geben Sie die  <strong>Kategoriebeschreibung</strong>  und <strong>Titel</strong> im entsprechenden Feld ein.</p>
       </li>
       <li>
         <p>Sie können eine <strong>bestimmte CSS Klasse</strong> für Ihre Kategorie eingeben. </p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Klicken Sie auf das<strong> Category Permissions</strong> Tab um die Berechtigungen der Kategorie einzugeben:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Sie werden 2 verfügbare Optionen sehen:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Berechtigungs Kontrolltyp</strong> verwendet in dieser Kategorie.</p>
      </li>
      <li>
        <p align="justify">Die <strong>Zugriffsebene Gruppe</strong>, die erlaubt ist, die Kategorie zu sehen. Sie können Ihre eigenen Zugriffsebenen in <strong> Users</strong> &gt; <strong>Access Levels</strong> definieren. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p><strong>Kategorie Einstellungen</strong> Tab:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Gesperrt</strong>. Setzen Sie es auf Ja, wenn Sie die Kategorie sperren möchten. Site Moderatoren und Admins sind die einzigen, die neue Antworten in den gesperrten Kategorien erstellen können.</p>
      </li>
      <li>
        <p align="justify"><strong>Beiträge überprüfen</strong>. Setzen Sie auf Ja, wenn Sie Beiträge von Moderatoren vor der Veröffentlichung in dieser Kategorie überprüfen möchten.</p>
      </li>
      <li>
        <p align="justify"><strong>Anonyme Nachrichten zulassen </strong>Mit dieser Option können Sie anonyme Nachrichten für die Kategorie aktivieren / deaktivieren.</p>
      </li>
      <li>
        <p align="justify"><strong>Standardmäßig antwortet als Option</strong>. Wenn anonyme Nachrichten aktiviert wurden, wählt diese Option die Standardauswahl für den Benutzer aus. Regelmäßige Benutzer können später ihre eigenen Beiträge anonymisieren, aber nur Morator haben die Möglichkeit, anonyme Beiträge zu bearbeiten.</p>
      </li>
      <li>
        <p>Sie können <strong>Umfragen Erlauben</strong> für die Kategorie, die Sie erstellen.</p></li></ol>
