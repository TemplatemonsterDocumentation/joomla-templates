<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> ist ein Diskussionsforum für die Joomla Seite. Es ist flexibel und einfach zu bearbeiten. Das Standard-Layout des Forums ist verschieden für nicht registrierte und registrierte Benutzer, sowie die anderen verfügbaren Optionen.</p>
<p><strong>Kunena</strong> ist eine Forum-Komponente für Joomla. Es ermöglicht die Implementierung eines voll funktionsfähigen Forums basierend auf Joomla! </p>
<p>Diese Funktion befindet sich unter dem '<strong>Pages</strong>' Menüpunkt im Frontend der Website. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p> Die Haupteinstellungen von <strong>Kunena</strong> und dessen Inhalt kann man in Ihrer Website Admin Panel bedienen. </p>
<p>Scrollen Sie runter zu Erweiterungen und wählen Sie dort <strong>Kunena Forum</strong>:</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p> Es werden mehrere Tabs zur Verfügung stehen:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Category manager</strong>. In der Mitte gibt es alle aktuellen Kategorien in deinem Forum. Wählen Sie eine beliebige Option aus. Sie können die übergeordnete Sektion, den Namen, den Alias, die Beschreibung, den Header und andere Einstellungen bearbeiten:</p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify"> Dies wird der Bereich sein, dass Ihre Mitglied Diskussion beinhaltet. Sie können so viele Diskussionen wie nötig erstellen. Jede Diskussion sollte dem allgemeinen Abschnitt zugewiesen werden [es wird als Header für Forumsthemen verwendet]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Kategorie Berechtigungen </strong> Tab spezifiziert die Zugriffsebene einer bestimmten Kategorie - Gruppe von Benutzern, um sie anzuzeigen und zu bearbeiten. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Kategorie Einstellungen</strong> erlaubt Ihnen die Haupteinstellungen der Kategorienachrichten zu verwalten. </p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify"><strong>Benutzer</strong> Sektion. Hier finden Sie Benutzer des aktuellen Forums und verwalten ihre Account-Einstellungen, Berechtigungsbearbeitung und Forumansicht: </p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify"> Drücken Sie auf den <strong> Benutzernamen Link</strong> und Sie werden das Fenster sehen, wo Sie , bei Bedarf, das <strong> Anzeigebild </strong> und <strong> Signaturtext </strong> im <strong>Grundeinstellungen</strong> Tab wählen können. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify"><strong>Moderation</strong> Tab erlaubt die anzugeben, ob dieser Benutzer Moderator ist oder nicht, und welche Kategorien er moderieren kann. Klicken Sie auf die <strong>Save &amp; Close</strong> Taste, wenn Sie mit den Benutzereinstellungen fertig sind.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Anhang</strong>. Hier können Sie mit den hochgeladenen Dateien arbeiten.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emotionen</strong>. Alle verfügbaren Emotionen befinden sich hier. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Benutzen Sie den <strong>New Smile</strong> Bereich, um neue zu bekommen, wenn Sie wollen : </p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Ränge</strong>&rsquo;. Hier sehen Sie alle verfügbaren Ränge für den Benutzer Sie können neue Ränge hinzufügen, wenn Sie wollen. Jeder Rang kann auch manuell bearbeitet werden: </p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Vorlagen</strong>. Wählen Sie eine Vorlage für das Forum: </p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Konfiguration</strong>. Diese Registerkarte ermöglicht die Steuerung der Einstellungen des Forums im Detail. Titel, E-Mail-Adresse, SEO, Cache und andere Einstellungen können unter dem Tab <strong> Allgemein </strong> verwaltet werden. Mit der <strong> Frontend </strong> Sektion Können Sie das Layout des Forums ändern. Allgemeine Einstellungen für Benutzer werden mit der <strong> Benutzer </strong> Sektion verwaltet.Das <Strong> Sicherheit </strong> Tab steuert die Berechtigungen für Gäste und registrierte Benutzer mit dem Forum. <Strong> Anzeigebild</strong> Tab erlaubt/verbietet Benutzern eigene Anzeigebilder hochzuladen und die Bildeinstellungen festzulegen.Mit <strong> Uploads </strong> können Sie das Hochladen von Anhängen zu den Nachrichten zulassen / verbieten,geben Sie die Einstellungen dieser Dateien an.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
<p align="justify">
Klicken Sie auf die <strong>Save &amp; Close</strong> Taste oben, wenn Sie fertig mit dem Bearbeiten sind. </p>
<p><strong>Werkzeuge</strong>. Hier können Sie bestimmte Optionen wie Berichte, Synchronisation, IPs und so weiter verwalten: </p>
<p>
    <figure class="img-polaroid"><img src="img/forum-17.jpg" alt=""></figure>
</p>
<p>Nach dem Aktualisieren der Seite des Forums, werden die implementierten Änderungen angezeigt.
</p>
<p>Wenn Sie zusätzliche Funktionen für Kunena benötigen,dann können Sie sich auch etwas aus einer großen Anzahl von <strong> Plugins </strong> auswählen, die speziell für Kunena gebaut wurden.</p>
