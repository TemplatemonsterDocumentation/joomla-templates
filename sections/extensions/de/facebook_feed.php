<h3>TM Facebook Page Plugin</h3>
 
<p>TM Facebook Page ist ein Modul, welches Ihnen erlaubt Ihre Facebook-Seite auf der Website anzuzeigen:</p>

<figure class="img-polaroid">
    <img src="img/facebook_feed-1.jpg" alt="">
</figure>

<p>So sehen die Grundeinstellungen des TM Facebook Page Modules aus. Das Feld Facebook App ID ist erforderlich. Das ist die Facebook App Identifikation. Um den 'Identifier' zu erhalten, öffnen Sie bitte die Website: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a>
    und erstellen Sie eine App für Ihre Website . Sie können mehr über andere Einstellungen erfahren <a href="https://developers.facebook.com/docs/plugins/page-plugin"
                       rel="nofollow" target="_blank">auf der offiziellen Website</a>:</p>

