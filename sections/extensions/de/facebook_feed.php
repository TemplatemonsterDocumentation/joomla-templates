<h3>TM Facebook Page Plugin</h3>
 
<p><strong>TM Facebook Page</strong> ist ein Modul, dass Ihnen erlaubt Ihre Facebook Seite auf Ihre Webseite anzuzeigen:</p>

<figure class="img-polaroid">
    <p><img src="img/facebook_feed-1.jpg" alt="">
    </p></figure>
    <p>Um das <strong>TM Facebook Page Plugin</strong> Modul zu konfigurieren, gehen Sie zu <strong>Extensions</strong> &gt; <strong>Modules</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Drücken Sie auf den &quot;<strong>Search Tool</strong>&quot; Knopf und wählen Sie das <strong>TM Facebook Page Plugin </strong>im &quot;- Select Type -&quot; Filter Dropdown Liste. Sie werden das <strong>Facebook Box</strong> Modul sehen.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-4.jpg" alt="">
</p></figure>
    <p>Klicken Sie auf den Modul Titellink, um zum Einstellungenfenster des Moduls zu gelangen. </p>
 <figure class="img-polaroid">
    <p><img src="img/facebook_feed-5.jpg" alt=""></p></figure>
   <p>Im <strong>Module</strong> Tab können Sie die folgenden Optionen einstellen:</p>
    <ol class="marked-list">
      <li> <strong>Facebook App ID</strong> *.</li>
      <li> <strong>Facebook Page URL</strong>.</li>
      <li> <strong>Ihr Firmenname</strong>.</li>
      <li> <strong>Breite</strong>. Die Pixelbreite des Widgets.</li>
      <li> <strong>Höhe</strong>. Die Pixelhöhe des Widgets. </li>
      <li> <strong>Profilbilder anzeigen</strong>. Mit dieser Option können Profilfotos im Modul angezeigt werden.</li>
      <li> <strong>Seitenbeiträge anzeigen</strong>.Beiträge aus der Zeitleiste der Seite anzeigen.</li>
      <li> <strong>Titelbild ausblenden</strong>. Titelbild im Header ausblenden.</li>
    </ol>
<p align="justify">Das Feld <strong>Facebook App ID</strong> ist erforderlich. Es ist ein Facebook App Identifier. Um den Identifier zu erhalten, öffnen Sie bitte die Website: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a> und erstellen Sie eine App für Ihre Webseite. </p>
<p align="justify">Folgen Sie einfach der Anleitung, und schon werden Sie den Identifier in kurzer Zeit haben. </p>
<h5>Schritt für Schritt Anleitung</h5>
        <h5>1. Loggen Sie sich in Facebook ein</h5>
        <p>Bevor Sie ein Entwicklerkonto erstellen, melden Sie sich bei Ihrem Facebook-Konto an.</p>
<a href="https://www.facebook.com/login.php">Login to Facebook</a>
        <h5>2.Entwicklerkonto</h5>
        <p align="justify">Sie benötigen ein Facebook-Entwickler-Account, um loszulegen. Wenn Sie ihn nicht haben, aktualisieren Sie Ihr persönliches Facebook-Konto auf ein Facebook-Entwickler-Konto jetzt. Überspringen Sie diesen Schritt, wenn Sie bereits ein Entwicklerkonto haben.</p>
<a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Create Developer Account</a>
<h5>3. Eine neue Facebook App erstellen</h5>
        <p>Wählen Sie <a href="https://developers.facebook.com/apps/"><strong>Apps</strong></a> in der Header Steuerung und wählen Sie <strong>Add a New App</strong> oder benutzen Sie den Knopf unten.</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Create new Facebook App</a>
   <h5>4. Wählen Sie eine Plattform</h5>
        <p align="justify">Wählen Sie die Plattform, die Sie hinzufügen möchten. Wenn Ihre App auf mehreren Plattformen ist, können Sie sie zu Ihrer Facebook-App später hinzufügen.    </p>
        <figure class="img-polaroid">
    <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
    <h5>5. Wählen Sie einen Namen</h5>
    <p align="justify">Wählen Sie einen Namen für Ihre App und wählen Sie <strong> Erstellen Sie eine neue Facebook App ID </strong>. Wählen Sie eine <strong> Kategorie</strong> für Ihre App. Wenn Sie eine Testversion einer vorhandenen Facebook App erstellen, wählen Sie die App aus, die Sie testen.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
    <h5>6. Folgen Sie den &quot;Quick Start&quot; Schritten</h5>
    <p align="justify">Der Schnellstart-Assistent hilft Ihnen bei der Einrichtung Ihrer Facebook-App für die ausgewählte Plattform. Sie können zu einem späteren Zeitpunkt wieder zum Schnellstart-Assistenten zurückkehren, um entweder Ihr Setup fortzusetzen oder eine neue Plattform für Ihre App hinzuzufügen.</p>

<h5>7. App ID</h5>
    <p align="justify">Ihre App wird eine einzigartige App-IDhaben. Sie werden diese ID verwenden, wann immer Sie eine unserer <a href="https://developers.facebook.com/docs/apis-and-sdks">SDKs</a> oder <a href="https://developers.facebook.com/docs/sharing/webmasters/">Open Graph tags for sharing</a> verwenden. Sie können Ihre App ID in Ihrem <a href="https://developers.facebook.com/apps/">App Dashboard</a> finden.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
    <h5>8.Schützen Sie Ihr App Geheimnis</h5>
    <p align="justify">Facebook auto-generiert ein App-Geheimnis mit Ihrer Anwendung verbunden. Das Anwendungsgeheimnis authentifiziert Anfragen von Ihrer Anwendung auf Facebook-Servern und sollte wie ein Passwort behandelt werden.</p>
<p align="justify">Geben Sie nicht Ihr Facebook-Anwendung Geheimnis in Ihrer Web-Seite Markup ein; Seien Sie vorsichtig, wenn Sie Dateien, mit diesem Wert, zu einer Quellcodeverwaltung Software wie ein öffentlicher Git oder Subversion-Repository senden.</p>
<h5>9. Kontakt Email</h5>
    <p align="justify">Sie müssen eine Kontakt EMail für jede App hinzufügen. Gehen Sie zu Ihrem <a href="https://developers.facebook.com/apps/"> App Dashboard </a> und wählen Sie das Navigationselement <strong> Einstellungen </strong> in der linken Seitenleiste und geben Sie eine gültige Kontaktemail ein.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
    <h5>10. App Details</h5>
    <p>Wählen Sie die App-Details auf der linken Seitenleiste aus, um zu sehen und zusätzliche Informationen zu Ihrer App hinzuzufügen.</p>
    <p align="justify">Wenn Sie möchten, dass Ihre App im App-Center aufgeführt wird, schalten Sie die App Center Listed Platform-Einstellung für Ihre Website oder mobile Website ein. Fügen Sie Icons, Werbebilder und Videos hinzu. Halten Sie sich an die <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">App Center Guidelines</a> um zur Auflistung im App Center zugelassen werden.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11.Deaktivieren Sie den Entwicklungsmodus</h5>
    <p align="justify">Neue Facebook-Apps existieren standardmäßig im Entwicklungsmodus. Dies begrenzt das Veröffentlichten und verwandte Objekte wie Freund-Tagging auf Facebook Timelines im Besitz eines Entwicklers der Anwendung.   </p>
<p align="justify">Sobald Ihre Anwendung für den öffentlichen Gebrauch bereit ist, können Sie vom Entwicklungsmodus zum öffentlichen Modus wechseln, damit Ihre App mit Facebook-Konten interagieren kann, die nicht mit Ihrer App verbunden sind.</p>
   <figure class="img-polaroid">
    <p><img src="img/facebook_feed-12.jpg" alt="">
</p></figure>
  
    <p>Sie können auch die folgenden Optionen für das Modul im <strong>Module</strong> Tab einstellen:</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-13.jpg" alt="">
</p></figure>
<ol class="marked-list">
      <li> <strong>Titel anzeigen</strong>. Sie können den Modultitel auf dem Display anzeigen oder ausblenden. Der Effekt hängt vom Chromstil in der Vorlage ab.</li>
      <li>
        <div align="justify"> <strong>Position</strong>. Sie können eine Modulposition aus der Liste der vordefinierten Positionen auswählen oder Ihre eigene Modulposition eingeben, indem Sie den Namen im Feld eingeben und die Eingabetaste drücken.</div>
      </li>
      <li><strong> Status</strong>. Wenn es veröffentlicht wird, wird dieses Modul auf Ihrer Website Frontend oder Backend je nach Modultyp angezeigt.</li>
      <li> Datum für die Veröffentlichung [<strong> Start der Veröffentlichung </strong> / <strong> Ende der Veröffentlichung </strong>] Ein optionales Datum zum Starten / Beenden der Veröffentlichung des Moduls.</li>
      <li> <strong>Zugriff</strong>.Die Zugriffsstufengruppe, die dieses Element sehen darf.</li>
      <li> <strong>Reihenfolge</strong>. Wählen Sie die Reihenfolge.</li>
      <li> <strong>Sprache</strong>. Weisen Sie diesem Modul eine Sprache zu.</li>
    </ol>
<p align="justify">Die <strong>Menu Assignment</strong> &amp; <strong>Module Permissions</strong> Tabs sind Standarttabs von Joomla Modulen. Sie können Menüpunkte für das Modul einrichten, die erscheinen und die Berechtigungen definieren.</p>
