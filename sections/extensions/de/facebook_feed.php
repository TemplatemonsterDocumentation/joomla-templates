      <li> <strong>Offset Spalten </strong>. Den linken Margin einer Spalte durch eine ausgewählte Anzahl von Spalten vergrößern.</li>
      <li> <strong>Header Tag</strong>. Das HTML-Tag für das Modul Header oder Titel.</li>
      <li> <strong>Header Klasse</strong>. Die Klasse für das Modul Header oder Titel.</li>
      <li> <strong>Modulstil</strong>. Verwenden Sie diese Option, um den Vorlagenstil für diese Position zu überschreiben.</li>
    </ol>
   