<h3>TM Parallax Module</h3>

<p>TM Parallax ist ein Modul, das für die Implementierung der Parallaxe-Funktionalität in der Vorlage RD Parallax-Skript verwendet.</p>

<h6>Alle notwendigen Moduloptionen sind in Moduleinstellungen verfügbar:</h6>

<ol class="index-list">
    <li>Bildeinstellungen (Image settings);</li>
    <li>Parallax Scroll-Geschwindigkeit Einstellungen(Parallax scroll speed settings);</li>
    <li>Parallax Richtung Einstellungen(Parallax direction settings).</li>
</ol>
