<p class="alert alert-warning">
In unserer Vorlagen werden TM Parallax Inhalte des Moduls durch Laden eines bestimmten Moduls oder Position dargestellt, zum Beispiel, "{loadmodule articles_single, About us}". Um Fehler 404 auf Ihrer Website zu verhindern, stellen Sie sicher, dass der Name des geladenen Moduls mit dem zusammenfällt , welchen Sie für Laden angegeben haben. In diesem Fall sollte das "Über uns" Modul veröffentlicht werden und sollte nicht mehr als ein Leerzeichen, im Modulnamen enthalten.
    Sie können an der mehr über Lademodul / Ladeposition auf der  <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">offiziellen Website</a> erfahren.
</p>
