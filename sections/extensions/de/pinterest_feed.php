<p>Im <strong>Advanced Options</strong> Tab – können Sie dem <strong>Modul einen Klassensuffix</strong> geben, wenn Sie wollen.</p>
<p align="justify">Als nächstes wählen Sie die Seite, wo Sie das Modul platzieren wollen. Dazu sollten Sie das <strong> Menüzuordnung </strong>Tab verwenden. </p>
<p align="justify">In diesem Tab befindet sich eine Liste der Menüpunkte. Sie können zwischen Auf allen Seiten, Keine Seiten, Nur auf den ausgewählten Seiten und auf allen Seiten, außer denen, die aus der Liste ausgewählt wurden wählen. Wenn die beiden letzteren Optionen ausgewählt sind, wird eine Liste mit allen Menüpunkten angezeigt, wie im obigen Screenshot gezeigt. Dies ermöglicht Ihnen, Module zu einigen, aber nicht allen Seiten zuzuordnen, indem Sie die Menülinks auswählen, auf denen Sie das Modul anzeigen möchten,dadurch können Sie anpassen, auf welchen Seiten Module erscheinen / nicht angezeigt werden.</p>
<div align="justify">Scrollen Sie die Seite nach oben und klicken Sie auf die grüne <strong>Save</strong>Taste oben links, um die Änderungen anzunehmen:</div></li>
