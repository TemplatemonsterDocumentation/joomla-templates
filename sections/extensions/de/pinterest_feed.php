    <img src="img/pinterest_feed-1.jpg" alt="">
</figure>

<p align="justify"> Gehen Sie zu <strong>Module Manager</strong>. Sie werden ein Modul &ldquo;<strong>SW Pinterest Display</strong>&rdquo; in der Liste sehen. Klicken Sie auf darauf, um es zu bearbeiten, oder Sie können auf den &ldquo;<strong>New</strong>&rdquo; Knopf drücken, um ein neues Modul zu erstellen.</p>
