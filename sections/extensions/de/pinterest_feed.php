<h3>SW Pinterest Display</h3>


<p align="justify"><strong>SW Pinterest</strong> ist ein Modul zum Anzeigen von "Pinterest Board Widget". Dieses Modul kann Ihnen helfen, Pinterest Profile Pins oder Board Pins anzuzeigen. Breite und Höhe können skalierbar sein</p>
