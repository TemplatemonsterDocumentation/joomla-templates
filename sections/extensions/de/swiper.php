<h3>Swiper</h3>

<p align="justify"><strong>Swiper Module</strong> ist ein wunderbarer adaptiver Slider, das eine Integration von Standard Joomla Artikel Newsflash-Modul und dem kostenlosen jQuery Swiper Plugin ist. Es ist für IOS erstellt,und funktioniert perfekt für die neuesten Android-, Windows Phone 8 und Desktop-PC-Browsern.</p>
<p align="justify">Lassen Sie uns überprüfen, welche Arten von Slides wir zum Website Slider hinzufügen können. Zuerst einmal könnte es eine standartmäßiges animiertes Slide mit einem Bild und eine Beschriftung darauf sein:</p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">Aber es können auch Slides mit einem Hintergrundvideo oder konfigurierbare Slides mit dem Parallaxenbildhintergrund sein. Es gibt gibt wirklich eine große Auswahl von Optionen . Lasst uns lernen, wie man sie benutzt.</p>

