<h3>Swiper</h3>

<p>Swiper Module ist ein adaptiver Slider , was eine wunderbare Integration von Standard-Joomla Artikel Newsflash -Modul und dem kostenlosen jQuery swiper Plugin ist. Geschaffen für IOS,und funktioniert perfekt mit dem neuesten Android, Windows Phone 8 und Desktop-PC-Browser:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Swiper Modul enthält verschiedene Einstellungen und kann die man im <b>Extensions
        > Module Manager</b> Tab Ihrer Joomla Admin PAnel finden kann. Alle Haupt
jQuery swiper Plugin-Optionen können in Moduleinstellungen geändert werden. </p>

<p>Platzieren Sie den Mauszeiger auf die Einstellung um die Beschreibung zu sehen. Sie können an der mehr über jQuery Swipe auf der <a
        href="http://idangero.us/swiper/" target="_blank">offiziellen Website erfahren</a>.</p>
<p>Alle notwendigen Karussell Einstellungen können Sie in den Moduleinstellungen finden:</p>

<ol class="index-list">
    <li>Wiedergabeeinstellungen (Playback settings);</li>
    <li>Höheeinstellungen(Height settings);</li>
    <li>Sichtbare Elemente(Visible elements);</li>
    <li>Animationseinstellungen (Animation settings);</li>
    <li>Navigation und Steuerung(Navigation and control);</li>
    <li>Miniaturbild-Einstellungen(Thumbnail settings).</li>
</ol>

