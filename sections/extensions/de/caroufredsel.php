<h3>CarouFredSel</h3>

<p> CarouFredSel ist einen Bilderkarusell-Modul, den man als eine Kombination vom
    Standard Joomla Articles Newsflash Modul und CarouFredSel jQuery Plugin entwickelt
    hat. </p>

<figure class="img-polaroid">
    <img src="img/caroufredsel-1.jpg" alt="">
</figure>

<h6> In Einstellungen des Moduls sind alle benötige Optionen vom Karussell
    verfügbar. </h6>

<ol class="index-list">
    <li>Richtung von Karussell-Rotation</li>
    <li> Breite eines Eintrags</li>
    <li> Sichtbare Einträge</li>
    <li> Animationsmöglichkeiten</li>
    <li> Navigation und Kontrollfunktionen</li>
</ol>

<figure class="img-polaroid">
    <img src="img/caroufredsel-2.jpg" alt="">
</figure>

<p> Das Plugin „CarouFredSel” hat noch mehr <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">Konfigurationsoptionen</a>.
    Wenn Sie Ihre Optionen hinzufügen möchten, bearbeiten Sie die Datei
    <b>\modules\mod_caroufredsel\tmpl\default.php</b>.
</p>