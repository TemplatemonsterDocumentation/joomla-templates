<h3>Articles Newsflash [Advanced]</h3>

<p align="justify">Articles-Newsflash (Erweitert) Modul ist eine verbesserte Version vom <strong>Standard Joomla Articles Newsflash Modul</strong>. Zusätzlich zu Standard Eigenschaften werden extra Konfigurationsoptionen und Bootsrap basierten Modul Templates angeboten. Um Ihr Joomla-basiertes Website-Design zu verbessern, ist dies ein drastisch höheres Qualitätsmodul, um Ihren Inhalt zu vermitteln. Einfach zu bedienen, einfach zu aktualisieren, mit großartigen und leistungsfähigen Anpassungsoptionen.</p>

