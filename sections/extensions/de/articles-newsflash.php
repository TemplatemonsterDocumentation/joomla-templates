<h3>Articles Newsflash (Advanced)</h3>

<p>Das Articles Newsflash (Advanced) Modul ist eine erweiterte Version des Standard Joomla Articles Newsflash Modules. Zusätzlich zu Standard Eigenschaften werden extra Konfigurationsoptionen und Bootsrap basierende Modulvorlagen angeboten:</p>

<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Zusätzliche Optionen</h4>

<p><b>Number of columns (Anzal von Spalten) </b> - erlaubt Artikel in mehreren Spalten anzuzeigen.</p>

<p><b>Custom Link (benutzerdefinierter Link)</b> - erlaubt Ihnen einen benutzerdefinierten Link unten dem Modul-Inhalt anzuzeigen. Er kann mit einer externen URL oder mit einem Joomla Menü-Punkt verknüpft werden.</p>

<p><b>Pretext</b> - erlaubt einen Custom Text vor dem Modul Inhalt anzuzeigen.</p>

<p><b>Published Date(Veröffentlichungsdatum )</b> - stellt das Veröffentlichungsdatum des Artikels dar. </p>

<p><b>Created by (Erstellt von)</b> -  zeigt den Autor des Artikels an.</p>

<p><b>Show intro images (Intro Bilder anzeigen)</b> - erlaubt das Intro Bild anzuzeigen.</p>

<p><b>ntro image align (Ausrichtung des Intro Bildes)</b> - erlaubt Intro Bilder auszurichten.</p>

