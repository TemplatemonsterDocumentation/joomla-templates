<h3>Articles Newsflash</h3>


<p> Articles-Newsflash (Erweiterter) Modul ist eine verbesserte Version vom Standard Joomla Articles Newsflash Modul.
    Zusätzlich zu Standard Eigenschaften werden extra Konfigurationsoptionen und Bootsrap basierten Modul Templates
    angeboten.</p>
<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>
<h4>Zusätzliche Optionen </h4>

<p><b>Number of columns</b> (Anzal von Spalten) – erlaubt Artikel in mehreren Spalten anzuzeigen.</p>

<p>
    <b>Custom Link</b> (benutzerdefinierter Link)- erlaubt Ihnen einen benutzerdefinierter Link unten dem Modul-Inhalt
    anzuzeigen. Er kann mit der externen URl oder mit einigem Joomla Menü-Punkt verknüpfen werden.
</p>

<p><b>Pretext</b> (Vorwand) – erlaubt einen Custom Text vor dem Modul Inhalt anzuzeigen. </p>

<p><b>Published Date</b>(veröffentlichtes Datum) – stellt das Veröffentlichungszeitpunkt des Artikels dar. </p>

<p><b>Created by</b> (Erstellt von) – zeigt den Autor des Artikels an</p>

<p><b>Show intro images </b> (Intro Bilder anzeigen)- erlaubt das Intro Bild anzuzeigen. </p>

<p><b>Intro image align</b> (Ausrichtung des Intro Bildes)- erlaubt Intro Bilder auszurichten</p>
