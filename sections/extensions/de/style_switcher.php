<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> ist ein Modul, das Ihnen ermöglicht , Farbschemas im Frontend der Webseite umzuschalten. Das Modul verwendet automatisch Farbschemas aus dem Ordner color_schemes\css\ vom aktiven Thema.</p>

<p>Dieses Modul hat keine speziellen Einstellungen in der Admin Panel. Es hat nur Standardeinstellungen, die alle Module haben.:</p>

