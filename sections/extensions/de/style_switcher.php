</figure>

<p class="alert alert-warning">Achten Sie darauf, dass dieses Modul auf der Demoversion im Demo-Modus (mit Hilfe von Cookies) funktionieren kann. Auf der aktuellen Website funktioniert das Modul im Vollmodus und ist nur für eingeloggte Benutzer mit den Rechten der Benutzerkategorie SuperAdmin verfügbar.</p>
