<h3>TM Ajax Style Switcher Module</h3>

<p align="justify"><strong>TM Ajax Style Switcher Module</strong> ist ein Modul für das Umschalten von Farbschemata über Front-End. Das Modul wendet automatisch Farbschemata aus dem templates\themexxx\css\ Ordner des aktuellen Themas an.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Hinweis</strong>: Auf der Vorlagen Live Demo arbeitet die Vorlage im Vorschau-Modus mit Cookies. Auf Ihrer Website wird das Modul im Vollmodus arbeiten und steht für autorisierte Benutzer mit SuperAdmin User Category Rechten zur Verfügung.</p>
<h5> Wie man TM ajax style switcher verwaltet </h5>
<p align="justify">Um das <strong>TM Ajax Style Switcher Modul</strong> zu konfigurieren, gehen Sie zu <strong>Extensions </strong>&gt;<strong>Modules</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify"> Drücken Sie den &quot;<strong>Search Tool</strong>&quot; Knopf und wählen Sie den <strong>TM Ajax Style Switcher Module</strong> Typ in der &quot;- Select Type -&quot; Filter Dropdown Liste. Sie werden <strong>Ajax Style Switcher Module</strong> sehen.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Klicken Sie auf den Modul Titellink, um auf das Einstellungsfenster des Moduls zu kommen. Es gibt keine spezifischen Einstellungen für das Modul im Admin Panel, nur die Standardeinstellungen für alle Module:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Stellen Sie sicher, dass das Modul <strong>veröffentlicht</strong> ist.</p>
  <h5><p>Um den Style Switcher zu aktivieren, folgen Sie bitte den unten aufgeführten Anweisungen:  </p></h5>
  <ol class="marked-list">
    <li>
      <p>Füge einen neuen Joomla-Benutzer in <strong>Users</strong> &gt; <strong>Manage</strong> hinzu.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Aktivieren</strong> und	<strong>schalten</strong> Sie den neuen Benutzer ein.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Klicken Sie auf den neuen Benutzeranmeldung Link, um in das Einstellungsfenster zu gelangen. </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Stellen Sie sicher, dass Sie die Benutzer der &lsquo;<strong>Super Users</strong>&rsquo; Gruppe zugewiesen haben. Gehen Sie zum <strong>Assigned Users</strong> Tab und haken Sie &quot;<strong>Super Users</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Loggen Sie sich in Ihre Website ein mit dem <strong>Login or register</strong> Modul im Front-End:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>Style Switcher wurde erfolgreich aktiviert. Aktualisieren Sie Ihre Website, um die Anpassung Tool-Box zu sehen: </p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
<p>Um Farbänderungen in Ihrem Thema vorzunehmen, folgen Sie bitte den unten aufgeführten Schritten:</p>
  <ol class="marked-list">
    <li>
      <p>Wählen Sie das gewünschte Farbschema aus.</p>
    </li>
    <li>
      <p>Klicken Sie auf den <strong>Save</strong> Knopf, um die Änderungen zu speichern und das Farbschema zu aktualisieren.</p>
      
    </li>
     <p>
       <figure class="img-polaroid">
         <img src="img/style_switcher_14.jpg" alt="">
       </figure>
     </p>
     <div class="alert alert-warning">
    <div align="justify">
      <p>Wenn das Farbschema gespeichert wurde, bedeutet es, dass alle zukünftigen Besucher der Website die Änderungen sehen werden. </p>
      <p><strong>Denken Sie daran!!!</strong>Nur die Benutzer, die der <strong> Super Users </strong> Gruppe zugeordnet sind, können das Farbschema der Website ändern.</p>
    </div></div>

  </ol>
  <div class="alert alert-info">
    <p align="justify"><strong>Hinweis</strong>: </p>
    <div align="justify">
      <p>Sobald Sie als <strong>Super User</strong> eingeloggt sind, haben Sie alle Website Administrationsrechte [Löschen / Bearbeiten / Status ändern / Frontend Editing]. Achten Sie also darauf, wenn Sie den Benutzer zur <strong> Super Users </strong> Gruppe zuweisen.</p>
</div></div>
