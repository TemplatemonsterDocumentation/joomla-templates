<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> – ist ein Modul, das ermöglicht es, Farbschemas im Frontend der Webseite umzuschalten.
    Das Modul verwendet automatisch Farbschemas aus dem Ordner color_schemes\css\ vom aktiven Theme.</p>

<p>Das jeweilige Modul hat keine speziellen Einstellungen im Control Panel. Es hat nur Standardeinstellungen, die alle
    Module haben.</p>

