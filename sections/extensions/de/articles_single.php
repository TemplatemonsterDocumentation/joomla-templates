<h3>Articles Single</h3>

<p>” Articles single” ist einen Modul, das Ihnen ermöglicht, einen einzigen Artikel in der gewünschten Position anzuzeigen: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p><b>Custom Link</b> ist die einzige ungewöhnliche Option dieses Moduls. Es ermöglicht Ihnen, einen benutzerdefinierten Link unter dem Modul Inhalt anzuzeigen. Sie können es mit einer externen URL verknüpfen oder mit einem Menüpunkt Ihrer Joomla-Website:</p>

