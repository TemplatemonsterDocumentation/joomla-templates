<h3>Articles Single</h3>

<p>” Articles single” ist einen Modul, der Ihnen ermöglicht, einen einzigen Artikel in
    der gewünschten Position anzeigen. </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>


<p><b>Custom Link</b> ist die einzige ungewöhnliche Option dieses Moduls. Dank ihr
    können Sie einen benutzerdefinierten Link unter dem Modul-Inhalt anzeigen. Er kann
    mit der externen URL oder einigem Joomla Menü-Punkt verbunden werden. </p>
