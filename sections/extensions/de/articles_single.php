<h3>Articles Single</h3>

<p><strong>Articles Single</strong> ist ein Modul, das Ihnen erlaubt einen einzelnen Artikel in der gewünschten Position anzuzeigen: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">Die einzige ungewöhnliche Option dieses Moduls ist <b>Custom Link</b>. Dank ihr können Sie einen benutzerdefinierten Link unter dem Modul-Inhalt anzeigen. Er kann mit der externen URL oder einigem Joomla Menü-Punkt verbunden werden:</p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">Wenn Sie die Ansicht der &quot;<strong>HOW WE WORK</strong>&quot; Sektion ändern wollen, gehen Sie zu <strong>Extensions</strong> &gt; <strong>Modules</strong>. Dann wählen Sie das &quot;<strong>How we work</strong>&quot; Modul, das zum <strong> Artikel - Single </strong> Typ gehört.Sie können Strg + F verwenden, um es zu finden. Drücken Sie die Strg + F Tastenkombination und Typ <strong> 220 </strong> [es ist die &quot;How we work&quot; Modul-ID]. Klicken Sie auf den Themen Link und Sie werden das Konfigurationsfenster davon sehen.</p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
 </p>
 <p>Das <strong>Single Article</strong> Modul hat die folgenden Einstellungen:</p>
 <p align="justify"><strong>Wählen Sie den Artikel.</strong> Diese Feld beinhaltet, welcher Artikel im Layout angezeigt werden soll. Klicken Sie auf den <strong>Select/Change</strong> Knopf, um das Artikel Auswahlfenster zu sehen. </p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify"> Es ähnelt dem <strong>Article Manager</strong>. Sie können die <strong>Filter Felder</strong> benutzen, um den gewünschten Artikel zu finden. Danach klicken Sie auf <strong>den Artikeltitel</strong>, um es auszuwählen. An diesem Punkt wird das modale Fenster geschlossen und der Titel wird im <strong> Select Article field </strong> angezeigt. </p>
     <p align="justify">Jetzt haben Sie die Möglichkeit, Artikel zu bearbeiten. Um den Artikel zu bearbeiten, klicken Sie auf die Schaltfläche <strong> Bearbeiten </strong> und Sie sehen das Fenster <strong> Artikel bearbeiten </strong>. Hier haben Sie die Möglichkeit, den Artikelinhalt und seine Bilder und Links zu bearbeiten.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify"><strong>Article Single</strong>  hat die folgenden <strong>Artikeleinstellungen</strong> [Optionen Tab], wie unten angezeigt. Diese Optionen bestimmen, wie der Artikel auf dem Front-End-Site-Layout angezeigt wird. Beachten Sie, dass diese Optionen &quot;<strong>Use Global</strong>&quot; verwenden. Wenn diese Option ausgewählt ist, wird die Einstellung aus den Artikel-Manager-Optionen verwendet.</p>
    <figure class="img-polaroid">
     <p><img src="img/articles-single-8.jpg" alt="">
