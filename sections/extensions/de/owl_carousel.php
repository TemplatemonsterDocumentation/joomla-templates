<h3>Owl Carousel</h3>
<p><strong>Owl Carousel </strong>Modul ist eine Kombination aus Standard-Joomla Artikel Newsflash-Modul und dem Owl Carousel jQuery Plugin.</p>
<figure class="img-polaroid">
  <img src="img/owl-carousel-1.jpg" alt="">
</figure>
<h5>Wie man darauf zugreift</h5>
<p>Um ein neues <strong>Owl Carousel</strong> Modul hinzuzufügen, öffnen Sie den Module Manager:</p>
<p>Wählen Sie <strong>Extensions</strong> &gt; <strong>Modules</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Klicken Sie auf den '<strong>New</strong>' Knopf, und dann auf <strong>Owl Carousel</strong> im Modul Popup Fenster.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Details</h5>
<ol class="marked-list">
  <li><strong>Titel:</strong> Das Modul muss einen Titel haben [in unsrem Falle ist es <strong>Carousel</strong>].</li>
</ol>
<h5>Modul</h5>
<p>Das <strong>Owl Carousel </strong> Modul zeigt eine feste Anzahl von Artikeln aus einer bestimmten Kategorie oder einem Satz von Kategorien an.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Kategorie</strong>: Wählen Sie Artikel aus einer bestimmten Kategorie oder einem Satz von Kategorien aus. Wenn keine Kategorien ausgewählt sind, werden standartmäßig alle angezeigt. </div>
  </li>
  <li><strong>Bilder</strong>: [Ausblenden / anzeigen]. Anzeigen von Artikelbildern.</li>
  <li><strong>Artikeltitel</strong>: Ausblenden / anzeigen Artikeltitel.</li>
  <li><strong>Verlinkte Titel</strong>: [Verwenden Sie Global / Nein / Ja]. Verlinkt die Artikeltitel mit den Artikeln.</li>
  <li> <strong>Veröffentlichungsdatum</strong>: Anzeigen des Veröffentlichungsdatums eines Artikels anzeigen.</li>
  <li><strong>Header Level</strong>: [h1/h2/h3/h4/h5]. Wählen Sie das gewünschte HTML-Header Level für die Artikeltitel aus.</li>
  <li><strong>Introbilder anzeigen</strong>:  	Wählen Sie aus, ob Sie Artikel Intro-Bilder anzeigen möchten.</li>
  <li><strong> Introbilder Ausrichtung</strong>: [Keine / links / rechts] Intro-Bildausrichtung auswählen. Überschreibt Artikeleinstellungen.</li>
  <li>
    <div align="justify"><strong>'Read more...' Link:</strong> [Ausblenden / anzeigen]. Wenn auf Anzeigen eingestellt ist, wird der Link "Read more ..." angezeigt , wenn ein Haupttext für einen Artikel bereitgestellt wurde.</div>
  </li>
  <li><strong>Anzahl der Artikel</strong>: Die Anzahl der Artikel, die in diesem Modul angezeigt werden sollen.</li>
  <li>
    <div align="justify"><strong>Ergebnis Reihenfolge</strong>: [Veröffentlichter Termin / Erstellungsdatum / Sortieren / Zufällig]. Wählen Sie die Reihenfolge aus, in der die Abfrageergebnisse angezeigt werden sollen.</div>
  </li>
  <li><strong>Vortext</strong>: Sie können Text eingeben, den Sie vor dem Modulinhalt anzeigen möchten. Unterstützt keine HTML-Tags.</li>
</ol>
<h5>Allgemeine Details</h5>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Titel anzeigen</strong>. [Ausblenden / anzeigen] Wählen Sie aus, ob der Modultitel im Frontend angezeigt oder ausgeblendet werden soll. Der Titel wird die in dem Formularfeld oben sein.</div>
  </li>
  <li>
    <div align="justify"><strong>Modulposition</strong>. Wählen Sie die Modulposition, in der dieses Modul angezeigt werden soll. Eine benutzerdefinierte Modulposition kann für die Verwendung mit dem Ladepositions-Plugin eingegeben werden oder mit der Positionstaste , um eine Modulposition aus der Vorlage auszuwählen.</div>
  </li>
  <li><strong>Status</strong>. [Veröffentlicht / Unveröffentlicht / Trashed] Der veröffentlichte Status des Artikels.</li>

  <li><strong>Zugriff</strong>. Die Ansict Zugriffsebene für diesen Artikel.</li>

  <li>
    <div align="justify"><strong>Modulreihenfolge</strong>. Dies zeigt einen Dropdown von jedem Modul in der Position, in der sich das aktuelle Modul befindet. Dies ist die Reihenfolge, in der die Module angezeigt werden, sowohl im Frontend als auch im Modulmanager.</div>
  </li>

  <li>
    <div align="justify"><strong>Anfang der Veröffentlichung</strong>. Datum und Uhrzeit für die Veröffentlichung. Verwenden Sie dieses Feld, wenn Sie den Inhalt vorzeitig eingeben möchten und dann zu einem späteren Zeitpunkt automatisch veröffentlichen wollen.</div>
  </li>

  <li>
    <div align="justify"><strong>Ende der Veröffentlichung</strong>. Datum und Uhrzeit für das Ende die Veröffentlichung. Verwenden Sie dieses Feld, wenn Sie möchten, dass der Inhalt zu einem späteren Zeitpunkt automatisch in den unveröffentlichten Zustand geändert wird [z. B. wenn es nicht mehr anwendbar ist].</div>
  </li>

  <li><strong>Sprache</strong>. Sprache des Elements.</li>

  <li>
    <div align="justify"><strong>Hinweis</strong>. Item note. This is normally for the site   administrator's use [for example, to document information about this   item] and does not show in the front end of the site.</div>
  </li>
</ol>
<p>Nun sollten wir die Modulparameter wie im Bild unten angezeigt einstellen [Sie sollten den Positionstyp <strong> Feature </strong> im Feld Position wählen.:</p>
<figure class="img-polaroid">
    <p><img src="img/owl-carousel-6.jpg" alt="" /></p></figure>
    <p align="justify">Als nächstes wählen Sie die Seite, wo Sie das Modul platzieren können. Dazu sollten Sie die Registerkarte <strong> Menüzuordnung </strong> verwenden. </p>
  <p align="justify">In diesem Tab befindet sich eine Liste der Menüpunkte. Sie können zwischen Auf allen Seiten, Keine Seiten, Nur auf den ausgewählten Seiten und auf allen Seiten, außer denen, die aus der Liste ausgewählt wurden wählen. Wenn die beiden letzteren Optionen ausgewählt sind, wird eine Liste mit allen Menüpunkten angezeigt, wie im obigen Screenshot gezeigt. Dies ermöglicht Ihnen, Module zu einigen, aber nicht allen Seiten zuzuordnen, indem Sie die Menülinks auswählen, auf denen Sie das Modul anzeigen möchten,dadurch können Sie anpassen, auf welchen Seiten Module erscheinen / nicht angezeigt werden.</p>
    <p align="justify">Wählen Sie die <strong>Blog</strong> Seite, dann wird das Modul nur auf dieser Seite erscheinen.  </p>
    <p align="justify">Vergessen Sie nicht aud den <strong>Save </strong>Knopf zu klicken.  </p>
