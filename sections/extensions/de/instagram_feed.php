<h3>TM Instagram</h3>

<p>TM Instagram ist ein Modul, das "Instagram Widget" anzeigt</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>So sehen die Grundeinstellungen des Moduls TM Instagram aus. Im Feld Instagram
    username wird der Profilnamen in Instagram angegeben. Im Feld Number of photos wird
    die Anzahl von Bildern, die im Widget angezeigt werden, angegeben. Im Feld Your
    Instagram CLIENT ID wird die ID Ihrer Anwendung Instagram angegeben. Um die ID zu
    erhalten, folgen Sie dem Link <a
        href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
        target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    und registrieren Sie sich. Dann folgen Sie dem Link <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> und
    erstellen Sie dort die Anwendung. Danach erhalten Sie die Daten Ihrer Anwendung, in
    denen die ID (CLIENT ID) angegeben wird.</p>

