<h3>Image Swoop</h3>

<p> Der Modul "Image Swoop" ist einen atemberaubenden responsive Slider, den man als
    eine Kombination aus Standard Joomla Modul “Articles Newsflash” und kostenloses
    open-source- jQuery plugin “Camera Slideshow” entwickelt hat. Er fubktioniert
    perfekt auf dem Desktop-PC und auf den iOs oder Android mobilen Geräten. Er ist
    kompatibel mit alle modernen Browsern ab IE8</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>Der Modul "Image Swoop" hat verschiedene Konfigurationsoptionen, die Sie erreichen
    können, wenn Sie im Bereich <b>Extensions > Module Manager</b> vom Joomla
    Admin-Panel den Modul Image Swoop öffnen. Im Einstellungsbereich des Moduls können
    Sie alle Hauptoptionen vom jQuery Plugin "Camera Slideshow" konfigurieren. </p>

<p>Über dem Optionstitel bewegen Sie den Mauszeiger, um eine Beschreibung zu sehen. Mehr
    über die Optionen vom Camera Slideshow jQuery plugin erfahren Sie auf der <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank">offizielle
        Website</a></p>

