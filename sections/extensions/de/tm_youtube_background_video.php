<h3>TM Youtube background video module</h3>

<p>TM Youtube background video ist ein Modul,das Ihnen erlaubt einen Youtube Video Hintergrund auf Ihre Website hinzuzufügen.</p>

<h6>Alle verfügbaren Moduloptionen sind in den Einstellungen verfügbar:</h6>

<ol class="index-list">
    <li>Youtube Videolink;</li>
    <li>Video-Wiedergabe-Einstellungen(Video playback settings);</li>
    <li>Mobile Display-Einstellungen(Mobile display settings);</li>
    <li>"Play / Stop" Knopf Einstellungen(”Play/Stop” button settings).</li>
</ol>
