<h3>TM YouTube background video Modul</h3>

<p><strong>TM YouTube background video</strong> ist ein Modul für das Hinzufügen eines Hintergrund-YouTube-Videos auf Ihrer Website.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>Um das TM YouTube-Hintergrund-Videomodul zu konfigurieren, führen Sie die folgenden Schritte aus:</p></h5>
<ol class="marked-list">
<li><p>Gehen Sie zum <strong>Extensions</strong> &gt; <strong>Modules</strong> Tab:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Dann sollten Sie das <strong>TM YouTube background video</strong> Modul zum Bearbeiten auswählen.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Tipp:</strong> wenn Sie alle <strong>TM YouTube background video</strong> Module finden wollen , benutzen Sie die Filter, um es sich zu erleichtern. Drücken Sie den &quot;<strong>Search Tool</strong>&quot; Knopf und wählen Sie den &quot;<strong>TM YouTube background video</strong>&quot; Typ in der  &quot;<strong>- Select Type -</strong>&quot; Filter Dropdownliste. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>Sie können auf den &quot;<strong>New</strong>&quot; Knopf klicken, um ein neues Modul zu erstellen.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>Um die Moduleinstellungen zu ändern, klicken Sie auf den Titel. [Lassen Sie uns das <strong>Home youtube</strong> Modul bearbeiten.]</li>
<li>
  <div align="justify">Das <strong>Home youtube</strong> Modul Bearbeitungs Interface wird geöffnet. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Drücken Sie den <strong>Article</strong> Knopf, um das Modulfenster mit der Artikelauswahl zu öffnen, wie unten aufgeführt. Sie können den Filter verwenden, um den Suchvorgang zu vereinfachen.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>Um einen neuen Artikel hinzuzufügen verwenden Sie <strong>Content</strong> &gt; <strong>Articles</strong> &gt; <strong>Add New Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Hier finden Sie das <strong>Optionen Tab</strong>, wo Sie folgendes ändern können:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Vorbereitung von Inhalt</strong> – Wahlweise den Inhalt mit den Joomla Content Plugins vorbereiten. </p>
      </li>
      <li>
        <p><strong>YouTube Video URL eingeben *</strong> – Erlaubt es, benutzerdefinierte URL für Video von YouTube anzugeben, die auf der Seite sichtbar sein muss.</p>
      </li>
      <li>
        <p><strong>Videostart</strong> – Das Video beginnt von der angegebenen Sekunde.</p>
      </li>
      <li>
        <p><strong>Stumm</strong> – Video zum leisen oder lauten Modus wechseln.</p>
      </li>
      <li><strong>Automatisches Abspielen</strong> – Das Video startet automatisch, sobald die Web-Seite geladen ist.</li>
      <li><strong>Play / Pause-Tasten anzeigen</strong> – Sie können die Schaltflächen entweder anzeigen oder ausblenden.</li>
      <li> <strong>Play / Pause-Tasten Text</strong> – Geben Sie den Text der Schaltflächen an.</li>
      <li>
        <p><strong>Mobilbild</strong> – Zeigt dieses Bild auf mobilen Geräten statt eines Videos an. [Um Bilder hochzuladen, gehen Sie zur <strong>Content </strong>&gt; <strong>Media</strong> Sektion.]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>Um das Video zu ändern, <strong>ersetzen Sie einfach die Video URL</strong> mit dem vorher kopierten <strong>Youtube Video link</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Scrollen Sie die Seite nach oben und klicken Sie auf die grüne <strong>Speichern</strong> Taste oben links, um die Änderungen anzuwenden:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Gehen Sie zurück zu Ihrer Webseite und aktualisieren Sie die Seite, auf der Sie den text geändert haben. Wir können sehen, dass die Änderungen erfolgreich angenommen wurden. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">In unseren Vorlagen, ist der <strong>TM Youtube background video</strong> Inhalt mit einem bestimmten Modul oder Position dargestellt, z.B. "{loadmodule articles_single, About us}". Um Fehler 404 auf Ihrer Website zu vermeiden, vergewissern Sie sich bitte, dass der Name des geladenen Moduls identisch mit dem angegebenen Moddulnamen ist. In diesem Fall sollte das Modul "About us" veröffentlicht werden. Der Modulname darf nicht mehr als ein Leerzeichen enthalten.
    Sie können mehr über loadmodule/loadposition auf der <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">offiziellen Webseite</a> finden.
</p>
