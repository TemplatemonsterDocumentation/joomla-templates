<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> ist die Abonnement-Komponente für Joomla! AcyMailing ist eine zuverlässige Newsletter- und E-Mail-Marketing-Erweiterung für Joomla. AcyMailing ermöglicht es Ihnen, eine unbegrenzte Anzahl von Abonnenten effizient zu verwalten, sie in Mailinglisten zu organisieren und personalisierte Newsletter zu senden. </p>
<p>Auf Ihrer Webseite sehen Sie folgendes Anmeldeformular:</p>
<figure class="img-polaroid">
    <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Gehen Sie zu <strong>Components</strong> &gt; <strong>AcyMailing</strong> und Sie werden zum AcyMailing Dashboard weitergeleitet.</p>
