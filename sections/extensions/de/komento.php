<h3>Komento Kommentare </h3>

<p>Komento ist eine Kommentarkomponente für Joomla. Sie erlaubt Ihnen, Kommentare in
    Joomla Artikel hinzuzufügen. </p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Man kann die Komponent “Komanto” im Joomla Admin Panel im Bereich <b>Components/Kommentare
        > Komento</b> anrufen.</p>

<p>Die Konfigurationseinstellungen rufen Sie in <b>Components / Kommentare > Komento >
        Integrations</b> mit dem Druck auf <b>Articles/Artikel</b> an. </p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>Mit Hilfe von Konfigurationseinstellungen können Sie Kommentare für bestimmte
    Kategorien aktivieren, Captcha und Nutzerprofils hinzufügen usw. Mehr können Sie <a
        href="http://stackideas.com/docs/komento.html" target="_blank">in der
        offizielle Website</a> erfahren</p>

<p>In unseren Vorlage wird nur die Standardversion von Komento verwendet. Um zusätzliche
    Funktionen dieses Moduls nutzen zu können, empfehlen wir <a
        href="http://stackideas.com/komento/plans">die Vollversion des</a> Produktes
    zu abonnieren. Um die ganze Liste von Funktionen anzuschauen, folgen Sie <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
    Kostenpflichtige Version von Komento bietet Ihnen auch professionelle technische
    Unterstützung an.</p>