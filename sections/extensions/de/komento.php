<h3>Komento Comments</h3>

<p><strong>Komento</strong> ist eine weiterentwickelte Kommentar Erweiterung für Joomla, die Ihren Website-Besuchern kommentieren zu Artikel und Blogs ermöglicht.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Comments kann man im<b>Components > Komento</b> Tab Ihrer Joomla Admin Panel ändern.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Wie man Komento Komponentenintegrationen verwaltet</p></h5>
<ol class="marked-list">
  <li>
    <p>Melden Sie sich in Ihrer Joomla Admin-Panel an, gehen Sie zu <strong>Components</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrations</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Klicken Sie auf <strong>Joomla Article</strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">Im General Tab können Sie Kommentare <strong>Ein-</strong> oder <strong>Ausschalten</strong>. Sie können auch <strong>Moration</strong> oder andere Einstellungen ändern. </p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li><p>Wir habven die Kommentare <strong>ausgeschaltet</strong> [Keine] Kommentare [schauen Sie sich den Screenshot unten an]. </p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>Der Kommentarblock wird auf der Website ausgeschaltet.</p></li>
  <p><strong>Einsgeschaltet</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Ausgeschaltet</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Wir haben die Kommentare wieder <strong>eingeschaltet </strong>. Sie können Komento allen Kategorien zuordnen oder bestimmte Kategorien auswählen. </p>
  </li>
  <li>Wir haben die Kommentare der &ldquo;<strong>Company</strong>&rdquo; zugeordnet.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Sie können auch die Kommentare für bestimmte Artikel <strong>ausschalten</strong> .Öffnen Sie den Artikel in <strong>Article Manager [Content &gt; Articles]</strong> und fügen Sie den folgenden Code zum Artikeltext hinzu:</p>
    <pre>{KomentoDisable}</pre>
  </li>
  <figure class="img-polaroid"><img src="img/komento-10.jpg" alt=""></figure>
  <li>
    <p>Speichern Sie die Änderungen. Der Artikel hat keine Kommentare mehr.</p>
  </li>
</ol>
<h5><p>Wie man Komento &quot;Terms and Conditions&quot; bearbeitet</p></h5>
  <p align="justify">Komento <strong>Terms and Conditions</strong> Text wird auf Einzelartikel Seiten angezeigt. Die Besucher müssen sich mit diesen Bedingungen einverstanden erklären, um ihre Rückmeldungen einzureichen, wenn diese Option aktiviert ist. Bitte sehen Sie sich den Screenshot unten an: </p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Bitte führen Sie die folgenden Schritte aus um den Text zu ändern:</p>
<ol class="marked-list">
  <li>
    <p>Öffnen Sie Ihre Joomla Admin Panel und gehen Sie zum <strong>Components</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrations</strong> Menü:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Öffnen Sie das &ldquo;<strong>Joomla Article</strong>&rdquo; Komponent. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p>Dann schalten Sie zur <strong>Layout</strong> Sektion um und gehen Sie zum <strong> Comment Form</strong> Tab:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>Wählen Sie zuerst bitte die benötigte Gruppe von Benutzern am unteren Rand der Seite, in der Enable Terms and Conditions  Sektion. </p>
  Standardmäßig ist es auf Gast eingestellt,aber Sie können eine andere Benutzergruppe auswählen. 
  <p>Wir schalten <strong>Terms and Conditions</strong> für alle Benutzergruppen aus.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>Danach sollten Sie den Checkboxtext im <strong>Terms and Conditions Textfeld</strong> eingeben. </p>
  Bitte geben Sie hier den gewünschten Text ein und speichern Sie die Änderungen mit dem <strong> Save </strong> Knopf oben links.
  </li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

<p>Der Text wurde erfolgreich aktualisiert:</p>
