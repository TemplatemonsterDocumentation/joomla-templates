<h3>Komento Comments</h3>

<p>Komento ist eine Kommentarkomponente für Joomla. Sie erlaubt Ihnen, Kommentare zu Joomla Artikel hinzuzufügen:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Man kann die Komponente “Komanto” im <b>Components/Kommentare > Komento</b> Tab in Joomla Admin Panel aufrufen.</p>

<p>Um zu den Konfigurationseinstellungen zu genlangen, öffnen Sie bitte <b>Components / Kommentare > Komento > Integrations</b> mit dem Druck auf <b>Articles/Artikel</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>Mit Hilfe von Konfigurationseinstellungen können Sie Kommentare für bestimmte Kategorien aktivieren, Captcha und Nutzerprofils hinzufügen usw. Mehr können Sie <a
        href="http://stackideas.com/docs/komento.html" target="_blank">auf der offiziellen Website</a> erfahren.</p>

<p>In unseren Vorlagen wird nur die Standardversion von Komento verwendet. Um zusätzliche Funktionen dieses Moduls nutzen zu können, empfehlen wir  <a href="http://stackideas.com/komento/plans">Komento Professional</a> zu abonnieren. Um die ganze Liste von Funktionen anzuschauen, öffnen Sie: <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
   Kostenpflichtige Version von Komento bietet Ihnen auch professionelle technische Unterstützung an..</p>
