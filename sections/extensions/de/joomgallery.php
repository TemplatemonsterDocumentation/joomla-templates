<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> ist eine Komponente, mit der Sie eine komplette Foto-Galerie auf Basis von Joomla erstellen können! Sie können sich gerne mehr Information auf der <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">offiziellen Website</a> ansehen. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Kategorien erstellen</p></h5>

<p> Gehen Sie zu <strong>Components</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Category Manager</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Klicken Sie auf den <strong>New</strong> Knopf oben links: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
  <p align="justify">Geben Sie den Kategorie <strong>Titel</strong> und <strong>Beschreibung</strong> mit Bildern ein und <strong>Speichern</strong> Sie die Einstellungen. Sie können auch die <strong> Meta Beschreibung</strong> für die Kategorie eingeben und Zugriffsbeschränkungen bei Bedarf festlegen.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-9.jpg" alt="">
  </figure>
    
</p>
