<h5><p>Lasst uns den <strong>Image Upload</strong> ausprobieren. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Drücken Sie auf den <strong>Browse</strong> Knopf in der <strong>Image Selection</strong> Sektion und wählen Sie die Mediadateien auf Ihrem PC. </div>
  </li>
  <li>
    <div align="justify">Geben Sie die <strong>Category</strong> for these images [you can upload bunch of images for each category this way], <strong>Generic Titel</strong> for these images [for example, if you upload 3 files with generic title &quot;test&quot;, they will appear in your Image manager as test_1, test_2 and test_3]. </div>
  </li>
  <li>
    <div align="justify">Geben Sie die <strong>Allgemeine Beschreibung</strong> und <strong>Autor</strong> bei Bedarf an. Wählne Sie den <strong>Zugriff</strong> Typ: public, super users, registriert, speziell. </div>
  </li>
  <li>
    <div align="justify">Benutzen Sie die <strong>Special Image Files</strong> Otion. Diese Option ermöglicht das Hochladen von speziellen Bilddateien. Wählen Sie diese Option aus, falls Ihr Upload ausschließlich aus animierten oder transparenten .png- oder .gif-Dateien besteht. Denken Sie daran, dass die Dateien nicht verkleinert werden und daher in voller Größe in Detailansicht angezeigt werden.</div>
  </li>
  <li>
    <div align="justify"><strong>Debug Modus</strong>. Mit dem aktivierten <strong>debug Modus</strong> werden alle Bearbeitungsschritte angezeigt. Im Fehlerfall werden sie immer angezeigt.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Bitte klicken Sie auf den <strong> Upload</strong> Knopf unten, wenn die Konfiguration beendet ist..</p>
</div></div>
<h5><p>Bearbeiten vorhandener Galerieelemente</p></h5>
<div align="justify">Gehen Sie zur <strong>Components</strong> > <strong>JoomGallery</strong> > <strong>Image Manager</strong> Sektion. Hier finden Sie eine Liste der bereits hochgeladenen Bilder, damit Sie die vorhandenen bearbeiten können:</div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p>Wenn Sie diese <strong> bearbeiten</strong> müssen, sollten Sie folgendes tun: </p>
<ol class="marked-list">
  <li>Öffnen Sie ein beliebiges Element aus der Liste.     </li>
  <li>Ändern Sie<strong> Titel</strong>, <strong>Kategorie</strong>, <strong>Beschreibung</strong>, <strong>Hits</strong>, etc.  im <strong> Bilder</strong> Tab. Scrollen Sie runter, und sehen Sie sich die <strong>Miniaturansicht Vorschau</strong> und <strong>Bild Vorschau</strong> an. </li>
  <li>
    <div align="justify">Benutzen Sie <strong>Verbergen</strong> Option. Wenn man dieses Bild versteckt, wird es nicht mehr in der Kategorieansicht angezeigt oder von der Suche oder den Toplisten erkannt, aber es wird immer noch durch direkte Verknüpfung verfügbar sein. Wenn Sie nicht wollen, dass es verfügbar ist, veröffentlichen Sie es bitte stattdessen. Durch das Ausblenden von Bildern ist es möglich, sie als Kategorie-Thumbnails zu verwenden, ohne sie beispielsweise in der entsprechenden Kategorie anzuzeigen.</div>
  </li>
  <li>
    <div align="justify"><strong>Featured </strong>Option. Hier können Sie ein Bild als 'Featured' markieren. Beispielsweise kann das Modul JoomImages so konfiguriert werden, dass nur eingebaute Bilder auf einer Modulposition angezeigt werden.</div>
  </li>

<p>
  <figure class="img-polaroid">
    <img src="img/gallery-17.jpg" alt="">
    
  </figure>
</p>

  <li>Gehen Sie zum <strong>Parameters</strong> Tab, um den <strong>Besitzer</strong> und <strong>Autor </strong>für dieses Galerieelement.</li>

<figure class="img-polaroid">
  <div align="justify"><img src="img/gallery-19.jpg" alt="">
    
  </div>
</figure>

  <li>
    <div align="justify">Im <strong>Ersetzen von Bilddateien</strong> Tab können Sie Bilddateien von Ihrer Festplatte auswählen, um die entsprechenden aktuellen Dateien in der Galerie zu ersetzen. Laden Sie neue Bilder mit der Schaltfläche Durchsuchen hoch.</div>
  </li>

<p>
  <figure class="img-polaroid">
    <img src="img/gallery-18.jpg" alt="">
    
  </figure>
</p>

  <li>Sie können auch <strong>Beschreibung</strong> und <strong>Schlüsslewörter</strong> im<strong> Metadata</strong> Tab ändern:</li>

