<h3>TM Ajax Contact Form Module</h3>

<p align="justify"><strong>TM Ajax Contact Form Module</strong> ist ein Kontaktformular Modul für Joomla Engine.
Dieses Modul ermöglicht das Hinzufügen von Kontaktformular zu jeder Seite Ihrer Joomla-Website.</p>
<p align="justify"><strong>Kontaktformular </strong>ist im Grunde eine Reihe von Fragen auf der Website , die Ihre Besucher ausgefüllen, die automatisch an Ihre E-Mail gesendet wird, wenn es ausgefüllt wird. Normalerweise enthält das Kontaktformular Felder zum Ausfüllen mit Namen, Adresse, Telefon, E-Mail und Art des Kommentars. Sobald die Besucher Ihrer Website die Kontaktformularfelder ausgefüllt haben, klicken sie auf die Schaltfläche Senden [Senden], um die Informationen zu übermitteln.</p>
