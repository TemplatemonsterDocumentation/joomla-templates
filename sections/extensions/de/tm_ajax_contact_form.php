<h3>Modul TM Ajax Contact Form</h3>

<p>TM Ajax Contact Form ist ein Kontaktformular-Modul für Joomla. Es ermöglicht auf
    jeglicher Seite der Website das Kontaktformular zu platzieren.</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>
<p> Senden von Daten wird
    per AJAX Technologie durchgeführt. In den Einstellungen des Moduls gibt es die
    Möglichkeite den Schutz vor Bots (reCaptcha) zu aktivieren, und auch Ausgabefelder
    und ihre Stichwörter (Labels) einzustellen, E-Mail-Adresse des Empfängers,
    E-Mail-Adresse des Kopienempfängers, Textnachricht über Fehler und erfolgreiches
    Versenden festzulegen. Eine vollständige Liste der Einstellungen gibt es auf dem
    Bild unten.</p>

