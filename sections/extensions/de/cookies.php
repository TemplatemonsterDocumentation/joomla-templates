<h3>Joomla Cookie Directive Plugin</h3>

<p><strong>Joomla Cookie Directive Plugin</strong> ist in der Lage, ein Cookie-Akzeptanz-Banner an der Spitze der Website anzuzeigen, um über die Website mit Cookies zu warnen.</p>

<figure class="img-polaroid">
    <p><img src="img/cookies-1.jpg" alt="">
    </p></figure>
    <p align="justify">Um das <strong>Joomla Cookie Directive</strong> Plugin zu konfigurieren, gehen Sie zu <strong>Extensions</strong> &gt; <strong>Plugins</strong>. Um das Plugin zwischen den anderen zu finden, geben Sie &quot;cook/.../&quot; in das Suchfeld ein.</p>
    <p align="justify">Klicken Sie auf den &quot;<strong>system - EUCookieDirectiveLite</strong>&quot; Titellink und Sie werden die <strong>Joomla Cookie Directive</strong> plugin Haupteinstellungen sehen.</p>

<p>In diesem Bildschirm können Sie Plugins Details und Optionen bearbeiten  . </p>

<p>
  <figure class="img-polaroid">
