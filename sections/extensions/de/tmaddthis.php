<h3>TM Addthis</h3>

<p>TM Addthis ist ein Plugin, das Buttons "Teilen" anzeigt.</p>

<figure class="img-polaroid"><img src="img/addthis-1.jpg" alt=""></figure>

<p>So sehen die Grundeinstellungen des Plugins TM Addthis aus. Im Feld Profile ID wird
    die Profil-ID angegeben, die nach der Registrierung und Erstellung des Profils auf
    der Seite <a href="https://www.addthis.com/settings/publisher" target="_blank"
                 rel="nofollow">https://www.addthis.com/settings/publisher</a>eingegeben
    wurde.</p>

