<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> Ist die weltweit größte Content-Sharing-Plattform. Es bietet Werkzeuge an, die es einfacher machen Inhalte über Social-Web zu teilen , und Ihren Traffic und eingehende Paralytik zu erhöhen.<strong> AddThis</strong> kann in eine Website mit der Verwendung eines Web-Widget integriert werden. Sobald das Widget hinzugefügt ist, können Besucher der Website einen Artikel mit einer Vielzahl von Diensten wie Facebook, MySpace, Google Bookmarks, Pinterest und Twitter teilen. </p>
<p><strong>TM Addthis </strong>ist ein Plugin, um die ‘share’ Tasten anzuzeigen:</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br />
Das Hinzufügen unserer Share-Buttons zu Ihrem Joomla Website ist eine großartige Möglichkeit, um mehr von Ihrem Inhalt online zu verteilen. Je mehr Ihre Inhalte geteilt werden, desto mehr Verkehr bekommen Sie zurück zu Ihrer Website. AddThis Share-Buttons sind einfach hinzuzufügen, und bietet auch detaillierte Analysen, die Ihnen helfen, Ihre Webseite effektiver zu verwalten. </p>
<p><strong>EIGENSCHAFTEN</strong></p>
<ul>
  <li>Wählen Sie aus, wo die Share-Buttons auf Ihrer Seite angezeigt werden;</li>
  <li>Steuern Sie, wie viele Tasten angezeigt werden sollen,;</li>
  <li>Geben Sie Aktienzählungen ein, um das Teilen zu fördern;</li>
  <li>Personifizieren Sie Sharing-Buttons für jeden Benutzer oder wählen Sie sie selbst.</li>
</ul>
<p align="justify">Um das Plugin anzupassen, gehen Sie zu <strong>Extensions</strong> &gt; <strong>Plugins</strong>. Sie kommen zum <strong>Plugins Manager</strong>. Tippen Sie &quot;Add&quot; in das Suchfeld ein und Sie werden <strong>Content - TM AddThis</strong> sehen.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>Zuerst sollten Sie das Plugin <strong>einschalten</strong>. Drücken Sie auf den &quot;X&quot; Knopf, um es einzuschalten. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Sobald das Plugin <strong>eingeschaltet ist</strong>, werden Sie eine Bestätigungsnachricht sehen.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Um das Hauptfenster des Plugins zu erhalten, drücken Sie auf den <strong>Content - TM AddThis</strong> Titellink und es wird Sie zu dem folgenden Fenster führen:</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Hier sehen Sie die wichtigsten <strong>TM Addthis Einstellungen</strong> sehen. Wählen Sie die <strong> Kategorien, die Sie ausschließen wollen</strong> im entsprechenden Feld.  Im <strong>Profil ID</strong> Feld geben Sie Ihre Profil ID ein, die Sie nach der Registration bekommen haben, und erstellen Sie ein Konto auf der nächsten Seite: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>Grundsätzlich sollten Sie eine neue <strong>Profile ID</strong> bekommen, um die <strong>AddThis</strong> Social-Share Symbole zu ändern:</p>
<p>Nachdem Sie sich registriert haben, müssen Sie <strong>Share</strong> Buttons hinzufügen. </p>
<p>Um dies zu tun, drücken Sie &quot;<strong>AddThis</strong>&quot; Knopf in der Profil-Symbolleiste.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Dann müssen Sie auf &quot;<strong>Get Started</strong>&quot; in der in Share Buttons Sektion drücken.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Aktivieren Sie die <strong>Inline </strong>Share Buttons.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">Sie können den <strong>Share Counters</strong> Typ wählen. Sie können auch Ihre eigenen sozialen Netzwerke auswählen und die Reihenfolge anpassen mit der <strong>Select your own</strong> Option.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Ändern Sie das Design der Symbole in der <strong>Design</strong> Sektion. Passen Sie <strong>Stil</strong>, <strong>Tastengröße</strong> und <strong>Farbe</strong> der Symbole an. Wählen Sie die <strong>Rounded Corners</strong> Eigenschaft und <strong>Netzwerknamen verstecken</strong>, wenn nötig.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Wenn Sie mit der  <strong>Share Buttons</strong> Anpassung fertig sind, klicken Sie auf <strong>Save&amp;Continue</strong>, um mit dem nächsten Schritt fortzufahren.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Kopieren Sie Ihre Profil ID in der &quot;<strong>1</strong>&quot; Sektion. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>Sie sollten es in das <strong>Profile ID</strong> Feld im <strong>Content - TM AddThis</strong> Einstellungenfenster einfügen, damit die <strong>Share Buttons</strong> auf Ihrer Seite erscheinen.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Sie können Kategorien (Seiten) auch ausschließen, auf denen das Plugin nicht angezeogt werden soll.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Stellen Sie sicher, dass Sie das Plugin <strong>eingeschaltet </strong> haben.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Speichern Sie die Änderungen und aktualisieren Sie die <strong>Share Buttons</strong> Seite. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-18.jpg" alt=""></figure>
</p>