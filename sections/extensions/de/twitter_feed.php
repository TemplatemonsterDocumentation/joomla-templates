<h5>Finden Sie Ihre Twitter Widget ID</h5>
<p align="justify">Wenn ein Twitter Widget, das Sie versuchen einzuschalten,Sie nach einer Twitter-Widget-ID fragt, müsssen Sie ein neues Widget auf Ihrer <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">Twitter Widget Einstellungen</a> Seite sehen.</p>
<p align="justify">Um die Twitter Widget Einstellungen zu öffnen, loggen Sie sich in Ihr Twitterkonto ein, das Sie für das Widget benutzen wollen. Dann klicken Sie auf das Zahnrad in der rechten oberen Ecke und klicken Sie auf<strong> Einstellungen</strong>. Aus der Einstellungen Sidebar, wählen Sie das <strong>Widgets</strong> Tab ganz unten.</p>
<p align="justify">Auf dieser Seite suchen Sie den <strong>Neu erstellen</strong> Knopf und drücken Sie darauf, um ein neues Widget zu erstellen. Von hier aus erhalten Sie Konfigurationsoptionen und eine Vorschau auf das Widget. Sie können jederzeit die Konfigurationsoptionen des Widgets ändern.</p>
<p align="justify">Nachdem Sie das Widget erstellt haben, wird der <strong>Neu erstellen</strong> Knopf mit einem <strong>Änderungen speichern</strong> Knopf ersetzt. Wenn nun Ihr Widget erstellt wurde, können Sie die Widget ID aus der URL bekommen. Schauen Sie sich die Adressleiste Ihres Browsers an. Es sollte eine URL haben, die so in etwa aussieht:</p>
 <blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p>Ihre Widget-ID ist die 18-stellige Nummer. In diesem Fall, <strong>512253083833733120</strong>. Denken Sie daran, diese Nummer privat zu halten, dann wird Ihr Widget nicht von anderen ohne Ihre Erlaubnis verwendet werden.</p>
<p>Kopieren Sie Ihre ID und fügen Sie es in das Textfeld ein, das nach Ihrer Widget ID fragt. Nach dem Speichern Ihrer Einstellungen sollte Ihr neues Twitter-Widget funktionieren.</p>
