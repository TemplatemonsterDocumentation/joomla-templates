<h3>Image Swoop</h3>

<p>Модуль "Image Swoop" – это отличный адаптивный слайдер, представляющий собой смесь
    стандартного модуля Joomla “Articles Newsflash” и бесплатного плагина jQuery с
    открытым исходным кодом “Camera Slideshow”. Великолепно работает на компьютерах и
    мобильных устройствах iOs или Android. Совместим со всеми современными браузерами,
    начиная с IE8</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>Модуль "Image Swoop" обладает большим многообразием настраиваемых опций, которые распологаются в меню <b>Extensions
        (Расширения)> Module Manager (Менеджер модулей)</b> админ-панели Joomla. Все
    общая настройка основных опции плагина jQuery "Camera Slideshow" меняется в настройках модуля. </p>

<p>При наведению курсора миши на конкретную опцию, появится подсказка, с описанием функции данной.</p>
<p>Более детальную
  информацию о плагине jQuery Camera Slideshow можно найти на <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank">официальном
    веб-сайте</a>.</p>

