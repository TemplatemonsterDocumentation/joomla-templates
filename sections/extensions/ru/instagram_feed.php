<h3>TM Instagram</h3>
 
<p>TM Instagram - модуль для вывода виджета Instagram:</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Ниже Вы найдёте основные настройки модуля TM Instagram. В поле Instagram username
    укажите название вашего профиля Instagram, в поле Number of photos - количество
    картинок, выводимых в виджете, в поле Your Instagram CLIENT ID укажите
    идентификатор Вашего приложения Instagram. Для того чтобы получить идентификатор, перейдите
    по ссылке <a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                 target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    и зарегистрируйтесь. Далее перейдите по ссылке <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> и создайте
    приложение. После этого Вы получите данные приложения, в которых будет
    указан идентификатор (CLIENT ID):</p>
 
