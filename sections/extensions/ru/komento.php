<h3>Komento Comments</h3>

<p><strong>Komento</strong> — это компонент для комментариев в Joomla. Он позволяет добавлять комментарии
к статьям Joomla.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Чтобы перейти настройкам компонента Komento Comments можно пройдя к разделу <b>Components > Komento</b> в админ-панели  Joomla.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Как управлять компонентом Komento </p>
</h5>
<ol class="marked-list">
  <li>
    <p>Войдите в  панель управления Joomla сайта и перейдите к разделу  <strong>Components</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrations</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Кликните на <strong>Joomla Article</strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">Во вкладке <strong>General</strong> вы можете Включить [Enable] или Отключить [Disable] комментарии для вашего сайта.  Вы также можете изменить <strong>Модерацию </strong>[Moderation] и другие настройки.  </p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li>
    <p>Сейчас мы <strong>отключили</strong> комментарии [см.  скриншот ниже].</p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>Блок комментариев будет неактивен на вашем сайте.</p></li>
  <p><strong>Enabled</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Disabled</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Теперь мы снова<strong> включили </strong>комментарии.  You can assign Komento to all categories or select particular categories. Вы можете открыть комментирование для  всех категорий сразу, либо же выбрать только определенные категории. </p>
  </li>
  <li>Мы открыли комментарии к категории &quot;<strong>Company</strong>&quot;.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Вы также можете отключить комментирование  определенной статьи.  Для этого перейдите в <strong>Article Manager [Content &gt; Articles]</strong> и вставьте в текст [тело] статьи следующий код:</p>
    <pre>{KomentoDisable}</pre>
  </li>
  <figure class="img-polaroid"><img src="img/komento-10.jpg" alt=""></figure>
  <li>Сохраните изменение. Теперь блок комментариев к этой статье будет неактивен. </li>
</ol>
<h5>
  <p>Как изменить текст сообщения &quot;Terms and Conditions&quot; для компонента Komento </p>
</h5>
<p align="justify">Текст сообщения<strong> Terms and Conditions</strong> компонента Komento отображается только на страницах отдельных статей. Если вы активируете этот  параметр для вашего сайта, его посетителям, которые хотят оставить комментарий к статье, необходимо будет подтвердить согласие  с условиями, описанными в данном сообщении. Смотрите  скриншот ниже: </p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Выполните следующие действия, чтобы изменить текст сообщения:</p>
<ol class="marked-list">
  <li>
    <p>Откройте панель управления Joomla и перейдите в меню <strong>Components</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrations</strong>:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Откройте компонент  &quot;<strong>Joomla Article</strong>&quot;. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p> Затем перейдите в раздел <strong>Layout</strong> и откройте вкладку<strong> Comment Form</strong>:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>Во-первых, внизу страницы, в разделе Enable Terms and Conditions, выберите группу пользователей, которым будет показано сообщении. </p>
   По умолчанию выбрана группа Guest, но вы можете выбрать любую другую группу.
   <p>Мы выбрали группу<strong> &quot;</strong>user&quot;.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>Затем, измените текст сообщения в поле опции <strong>Text for Terms and Conditions</strong>. </p>
  Введите желаемый текст и сохраните изменения, нажатием кнопки <strong>Save</strong> в левом верхнем углу:</li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

<p>Текст сообщения был успешно изменён:</p>
<figure class="img-polaroid"><img src="img/komento-15.jpg" alt=""></figure>
</ol>
