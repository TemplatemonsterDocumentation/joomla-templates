<h3>Komento Comments</h3>

<p>Komento - это компонент для комментариев в Joomla. Он позволяет добавлять комментарии
    к статьям Joomla:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Компонент Komento находится в меню <b>Components (Компоненты) > Komento</b> админ-панели Joomla.</p>

<p>Для того чтобы перейти в настройки модуля, откройте вкладку <b>Components (Компоненты) > Komento >
        Integrations (Интеграция)</b> и нажмите <b>Articles (Статьи)</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>При помощи настроек компонента, можно активировать комментарии для определенных
    категорий и статей, добавить captcha, профили пользователей и т.п. Более детальную
    информацию о Komento можно найти на <a
        href="http://stackideas.com/docs/komento.html" target="_blank">официальном
        сайте</a>.</p>

<p>В наших шаблонax используется только стандартная версия Komento. Для того чтобы
    воспользоваться дополнительными возможностями данного модуля, мы рекомендуем
    воспользоваться <a href="http://stackideas.com/komento/plans">полной версией</a>
    продукта. Чтобы увидеть полный список функций, посетите сайт <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
   С платной версией Komento Вы также получите профессиональную техническую поддержку.</p>
