<p>Во вкладке <strong>Advanced</strong> вы найдёте следующие настройки:</p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-16.jpg" alt="">
</p></figure>
    <ol class="marked-list">
      <li> 
        <div align="justify"><strong>Module Class Suffix</strong>. Суффикс который будет приписан  CSS классу этого модуля.</div>
      </li>
      <li> <strong>Module Tag</strong>. HTML-тэг для модуля.</li>
      <li> <strong>Bootstrap Size</strong>. Опция, с помощью которой сможете указать количество колонок, используемых модулем.</li>
      <li> <strong>Offsetting columns</strong>. Увеличьте значение отступа слева, выбрав соответствующие количество колонок.</li>
      <li> <strong>Header Tag</strong>. HTML-тэг для  заголовка/наименования модуля.</li>
      <li> <strong>Header Class</strong>. CSS класс для заголовка/наименования модуля.</li>
      <li> <strong>Module Style</strong>. Используйте эту опцию для отключения стиля шаблона по умолчанию, который применяется для этой позиции.</li>
    </ol>
   