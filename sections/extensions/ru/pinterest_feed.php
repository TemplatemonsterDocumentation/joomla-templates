<p>Во вкладке Advanced Options, вы можете присвоить суффикс классу модуля, если есть такая необходимость.</p>
<p align="justify">Затем,  странице сайта, на которую хотите поместить модуль. Для этих целей, перейдите во вкладку <strong>Menu Assignment</strong>. &lt;table width=&quot;980&quot;  class=&quot;options-table&quot;&gt;</p>
<p align="justify">В этой вкладке вы увидите перечень всех пунктов меню и страниц вашего сайта. Выберите между параметрами: на всех страницах [On All Pages], нигде [No Pages], на выбранных страницах [Only on the pages selected] или отображать на всех страницах, кроме отмеченных [On all pages except those selected from the List].  Последние две опции позволяют привязывать модуль только к некоторым страницам. Отметьте галочкой ту страницу или пункт меню, где вы хотите [не хотите, если выбран последний параметр] поместить данный модуль. </p>
