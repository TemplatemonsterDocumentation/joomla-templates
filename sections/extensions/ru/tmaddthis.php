<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> является крупнейшей в мире платформой обмена контентом. Платформа эта предоставляет инструменты упрощения процесса обмена  контентом через социальные сети, а также инструменты, которые способны помочь увеличить трафик вашего сайта.  <strong>AddThis</strong> может быть интегрирован в веб-сайт с помощью виджета. После добавления виджета на ваш сайт, ого посетители сайта могут делать закладки на ваш контент, используя широкий спектр сервисов, таких как Facebook, MySpace, Google Bookmarks, Pinterest и Twitter.</p>
<p><strong>TM Addthis </strong>— это плагин для отображения кнопок типа &quot;поделиться&quot; на вашем сайте:</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br />
Добавление  кнопок типа &quot;поделиться&quot;  на ваш сайт Joomla — это отличный способ на пути к увеличению масштаба распространения контента вашего сайта в  Интернете. Чем больше ваш контент распространяется, тем больше трафика будет возвращаться на ваш сайт. AddThis кнопки типа &quot;поделиться&quot;  легко добавить на сайт, что еще немаловажно, добавление кнопок сопровождается функцией сбора аналитических данных, что поможет вам  в повышении эффективности контента вашего сайта. </p>
<p><strong>ВОЗМОЖНОСТИ TM Addthis</strong></p>
<ol class="marked-list">
  <li>Выбирайте, где расположить кнопки на вашем сайте;</li>
  <li>Контролируйте количество кнопок;</li>
  <li>Добавляйте счетчик к кнопкам типа &quot;поделиться&quot; [сколько раз поделились];</li>
  <li>Настраивайте внешний вид кнопок по своему вкусу.</li>
</ol>
<p align="justify">Перейдите к разделу <strong>Extensions</strong> &gt; <strong>Plugins </strong>и вы попадете к менеджеру плагинов [<strong>Plugins Manager</strong>]. Введите, к примеру, текст &quot;Add&quot; в поле поиска, затем, нажмите кнопку поиска и в таблице менеджера вы увидите плагин <strong>Content - TM AddThis</strong>. Перейдите по ссылке его заголовка, чтобы приступить в настройке модуля.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p><strong>Активируйте</strong> плагин, нажав на значок с красным крестиком возле заголовка плагина. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Вы увидите сообщение о подтверждении <strong>активации</strong> плагина.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Перейдите по ссылке  заголовка плагина [<strong>Content - TM AddThis]</strong>, чтобы приступить к его настройке.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">На открывшейся странице вы можете увидеть главные настройки плагина <strong>TM Addthis</strong>. Определите <strong> категорию для исключения [categories to exclude]</strong> в соответствующем поле опции.  Введите <strong> ID профиля </strong> <strong>AddThis</strong> в поле опции Profile ID. <strong>ID профиля </strong> <strong>AddThis</strong> вы можете получить, создав профиль в системе AddThis: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p align="justify">Первое, что вы должны сделать, чтобы поменять уже существующие кнопки типа &quot;поделиться&quot; на вашем сайте — это создать свой профиль в системе <strong>AddThis</strong>:</p>
<p>После того как вы создали профиль в системе, вам нужно приступить к процессу создания кнопок  типа &quot;поделиться&quot;. </p>
<p>Для этого нажмите кнопку &quot;<strong>AddThis</strong>&quot; на панели инструментав, в верхней части страницы  вашего профиля <strong>AddThis</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Затем, нажмите кнопку &quot;<strong>Get Started</strong>&quot;,   что находится в секции  Share Buttons.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Активируйте опцию <strong>Inline </strong>для кнопок, этим вы зададите  горизонтальное их расположение.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">У вас есть возможность выбрать тип <strong>Share Counters</strong> [счетчика] для ваших кнопок. Выберите нужные вам социальные сети, через которые пользователи смогут делиться контентом вашего сайта. Изменяйте порядок расположения кнопок с помощью опции <strong>Select your own</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Измените внешний вид кнопок в разделе <strong>Design</strong>. Настраивайте<strong> стиль кнопок</strong>,<strong> размер</strong> <strong>и цвета</strong>. Задайте значение <strong>скругления углов</strong> кнопки или же скройте <strong>название соцсетей</strong> на кнопках, если нужно.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Как только вы вы закончите настройку внешнего вида кновок, нажмите<strong> Save&amp;Continue</strong> и вы перейде к следующему этапу.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p align="justify">На этом этапе, вы можете получить  ID вашего профиля. ID вашего профиля вы почете найти в секции &quot;<strong>1</strong>&quot; . Скопируйте его [ID вашего профиля, см. изображение ниже]</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p align="justify">Внесите скопированное значение в соответствующее поле настроек плагина<strong> Content - TM AddThis</strong> и созданные вами кнопки появятся на вашем сайте.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Исключите категории страниц, где не хотите отображать данный плагин.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Убедитесь в том, что вы <strong>активировали</strong> плагин.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Save the changes and refresh the <strong>Share Buttons</strong> page. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-18.jpg" alt=""></figure>
</p>