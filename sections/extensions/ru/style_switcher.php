<h3>TM Ajax Style Switcher Module</h3>

<p align="justify"><strong>TM Ajax Style Switcher Module</strong> – это модуль позволяющий переключать цветовые схемы на фронтенде
сайта. Модуль автоматически применяет цветовые схемы из папки color_schemes\css\ активной темы.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning">Обратите внимание, что на демо-версии шаблона, принцип действия модуля показан лишь частично,
    используя cookies файлы. На действующем сайте модуль будет работать в полноценном режиме и будет доступен только для авторизованных
пользователей с правами категории пользователей  Super Users.</p>
<h5>Как работать с модулем TM ajax style switcher  </h5>
<p align="justify">Перейдите к разделу<strong> Extensions</strong> &gt; <strong>Modules</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify">Нажмите кнопку &quot;<strong>Search Tool</strong>&quot; и выберите <strong>TM Ajax Style Switcher Module</strong> в списке поискового фильтра &quot;- Select Type -&quot;. В таблице менеджера, вы увидите модуль <strong>Ajax Style Switcher Module</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Кликните по ссылке заголовка модуля и вы перейдете к главной странице настроек данного модуля. Здесь вы найдете только стандартные настройки для модуля Joomla:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Убедитесь что модуль <strong>опубликован</strong>.</p>
<h5 align="justify">
  <p align="justify"> Для активации переключателя стилей для нового пользователя  на вашем сайте, следуйте инструкциям: </p>
</h5>
<h5 align="justify">
  <p align="justify">Владелец сайта принадлежит к категории пользователей Super Users, и следовательно, имеет доступ к управлению переключателем стилей через фронтенд сайта. Для этого он [владелец сайта] должен авторизоваться в системе своего сайта, используя тот логин и пароль, который был указан в процессе установки Joomla. </p>
</h5>
<ol class="marked-list">
    <li>
