<h3>Articles Single
</h3>
<p align="justify"><strong>Articles Single</strong> — это модуль, позволяющий поместить  блок с одной статьей в любое место сайта [конкретной страницы].</p>
<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">Единственной довольно сложной, но только первый взгляд, опцией модуля является <strong>Custom Link</strong> [Кастомная
        ссылка]. Она позволяет отображать любую кастомную ссылку под контентом модуля. Ее
можно задать на внешний URL или страницу вашего Joomla веб-сайта. </p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">
  <сли вы>
Если вы хотите перейти к настройке модуля &quot;<strong>HOW WE WORK</strong>&quot; (например), пройдите к разделу <strong>Extensions</strong> &gt; <strong>Modules</strong>. В менеджере найдите модуль &quot;<strong>How we work</strong>&quot; типа<strong> Articles - Single</strong>, для поиска пользуйтесь фильтрами. Найти данный модуль вы также можете используя сочетание клавиш  Ctrl+F. И перейдите по ссылке заголовка модуля, после чего вы окажетесь на главной станице настройки модуля &quot;How we work&quot;. </p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
</p>
 <p>Модуль <strong>Single Article</strong> обладает таким набором настроек:</p>
 <p align="justify"><strong>Select Article.</strong> В этом поле предлагается выбрать статью, которая будет показана в модуле. Нажмите кнопку <strong>Select/Change</strong>, чтобы открыть  окно выбора статьи, такое как показано ниже.</p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify">Окно  выбора статьи, по своим функциональным возможностям, идентично Articles Manager. Используете <strong>Filter fields</strong>, для поиска нужной статьи, а после, выберите статью, кликнув по ссылке ее заголовка. Как только статья будет выбрана — окно закроется, а название выбранной статьи будет отображаться в поле <strong>Select Article</strong>.</p>
<p align="justify">Теперь у вас появилась возможность редактировать статьи прямо через  окно настройки модуля. Для этого, нажмите кнопку <strong>Edit</strong>, и вы увидите окно редактирования. Меняйте содержание статьи, добавляйте изображения и ссылки.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify">В настройках модуля <strong>Article Single</strong>  вы найдете вкладку <strong>Options</strong> [см. слайд ниже]. Опции этой вкладки регулируют правила отображения статей на  Frontеnd сайта. Обратите внимание на значение &quot;<strong>Use Global</strong>&quot;. Если это значение было выбрано, то к модулю будет применяться все настройки  указанные в Article Manager.</p>
