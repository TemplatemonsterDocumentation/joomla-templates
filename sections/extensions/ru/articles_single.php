<h3>Articles Single</h3>

<p>Articles single — это модуль, позволяющий поместить поместить блок с одной статьей в любое место сайта (конктетной страницы). </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p>Единственной довольно сложной, но первый взгляд, опцией модуля является <b>Custom Link (Кастомная
        ссылка)</b>. Она позволяет отображать кастомную ссылку под контентом модуля. Ее
    можно задать на внешний URL или страницу веб-сайта Joomla. </p>

