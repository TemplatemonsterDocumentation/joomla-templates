<h3>Articles Single</h3>

<p>Articles single - это модуль, позволяющий поместить статью в нужной позиции: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p>Полезной настройкой модуля является <b>Custom Link</b> (Пользовательская
        ссылка). Она позволяет отобразить пользовательскую ссылку под содержимым модуля. Вы можете задать внешнюю ссылку или ссылку на страницу вашего веб-сайта Joomla:</p>

