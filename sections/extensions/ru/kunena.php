<h3>Форум Kunena</h3>

<p align="justify">Kunena — компонент форума для Joomla. Он позволяет реализовать полностью
    функциональный форум, базирующийся на Joomla!</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Компонент Kunena доступен в панели управления Joomla в меню <b>Components
        (Компоненты) > Kunena</b>.</p>

<p align="justify">Более детальную информацию об использовании и настройках компонента Kunena можно
    найти на <a href="http://www.kunena.org/docs/Main_Page" target="_blank">официальном
        веб-сайте</a>.</p>

