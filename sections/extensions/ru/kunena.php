<p align="justify"><strong>Kunena Forum</strong> — это компонент необходимый для внедрения форума  на вашем сайте Joomla. Он достаточно гибок и прост в использовании. Отображение макета страницы форума на вашем сайте различно для зарегистрированных и незарегистрированных пользователей, так же как и различен  перечень доступных возможностей использования  форума для разных категорий пользователей. </p>
<p align="justify">Форум на вашем сайте прикреплен за пунктом главного меню '<strong>Pages</strong>'. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p align="justify"> Вы можете управлять общими настройками форума <strong>Kunena</strong> и его содержимым через админ панель вашего сайта. </p>
<p align="justify">В боковом меню на главной странице админ-панели в разделе Extensions пройдите по ссылке<strong> Kunena Forum</strong>:</p>
<figure class="img-polaroid">
  <img src="img/forum-2.jpg" alt="">
</figure>

<p> Перед вами откроется страница настроек компонента с несколькими доступными вкладками:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Category manager</strong>. В центральной части экрана вы увидите список всех категорий, доступных на форуме в данный момент. Вы можете выбрать и отредактировать настройки любой тему при необходимости. Вы также имеете возможность изменять родительскую категорию, название категории, алиас, описание и заголовок: </p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify">В данной области будут отображаться все темы обсуждения пользователей. Вы можете создавать столько тем обсуждений, сколько необходимо. Каждая дискуссия должна быть закреплена за одним из общих разделов [раздел используется как заголовок для тем форума]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Category Permissions </strong>позволит задать необходимый уровень доступа к конкретной категории:   группы пользователей  с правом просматривать и редактировать данную категорию. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Category Settings</strong> позволяет управлять всеми общими настройками отображения сообщений категории.</p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify">Вы можете использовать кнопку <strong>New Category</strong> для того, что бы создать новую категорию для форума. Заполните все необходимые поля параметров настроек новой категории и сохраните изменения.</p>
<p align="justify"><strong>Users section.</strong> Здесь вы можете производить настройки профиля,  правил доступа к редактированию и просмотру форума для каждого отдельного пользователя:</p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify">Перейдите по ссылке имени определенного юзера и перед собой вы увидите окно настроек, в котором, в разделе <strong>Basic settings</strong>, вы можете изменить  <strong>аватар </strong>и <strong>подпись</strong> этого пользователя, если есть такая необходимость. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify">Вкладка<strong> Moderation </strong>позволит вам выбрать, отдельного пользователь как модератора, а также указать категории нуждающиеся в модерации. Нажмите  кнопку  <strong>Save &amp; Close</strong><strong> </strong>вверху экрана, когда закончите производить настройку параметров для этого пользователя.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Attachments</strong>. Здесь вы можете работать с загруженными файлами.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emotions</strong>. Здесь расположены все доступные смайлы для форума. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Используйте область <strong>New Smile</strong>, что бы добавить новые смайлы если необходимо:</p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify"><strong>Ranks</strong>. Здесь вы найдёте список рангов, доступных для пользователей. Каждый ранг можно изменить вручную:</p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Templates</strong>. Выберите шаблон, к которому будет применяться компонент форума: </p>
<p>
  <figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
</p>
<p align="justify"><strong>Configuration</strong>. Эта вкладка позволяет управлять разнообразными подробными настройками форума. Названия, адрес электронной почты, SEO, кэширование и другие настройки можно задать во вкладке <strong>General</strong>. В разделе <strong>Frontend</strong>  вы можете изменить макет отображения форума на вашем сайте. Управлять общими настройками для пользователей форума можно через секцию <strong>Users</strong>. Вкладка<strong> Security</strong> контролирует правила доступа  для гостей форума и зарегистрированных С помощью вкладки<strong> Avatars</strong> вы можете позволить [либо запретить] пользователям загружать аватары, а также уточнить настройки отображения изображений. Во вкладке <strong>Uploads </strong>вы можете разрешить или запретить пользователям  прикреплять файлы к сообщениям, а также указать настройки загрузки для этих файлов.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>

