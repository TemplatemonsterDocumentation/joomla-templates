<h5>Создание категорий</h5>
<ol class="marked-list">
  <li>
    <p>Перейдите во вкладку <strong>Components</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Categories</strong>.</p>
</li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Нажмите кнопку <strong>New Category </strong>в верхнем левом углу.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>Задайте общие настройки для категории во вкладке <strong>Category Name and Description</strong>:  
    <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify">Для того чтобы создать раздел, выберите <strong>Top Level</strong>, как родительский. </p>
      </li>
      <li>
        <p>Введите <strong>заголовок</strong> для категории и <strong>Alias</strong>.</p>
      </li>
      <li>
        <p align="justify">Убедитесь, что категория <strong>опубликована</strong>.</p>
      </li>
      <li>
        <p>Введите  <strong>описание</strong> категории и<strong> заголовок</strong> в соответствующие поля.</p>
      </li>
      <li>
        <p>Добавляйте <strong>custom css class</strong> для каждой категории. <strong>Суффиксы CSS категорий</strong>    будут применяться к основной странице, демонстрационному блоку и другим   элементам. В результате, вы сможете создать разный дизайн для разных   категорий.</p>
      </li>
    </ol>
  <li>Перейдите во вкладку <strong> Category Permissions</strong>, для того чтобы задать правила доступа для категории:

    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Здесь Вы найдёте две настройки: </p>
    
    <ol class="marked-list">
      <li><strong>Тип контроля доступа</strong>Access Control Type], который будет использоваться в этой категории.
      </li>
      <li>
        <p align="justify">Группа <strong>Уровня доступа</strong> [Access Level] – группа, которая будет иметь доступ к этой категории. Вы можете определить ваш уровень доступа во вкладке <strong>Users</strong> &gt; <strong>Access Levels</strong>.</p>
      </li>
    </ol>
</li>  <li>Вкладка<strong> Category Settings</strong>:
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Locked</strong>. Задайте значение Да [Yes], если вы хотите   закрыть категорию. Только Модераторы сайта и Админы смогут создавать   новые ответы в закрытой категории.</p>
      </li>
      <li><strong>Review posts</strong>. Задайте значение Да [Yes], если вы   хотите, чтобы записи проверялись модераторами, перед тем как они будут   опубликованы в категории.
      </li>
      <li>
        <p align="justify"><strong>Allow anonymous messages </strong>позволит вам включить/отключить анонимные сообщения для категории.</p>
      </li>
      <li>
        <p align="justify"><strong>By default post replies as option</strong>.  Если вы включили   анонимные сообщения, эта настройка определят варианты выбора   пользователя по умолчанию. Постоянные пользователи сайта могут позже   сделать свою запись анонимной, но лишь модераторы могут редактировать   анонимные записи..</p>
      </li>
      <li>
        <p>Вы можете <strong>Разрешить создание опросов</strong> [Allow Polls] для категории, которую Вы создали.</p>
      </li></ol></ol>
<h5>Добавление страницы форума в главное меню</h5>
<ol class="marked-list">
  <li>
    <p>Откройте <strong>Menus</strong> &gt; <strong>Main Menu</strong> &gt; <strong>Add New Menu Item</strong>:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-24.jpg" alt=""></figure>
  <li>
    <p>В следующем окне выберите <strong>Тип элемента меню</strong> [Menu Item Type].</p>
  </li>
   <figure class="img-polaroid"><img src="img/forum-25.jpg" alt=""></figure>
  <li>
    <p>Выберите тип меню &quot;Kunena Forum&quot; в появившемся окне:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-26.jpg" alt=""></figure>
  <li>
    <p>Измените настройки элемента меню, сохраните меню и проверьте ваш сайт.</p>
  </li>
</ol>
