<h5>Как управлять модулем <strong>Owl Carousel</strong></h5>
<p>Для того, что бы добавить модуль типа  <strong>Owl Carousel</strong>, перейдите к  менеджеру модулей [Module Manager]:</p>
<p>Пройдите к разделу <strong>Extensions</strong> &gt; <strong>Modules</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Нажмите кнопку &quot;<strong>New</strong>&quot; и в появившемся окне выберите тип модуля <strong>Owl Carousel</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Информация о модуле</h5>
<ol class="marked-list">
  <li><strong>Title:</strong> модуль обязан иметь заголовок [в данном случае <strong>Carousel</strong>].</li>
</ol>
<h5>Настройка параметров отображения модуля</h5>
<p>Модуль <strong>Owl Carousel </strong>будет отображать  фиксированное количество статей [Articles]   одной или нескольких категорий [Categories].</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Category</strong>:  Выберите статью [статьи] из одной или  нескольких категорий. Если выбор не сделан, по умолчанию, в модуле будут  отображаться все категории сразу.</div>
  </li>
  <li><strong>Images</strong>: [Hide/Show]. Опция отвечает за отображения изображения [изображений] статьи.</li>
  <li><strong>Article title</strong>: Определите, показать или скрыть заголовок статьи.</li>
  <li><strong>Linked Titles</strong>: [Use Global/No/Yes]. Выберите, хотите ли вы отображать заголовок статьи ссылкой [на основной текст статьи].</li>
  <li> <strong>Publishing Date</strong>: Отображение даты публикации статьи.</li>
  <li><strong>Header Level</strong>: [h1/h2/h3/h4/h5]. Выберите HTML уровень заголовка статьи.</li>
  <li><strong>Show intro images</strong>:  	Выберите, хотите ли вы показать вложенные изображения статьи.</li>
  <li>
    <div align="justify"><strong> Intro image align</strong>: [Mone/left/right] Выберите стиль выравнивания  вложенных изображений. Настройка данной опции не влияет на  настройки отображения статьи.</div>
  </li>
  <li>
    <div align="justify"><strong>'Read more...' Link:</strong> [Show/Hide]. Если вы выберите параметр &quot;Show&quot; будет показана ссылка  &quot;Read more...&quot; на странице полного содержания статьи, в случае если таковой имеется.</div>
  </li>
  <li><strong>Number of Articles</strong>: Задайте количество статей, отображаемых в модуле.</li>
  <li>
    <div align="justify"><strong>Order Results</strong>: [Published Date/Created Date/Ordering/Random]. Установите критерий порядка отображения статей в модуле.</div>
  </li>
  <li><strong>Pretext</strong>: Вы можете ввести текст, который будет отображаться над основным контентом модуля. Поле не поддерживает  HTML-тэги.</li>
</ol>
