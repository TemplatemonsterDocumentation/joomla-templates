<h3>Swiper</h3>

<p>Модуль Swiper - это отличный адаптивный слайдер, представляющий собой интеграцию
    стандартного модуля Joomla Articles Newsflash и бесплатного плагина jQuery Swiper. Разработанный в основном для IOS, этот плагин отлично работает и на последних Android, Windows Phone 8 и в современных браузерах персональных компьютеров:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Модуль Swiper содержит различные настройки и находится в меню <b>Extensions
        (Расширения) > Module Manager (Менеджер модулей)</b> админ-панели Joomla. Все
    основные опции плагина jQuery Swiper меняются в настройках модуля. </p>

<p>Наведите курсором мыши на настройку, для того чтобы увидеть ее описание. Более детальную
    информацию о плагине jQuery Swiper можно найти на <a
        href="http://idangero.us/swiper/" target="_blank">официальном
        сайте</a>.</p>
<p>Все необходимые настройки карусели доступны в настройках модуля:</p>

<ol class="index-list">
    <li>Настройки воспроизведения;</li>
    <li>Настройки высоты;</li>
    <li>Видимые элементы;</li>
    <li>Настройки анимации;</li>
    <li>Навигация и контроль;</li>
    <li>Настройки миниатюр.</li>
</ol>

