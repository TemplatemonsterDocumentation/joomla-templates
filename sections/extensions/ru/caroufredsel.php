<h3>CarouFredSel</h3>

<p>CarouFredSel представляет собой модуль карусели изображений, разработанный на основе
    стандартного модуля Joomla Articles Newsflash и плагина jQuery CarouFredSel. </p>

<figure class="img-polaroid">
    <img src="img/caroufredsel-1.jpg" alt="">
</figure>

<h6>Все необходимые опции карусели доступны в настройках модуля. </h6>

<ol class="index-list">
    <li>Направление карусели</li>
    <li>Ширина элемента</li>
    <li>Видимые элементы</li>
    <li>Настройки анимации</li>
    <li>Навигация и контроль</li>
</ol>

<p>Плагин CarouFredSel содержит гораздо больше <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">настроек
        конфигурации</a>. Если Вам нужно добавить еще некоторые опции, необходимо
    редактировать файл <b>\modules\mod_caroufredsel\tmpl\default.php</b>. </p>

