  </figure>
</p>

  <li>
    <div align="justify">Перейдите во вкладку <strong>Parameters</strong>, для того чтобы обозначить <strong>Owner </strong>[владельца прав на изображение] и <strong>Author</strong> [автора] этого элемента галереи.</div>
  </li>

<figure class="img-polaroid">
  <img src="img/gallery-19.jpg" alt="">
