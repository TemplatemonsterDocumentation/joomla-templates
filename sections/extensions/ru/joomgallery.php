<h5><p>Давайте воспользуемся <strong>Image Upload</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Нажмите на кнопку <strong>Browse</strong>  в разделе Выбор изображения [Image Selection] и выберите медиа-файлы на вашем компьютере.</div>
  </li>
  <li>
    <div align="justify">Укажите <strong>Категорию</strong> для этих изображений [вы можете загружать сразу несколько изображений], а также<strong> Общий заголовок</strong> [Generic Title] для этих изображений [например, если вы загружаете 3   изображения с общим названием «test», они отобразятся в вашем менеджере   изображений как test_1, test_2 and test_3]. 
    </div>
  </li>
  <li>
    <div align="justify">Укажите <strong>Generic Description</strong> и задайте автора [<strong>Author</strong>], если есть такая необходимость. Выберите тип доступа [<strong>Access</strong>]: для всех [public], только для super users, только для авторизированных пользователей [registered]. </div>
  </li>
  <li>
    <div align="justify">Используйте опцию <strong>Special Image Files</strong>. Эта опция будет необходима, при условии если вы  загружаете анимированные, .png изображения с эффектом прозрачности или же .gif изображения. Имейте в виду, изображения перечисленных типов не поддерживают функцию  resized [адаптация размера изображения] и, следовательно, на странице просмотра, изображения таких типов, будут отображаться в полном размере.</div>
  </li>
  <li>
    <div align="justify"><strong>Debug Mode</strong>. В случае активации опции <strong>debug mode</strong> будут показаны сообщения о текущем состоянии. Сообщения будут показаны при ошибке, всегда.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Как только вы завершите процесс настройки, нажмите кнопку <strong> Upload</strong>.</p>
    <p>Ваши изображения появятся в разделе <strong>Components</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Images</strong>]. Каждая картинка — это отдельный элемент галереи.</p>
  </div></div>
<h5><p>Редактирование созданного элемента галереи</p>
</h5>
<div align="justify">Перейдите к разделу <strong>Components</strong> > <strong>JoomGallery</strong> > <strong>Image Manager</strong>. Здесь вы найдёте перечень  загруженных ранее изображений. Появляется возможность редактировать изображения: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p align="justify">Если вам необходимо внести изменения в настройки отображения изображений,  следуйте следующей инструкции:</p>
<ol class="marked-list">
  <li>Откройте любой элемент  списка.</li>
  <li>
    <div align="justify">Во вкладке <strong> Image</strong> изменяйте <strong> Title</strong>, <strong>Category</strong>, <strong>Description</strong>, <strong>Hits</strong> и т.д. Прокрутите страницу настроек ниже и вы увидите область <strong>Thumbnail preview</strong> and <strong>Image preview</strong>, где будет доступна функция предпросмотра миниатюры и изображения в полном размере данного элемента галереи. </div>
  </li>
  <li>
    <div align="justify">Используйте опцию <strong>Hidden</strong>.  Скрыв  изображение, оно больше не будет отображаться в категории и топ-листах. Изображение не будет присутствовать в результатах поиска, но, тем не менее,  изображения останется доступным по прямой ссылке. Если вы  хотите закрыть доступ к изображению просто отмените публикацию [дезактивируйте изображение]. Миниатюру скрытого изображения можно использовать  в качестве иконки [ярлыка] для категории, не отображая его при этом, в соответствующей категории.</div>
  </li>
  <li>
    <div align="justify">Опция<strong> Featured</strong>. Отметьте изображение как &quot;featured&quot; [рекомендуемое]. К примеру, модуль JoomImages может быть настроен таким образом, что в нем будут отображаться только &quot; featured&quot; [рекомендуемые] изображения.</div>
  </li>

<p>
  <figure class="img-polaroid">
