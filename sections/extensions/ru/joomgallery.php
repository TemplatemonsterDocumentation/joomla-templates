<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> — это компонент для реализации полноценной фотогалереи на Joomla сайте. Для более детальной информации обращайтесь к  <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">официальному веб-сайту</a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Создание категории</p>
  </h5>

<p>Перейдите к разделу <strong>Components</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Category Manager</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Нажмите кнопку <strong>New</strong> в левом верхнем углу: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
<p align="justify">Введите <strong>Название</strong> [Title] категории, <strong>Описание</strong> [Description]. Затем, сохраните настройки. Вы также можете  задать   мета-описание и, если есть такая необходимость, определить   ограничения для данной категории.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-9.jpg" alt="">
  </figure>
    
</p>
<p>В процессе настройки, вы можете толкнуться со следующими опциями:</p>
<table width="980"  class="options-table">
<tr>
        <td width="154" class="col-1"> <strong>Alias </strong></td>
        <td width="29">-</td>
        <td width="781"><div align="justify">
          <p>Оставьте это поле пустым и JoomGallery  применит настройки по умолчанию, при которых  путь к категории указывается  строчными буквами с тире вместо пробелов. Вы можете ввести настройки для опции  самостоятельно. В таком случае, используйте только строчные буквы и дефис [-]. Пробелы или подчеркивания — НЕ допускаются.</p>
</div></td>
  </tr>
    <tr>
        <td class="col-1"><strong>Hidden</strong></td>
        <td>-</td>
        <td><div align="justify">
          <p>Скрыв  изображение, оно больше не будет отображаться в категории и топ-листах. Изображение не будет присутствовать в результатах поиска, но, тем не менее,  изображения останется доступным по прямой ссылке. Если вы  хотите закрыть доступ к изображению просто отмените публикацию [дезактивируйте изображение]. Миниатюру скрытого изображения можно будет использовать  в качестве иконки [ярлыка] для  категории, не отображая само изображение при этом, в соответствующей категории.       </p>
      </div></td>
    </tr> <tr>
