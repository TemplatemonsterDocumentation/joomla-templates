<h3>TM Youtube background video module</h3>

<p>TM Youtube background video is a module for adding a background youtube video to your website.</p>

<h6>All the available module options are available in settings:</h6>

<ol class="index-list">
    <li>Youtube video link;</li>
    <li>Video playback settings;</li>
    <li>Mobile display settings;</li>
    <li>”Play/Stop” button settings.</li>
</ol>
