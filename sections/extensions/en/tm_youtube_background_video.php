<h3>TM YouTube background video module</h3>

<p><strong>TM YouTube background video</strong> is a module for adding a background youtube video to your website.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5><p>In order to configurate the <strong>TM Parallax </strong>module, perform the following steps:</p></h5>
<ol class="marked-list">
<li><p>Navigate to <strong>Extensions</strong> &gt; <strong>Modules</strong> tab:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Next you should select the<strong> TM Parallax </strong>module to edit.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Tip:</strong> if you want to find all <strong>TM YouTube background video</strong> modules, use the filters to simplify the process. Press &quot;<strong>Search Tool</strong>&quot; button and select the &quot;<strong>TM YouTube background video</strong>&quot; type in the &quot;<strong>- Select Type -</strong>&quot; filter dropdown list. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>You can click &quot;<strong>New</strong>&quot; Button to create a new Module.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>To open any edit required module settings, click on  its title. (Let's edit <strong>Home youtube</strong> module.)</li>
<li>
  <div align="justify">The <strong>Home youtube </strong>module editing interface is opened in front of you. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Press the <strong>Article</strong> button to open the article selection modal window, as shown below. You can use the filter to simplify the search process.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>To add new article use <strong>Content</strong> &gt; <strong>Articles</strong> &gt; <strong>Add New Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Here you can find <strong>Options tab</strong>, where you will be able to change:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Prepare Content</strong> – optionally prepare the content with the Joomla Content Plugins. </p>
      </li>
      <li>
        <p><strong>Enter a YouTube video url *</strong> – allows  specifying custom URL for video from YouTube that has to be visible on the page.</p>
      </li>
      <li>
        <p><strong>Start video</strong> – the video starts from the specified seconds.</p>
      </li>
      <li>
        <p><strong>Mute</strong> – change video to silent or loud mode.</p>
      </li>
      <li><strong>Autoplay</strong> – the video starts automatically,  once the web-page is loaded.</li>
      <li><strong>Show play/pause button</strong> – you can either show or hide the buttons.</li>
      <li> <strong>Play/Pause button text</strong> – specify the buttons' text.</li>
      <li>
        <p><strong>Mobile Image</strong> – show this image on mobile devices instead of a video. (To upload images go to the <strong>Content </strong>&gt; <strong>Media</strong> section.)</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>In order to change video, simply <strong>replace video URL</strong> with previously copied <strong>youtube video link</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Scroll the page up and click the green <strong>Save</strong> button at the top left of the screen to apply changes:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Go back to your website and refresh the page with text that you have changed. Now we can see that editing was successful. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">In our templates <strong>TM Youtube background video</strong> content is presented by loading certain module or position, for example, "{loadmodule articles_single, About us}". In order to prevent Error 404 on your website, please make sure that the name of the loaded module identical to the one, specified for loading. In this case, the "About us" module should be published. The module name  should not contain more than one space.
    You can learn more about loadmodule/loadposition at the <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">official website</a>.
</p>
