<h3>TM YouTube background video module</h3>

<p><strong>TM YouTube background video</strong> is a module for adding a youtube background video to your website.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>In order to configure the TM YouTube background video module, perform the following steps:</p></h5>
<ol class="marked-list">
<li><p>Navigate to the <strong>Extensions</strong> &gt; <strong>Modules</strong> tab:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Next you should select the <strong>TM YouTube background video</strong> module to edit.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Tip:</strong> if you want to find all the <strong>TM YouTube background video</strong> modules, use the filters to simplify the process. Press the &quot;<strong>Search Tool</strong>&quot; button and select the &quot;<strong>TM YouTube background video</strong>&quot; type in the &quot;<strong>- Select Type -</strong>&quot; filter dropdown list. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>You can click the &quot;<strong>New</strong>&quot; Button to create a new Module.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>To open and edit the required module settings, click on its title. [Let's edit <strong>Home youtube</strong> module.]</li>
<li>
  <div align="justify">The <strong>Home youtube </strong>module editing interface is opened in front of you. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Press the <strong>Article</strong> button to open the article selection modal window, as shown below. </li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>To add a new article go to <strong>Content</strong> &gt; <strong>Articles</strong> &gt; <strong>Add New Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Here you can find the <strong>Options tab</strong>, where you will be able to change:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Prepare Content</strong> - optionally prepare the content with the Joomla Content Plugins. </p>
      </li>
      <li>
        <p><strong>Enter a YouTube video url *</strong> - allows  specifying custom URL for the YouTube video that has to be visible on the page.</p>
      </li>
      <li>
        <p><strong>Start video</strong> - the video starts from the specified seconds.</p>
      </li>
      <li>
        <p><strong>Mute</strong> - change video to silent or loud mode.</p>
      </li>
      <li><strong>Autoplay</strong> - the video starts automatically,  once the webpage is loaded.</li>
      <li><strong>Show play/pause button</strong> - you can either show or hide the buttons.</li>
      <li> <strong>Play/Pause button text</strong> - specify the buttons' text.</li>
      <li>
        <p><strong>Mobile Image</strong> - this image shows up on mobile devices instead of a video. [To upload images go to the <strong>Content </strong>&gt; <strong>Media</strong> section.]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>In order to change the video, simply <strong>replace video URL</strong> with the previously copied <strong>youtube video link</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Scroll the page up and click the green <strong>Save</strong> button at the top left of the screen to apply changes:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Go back to your website and refresh the page that you changed. Now we can see that the edits were applied successfully. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">In our templates <strong>TM Youtube background video</strong> content is presented by loading certain module or position, for example, "{loadmodule articles_single, About us}". In order to prevent Error 404 on your website, please, make sure that the name of the loaded module is identical to the one, specified for loading. In this case, the "About us" module should be published. The module name should not contain more than one space.
    You can learn more about loadmodule/loadposition at the <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">official website</a>.
</p>