<p class="alert alert-warning">
In our templates TM Youtube background video content is presented by loading certain module or position, for example, "{loadmodule articles_single, About us}". In order to prevent Error 404 on your website, please make sure that the name of the loaded module coincides with the one, specified for loading. In this case, the "About us" module should be published and should not contain more than one space in the module name.
    You can learn more about loadmodule/loadposition at the <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">official website</a>.
</p>
