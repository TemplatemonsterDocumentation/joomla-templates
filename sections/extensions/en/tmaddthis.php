<h3>TM AddThis</h3>

<p align="justify"><strong>AddThis</strong> is the world's largest content sharing platform. It provides tools that make it easier to share content across the social web, and provides publishers with the increased traffic and in-depth paralytics.<strong> AddThis</strong> can be integrated into a website with the use of a web widget. Once the widget is added, visitors to the website can bookmark an item using a variety of services, such as Facebook, MySpace, Google Bookmarks, Pinterest, and Twitter. </p>
<p><strong>TM AddThis </strong>is a plugin for displaying social ‘share’ buttons:</p>

<p align="justify">
 <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
 <br />
Adding the share buttons to your Joomla website is a great way to get more of your content distributed online. The more your content is shared, the more traffic you’ll drive back to your site. AddThis share buttons are easy to add, and also provide the detailed analytics that will help you become a more effective publisher. </p>
<p><strong>FEATURES</strong></p>
<ul>
 <li>Choose where the share buttons appear on your page;</li>
 <li>Control the number of buttons to show up;</li>
 <li>Include share counts to encourage sharing;</li>
 <li>Personalize sharing buttons to each user or pick them yourself.</li>
</ul>
<p align="justify">To customize the plugin go to the <strong>Extensions</strong> &gt; <strong>Plugins</strong> section. This will bring you to the <strong>Plugins Manager</strong>. Type &quot;Add&quot; in the search field and you'll see the <strong>Content - TM AddThis</strong>.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>First you need to <strong>enable</strong> the plugin. Press the red &quot;X&quot; button to enable it. </p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Once the plugin is <strong>enabled</strong>, you will see a confirmation message.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>To get the main settings window of the plugin press the <strong>Content - TM AddThis</strong> title link and you'll be brought to the following window:</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Here you can see the main <strong>TM Addthis settings</strong>. Set the<strong> categories to exclude</strong> in the corresponding field. Enter your Profile ID into the <strong>Profile ID</strong> field. You can get it after registering and creating an account at the following page: <a
  href="https://www.addthis.com" target="_blank"
  rel="nofollow">https://www.addthis.com</a>:</p>

<p>
 <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>Basically, you should get a new <strong>Profile ID</strong> in order to change the <strong>AddThis</strong> social share icons:</p>
<p>After you've registered, you need to add <strong>share</strong> buttons. </p>
<p>In order to do this press &quot;<strong>AddThis</strong>&quot; button on the profile toolbar.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Then you need to press the &quot;<strong>Get Started</strong>&quot; in Share Buttons section.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Activate the <strong>Inline </strong>Share Buttons.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">You can choose <strong>Share Counters</strong> type. You're also able to select your own social networks and customize the order using the <strong>Select your own</strong> option.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Change the design of the icons in the <strong>Design</strong> section. Customize the <strong>Style</strong>, <strong>Button Size</strong> and <strong>Colors</strong> of the icons. Set the <strong>Rounded Corners</strong> parameter and <strong>hide network names</strong> if needed.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Whenever you are done with the <strong>Share Buttons</strong> customization, press the <strong>Save&amp;Continue</strong> to proceed to the next step.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Copy your profile ID in the &quot;<strong>1</strong>&quot; section. </p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>You need to insert it to the <strong>Profile ID</strong> field in the <strong>Content - TM AddThis</strong> plugin settings window to make the new <strong>Share Buttons</strong> appear on your site.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>You have an option to exclude categories [pages] where you don't want to place the plugin.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Make sure you have <strong>enabled</strong> the plugin.</p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Save the changes and refresh the <strong>Share Buttons</strong> page. </p>
<p>
 <figure class="img-polaroid"><img src="img/addthis-18.jpg" alt=""></figure>
</p>