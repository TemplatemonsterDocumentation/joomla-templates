<h3>CarouFredSel</h3>

<p>CarouFredSel is an image carousel module developed as a combination of the default
    Joomla Articles Newsflash module and the CarouFredSel jQuery plugin. </p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>

<p>All the necessary carousel options are available in module settings. </p>

<ol class="index-list">
    <li>Carousel direction</li>
    <li>Item width</li>
    <li>Visible items</li>
    <li>Animation options</li>
    <li>Navigation and controls</li>
</ol>

<p>CarouFredSel plugin has many more <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">configuration
        options</a>. If you want to add your options, you can edit <b>\modules\mod_caroufredsel\tmpl\default.php</b>
    file. </p>

