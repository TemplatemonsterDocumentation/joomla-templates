<h3>Articles Newsflash [Advanced]</h3>

<p align="justify">This is an enhanced version of default <strong>Joomla Articles Newsflash Module</strong>. In addition to the default module features, it has some extra configuration options and Bootstrap based module templates. To add a great deal to the polish of your Joomla-based website design, this is a dramatically higher quality module to give way to your content. Easy to use, easy to update, with some great and powerful customization options.</p> 
