<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> is a component that allows to create a complete photo gallery based on Joomla! Feel free to check more details at the <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">official website</a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Category Creating</p></h5>

<p> Go to <strong>Components</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Category Manager</strong> section. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Press the <strong>New</strong> button in the top left corner: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
  <p align="justify">Input category <strong>Title</strong> and <strong>Description</strong> with images and <strong>Save</strong> the settings. You can also specify<strong> Meta Description</strong> for this category and set access restrictions if needed.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-9.jpg" alt="">
  </figure>
    
</p>
<p>When configuring, you can face the settings in a table below:</p>
<table width="980"  class="options-table">
    <tr>
        <td width="154" class="col-1"> <strong>Alias </strong></td>
        <td width="29">-</td>
        <td width="781"><div align="justify">Leave this blank and JoomGallery will fill in a default value, which is the category path in lowercase and with dashes instead of spaces. You may enter the Alias manually. If you do so, please, use lowercase letters and hyphens [-]. No spaces or underscores are allowed. The Alias will be used in the SEF URL. </div></td>
  </tr>
    <tr>
        <td class="col-1"><strong>Hidden</strong></td>
        <td>-</td>
        <td><div align="justify">With hiding this category it won't be displayed in a gallery or category view anymore, but it will still be available through direct linking. If you don't want it to be available, please, unpublish it instead. By hiding categories it is possible to create separate areas in the gallery, for example. For that, please, create menu items for the hidden categories. It is also possible to manage certain images in the gallery without showing them in it [e.g., you can use the gallery as the additional manager for images that are included into articles content].</div></td>
    </tr> <tr>
        <td class="col-1"><strong>Access</strong></td>
        <td>-</td>
        <td>Access level for this category.</td>
    </tr>
    <tr>
        <td class="col-1"><strong> Exclude from toplists </strong></td>
        <td>-</td>
        <td>With enabling this option you can exclude this category from the toplists. Please, note, that this option is not automatically passed on to subcategories. Of course, enabling this option distorts the toplists which could be detected by  your site visitors.</td>
    </tr>
   
    <tr>
        <td class="col-1"><strong>Exclude from search</strong></td>
        <td>-</td>
        <td><div align="justify">With enabling this option you can exclude this category from the search results. Please, note, that this option is not automatically passed on to subcategories. Of course, enabling this option could create confusion at the visitors of your site, as some images won't appear in the search results even if they type in correct keywords.</div></td>
    </tr>
        <td class="col-1"><strong>Thumbnail</strong></td>
        <td>-</td>
        <td>Here you may select an image which is set as the thumbnail of this category.</td>
    </tr><td class="col-1"><strong> Thumbnail alignment </strong></td>
        <td>-</td>
        <td>Please, select the alignment for the category thumbnail you have chosen above.</td>
    </tr>    <td class="col-1"><strong>Ordering</strong></td>
        <td>&nbsp;</td>
        <td>You may change the position of the category here.</td>
    </tr>  <td height="26" class="col-1"><strong> Password </strong></td>
      <td>-</td>
        <td><div align="justify">Here you can specify a password for the category. With that, the category will only be accessible for the users in frontend if they enter the correct password. Please, note, that restricting access via access levels is stronger which means that users cannot access categories with too high access level even if they know the respective password.</div></td>
    </tr>
</table>
<div class="alert alert-info">
  <p align="justify"><strong>Advise</strong></p>
  <div align="justify">
      <p>To save changes, you should click the Save button.</p>
</div></div>
