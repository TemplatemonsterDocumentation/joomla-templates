<h5><p>Let's try <strong>Image Upload</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Press the <strong>Browse</strong> button in the <strong>Image Selection</strong> section and select media files on your computer. </div>
  </li>
  <li>
    <div align="justify">Specify a <strong>Category</strong> for these images [you can upload bunch of images for each category this way], <strong>Generic Title</strong> for these images [for example, if you upload 3 files with generic title &quot;test&quot;, they will appear in your Image manager as test_1, test_2 and test_3]. </div>
  </li>
  <li>
    <div align="justify">Specify <strong>Generic Description</strong> and <strong>Author</strong> if needed. Set the <strong>Access</strong> type: public, super users, registered, special. </div>
  </li>
  <li>
    <div align="justify">Use the <strong>Special Image Files</strong> option. This option enables the upload of special image files. Solely choose this option in case your upload exclusively consists of animated or transparent .png or .gif files. Keep in mind that the files do not get resized and therefore are displayed full size in detail view.</div>
  </li>
  <li>
    <div align="justify"><strong>Debug Mode</strong>. With activated <strong>debug mode</strong> all processing steps will be shown. In case of errors they are always displayed.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Please, click on the <strong>Upload</strong> button at the bottom after the configuration process is completed.</p>
</div></div>
<h5><p>Editing existing Gallery item</p></h5>
<div align="justify">Go to <strong>Components</strong> > <strong>JoomGallery</strong> > <strong>Image Manager</strong> section. Here you may find list of images that were already uploaded, so that you can edit the existing ones: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">  
  </figure>
</p>
<p>In case you need to<strong> modify</strong> them, you should perform the following: </p>
<ol class="marked-list">
  <li>Open any item from the list.</li>
  <li>Change the<strong> Title</strong>, <strong>Category</strong>, <strong>Description</strong>, <strong>Hits</strong>, etc. in the<strong> Image</strong> tab. Scroll down to check the <strong>Thumbnail preview</strong> and <strong>Image preview</strong>. </li>
  <li>
    <div align="justify">Use <strong>Hidden</strong> option. With hiding this image it won't be displayed in category view or recognized by search or the toplists anymore, but it will still be available through direct linking.  If you don't want it to be available, please, unpublish it instead. By hiding images it is possible to use them as category thumbnails without showing them in the respective category, for example.</div>
  </li>
  <li>
    <div align="justify"><strong>Featured </strong>option. Here you can mark an image as 'Featured'. For example, the JoomImages module can be configured to display just the featured images on a module position.</div>
  </li>

<p>
  <figure class="img-polaroid">
    <img src="img/gallery-17.jpg" alt="">  
  </figure>
</p>

