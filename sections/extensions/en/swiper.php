<h3>Swiper</h3>

<p align="justify"><strong>Swiper Module</strong> is a wonderful adaptive slider that is an integration of standard Joomla Articles Newsflash module and free jQuery Swiper plugin. Created for IOS, this plugin works perfectly in the latest Android, Windows Phone 8 and desktop PC browsers.</p>
<p align="justify">Let us check what kinds of slides can we add to the site slider using this module. First of all, it might be a regular animated slide with an image and a caption on it: </p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">But it can be also a slide with the background video or a configurable slide with the parallax image background. There&rsquo;s actually a vast of options to choose from. Let&rsquo;s learn how to use them.</p>

