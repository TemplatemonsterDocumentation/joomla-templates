<h3>Swiper</h3>

<p>Swiper Module is a wonderful adaptive slider that is an integration of standard Joomla Articles Newsflash module and free jQuery Swiper plugin. Created for IOS, this plugin works perfectly in the latest Android, Windows Phone 8 and desktop PC browsers:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Swiper Module contains different settings and can be found in the <b>Extensions
        > Module Manager</b> tab of your Joomla admin panel. All the main 
jQuery Swiper plugin options can be changed in module settings. </p>

<p>Place your mouse cursor on the setting to see its description. You can learn more about jQuery Swiper at the <a
        href="http://idangero.us/swiper/" target="_blank">official website</a>.</p>
<p>All the necessary carousel settings can be found in module settings:</p>

<ol class="index-list">
    <li>Playback settings;</li>
    <li>Height settings;</li>
    <li>Visible elements;</li>
    <li>Animation settings;</li>
    <li>Navigation and control;</li>
    <li>Thumbnail settings.</li>
</ol>

