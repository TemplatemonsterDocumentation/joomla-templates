<h3>TM Parallax Module</h3>

<p><strong>TM Parallax</strong> is a module that uses RD Parallax script for implementing parallax functionality to the template. </p>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-20.jpg" alt=""></p></figure>
<p align="justify">The module will now be available from the Module Manager found under the <strong>Extensions</strong> tab in the top menu of the admin interface. You can create additional modules by clicking on the <strong>New</strong> button. Select the modules you have already made in order to configure them. Don't forget to publish them in the correct positioning and on the right pages.</p>

<h5><p>In order to configure the <strong>TM Parallax </strong>module, perform the following steps:</p></h5>
<li><p>Navigate to the <strong>Extensions</strong> &gt; <strong>Modules</strong> tab:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Next, select the<strong> TM Parallax </strong>module to edit.</p>
      </li>
    <div class="alert alert-warning">
 <div align="justify"><strong>Tip:</strong> if you want to find all the <strong>TM Parallax </strong>modules, use the filters to simplify the process. Press the &quot;<strong>Search Tool</strong>&quot; button and select the &quot;<strong>Articles - Single</strong>&quot; type in the &quot;<strong>- Select Type -</strong>&quot; filter dropdown list. </div>
  <figure class="img-polaroid">
<p> <img src="img/tm-parallax-1.jpg" alt=""></p></figure></div>
<ol class="marked-list">
      <li>To open and edit the required module settings, click on its title. [Let's edit the <strong>Home parallax</strong> module.]</li>
<li>
  <div align="justify">The<strong> TM Parallax </strong>module editing interface is opened in front of you.            
  </div>
  <figure class="img-polaroid">
    <p> <img src="img/tm-parallax-2.jpg" alt=""></p></figure>
<li>Press the <strong>Article</strong> button to open the article selection modal window, as shown below.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>
<div class="alert alert-warning">
    <div align="justify">In our templates <strong>TM Parallax Module</strong> content is presented by loading a certain module, for example, "{loadmodule articles_single, Home parallax article}". In order to prevent Error 404 on your website, please, make sure that the name of the loaded module is identical to the one, specified for loading. In this case, the "<strong>Home parallax article</strong>" module should be published. The module name should not contain more than one space. </div>
          You can learn more about loadmodule/loadposition rules at the <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">official website</a>.
  </p>
    </div>
  <p align="justify">To edit the <strong>Home parallax article</strong> module, navigate to <strong>Extensions</strong> &gt; <strong>Modules</strong> tab. Search for <strong>Home parallax article</strong> using the search field. To open the <strong>Home parallax article</strong> module settings, click on its title.</p>
 <figure class="img-polaroid"> <p> <img src="img/tm-parallax-4.jpg" alt=""></p></figure>
  <p align="justify">Now you have an opportunity to edit an article. Click the<strong> Edit </strong>button to see the Edit Article window. Here you have an option to edit the article content with its images and links.</p>
  <div class="alert alert-warning">
  <p>To add a new article go to <strong>Content</strong> &gt; <strong>Articles</strong> &gt; <strong>Add New Article</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
  </div>
<p></p>
<li>Open the <strong>Advanced </strong>tab.</li>
<figure class="img-polaroid">
     <p><img src="img/tm-parallax-5.jpg" alt=""></p>
  </figure>
