<h3>TM Parallax Module</h3>

<p>TM Parallax is a module that uses RD Parallax script for implementing parallax functionality in the template.</p>

<h6>All the necessary module options are available in module settings:</h6>

<ol class="index-list">
    <li>Image settings;</li>
    <li>Parallax scroll speed settings;</li>
    <li>Parallax direction settings.</li>
</ol>
