<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> is the subscription component for Joomla! AcyMailing is a reliable Newsletter and email marketing extension for Joomla. AcyMailing enables you to efficiently manage an unlimited number of subscribers, organize them into mailing lists and send personalized newsletters. </p>
<p>On your website you will see the following subscription form:</p>
<figure class="img-polaroid">
    <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Navigate to <strong>Components</strong> &gt; <strong>AcyMailing</strong> and you'll  be directed to AcyMailingt Dashboard.</p>
<figure class="img-polaroid">
    <img src="img/acymailing-4.jpg" alt="">
</figure>


<p align="justify"> To see the main settings of this extension  go to <strong>Extensions</strong> &gt; <strong>Modules</strong>. Click to the <strong>New</strong> button.</p>
