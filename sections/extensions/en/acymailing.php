<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> is the subscription Joomla! component, a reliable Newsletter and email marketing extension. AcyMailing enables you to efficiently manage an unlimited number of subscribers, organize them into mailing lists and send personalized newsletters.</p>
<p>On your website you will see the following subscription form:</p>
<figure class="img-polaroid">
  <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Navigate to the <strong>Components</strong> &gt; <strong>AcyMailing</strong> section to be directed to the AcyMailing Dashboard.</p>
<figure class="img-polaroid">
  <img src="img/acymailing-4.jpg" alt="">
</figure>


<p align="justify">To see the main settings of this extension go to the <strong>Extensions</strong> &gt; <strong>Modules</strong> section. Click on the <strong>New</strong> button.</p>
<figure class="img-polaroid">
 <img src="img/acymailing-6.jpg" alt="">
</figure>
<p align="justify">Select the <strong>AcyMailing Module Subscribe</strong> from the new modules list.</p>
