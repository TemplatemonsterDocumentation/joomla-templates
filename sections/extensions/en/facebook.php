<h3>TM Facebookfeed</h3>
 
<p align="justify"><strong>TM Facebookfeed</strong> is a module that allows displaying your Facebook feeds on the website.  It will grab facebook wall post automatically and display feeds of the posts of user's Facebook page or profile. Just put the facebook access token.</p>

<figure class="img-polaroid">
    <p><img src="img/facebookfeed-30.jpg" alt="">
    </p></figure>
    <p>To configure the <strong>TM Facebookfeed </strong> module navigate to <strong>Extensions</strong> &gt; <strong>Modules</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Press &quot;<strong>Search Tool</strong>&quot; button and select the <strong>TM Facebookfeed </strong>in the &quot;- Select Type -&quot; filter dropdown list. You will see the<strong>  See It In Action  </strong>Module.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebookfeed-31.jpg" alt="">
</p></figure>
    <p>Click on module title link to be brought to the settings window of the module. </p>
 <figure class="img-polaroid">
    <p><img src="img/facebookfeed-5.jpg" alt=""></p></figure>
    <figure class="img-polaroid">
    <p><img src="img/facebookfeed-20.jpg" alt=""></p></figure>
   <p>At the <strong>Module</strong> tab you may set up the following options:   </p>
   <ol class="marked-list">
     <li><strong>Pretext</strong> — allows displaying custom text before the module content;</li>
     <li><strong> AccessToken</strong>* — create an application in your Facebook account, generate the AccessToken and insert here.</li>
    </ol>
<h5>Items settings </h5>
    <ol class="marked-list">
      <li><strong> Number of items</strong> — the number of items to display within this module;</li>
      <li> <strong>Columns</strong> — set the number of columns;</li>
    </ol>
<h5>Date format settings</h5>
    <ol class="marked-list">
      <li> <strong>Year format</strong> — set a year format (display full year format/display 2 last digits of the year);</li>
      <li> <strong>Month format</strong> —  set a month format (displays full month name/displays month number);</li>
      <li><strong>Number of post comments</strong> — The number of post comments to display in each item.</li>
    </ol>
<h5> Display settings &nbsp;</h5>
    <ol class="marked-list">
     
      <li><strong>Display post publishing time</strong> — whether to show the post publishing  date;</li>
      <li><strong> Display profile</strong> — whether to show the profile;</li>
      <li><strong>Display post image</strong> — whether to show the  post image;</li>
      <li> <strong>Display post comments </strong>— whether to show the comments;</li>
      <li><strong> Post comments title</strong> — whether to show the comments' title;</li>
      <li><strong> Display post text</strong> — whether to show the text of the post;</li>
      <li><strong>Display attachment name</strong> — whether to show the attachment name;</li>
      <li><strong>Display attachment description</strong> — whether to show the attachment description;</li>
      <li><strong> Display post link</strong> — whether to show the post link;</li>
      <li><strong> Post link text </strong>— whether to show the link text;</li>
      <li><strong> Display likes count</strong> — whether to show the  likes count;</li>
      <li><strong>Display comments count </strong>— whether to show the comments count.</li>
    </ol>
<p align="justify">The field<strong> AccessToken </strong>is required.  In order to get <strong>AccessToken</strong>, please open the website: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a> and create an app for your website.</p>
<p>You may also set up the following options for the module at the <strong>Module</strong> tab:</p>
    <figure class="img-polaroid">
