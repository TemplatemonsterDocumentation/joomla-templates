<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> is a discussion forum for the Joomla site. It is flexible and easy to work with. The default layout of forum for non-registered and registered users is different, as well as available options. </p>
<p><strong>Kunena</strong> is a forum component for Joomla. It allows implementing a fully functional forum based on Joomla! </p>
<p>This feature is located under '<strong>Pages</strong>' menu item at the site's front end. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p> General settings of the <strong>Kunena</strong> and its content can be controlled via the admin panel of your site. </p>
<p>Scroll down the sidebar to Extensions and select <strong>Kunena Forum</strong> there:</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p> There will be several tabs available to work with:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Category manager</strong>. In the middle there will be all   current categories available in your Forum. Select any to modify if   needed. You can edit its parent section, the name, alias, description, header, and other settings:  </p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify"> This will be the area that holds your member&rsquo;s discussion. You can   create as many discussions as needed. Each discussion should be assigned   to the general Section [it is used as a header for forum topics]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Category Permissions</strong> tab specifies the access level of the specific category – groups of users to view and edit it. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Category Settings</strong> allows controlling general settings for category messages. </p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify"><strong>Users</strong> section. Here you can find users of   the current forum and manage their account settings, permissions editing   and viewing forum: </p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify"> Press the<strong> username  link</strong> and you will see the window, where you can select the <strong>avatar</strong> and <strong>signature text</strong> if needed under the <strong>Basic settings</strong>. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify"><strong>Moderation</strong> tab allows specifying if this user is moderator or not, selecting categories to moderate as well. Click <strong>Save &amp; Close</strong> button on top once you are done with user settings.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Attachments</strong>. Here you can work with uploaded files.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emotions</strong>. All available emotions are located here. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Use <strong>New Smile</strong> area to get some new ones if necessary: </p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Ranks</strong>&rsquo;. Here you see all available ranks for user You cam add new rank, if you want. Each rank can be edited manually as well: </p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Templates</strong>. Select the template to apply Forum to: </p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Configuration</strong>. This tab allows controlling settings of the Forum in details. Titles, email address, SEO, cache and other settings can be managed under the <strong>General</strong> tab. With the <strong>Frontend</strong> section you can modify the layout of the Forum. General settings for users are managed with the <strong>Users</strong> section. <strong>Security</strong> tab controls the permissions for guests and registered users using the Forum. <strong>Avatars</strong> tab is the one to allow/forbid users uploading own avatars, and specifies the image settings. Using the <strong>Uploads</strong> you can allow/forbid uploading attachments to the messages, specify the settings of these files.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
<p align="justify">
Click <strong>Save &amp; Close</strong> button on top once you are done configuring forum settings. </p>
<p><strong>Tools</strong>. Here you can manage specific options like reports, synchronizing, IP&rsquo;s and so on: </p>
<p>
    <figure class="img-polaroid"><img src="img/forum-17.jpg" alt=""></figure>
</p>
<p>Refreshing the Forum page in the site you will see the implemented changes.
