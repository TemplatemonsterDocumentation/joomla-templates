<h3>Kunena Forum</h3>

<p>Kunena is a forum component for Joomla. It allows you to implement a fully functional
    forum based on the Joomla CMS.</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Kunena component can be accessed through the Joomla administration panel under the
    <b>Components -> Kunena</b> menu.</p>

<p>You can learn more about Kunena component usage and configuration at the <a
        href="http://www.kunena.org/docs/Main_Page" target="_blank">official website</a>.
</p>

