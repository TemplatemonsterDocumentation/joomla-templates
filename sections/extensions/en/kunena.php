<h5>Create Categories</h5>
<ol class="marked-list">
  <li>
    <p>Go to <strong>Components</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Categories</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Click <strong>New Category</strong> button in the top left corner.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Enter general category settings under the <strong>Category Name and Description </strong>tab:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">To create a section, choose <strong>Top Level</strong> as   parent. </p>
       </li>
       <li>
         <p>Enter category <strong>Name</strong> and <strong>Alias</strong>.</p>
       </li>
       <li>
         <p>Make sure category is <strong>published</strong>.</p>
       </li>
       <li>
         <p>Fill in the category <strong>description</strong> and <strong>title</strong> to the corresponding fields.</p>
       </li>
       <li>
         <p>You can add <strong>custom css class</strong> for your category. </p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Click the<strong> Category Permissions</strong> tab to define permissions for categories:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>You can see two options available here:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Access Control Type</strong> to be used in this category.</p>
      </li>
      <li>
        <p align="justify">The <strong>Access Level Group</strong> that is allowed to view this category. You can define your own access levels in<strong> Users</strong> &gt; <strong>Access Levels</strong>. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p><strong>Category Settings</strong> tab:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Locked</strong>. Set it to Yes   if you want to lock the category. Site Moderators and Admins are the   only ones who can create new replies to the locked category.</p>
      </li>
      <li>
        <p align="justify"><strong>Review posts</strong>. Set to Yes if you want posts to be reviewed by moderators prior to publishing them in this category.</p>
      </li>
      <li>
        <p align="justify"><strong>Allow anonymous messages </strong>option lets you enable/disable anonymous messages for the category.</p>
      </li>
      <li>
        <p align="justify"><strong>By default post replies as option</strong>. If anonymous   messages have been enabled, this option selects the default choice for   the user. Regular users can later edit their own posts to be anonymous,   but only moderators have the ability to edit anonymous posts.</p>
      </li>
      <li>
        <p>You can <strong>Allow Polls</strong> for the category you create.</p></li></ol>
<h5>Add Forum page to the menu</h5>
<ol class="marked-list">
  <li>
    <p>Open <strong>Menus</strong> &gt; <strong>Main Menu</strong> &gt; <strong>Add New Menu Item</strong>:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-24.jpg" alt=""></figure>
  <li>
    <p><strong>Select </strong>Menu Item Type on the next screen.</p>
  </li>
   <figure class="img-polaroid"><img src="img/forum-25.jpg" alt=""></figure>
  <li>
    <p>Select Kunena Forum menu type from the popup window you will see:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-26.jpg" alt=""></figure>
  <li>
    <p>Change menu item settings, save menu and check your site.</p>
  </li>
</ol>
