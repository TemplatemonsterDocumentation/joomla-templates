<h3>Kunena Forum</h3>

<p>Kunena is a forum component for Joomla. It allows implementing a fully functional forum based on Joomla! CMS:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>You can access Kunena component in your Joomla admin panel in <b>Components -> Kunena</b> tab.</p>

<p>You can learn more about the use and configuration of Kunena component on the <a href="http://www.kunena.org/docs/Main_Page" target="_blank">official website</a>:</p>

