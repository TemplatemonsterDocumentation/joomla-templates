<p>To make color changes in your theme, please, follow the steps listed below:</p>
 <ol class="marked-list">
  <li>
   <p>Select a desired color scheme.</p>
  </li>
  <li>
   <p>Click <strong>Save</strong> button to keep your changes and update the color scheme.</p>
   
  </li>
   <p>
    <figure class="img-polaroid">
     <img src="img/style_switcher_14.jpg" alt="">
    </figure>
   </p>
