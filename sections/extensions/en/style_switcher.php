<h3>TM Ajax Style Switcher Module</h3>

<p align="justify"><strong>TM Ajax Style Switcher Module</strong> is a module for switching color schemes via front-end. The module automatically applies color schemes from the templates\themexxx\css\ folder of the current theme.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Note</strong>: on the template Live Demo the template works in preview mode using cookies. On your website the module will work in full mode and will be available for authorized users with SuperAdmin user category rights.</p>
<p align="justify" class="alert alert-warning">Обратите внимание, что на демо-версии шаблона, принцип действия модуля показан лишь частично,
    с помощью cookies. На действующем сайте модуль работает в полноценном режиме и доступен только для авторизованных
пользователей с правами категории пользователей SuperAdmin.</p>
<h5> Как работать с модулем TM ajax style switcher module </h5>
<p align="justify">To configurate the <strong>TM Ajax Style Switcher Module</strong> navigate to <strong>Extensions </strong>&gt;<strong>Modules</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify"> Press &quot;<strong>Search Tool</strong>&quot; button and select the <strong>TM Ajax Style Switcher Module</strong> type in the &quot;- Select Type -&quot; filter dropdown list. You will see the <strong>Ajax Style Switcher Module</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Click on  module title link to be brought to the settings window of the module. There are no specific settings for the module in the admin panel, just the standard settings for all modules:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Make sure the module is <strong>published</strong>.</p>
  <h5><p>To activate style switcher, please follow instructions listed below:  </p></h5>
  <ol class="marked-list">
    <li>
      <p>Add a new Joomla user via <strong>Users</strong> &gt; <strong>Manage</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Enable</strong> and	<strong>activate</strong> new user.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Press The new user login link to be brought to settings window. </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Make sure to assign your user to &lsquo;<strong>Super Users</strong>&rsquo; group. Navigate to <strong>Assigned Users</strong> tab and tick off &quot;<strong>Super Users</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Log into your website via <strong>Login or register</strong> module at the front-end:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>Style switcher has been successfully activated. Refresh your website to see customization tool box: </p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
<p>To make color changes in your theme, please follow the steps listed below:</p>
  <ol class="marked-list">
    <li>
      <p>Select desired color scheme.</p>
    </li>
    <li>
      <p>Click <strong>Save</strong> button to keep your changes and update the color scheme.</p>
      
    </li>
     <p>
       <figure class="img-polaroid">
         <img src="img/style_switcher_14.jpg" alt="">
       </figure>
     </p>
     <div class="alert alert-warning">
    <div align="justify">
      <p>If the color scheme was saved  it means that all the future visitors will see the one on the website. </p>
      <p><strong>Remember!!!</strong> Nobody but  users assigned to a <strong> Super Users</strong> group can change the color  scheme of the website.</p>
    </div></div>

  </ol>
