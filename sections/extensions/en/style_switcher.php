    <li>
      <p>Add a new Joomla user via <strong>Users</strong> &gt; <strong>Manage</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Enable</strong> and	<strong>activate</strong> new user.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
