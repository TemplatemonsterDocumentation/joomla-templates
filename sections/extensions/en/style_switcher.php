<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> is a module for switching color schemes via front-end. The module automatically applies color schemes from the color_schemes\css\ folder of the current theme.</p>

<p>There are no specific settings for the module in the admin panel, just the standard settings for all modules:</p>

