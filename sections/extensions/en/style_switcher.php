<h3>TM Ajax Style Switcher Module
</h3>
<p align="justify"><strong>TM Ajax Style Switcher Module</strong> is a module for switching color schemes directly at site frontend. The module automatically applies color schemes from the templates\themexxx\css\ folder of the current theme.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Note</strong>: on the template's Live Demo the template works in preview mode using cookies. On your website the module will work in full mode and will be available for authorized users with SuperAdmin user category rights.</p>
<h5> How to manage the TM ajax style switcher module </h5>
<p align="justify">To configure the <strong>TM Ajax Style Switcher Module</strong> navigate to <strong>Extensions </strong>&gt;<strong>Modules</strong> section. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>
<p align="justify">Press the &quot;<strong>Search Tools</strong>&quot; button and select the <strong>TM Ajax Style Switcher Module</strong> type in the &quot;- Select Type -&quot; filter dropdown list. You will see the <strong>Ajax Style Switcher Module</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
<p align="justify">Click on module title link to be brought to the settings window of the module. There are no specific settings for the module in the admin panel, just the standard modules' settings:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Make sure the module is <strong>published</strong>.</p>
  <h5>
    <p>To activate style switcher, please, follow instructions listed below: </p>
 
  </h5>
  <ol class="marked-list">
    <li>
      <p align="justify">Log into your website via <strong>Login or register</strong> module at the front-end as super user (the username and the password you use to log into the Joomla website administrator panel);</p>
    </li>
    <figure class="img-polaroid"> <img src="img/style_switcher_12.jpg" alt="" /> </figure>
    <li>
      <p>Style switcher has been successfully activated. Refresh your website to see customization toolbox: </p>
    </li>
    <figure class="img-polaroid"> <img src="img/style_switcher_13.jpg" alt="" /> </figure>
  </ol>
<h5>
