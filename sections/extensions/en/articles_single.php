<h3>Articles Single</h3>

<p>Articles single is a module that allows you to output a single article to the desired
    position. </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p>The only unusual option in this module is <b>Custom Link</b>. It allows you to
    display the custom link under the module content. It can be linked to the external
    URL or to some Joomla menu item. </p>

