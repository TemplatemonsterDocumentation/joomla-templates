<h3>Articles Single</h3>

<p><strong>Articles Single</strong> is a module that allows you to display a single article in the desired position: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">The only unusual setting of the module is <b>Custom Link</b>. It allows you to display a custom link under the module content. You can link it to an external URL or to a menu item of your Joomla website:</p>

