<h3>TM Instafeed</h3>
 
<p align="justify">This module displays recent photo you posted on Instagram using your Instagram access_token.</p>

<figure class="img-polaroid">
    <p><img src="img/insta-30.png" alt="">
    </p></figure>
    <p>To configure the <strong>TM Instafeed </strong> module navigate to <strong>Extensions</strong> &gt; <strong>Modules</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Press &quot;<strong>Search Tool</strong>&quot; button and select the <strong>TM Instafeed </strong>in the &quot;- Select Type -&quot; filter dropdown list. You will see the<strong>  See It In Action  </strong>Module.</p>
    <figure class="img-polaroid">
    <p><img src="img/insta-31.png" alt="">
</p></figure>
    <p>Click on module title link to be brought to the settings window of the module. </p>
 <figure class="img-polaroid">
    <p><img src="img/insta-5.png" alt=""></p></figure>
 
<p>At the <strong>Module</strong> tab you may set up the following options:</p>
<ol class="marked-list">
  <li> <strong> Accesstoken </strong> —<strong> </strong>create an application in your Instagram account, generate the AccessToken and insert here;</li>
      <li><strong> Client id</strong> —  create an application in your Instagram account, get the client id and insert here;</li>
      <li><strong>Display items by</strong> — choose if you want to display items by the username or tag;</li>
      <li><strong>Tag</strong> — set the hashtag the items will be displayed by;</li>
      <li><strong>User id</strong> — set the user id the items will be displayed by;</li>
      <li><strong>Number of items</strong> —  the number of items to display within this module;</li>
      <li><strong> Columns</strong> — set the number of columns;</li>
      <li><strong> Item image resolution</strong> — set the  item image resolution [low/standard/thumbnail].</li>
</ol>
<h5> Display settings &nbsp;</h5>
    <ol class="marked-list">
     
      <li><strong>Display item link</strong> — whether to show the photo link;</li>
      <li><strong>Link format</strong> — choose the link format [Link title like url/ Link title like custom text];</li>
      <li><strong>Custom link title</strong> — specify the custom link title;</li>
      <li><strong>Display comments count </strong> — whether to show the comments count;</li>
      <li><strong> Display profile</strong> — whether to show the profile;</li>
      <li><strong>Display post image</strong> — whether to show the  post image;</li>
      <li> <strong>Display post comments </strong>— whether to show the comments;</li>
      <li><strong>Display likes count</strong> — whether to show the comments' title;</li>
      <li><strong>  Display item username </strong> — whether to show the text of the post;</li>
      <li><strong> Display tags </strong> — whether to show the attachment name;</li>
      <li><strong> Display item description </strong> — whether to show the attachment description.</li>
    </ol>
    
    <p>You may also set up the following options for the module at the <strong>Module</strong> tab:</p>
    <figure class="img-polaroid">
    <p><img src="img/modu.jpg" alt="">
</p></figure>
<ol class="marked-list">
      <li> <strong>Show Title</strong>. You can show or hide module title on display. Effect will depend on the chrome style in the template.</li>
      <li>
        <div align="justify"> <strong>Position</strong>. You may select a module position from the list of predefined positions or enter your own module position by typing the name in the field and pressing enter.</div>
      </li>
