<h3>Komento Comments</h3>

<p>Komento is a comments component for Joomla. It allows you to add comments to Joomla
    articles. </p>

<figure class="img-polaroid">
    <img src="img/komento-1.jpg" alt="">
</figure>

<p>Komento component can be accessed through the Joomla administration panel in the <b>Components
        > Komento</b> section.</p>

<p>To access configuration settings go to <b>Components > Komento > Integrations</b> and
    click <b>Articles.</b></p>

<figure class="img-polaroid">
    <img src="img/komento-2.jpg" alt="">
</figure>

<p>Using component settings you can enable comments for specific categories and Joomla
    articles, add captcha, user profiles, etc. You can learn more about Komento on the
    <a href="http://stackideas.com/docs/komento.html" target="_blank">official
        website.</a></p>

