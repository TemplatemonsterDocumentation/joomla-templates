<h3>Komento Comments</h3>

<p>Komento is a comments component for Joomla. It allows adding comments to 
Joomla articles:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Comments can be accessed under <b>Components > Komento</b> tab of your Joomla admin panel.</p>

<p>To access module settings, please open <b>Components > Komento >
        Integrations</b> tab and click on <b>Articles</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>Using component settings you can enable comments for specific categories and Joomla articles, add captcha, user profiles, etc. You can learn more about Komento on the <a
        href="http://stackideas.com/docs/komento.html" target="_blank">official website</a>.</p>

