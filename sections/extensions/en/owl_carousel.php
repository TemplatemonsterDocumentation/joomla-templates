<h3>Owl Carousel</h3>

<p>Owl Carousel is a module that displays image carousel, created on the base of the standard
 Joomla Articles Newsflash module and jQuery Owl Carousel plugin:</p>

<figure class="img-polaroid">
    <img src="img/owl-carousel.png" alt="">
</figure>

<h6>All the necessary carousel settings are available in the module settings: </h6>

<ol class="index-list">
    <li>Element width;</li>
    <li>Visible elements;</li>
    <li>Animation settings;</li>
    <li>Navigation and control.</li>
</ol>


