<p>On <strong>Advanced Options</strong> you can give the <strong>module a class suffix</strong> if needed.</p>
<p align="justify">Next you should choose the page, where to place the module in. Use the <strong>Menu Assignment </strong>tab for this purpose. </p>
<p align="justify">On this tab there is a list of menu items. You can choose between 'On All Pages', 'No Pages', 'Only on the pages selected' and 'On all pages except those selected' from the List. If the latter two options are selected a list will show up with all the menu items on as shown in the screenshot above. This allows you to assign modules to some but not all the pages, and by selecting the menu links that you want the module associated with you can customize on what pages modules appear/don't appear.</p>
<div align="justify">Scroll the page up and click the green <strong>Save</strong> button at the top left of the screen to apply your changes:</div></li>
