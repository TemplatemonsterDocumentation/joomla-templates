<h3>Komento Yorumlar</h3>

<p>Komento Joomla için bir yorum bileşenidir. Bununla Joomla makalelere yorum
    ekleyebilirsiniz. </p>

<figure class="img-polaroid">
    <img src="img/komento-1.jpg" alt="">
</figure>

<p>Komento bileşenlerine joomla yönetim paneli üzerinden <b>Components > Komento</b>
    bölümünden ulaşabilirsiniz.</p>

<p>Yapılandırma ayarlarına erişebilmek için <b>Components > Komento > Integrations</b>
    <b>Articles</b> tıklayın.</p>

<figure class="img-polaroid">
    <img src="img/komento-2.jpg" alt="">
</figure>

<p>Belirli kategorideki yorumları etkinleştirmek Joomla makaleler vb. yanı sıra bileşen
    ayarlarını kullanarak, kaptan, kullanıcı profilleri de ekleyebilirsiniz. Daha fazla
    bilgi edinebilmek adına <a href="http://stackideas.com/docs/komento.html"
                               target="_blank">resmi web sitesi</a></p>

<p>Şablonlarımız sadece Komento'nun standart versiyonunu içermektedir. Ek özelliklerden
    yararlanmak ve Komento için sınırsız destek hizmeti almak için, <a
        href="http://stackideas.com/komento/plans">Komento Professional'ı</a> satın
    almanızı öneririz. Profesyonel sürümdeki özelliklerin tam listesine <a
        href="http://stackideas.com/komento?ref=kmfeature">buradan
        ulaşabilirsiniz</a>.</p>