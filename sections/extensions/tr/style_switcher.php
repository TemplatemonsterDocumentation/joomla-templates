<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Müdülü</strong> – renk şemalarını kolaylıkla değiştirebilmenizi sağlayan bir modüldür.
    Modül renk şemalarını, kullanılmakta olan temanın color_schemes\css\ klasöründen otomatik olarak yapar.</p>

<p>Yönetici panelinde, modül için özel bir ayar bulunmaz. Sadece standart modül ayarları yer alır.</p>

