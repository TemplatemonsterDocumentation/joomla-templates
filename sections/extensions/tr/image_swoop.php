<h3>Resim Swoop</h3>

<p>"Resim Swoop" modülü varsayılan Joomla "Makaleler Flaş Haberler" bir arada olup
    ücretsiz açık kaynak kodlu jQuery eklentisi "Kamera Slayt" olarak geliştirilen
    olağan üstü bir kaynaktan sunulur. Masaüstü, İOS veya Adnroid mobil cihazlarda
    mükemmel çalışır. IE8 den itibaren başlayarak bütün modern tarayıcılarda
    uyumludur.</p>

