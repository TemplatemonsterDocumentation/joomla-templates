    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>


<p>"Resim Swoop" modülü çeşitli yapılandırma seçenekleri varsa da, Joomla "Yönetim
    paneli > Modül yöneticisi" eklentilerine görüntülü swoop modülünü açarak erişim
    sağlayabilirsiniz. Tüm "Kamera Slayt" jQuery eklenti seçenekleri modülü, altında yer
    almaktadır. </p>

<p>Bu açıklama seçeneğini görebilmek için fare imlecini başlığın üzerine getirelim.
    Ayrıca Kamera Slayt jQuery eklentisi seçenekleri hakkında daha fazla bilgi
    edinebilirsiniz, <a href="http://www.pixedelic.com/plugins/camera/" target="_blank">resmi
        web sitesi</a>.</p>

