<h3>Kunena Forum</h3>

<p>Kunena Joomla için bir forum bileşenidir. Bu Joomla CMS dayalı tamamen işlevsel bir
    forum uygulamasına olanak tanır.</p>
<figure class="img-polaroid">
    <img src="img/forum-1.jpg" alt="">
</figure>


<p>Kunena bileşeni <b>Bileşenler -> Kunena</b> menüsü altında Joomla yönetim paneli
    üzerinden ulaşılabilir.</p>


<p>Sen resmi <a href="http://www.kunena.org/docs/Main_Page" target="_blank">web
        sitesinde</a> Kunena bileşeni kullanımı ve yapılandırması hakkında daha fazla bilgi
    edinebilirsiniz.</p>
