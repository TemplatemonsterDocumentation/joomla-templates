<h3>TM Ajax Contact Form Modülü</h3>


<p>TM Ajax Contact Form, Joomla için hazırlanmış bir iletişim formu modülüdür. Bu modül
    sayesinde istediğiniz bir Joomla sayfasına, iletişim formu ekleyebilirsiniz.</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>

<p> Veriler
    AJAX kullanılarak gerçekleştirilir, böylelikle sayfa yeniden yüklenmeye gerek
    duymaz. Modül ayarlarından; bot koruması(reCaptcha) ekleyebilir, form alanlarını,
    isimlerini, alıcının mail adresini, form içeriğinin kopyasının gönderileceği mail
    adresini, hata ve başarı mesajlarını ayarlayabilirsiniz. Ayar listesinin seçenekleri
    aşağıda yer almaktadır.</p>

