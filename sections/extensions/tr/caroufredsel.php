<h3>CarouFredSel</h3>

<p>CarouFredSel varsayılan Joomla Makaleler, Haber modülü ve CarouFredSel jQuery
    eklentisi bir birleşimi olarak geliştirilen bir görüntü carousel modülüdür. </p>

<figure class="img-polaroid">
    <img src="img/caroufredsel-1.jpg" alt="">
</figure>

<p>Tüm gerekli carousel modül seçenekleri ayarları aşşağıda mevcuttur. </p>

<ol class="index-list">
    <li>Carousel Yönü</li>
    <li>Madde Genişliği</li>
    <li>Görünebilir ürün</li>
    <li>Animasyon seçenekleri</li>
    <li>Navigasyon ve kontrolleri</li>
</ol>

<p>CarouFredSel eklentisinin daha fazla <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">yapılandırma
        seçenekleri</a> vardır. Siz seçenekleri eklemek istiyorsanız buradan <b>\modules\mod_caroufredsel\tmpl\default.php</b>
    düzenleyebilirsiniz. </p>

