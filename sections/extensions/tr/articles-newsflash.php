<h3>Makaleler Flaş Haberler</h3>


<p>Makaleler Flash Haber (Gelişmiş) modülü varsayılan joomla Modülünün gelişmiş bir
    sürümüdür. Varsayılan modüle özellikle ek olarak, modül şablonu ve varsayılan modül
    özelliğine dayalı ekstra yapılandırma seçenekleri ve Bootstrap sunuyor.</p>

