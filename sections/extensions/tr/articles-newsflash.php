    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Ek Seçenekler</h4>

<p><b>Number of columns</b> - Birden çok makaleler görüntülemenize olanak sağlar.</p>

<p><b>Custom Link</b> - Sizin modülünüzün içerik altındaki özel bağlantılarını
    görüntüler. Ayrıca harici URL ve Bazı Joomla menü öğesi ile bağlantıları olabilir.
</p>


<p><b>Pretext</b> - Modül içeriği, önce özel metnin görüntülenmesine öncelik sunar.</p>

<p><b>Published Date</b> - Makalede tarihi görüntüler. </p>

<p><b>Created by</b> - Makalede yazarı görüntüler</p>

<p><b>Show intro image</b>s - Makaleyi intro olarak görüntülemek için izin verir.</p>

<p><b>Intro image align</b> - İntro görüntüleri ile uyum sağlar</p>

