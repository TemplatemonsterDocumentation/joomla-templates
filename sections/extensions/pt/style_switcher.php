<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> – é um módulo que te permite mudar os esquemas de cor através do
    front-end. O módulo aplica esses esquemas automaticamente através da pasta color_schemes\css\ do tema usado.</p>

<p>Não ha configurações de módulo específicas no painel administrativo, isso inclui a configuração padrão do módulo.</p>

