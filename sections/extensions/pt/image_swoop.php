<h3>Image Swoop (em Português, &quot;Movimento de Imagem&quot;)</h3>

<p>O módulo "Image Swoop" é um slider sensível incrível desenvolvido como uma combinação
    de padrão do módulo &quot;Articles Newsflash&quot; do Joomla com o plug-in de fonte
    aberta gratuito em jQuery chamado &quot;Camera Slideshow&quot;. Funciona
    perfeitamente no desktop e em aparelhos com iOS ou Android. Compatível com todos os
    navegadores modernos a começar pelo IE8.</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>O módulo "Image Swoop" possui várias opções de configuração, você pode acessá-las
    abrindo o módulo &quot;Image Swoop&quot; na seção <b>Extensões > Gerenciador de
        Módulo (em Inglês, &quot;Extensions &gt; Module Manager&quot;)</b> do seu painel
    de administração do Joomla. Todas as opções do plug-in em jQuery &quot;Camera
    Slideshow&quot; podem ser configuradas na área de configurações do módulo. </p>

<p>Passe o cursor do mouse no título da opção para ver a descrição. Você também pode
    saber mais sobre as opções do plug-in em jQuery &quot;Camera Slideshow&quot; no <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank">site
        oficial</a></p>

