<h3>Fórum Kunena</h3>

<p>Kunena é um componente fórum de Joomla. Ele permite que você implementar um fórum
    totalmente funcional baseado no CMS Joomla.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>


<p>Componente Kunena pode ser acessado através do painel de administração do Joomla sob
    os <b>Componentes -> Kunena</b> menu.</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>


<p>Você pode aprender mais sobre o uso do componente Kunena e configuração no <a
        href="http://www.kunena.org/docs/Main_Page" target="_blank">site oficial</a>.
</p>