<h3>Artigos de Última Hora</h3>

    <figure class="img-polaroid"><img src="img/newsflash-adv-2.jpg" alt=""></figure>

<p>O módulo Artigos de Última Hora (Avançado) (em Inglês, &quot;Articles Newsflash
    [Advanced]&quot;) é uma versão melhorada do módulo de Artigos de Última Hora do
    Joomla. Além das funções do módulo padrão, ele oferece opções de configuração extra
    e templates com base no módulo &quot;Bootstrap&quot;.</p>

<h4>Opções Adicionais</h4>

<p><b>Número de colunas</b> - permite mostrar artigos em várias colunas.</p>

<p><b>Link Padrão</b> - lhe permite mostrar link padrão abaixo do conteúdo do módulo.
    Ele pode ser ligado a uma URL externa ou a algum item do menu do Joomla. </p>

<p><b>Pré-texto</b> - permite mostrar texto padrão antes do conteúdo do módulo.</p>

<p><b>Data Publicada</b> - mostra a data que o artigo foi publicado. </p>

<p><b>Criado por</b> - mostra autor do artigo</p>

<p><b>Mostra imagens de introdução</b> - permite mostrar imagem de introdução do artigo.
</p>

<p><b>Alinhamento da imagem de introdução</b> - permite alinhar as imagens de introdução
</p>
