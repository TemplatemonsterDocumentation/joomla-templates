<h3>TM Instagram</h3>

<p>TM Instagram - módulo que permite mostrar o "Instagram Widget"</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Aqui você pode ver as configurações principais do TM Instagram. Coloque o nome do
    perfil do Instagram no campo "username", o número de fotos desejadas em "Number of
    photos", inclua o número de identificação (ID) da sua aplicação do Instagram no
    campo chamado "Your Instagram CLIENT ID". Para conseguir este número, siga o link <a
        href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
        target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    onde você precisará cadastrar uma conta. Então, siga para <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> e crie a
    aplicação. Depois disso você receberá os dados da sua aplicação, e seu CLIENT ID.
</p>

