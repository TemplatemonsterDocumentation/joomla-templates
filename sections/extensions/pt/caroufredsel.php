<h3>CarouFredSel</h3>

<p>O &quot;CarouFredSel&quot; é um módulo de carrossel de imagem desenvolvido como uma
    combinação do padrão do módulo &quot;Articles Newsflash&quot; do Joomla com o
    plug-in em jQuery &quot;CarouFredSel&quot;. </p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>

<p>Todas as opções necessárias do carrossel estão disponíveis nas configurações do
    módulo. </p>

<ol class="index-list">
    <li>Direção do Carrossel</li>
    <li>Largura do Item</li>
    <li>Itens Visíveis</li>
    <li>Opções de Animação</li>
    <li>Navegação e controles</li>
</ol>


<p>O plug-in &quot;CarouFredSel&quot; tem muito mais <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">opções
        de configuração</a>. Se você quiser adicionar suas opções você pode editar o arquivo
    <b>\modules\mod_caroufredsel\tmpl\default.php</b></p>

