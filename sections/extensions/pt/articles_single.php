<h3>Articles Single (em Português, &quot;Artigos Únicos&quot;)</h3>

<p>O &quot;Articles single&quot; é um módulo que lhe permite colocar um único artigo na
    posição desejada. </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>


<p>A única opção incomum no módulo é o <b>Link Padrão</b>. Ele lhe permite mostrar o
    link padrão abaixo do conteúdo do módulo. Ele pode ser ligado a uma URL externa ou a
    algum item do menu do Joomla. </p>
