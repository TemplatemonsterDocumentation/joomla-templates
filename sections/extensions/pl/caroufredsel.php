<h3>Moduł CarouFredSel</h3>

<p>CarouFredSel jest modułem karuzeli grafik opracowanym jako kombinacja podstawowego
    modułu Joomla Articles Newsflash i pluginu CarouFredSel jQuery. </p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>

<p> Wszystkie potrzebne opcje karuzeli są dostępne w ustawieniach modułów. </p>

<ol class="index-list">
    <li>Kierunek karuzeli (ang.Carousel direction)</li>
    <li>Szerokość przedmiotu (ang. Item width)</li>
    <li>Widoczne przedmioty ( ang.Visible items)</li>
    <li>Opcje Animacji (ang.Animation options)</li>
    <li>Nawigacja i regulacja (ang.Navigation and controls)</li>
</ol>

<p>Plugin CarouFredSel ma wiele więcej opcji konfiguracji <a
        href="http://caroufredsel.dev7studios.com/configuration.php"
        target="_blank"></a>. Jeżeli chcesz dodać własne opcje to możesz edytować plik
    <b>\modules\mod_caroufredsel\tmpl\default.php</b>. </p>

