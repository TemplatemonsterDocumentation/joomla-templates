<h3>Komentarze Komento (Komento Comments)</h3>

<p><strong>Komento</strong> jest zaawansowanym rozszerzeniem komentarzy do Joomla, które pozwala odwiedzającym twoją stronę internetową komentować artykuły i blogi.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Komentarze można ocenić w zakładce <b>Komponenty > Komento</b>  z panelu administracyjnego Joomla.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Jak zarządzać integracją komponentów Komento?</p></h5>
<ol class="marked-list">
  <li>
    <p>Należy zalogować się do swojego panelu administracyjnego Joomla, zatem przejść do <strong>Komponenty</strong> &gt; <strong>Komento</strong> &gt; <strong>Integracje</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Należy kliknąć na <strong>Artykuł Joomla </strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">W zakładce Ogólne można <strong>Włączyć</strong> lub <strong>Wyłączyć</strong> komentarze. Można również zmienić <strong>Moderacje</strong> oraz inne ustawienia. </p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li><p>Mamy <strong>wyłączone</strong> [No] komentarze [można sprawdzić poniższy zrzut ekranu]. </p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>Blok komentarzy zostanie wyłączony na stronie internetowej.</p></li>
  <p><strong>Włączony (Enabled)</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Wyłączony (Disabled)</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Mamy <strong>włączone </strong>komentarze z powrotem. Można przypisać Komento do wszystkich kategorii albo wybrać poszczególne kategorie. </p>
  </li>
  <li>Przypisaliśmy komentarze do &ldquo;<strong>Firmy</strong>&rdquo;.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Można również <strong>wyłączyć</strong> komentarze dla określonego artykułu. Należy otworzyć artykuł w <strong>Zarządzanie artykułami [Zawartość &gt; Artykuły]</strong> i wprowadzić następujący kod do treści artykułu:</p>
    <pre>{KomentoDisable}</pre>
  </li>
  <figure class="img-polaroid"><img src="img/komento-10.jpg" alt=""></figure>
  <li>
    <p>Można zapisać zmiany. Komentarze nie są wyświetlane w tym artykule.</p>
  </li>
</ol>
<h5><p>Jak edytować &quot;Zasady i warunki&quot;</p> Komento?</h5>
  <p align="justify">Tekst <strong>Zasady i warunki</strong>Komento jest wyświetlany na stronach pojedynczych artykułów. Odwiedzający witrynę muszą zgodzić się z tymi warunkami w celu złożenia informacji zwrotnych, jeżeli ta opcja jest aktywowana. Prosimy zapoznać się z poniższym zrzutem ekranowym: </p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Należy wykonać następujące czynności, żeby zmienić tekst:</p>
<ol class="marked-list">
  <li>
    <p>Należy otworzyć panel administracyjny Joomla i przejść do menu <strong>Komponenty</strong> &gt; <strong>Komento</strong> &gt; <strong>Integracje</strong>:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Zatem otworzyć komponent &ldquo;<strong>Artykuł Joomla</strong>&rdquo;. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p>Następnie należy przekierować się do sekcji <strong>Układ</strong> i przejść na zakładkę <strong> Formularz komentarza</strong>:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>Po pierwsze, należy wybrać potrzebną grupę użytkowników w dolnej części strony, w sekcji Włącz Zasady i warunki. </p>
  Standardowo jest ustawiony na odwiedzających, ale można wybrać dowolną inną grupę użytkowników. 
  <p>Włączamy <strong>Zasady i warunki</strong> dla wszystkich rozajów użytkowników.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>Następnie, można zmienić tekst pola wyboru w <strong>polu tekstowym Zasady i warunki</strong>. </p>
  Należy wprowadzić wymagany tekst tutaj i zapisać zmiany przez naciśnięcie przycisku<strong> Zapish </strong>w górnym lewym rogu.
  </li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

<p>Tekst został pomyślnie zmieniony:</p>
<figure class="img-polaroid"><img src="img/komento-15.jpg" alt=""></figure>
</ol>
