<h3>Komento Comments</h3>

<p>Komento jest komponentem komentarzy dla Joomla. Umożliwia dodawanie komentarzy do
artykułów Joomla:</p>

<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Comments jest dostępny w karcie <b>Components > Komento</b> panelu administracyjnego Joomla.</p>

<p>Aby uzyskać dostęp do ustawień modułu, należy otworzyć kartę <b>Komponenty > Komento >
        Integracje</b> i kliknąć na <b>Artykuły</b>:</p>

<figure class="img-polaroid"><img src="img/komento-2.jpg" alt=""></figure>

<p>Korzystając z ustawień komponentu, można włączyć komentarzy dla poszczególnych kategorii i artykułów Joomla, dodać captcha, profile użytkowników, itp. Więcej informacji na temat Komento można uzyskać na oficjalnej stronie internetowej <a
        href="http://stackideas.com/docs/komento.html" target="_blank"></a>.</p>

<p>Nasze szablony mają tylko wersję standardową uwzględnionego Komento. Aby uzyskać dostęp do dodatkowych funkcji oraz uzyskać kompletne usługi wsparcia dla rozszerzenia Komento zalecamy zakupić <a href="http://stackideas.com/komento/plans">Komento Professional</a>. Można sprawdzić pełną listę funkcji na stronie internetowej: <a
        href="http://stackideas.com/komento?ref=kmfeature">http://stackideas.com/komento.html?ref=kmfeature</a>.
   Z Komento Professional, będzie można również uzyskać pełne wsparcie techniczne.</p>
