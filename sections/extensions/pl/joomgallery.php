<h5><p>Spróbujmy <strong>Przesyłanie zdjęć</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Należy nacisnąć przycisk <strong>Przeglądaj</strong> w sekcji <strong>Wybór zdjęć</strong> a następnie wybrać pliki multimedialne na komputerze. </div>
  </li>
  <li>
    <div align="justify">Należy określić <strong>Kategorię</strong> dla wybranych zdjęć [w ten sposób można przesłać kilka zdjęć dla każdej kategorii], <strong>Tytuł generyczny</strong> dla wybranych zdjęć [Na przykład, jeśli przesłać 3 pliki z tytułem generycznym &quot;test&quot;, pojawią się w menedżerze grafiki jako test_1, test_2 oraz test_3]. </div>
  </li>
  <li>
    <div align="justify">Należy określić <strong>Ogólny opis</strong> oraz <strong>Autora</strong>, jeżeli jest taka potrzeba. Należy ustawić typ <strong>Dostępu</strong>: publiczne, super użytkownicy, zarejestrowane, specjalne. </div>
  </li>
  <li>
    <div align="justify">Należy wykorzystać opcję <strong>Specjalne pliki graficzne</strong>. Ta opcja umożliwia też przesyłanie specjalnych plików graficznych. Tę opcję należy wybrać jedynie w przypadku przesyłania składającego się wyłącznie z przezroczystych lub animowanych plików .png lub .gif. Należy mieć na uwadze, że wielkość plików nie będzie zmieniona, i z tego względu zostaną wyświetlane w pełnym rozmiarze pod czas przeglądania szczegółów.</div>
  </li>
  <li>
    <div align="justify"><strong>Tryb debugowania (Debug Mode)</strong>. Aktywując <strong>tryb debugowania</strong>, zostaną wyświetlone wszystkie etapy przetwarzania. W przypadku wystąpienia błędów, zawsze będą wyświetlane.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Prosimy kliknąć na przycisk<strong> Prześlij</strong> w dolnej części po zakończeniu procesu konfiguracji.</p>
</div></div>
<h5><p>Edytowanie istniejącego elementu Galerii</p></h5>
<div align="justify">Należy przejść do sekcji <strong>Komponenty</strong> > <strong>JoomGallery</strong> > <strong>Menedżer grafiki</strong>. Tutaj można znaleźć listę zdjęć, które zostały już przesłane, dzięki czemu można edytować te już istniejące: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p>W przypadku, jeżeli trzeba<strong> modyfikować</strong> ich, należy wykonać następujące czynności: </p>
<ol class="marked-list">
  <li>Otwórz dowolny element z listy.     </li>
  <li>Zmień<strong> Tytuł</strong>, <strong>Kategorię</strong>, <strong>Opis</strong>, <strong>Wyniki wyszukiwania</strong>, itd. w zakładce<strong> Zdjęcie</strong> . Przewiń w dół, aby sprawdzić <strong>Przegląd miniaturek</strong> oraz <strong>Podgląd zdjęcia</strong>. </li>
  <li>
    <div align="justify">Wykorzystaj opcję <strong>Ukryte</strong>. Dzięki ukryciu tego zdjęcia, ono więcej nie zostanie wyświetlone w widoku kategorii lub uznane przez wyszukiwanie lub toplisty, ale wciąż będzie dostępne poprzez bezpośrednie linkowanie.  Jeśli nie ma być dostępne, prosimy natomiast wycofać publikację. Dzięki ukryciu zdjęć, można ich używać jako miniaturki kategorii bez pokazywania ich w odpowiedniej kategorii, na przykład.</div>
  </li>
  <li>
    <div align="justify">Opcja <strong>Wyróżnione (Featured) </strong>. Tutaj można oznaczyć zdjęcie jako 'Wyróżnione'. Na przykład, moduł JoomImages może być skonfigurowany do wyświetlania tylko grafik wyróżnionych w pozycji modułu.</div>
  </li>

<p>
