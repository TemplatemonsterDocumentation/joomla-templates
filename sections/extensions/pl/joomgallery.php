<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> jest elementem, który umożliwia stworzenie kompletnej galerii zdjęć na podstawie Joomla! Zapraszamy do sprawdzenia więcej szczegółów na oficjalnej stronie internetowej <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow"></a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Tworzenie kategorii (Category Creating)</p></h5>

<p> Należy przejść do <strong>Komponenty</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Zarządzanie Kategoriami</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
  </figure>

<p>Należy nacisnąć przycisk <strong>Nowy</strong> w lewym górnym rogu: </p>

<figure class="img-polaroid">
      <img src="img/gallery-8.jpg" alt="">
     
</figure>
  <p align="justify">Należy wprowadzić <strong>Tytuł</strong> kategorii oraz <strong>Opis</strong> ze zdjęciami, a następnie <strong>Zapisz</strong> ustawienia. Można również określić <strong> Meta Opis</strong> dla danej kategorii oraz ustawić ograniczenia dostępu w razie potrzeby.</p>
