<h3>Owl Carousel (Karuzela Owl)</h3>
<p>Moduł <strong>Owl Carousel </strong>jest połączeniem domyślnego modułu Newsflash Artykułów Joomla i wtyczki jQuery Owl Carousel.</p>
<figure class="img-polaroid">
  <img src="img/owl-carousel-1.jpg" alt="">
</figure>
<h5>Jak uzyskać dostęp (How to Access)</h5>
<p>W celu 'dodania' nowego modułu <strong>Owl Carousel</strong>, należy przejść do Zarządzania Modułami:</p>
<p>Należy wybrać <strong>Rozszerzenia</strong> &gt; <strong>Moduły</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Zatem należy kliknąć przycisk '<strong>Nowy</strong>', a także kliknąć na <strong>Owl Carousel</strong> w modalnym wyskakującym okienku.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Szczegóły (Details)</h5>
<ol class="marked-list">
  <li><strong>Tytuł (Title):</strong> Moduł musi mieć tytuł [W naszym przypadku to <strong>Karuzela (Carousel)</strong>].</li>
</ol>
<h5>Moduł (Module)</h5>
<p>Moduł <strong>Owl Carousel </strong> wyświetli stałą liczbę artykułów z określonej kategorii lub zestawu kategorii.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Kategoria (Category)</strong>: Należy wybrać artykuły z określonej kategorii lub zestawu kategorii. W przypadku braku wyboru, pokaże wszystkie kategorie jako domyślny. </div>
  </li>
  <li><strong>Zdjęcia (Images)</strong>: [Schowaj/Pokaż]. Wyświetlanie zdjęć artykułu.</li>
  <li><strong>Tytuł artykułu (Article title)</strong>: Pokaż/ukryj tytuł artykułu.</li>
  <li><strong>Połączone tytuły (Linked Titles)</strong>: [Użyj wartość domyślną/Nie/Tak]. Łączenie tytułów artykułów z artykułami.</li>
  <li> <strong>Data publikacji (Publishing Date)</strong>: Wyświetlanie daty publikacji elementu.</li>
  <li><strong>Poziom nagłówka (Header Level)</strong>: [h1/h2/h3/h4/h5]. Wybierz potrzebny poziom nagłówka HTML dla tytułów artykułów.</li>
  <li><strong>Pokaż obrazu intro (Show intro images)</strong>:  	Zaznacz, jeśli chcesz wyświetlać zdjęcia intro artykułu.</li>
  <li><strong>Wyrównanie obrazu intro (Intro image align)</strong>: [Żadne/Lewe/Prawe] Wybierz wyrównanie obrazu intro. Zastępuje ustawienia artykułów.</li>
  <li>
    <div align="justify"><strong>Link 'Czytaj więcej...' ('Read more...' Link):</strong> [Pokaż/Ukryj]. Jeśli jest ustawiona opcja Pokaż, link "Czytaj więcej ..." zostanie wyświetlony, jeśli tekst główny jest dostarczony w tekście artykułu.</div>
  </li>
  <li><strong>Liczba artykułów (Number of Articles)</strong>: Liczba artykułów do wyświetlenia w tym module.</li>
  <li>
    <div align="justify"><strong>Kolejność wyników (Order Results)</strong>: [Data publikacji/Data stworzenia/Uporządkowany/Losowy]. Należy wybrać kolejność, w której trzeba wyświetlać wyniki zapytania.</div>
  </li>
  <li><strong>Pretekst (Pretext)</strong>: Można wprowadzić tekst, który ma zostać wyświetlony przed treścią modułu. Nie obsługuje żadnych tagów HTML.</li>
</ol>
<h5>Wspólne szczegóły (Common Details)</h5>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Pokaż tytuł (Show Title)</strong>. [Pokaż/Ukryj] Należy wybrać, czy trzeba pokazywać lub ukryć tytuł modułów w frontendzie. Tytuł będzie dotyczył pola formularza powyżej.</div>
  </li>
  <li>
    <div align="justify"><strong>Pozycja modułu (Module Position)</strong>. Należy wybrać pozycję modułu, w jakiej ma być wyświetlany ten moduł. Można wprowadzić niestandardową pozycję modułu do użycia wtyczki ładowania pozycji. Lub można nacisnąć przycisk pozycjonowania, aby wybrać pozycję modułu z szablonu.</div>
  </li>
  <li><strong>Status</strong>. [Opublikowane/Nie opublikowane/W koszu] Status publikowania elementu.</li>

  <li><strong>Dostęp (Access)</strong>. Poziom dostępu oglądania dla tej pozycji.</li>

  <li>
    <div align="justify"><strong>Kolejność modułów (Module Ordering)</strong>. Pokazuje to rozwijanie każdego modułu w pozycji, w której znajduje się obecny moduł. Jest to kolejność, w jakiej moduły zostaną wyświetlane, gdy są wyświetlane na frontendzie oraz w Zarządzaniu modułami.</div>
  </li>

  <li>
    <div align="justify"><strong>Rozpoczęcie publikowania (Start Publishing)</strong>. Data i godzina, aby rozpocząć publikowanie. Użyj tego pola, jeśli chcesz wprowadzić zawartość z wyprzedzeniem, a następnie opublikować ją automatycznie w przyszłości.</div>
  </li>

  <li>
    <div align="justify"><strong>Zakończenie publikowania (Finish Publishing)</strong>. Data i godzina, aby zakończyć publikowanie. Skorzystaj z tego pola, jeśli chcesz, aby zawartość w przyszłości została automatycznie zmieniona na stan nie opublikowany [na przykład, gdy nie ma już zastosowania].</div>
  </li>

  <li><strong>Język (Language)</strong>. Język elementu.</li>

  <li>
    <div align="justify"><strong>Notatka (Note)</strong>. Notatka elementu. Jest to zwykle na potrzeby administratora witryny [na przykład, do dokumentowania informacji o tym elemencie] i nie znajduje się na frontendzie witryny.</div>
  </li>
</ol>
<p>Teraz powinniśmy ustawić parametry modułu, jak pokazano na rysunku poniżej [Należy wybrać typ pozycji<strong> właściwości (feature) </strong>w polu pozycjonowania]:</p>
<figure class="img-polaroid">
    <p><img src="img/owl-carousel-6.jpg" alt="" /></p></figure>
    <p align="justify">Następnie, należy wybrać stronę, gdzie trzeba umieścić moduł. W tym celu należy wykorzystać zakładkę <strong>Przypisanie menu (Menu Assignment)</strong>. </p>
  <p align="justify">Na tej zakładce znajduje się lista elementów menu. Można wybrać między Na wszystkich stronach, Na żadnej stronie, Tylko na zaznaczonych stronach, a także Na wszystkich stronach z wyjątkiem wybranych z listy. Jeśli dwie ostatnie opcje zostaną wybrane, lista wyżwietli wszystkie elementy menu w sposób pokazany na powyższym zrzucie ekranu. To pozwala na przypisanie modułów do niektórych, lecz nie wszystkich stron. A także poprzez wybranie linków menu, które mają być powiązane z modułem, można dostosować na jakich stronach pojawią/nie pojawią się moduły. </p>
    <p align="justify">Należy wybrać stronę <strong>Blog</strong>, a moduł pojawi się tylko na tej stronie w witrynie.  </p>
    <p align="justify">Należy pamiętać, aby nacisnąć przycisk <strong>Zapisz</strong> na pasku narzędzi.  </p>
