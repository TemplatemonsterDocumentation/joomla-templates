<h3>Owl Carousel</h3>

<p>Owl Carousel jest modułem, który wyświetla karuzelę obrazów, utworzoną na podstawie standardowego
 modułu Joomla Articles Newsflash oraz olugin'u jQuery Owl Carousel:</p>

<figure class="img-polaroid">
    <img src="img/owl-carousel.png" alt="">
</figure>

<h6>Wszystkie niezbędne ustawienia karuzeli są dostępne w ustawieniach modułu: </h6>

<ol class="index-list">
    <li>Szerokość elementu;</li>
    <li>Widoczne elementy;</li>
    <li>Ustawienia animacji;</li>
    <li>Nawigacja i sterowanie.</li>
</ol>


