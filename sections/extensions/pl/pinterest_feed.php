<p>W <strong>Zaawansowanych opcjach</strong> – Można nadać <strong>sufiks klasy modułu</strong>, jeżeli jest taka potrzeba.</p>
<p align="justify">Następnie należy wybrać stronę, gdzie trzeba umieścić moduł. W tym celu, należy wykorzystać zakładkę <strong>Przypisanie menu (Menu Assignment)</strong>. </p>
<p align="justify">Na tej zakładce znajduje się lista elementów menu. Można wybrać między Na wszystkich stronach, Na żadnej stronie, Tylko na zaznaczonych stronach, a także Na wszystkich stronach z wyjątkiem wybranych z listy. Jeśli dwie ostatnie opcje zostaną wybrane, lista wyżwietli wszystkie elementy menu w sposób pokazany na powyższym zrzucie ekranu. To pozwala na przypisanie modułów do niektórych, lecz nie wszystkich stron. A także poprzez wybranie linków menu, które mają być powiązane z modułem, można dostosować na jakich stronach pojawią/nie pojawią się moduły.</p>
