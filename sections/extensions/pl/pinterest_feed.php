<h3>Wyświetlacz SW Pinterest (SW Pinterest Display)</h3>


<p align="justify"><strong>SW Pinterest</strong> jest modułem do wyświetlania "widżeta Pinterest Board". Ten moduł może pomóc w wyświetlaniu Pinterest Profile Pins lub Board Pins. Szerokość i wysokość mogą być skalowane.</p>
<figure class="img-polaroid">
    <img src="img/pinterest_feed-1.jpg" alt="">
</figure>

<p align="justify"> Go to <strong>Zarządzanie modułem (Module Manager)</strong>. Moduł o nazwie &ldquo;<strong>SW Pinterest Display</strong>&rdquo; pojawi się na liście dostępnych. Należy kliknąć na nim, aby go edytować lub można też kliknąć przycisk &ldquo;<strong>Nowy</strong>&rdquo;, aby stworzyć nowy moduł.</p>
