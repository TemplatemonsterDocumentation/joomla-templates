<h3>Kunena Forum</h3>

<p>Kunena Forum jest komponent dla Joomla. To pozwala na wdrożenie w pełni
    funkcjonalnego forum w oparciu o CMS Joomla.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>


<p>Komponent Kunena można uzyskać za pośrednictwem panelu administracyjnego Joomla pod
    <b>Komponenty -> Kunena</b> menu.</p>


<p>Możesz dowiedzieć się więcej na temat użytkowania i konfiguracji komponentu Kunena w
    <a href="http://www.kunena.org/docs/Main_Page" target="_blank">oficjalnej
        stronie</a>.</p>

