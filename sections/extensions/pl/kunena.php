<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> to forum dyskusyjne dla witryny Joomla. Jest on elastycznym i łatwym w obsłudze. Domyślny układ forum dla zarejestrowanych i niezarejestrowanych użytkowników jest inny, jak również dostępne opcje. </p>
<p><strong>Kunena</strong> jest składnikiem forum dla Joomla. Pozwala na wdrożenie w pełni funkcjonalnego forum opartego na Joomla! </p>
<p>Ta funkcja znajduje się w pozycji menu '<strong>Strony</strong>' na frontendzie witryny. </p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p> Ogólne ustawienia <strong>Kunena</strong> oraz jego zawartości można kontrolować za pośrednictwem panelu administracyjnego swojej strony internetowej. </p>
<p>Należy przewinąć pasek boczny do Rozszerzenia i tam wybrać <strong>Kunena Forum</strong>:</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p> Będzie tam kilka dostępnych do pracy zakładek:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Zarządzanie kategoriami (Category Manager)</strong>. W środku będzie widać wszystkie obecne kategorie dostępne na twoim forum. Można wybrać dowolny, aby zmodyfikować w razie potrzeby. Można edytować sekcję nadrzędną, nazwę, alias (pseudonim), opis, nagłówek, a także inne ustawienia:  </p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify"> Będzie to obszar, w którym zostanie prowadzona dyskusja twoich uczęstników. Można utworzyć tyle dyskusji, ile potrzeba. Każda dyskusja powinna być przydzielona do sekcji Ogólnej [jest wykorzystywana jako nagłówek tematów forum]. </p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify">Zakładka <strong>Uprawniena kategorii (Category Permissions)</strong> określa poziom dostępu do określonej kategorii - grupy użytkowników do przeglądania i edycji. </p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Ustawienia kategorii (Category Settings)</strong> umożliwia sterowanie ogólnymi ustawieniami dla wiadomości kategorii. </p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify">Sekcja Użytkowniki (<strong>Users</strong> section). Tutaj można znaleźć użytkowników aktualnego forum i zarządzać ustawieniami ich konta, uprawnieniami do edycji i przeglądania: </p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify"> Należy nacisnąć na<strong> link z nazwą użytkownika</strong> i pojawi się okno, w którym można wybrać <strong>awatara</strong> oraz, jeżeli to potrzebne, <strong>tekst podpisu</strong> w <strong>ustawieniach Podstawowych</strong>. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify">Zakładka <strong>Moderacja (Moderation)</strong> umożliwia określenie, czy dany użytkownik jest moderatorem lub nie, wybierając również kategorię do moderacji. Należy kliknąć na górny przycisk <strong>Zapisz &amp; Zamknij</strong> po wykonaniu ustawień użytkownika.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Załączniki (Attachments)</strong>. Tutaj można pracować z przesłanymi plikami.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emotikony (Emotions)</strong>. Wszystkie dostępne emocje znajdują się tutaj. </p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Można użyć obszaru <strong>Nowy uśmiech (New Smile)</strong>, aby uzyskać nowe w razie potrzeby: </p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Ranking (Ranks)</strong>&rsquo;. Tutaj znajdują się wszystkie dostępne klasy dla użytkownika. Można dodać nowy ranking, jeśli jest taka potrzeba. Każda klasa może być również edytowana ręcznie:: </p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Szablony (Templates)</strong>. Wybierz szablon, aby zastosować go do Forum: </p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Konfiguracja (Configuration)</strong>. Ta zakładka umożliwia szczegółową kontrolę ustawień Forum. Tytułami, adresami e-mail, SEO, pamięcią podręczną i innymi ustawieniami można zarządzać w zakładce <strong>Ogólne</strong>. Za pomocą sekcji <strong>Frontend</strong> można modyfikować układ Forum. Ogólne ustawienia użytkowników są zarządzane za pomocą sekcji <strong>Użytkownicy</strong>. Zakładka <strong>Bezpieczeństwo (Security)</strong> kontroluje uprawnienia dla gości i zarejestrowanych użytkowników, jakie korzystują z forum. Zakładka <strong>Awatary (Avatars)</strong> jest właśnie tą, która umożliwia/zabrania użytkownikom przesyłanie własnych awatarów, a także określi ustawienia obrazu. Korzystując z <strong>Przesłane</strong>, można umożliwić/zabronić przesyłanie załączników do wiadomości, Również jak określić ustawienia tych plików.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
<p align="justify">
Po zakończeniu konfiguracji ustawień forum, należy kliknąć górny przycisk <strong>Zapisz &amp; Zamknij</strong>. </p>
<p><strong>Narzędzia (Tools)</strong>. Tutaj można zarządzać konkretnymi opcjami, takimi jak raporty, synchronizacja, adresy IP i tak dalej: </p>
<p>
    <figure class="img-polaroid"><img src="img/forum-17.jpg" alt=""></figure>
</p>
<p>Odświeżenie strony Forum w witrynie spowoduje wyświetlenie wprowadzonych zmian.
</p>
<p>Jeśli potrzebne są dodatkowe funkcje dla Kunena, można wybrać spośród wielu <strong>wtyczek</strong>, specjalnie zbudowanych dla Kunena.</p>
<p>
  <figure class="img-polaroid"><img src="img/forum-18.jpg" alt=""></figure>
</p>
<h5>Tworzenie kategorii (Create Categories)</h5>
<ol class="marked-list">
  <li>
    <p>Należy przejść do <strong>Komponenty</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Kategorie</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Należy kliknąć przycisk <strong>Nowa Kategoria</strong> w lewym górnym rogu.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Zatem, należy wprowadzić ustawienia ogólne kategorii w zakładce <strong>Nazwa oraz opis kategorii</strong>:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">Aby utworzyć sekcję, należy wybrać <strong>Najwyższy poziom (Top Level)</strong> jako nadrzędny. </p>
       </li>
       <li>
         <p>Teraz trzeba wprowadzić <strong>Nazwę</strong> oraz <strong>Alias (pseudonim)</strong>.</p>
       </li>
       <li>
         <p>Upewnij się, że kategoria jest <strong>opublikowana</strong>.</p>
       </li>
       <li>
         <p>Należy wypełnić <strong>opis</strong> oraz <strong>tytuł</strong> kategorii w odpowiednich polach.</p>
       </li>
       <li>
         <p>Można również dodać <strong>niestandardową klasę CSS</strong> dla swojej kategorii. </p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Aby definiować uprawnienia dla kategorii, należy kliknąć na zakładce<strong> Uprawnienia kategorii</strong>:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Można zobaczyć dwie dostępne opcje tutaj:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Typ kontroli dostępu (Access Control Type)</strong> używany w tej kategorii.</p>
      </li>
      <li>
        <p align="justify"><strong>Grupa poziomów dostępu (Access Level Group)</strong>, która może wyświetlać tę kategorię. Można zdefiniować własne poziomy dostępu w<strong> Użytkownicy</strong> &gt; <strong>Poziomy dostępu</strong>. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p>Zakładka <strong>Ustawiena kategorii (Category Settings)</strong>:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Zamknięta (Locked)</strong>. Trzeba ustawić opcję Tak, jeśli chcesz zablokować kategorię. Moderatorzy i administratorzy witryn są jedyni, którzy mogą tworzyć nowe odpowiedzi na zablokowaną kategorię.</p>
      </li>
      <li>
        <p align="justify"><strong>Przegląd wpisów (Review posts)</strong>. Trzeba ustawić opcję Tak, jeśli chcesz, aby wpisy zostali zweryfikowane przez moderatorów przed opublikowaniem ich w tej kategorii.</p>
      </li>
      <li>
        <p align="justify">Opcja <strong>Zezwalaj na anonimowe wiadomości (Allow anonymous messages) </strong> umożliwia włączenie/wyłączenie anonimowych wiadomości dla kategorii.</p>
      </li>
      <li>
        <p align="justify"><strong>Domyślnie odpowiedzi na wpisy jako opcja (By default post replies as option</strong>. Jeśli anonimowe wiadomości zostały włączone, ta opcja wybiera domyślny wybór dla użytkownika. Zwykli użytkownicy mogą później edytować własne wpisy, aby byli anonimowe, ale tylko moderatorzy mają możliwość edycji anonimowych wpisów.</p>
      </li>
      <li>
        <p>Można także <strong>Zezwolić na ankiety (Allow Polls)</strong> dla utworzonej kategorii.</p></li></ol>
<h5>Dodawanie strony Forum do menu</h5>
<ol class="marked-list">
  <li>
    <p>Należy otworzyć <strong>Menu</strong> &gt; <strong>Menu Główne</strong> &gt; <strong>Dodaj nowy element menu</strong>:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-24.jpg" alt=""></figure>
  <li>
    <p>Należy wybrać<strong> Typ elementu menu</strong> na następnym ekranie.</p>
  </li>
   <figure class="img-polaroid"><img src="img/forum-25.jpg" alt=""></figure>
  <li>
    <p>Należy wybrać typ menu Forum Kunena w wyskakującym okienku, ktore pojawi się:</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-26.jpg" alt=""></figure>
  <li>
    <p>Teraz można zmienić ustawienia elementów menu, zapisać menu i sprawdzić swoją witrynę.</p>
  </li>
</ol>
<p>Więcej informacji na temat używania i konfiguracji komponentów Kunena można znaleźć na oficjalnej stronie internetowej <a href="http://www.kunena.org/docs/en/manual/backend/configuration" target="_blank"></a>.</p>

