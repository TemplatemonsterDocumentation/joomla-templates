<h3>Kunena Forum</h3>

<p>Kunena jest komponentem forum dla Joomla. Umożliwia wdrażanie w pełni funkcjonalny forum na podstawie Joomla! CMS:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Można uzyskać dostęp do komponentu Kunena w panelu administracyjnym Joomla na karcie <b>Komponenty -> Kunena</b> .</p>

<p>Aby dowiedzieć się więcej na temat wykorzystania i konfiguracji komponentu Kunena, należy odwiedzić oficjalną stronę internetową <a href="http://www.kunena.org/docs/Main_Page" target="_blank"></a>:</p>

