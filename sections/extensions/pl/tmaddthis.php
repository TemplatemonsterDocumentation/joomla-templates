<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> jest największą na świecie platformą udostępniania treści. Dostarcza narzędzi, które ułatwiają udostępnianie treści w całej sieci społecznościowej, a także zapewnia wydawcom zwiększony ruch i szczegółowe analizy.<strong> AddThis</strong> może być zintegrowany z witryną przy pomocy widżetu sieciowego. Po dodaniu widżetu, odwiedzający witrynę mogą zakładać elementy przy użyciu różnych usług, takich jak Facebook, MySpace, Google Bookmarks, Pinterest, a także Twitter. </p>
<p><strong>TM Addthis </strong>is a plugin for displaying social ‘share’ buttons:</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br />
Dodanie przycisków udostępniania Joomla do twojej witryny Joomla to świetny sposób, aby uzyskać więcej zawartości rozprowadzanych online. Im więcej Twoich materiałów jest udostępnianych, tym większy ruch na twojej witrynie. Przyciski udostępniania AddThis są łatwe do dodania, oni także dostarczają szczegółowych analiz, które pomogą ci stać się bardziej skutecznym wydawcą. </p>
<p><strong>WŁAŚCIWOŚCI (FEATURES)</strong></p>
<ul>
  <li>Wybór miejsca, w których przyciski udostępniania zostaną wyświetlane na twojej stronie internetowej;</li>
  <li>Kontrolowanie liczby przycisków, które chcesz wyświetlać;</li>
  <li>Uwzględnienie liczby udostępnień, aby zachęcić do dalszego udostępniania;</li>
  <li>Spersonalizowanie przycisków udostępniania dla każdego użytkownika lub ich samodzielny wybór.</li>
</ul>
<p align="justify">Aby dostosować wtyczkę, należy przejść do <strong>Rozszerzenia</strong> &gt; <strong>Wtyczki</strong>. Spowoduje to przejście do strony <strong>Zarządzanie wtyczkami</strong>. Należy wprowadzić &quot;Dodaj&quot; w polu wyszukiwania a pojawi się <strong>Zawartość - TM AddThis</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>Najpierw należy <strong>wlączyć</strong> wtyczkę. Trzeba nacisnąć czerwony przycisk &quot;X&quot;, aby ją włączyć. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Kiedy wtyczka zostanie <strong>włączona</strong>, pojawi się komunikat z potwierdzeniem.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Aby dostać się do okna głównych ustawień wtyczki, należy nacisnąć link z tytułem <strong>Zawartość - TM AddThis</strong> a zostaniesz przeniesiony do następnego okna:</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Tutaj można zobaczyć główne <strong>ustawienia TM Addthis</strong>. W odpowienim polu należy ustawić<strong> kategorie do wykluczenia</strong>.  W polu <strong>ID profilu (Profile ID)</strong> prosimy wprowadzić swój Identyfikator Profilu, który można uzyskać po rejestracji i utworzeniu konta na następnej stronie: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>Zasadniczo powinieneś dostać nowy <strong>ID Profilu</strong> w celu zmiany społecznych ikon udostępniania <strong>AddThis</strong>:</p>
<p>Po zarejestrowaniu trzeba dodać przyciski <strong>udostępniania</strong>. </p>
<p>W tym celu należy nacisnąć przycisk &quot;<strong>AddThis</strong>&quot; na pasku narzędzi profilu.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Następnie należy nacisnąć przycisk &quot;<strong>Zacznij (Get Started)</strong>&quot;   w sekcji przycisków udostępniania.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Teraz należy uaktywnić przyciski udostępniania <strong>Inline </strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">Można wybrać rodzaj <strong>Liczniki udostępniania (Share Counters)</strong>. Również można wybrać własne sieci społecznościowe i dostosować kolejność, używając opcji <strong>Wybierz własne</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Można zmienić projekt ikon w sekcji <strong> Projekt </strong>. Spersonalizuj <strong>Styl</strong>, <strong>Rozmier przycisków</strong> oraz <strong>Kolory</strong> ikon. Jeżeli potrzebne, można ustawić parametr <strong>Zaokrąglone rogi</strong> oraz <strong>ukryć nazwy sieci</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Kiedy skończysz z personalizacją  <strong>Przycisków udostępniania</strong>, należy nacisnąć <strong>Zapisz&amp;Kontynuuj</strong>, aby przejść do następnej czynności.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Należy skopiować identyfikator profilu w sekcji &quot;<strong>1</strong>&quot;. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>Należy wstawić go do pola <strong> ID Profilu </strong> w oknie ustawień <strong> Zawartość - TM AddThis </strong>, aby nowe <strong> Przyciski udostępniania </strong> pojawiły się na twojej stronie internetowej.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Istnieje możliwość wykluczenia kategorii [stron], w których nie chcesz umieszczać wtyczki.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Należy upewnić się, że wtyczka została <strong>włączona</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Teraz trzeba zapisać zmiany i odświeżyć stronę <strong> Przyciski udostępniania </strong>. </p>
