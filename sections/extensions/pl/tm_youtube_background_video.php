<p class="alert alert-warning">
W naszych szablonach treść TM Youtube background video jest przedstawiona przez załadowanie pewnego modułu lub położenia, na przykład, "{loadmodule articles_single, O nas}". W celu uniknięcia błędów 404 na swojej witrynie, należy upewnić się, że nazwa załadowanego modułu jest zbieżna z tą określoną dla ładowania. W takim przypadku, moduł "O nas" powinien zostać opublikowany i nie powinien zawierać więcej niż jedną spację w nazwie modułu.
    Zapraszamy do zapoznania się ze szczegółami loadmodule/loadposition na oficjalnej stronie internetowej <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank"></a>.
</p>
