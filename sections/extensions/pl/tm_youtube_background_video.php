<h3>TM Youtube background video module</h3>

<p>TM Youtube background video to moduł do dodawania wideo youtube w tle na stronie internetowej.</p>

<h6>Wszystkie możliwe opcje modułu są dostępne w ustawieniach modułu:</h6>

<ol class="index-list">
    <li>Link wideo Youtube;</li>
    <li>Ustawienia odtwarzania wideo;</li>
    <li>Ustawienia mobilnego wyświetlacza;</li>
    <li>Ustawienia przycisków ”Play/Stop”.</li>
</ol>
