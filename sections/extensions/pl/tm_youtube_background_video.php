<h3>Moduł TM wideo YouTube w tle (TM YouTube background video module)</h3>

<p><strong>TM wideo YouTube w tle</strong> jest modułem do dodawania wideo youtube w tle na twojej stronie internetowej.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>W celu konfiguracji modułu TM wideo YouTube w tle, należy wykonać następujące czynności:</p></h5>
<ol class="marked-list">
<li><p>Należy przejść do zakładki <strong>Rozszerzenia</strong> &gt; <strong>Moduły</strong>:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Następnie należy wybrać moduł <strong>TM wideo YouTube w tle</strong> w celu edytowania.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Wskazówka:</strong> Jeśli chcesz znaleźć wszystkie moduły <strong>TM wideo YouTube w tle</strong>, należy użyć filtrów w celu uproszczenia procesu. Trzeba nacisnąć przycisk &quot;<strong>Narzędzie wyszukiwania</strong>&quot; oraz wybrać typ &quot;<strong>TM wideo YouTube w tle</strong>&quot; w liście rozwijanej filtrów &quot;<strong>- Wybierz typ -</strong>&quot;. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>Można kliknąć przycisk &quot;<strong>Nowy</strong>&quot;, aby utworzyć nowy moduł.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>Aby otworzyć dowolne zmiany wymaganych ustawień modułu, trzeba kliknąć jego tytuł. [Let's edit <strong>Home youtube</strong> module.]</li>
<li>
  <div align="justify">Interfejs edycji modułu <strong>Youtube na stronie domowej (Home youtube) </strong>jest otwarty przed tobą. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Należy nacisnąć przycisk <strong>Artykuł</strong>, aby otworzyć modalne okno wyboru artykułów, jak pokazano poniżej. Można użyć filtrów w celu uproszczenia procesu.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>Aby dodać nowy artykuł, trzeba wykorzystać <strong>Zawartość</strong> &gt; <strong>Artykuły</strong> &gt; <strong>Dodaj nowy artykuł</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Tutaj można znaleźć <strong>zakładkę opcji</strong>, gdzie będzie można zmienić:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Przygotuj zawartość (Prepare Content) </strong> – opcjonalnie można przygotować zawartość za pomocą wtyczek do zawartości Joomla. </p>
      </li>
      <li>
        <p><strong>Wprowadź adres url wideo YouTube (Enter a YouTube video url) *</strong> – umożliwia określenie niestandardowego adresu URL dla filmu z YouTube, który musi być widoczny na stronie.</p>
      </li>
      <li>
        <p><strong>Rozpocznij wideo (Start video)</strong> – wideo rozpoczyna się od określonych sekund.</p>
      </li>
      <li>
        <p><strong>Wycisz (Mute)</strong> – zmiana wideo na tryb cichy lub głośny.</p>
      </li>
      <li><strong>Autoodtwarzanie (Autoplay)</strong> – wideo rozpoczyna się automatycznie, gdy internetowa strona jest ładowana.</li>
      <li><strong>Pokaż przycisk odtwarzaj/wstrzymaj (Show play/pause button)</strong> – można pokazać lub schować przyciski.</li>
      <li> <strong>Tekst przycisków odtwarzaj/wstrzymaj (Play/Pause button text)</strong> – należy określić tekst przycisków.</li>
      <li>
        <p><strong>Obraz na urządzeniach mobilnych (Mobile Image)</strong> – pokaż ten obraz na urządzeniach mobilnych zamiast wideo. [Aby przesłać zdjęcia, należy przejść do sekcji <strong>Zawartość </strong>&gt; <strong>Media</strong>.]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>W celu zmiany wideo, trzeba po prostu <strong>zastąpić adres URL wideo</strong> uprzednio skopiowanym <strong>linkiem do wideo youtube</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Teraz można przewinąć stronę do góry i kliknąć zielony przycisk <strong>Zapisz</strong> w lewym górnym rogu ekranu, aby zastosować zmiany:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Teraz trzeba wrócić do swojej witryny i odświeżyć stronę z tekstem, który został zmieniony. Teraz widzimy, że edycja zakończyła się pomyślnie. </p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">W naszych szablonach zawartość <strong>TM Youtube background video</strong> jest przedstawiona POPRZEZ ładowanie pewnego modułu lub pozycjonowania, na przykład, "{loadmodule articles_single, About us}". Aby uniknąć Błędu 404 (Error 404) na swojej stronie internetowej, należy upewnić się, że nazwa załadowanego modułu jest taka sama, jak określona do załadowania. W takim przypadku należy opublikować moduł "O nas (About us)". Nazwa modułu nie powinna zawierać więcej niż jedną spację.
        Więcej informacji o ładowaniu modułu lub pozycjonowania (loadmodule/loadposition) można znaleźć na oficjalnej stronie internetowej <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank"></a>.
