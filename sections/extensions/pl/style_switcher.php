<h3>Moduł Przełącznik Stylów TM Ajax (TM Ajax Style Switcher Module)</h3>

<p align="justify"><strong>Moduł Przełącznik Stylów TM Ajax</strong> jest modułem do przełączania schematów kolorów pośrednictwem front-end. Moduł automatycznie stosuje schematy kolorów z folderu templates\themexxx\css\ obecnego motywu.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Uwaga</strong>: w szablonie Live Demo, szablon pracuje jedynie w trybie podglądu za pomocą ciasteczek (plików cookie). Na twojej stronie internetowej moduł będzie pracować w trybie pełnym oraz będzie dostępny dla uprawnionych użytkowników z prawami użytkowników kategorii SuperAdmin.</p>

<h5> W jaki sposób pracować z modułem Przełącznik Stylów TM Ajax </h5>
<p align="justify">W celu konfiguracji <strong>Moduł Przełącznik Stylów TM Ajax</strong> należy przejść do <strong>Rozszerzenia </strong>&gt;<strong> Moduły</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify"> Należy nacisnąć przycisk &quot;<strong>Narzędzie wyszukiwania</strong>&quot; oraz wybrać rodzaj <strong>Moduł Przełącznik Stylów TM Ajax</strong> w liście rozwijanej filtrów &quot;- Wybierz rodzaj -&quot;. You will see the <strong>Moduł Przełącznik Stylów Ajax</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Należy kliknąć na link tytułu modułu, żeby być doprowadzonym do okna ustawień modułu. Nie ma żadnych specyficznych ustawień dla modułu w panelu administracyjnym, tylko standardowe ustawienia dla wszystkich modułów:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Należy upewnić się, że moduł jest <strong>opublikowany</strong>.</p>
  <h5><p>Aby aktywować przełącznik stylu, należy postępować zgodnie z instrukcjami podanymi poniżej:  </p></h5>
  <ol class="marked-list">
    <li>
      <p>Dodaj nowego użytkownika Joomla za pomocą <strong>Użytkownicy</strong> &gt; <strong>Zarządzanie</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Włącz</strong> oraz	<strong>aktywuj</strong> nowego użytkownika.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Naciśnij link logowania nowego użytkownika, żeby być doprowadzonym do okna ustawień. </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Nie zapomnij, aby przypisać użytkownika do grupy &lsquo;<strong>Super Użytkownicy</strong>&rsquo;. Należy przejść do zakładki <strong>Przypisane Użytkownicy</strong> oraz odhaczyć &quot;<strong>Super Użytkownicy</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Zaloguj się do swojej strony internetowej za pomocą modułu na frontendzie <strong>Zaloguj się lub zarejestruj</strong>:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>Przełącznik Stylów został pomyślnie aktywowany. Odśwież swoją stronę internetową, aby zobaczyć skrzynkę narzędzi dostosowywania: </p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
