  <ol class="marked-list">
    <li>
      <p>Należy wybrać wymagany schemat kolorów.</p>
    </li>
    <li>
      <p>Zatem kliknąć przycisk <strong>Zapisz</strong>, aby zachować zmiany i zaktualizować schemat kolorów.</p>
      
    </li>
     <p>
       <figure class="img-polaroid">
