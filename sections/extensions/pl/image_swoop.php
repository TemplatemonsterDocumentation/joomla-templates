<h3>Image Swoop</h3>

<p>Moduł "Image Swoop" jest wybitnie czułym sliderem opracowanym jako kombinacja
    podstawowego modułu "Articles Newsflash" i darmowego pluginu open source jQuery
    "Camera Slideshow". Perfekcyjnie działa na urządzeniach desktop i iOs albo Android.
    Kompatybilny z wszystkimi współczesnymi przeglądarkami, zaczynając od IE8. </p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>Moduł "Image Swoop" ma różne opcje konfiguracyjne, dostęp do nich uzyskuje się
    poprzez otwarcie modułu Image Swoop <b>Rozszerzenia > Menadżer Modułów
        (ang.Extensions > Module Manager)</b> w sekcji panelu administracyjnego Joomla.
    Wszystkie główne opcje pluginu jQuery "Camera Slideshow" mogą być konfigurowane w
    sekcji ustawień modułu. </p>

<p>Nakieruj kursor myszki na nazwę opcji by zobaczyć jej opis. Możesz również dowiedzieć
    się więcej o pluginie Camera Slideshow jQuery na oficjalnej stronie: <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank"></a></p>

