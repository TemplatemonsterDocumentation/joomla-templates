<h3>Moduł TM Parallax</h3>

<p>TM Parallax to moduł, który używa skryptu paralaksy RD na wdrożenie funkcjonalności paralaksy do szablonu.</p>

<h6>Wszystkie niezbędne opcje modułu są dostępne w ustawieniach modułu:</h6>

<ol class="index-list">
    <li>Ustawienia obrazów;</li>
    <li>Ustawienia prędkości przewijania paralaksy;</li>
    <li>Ustawienia kierunku paralaksy.</li>
</ol>
