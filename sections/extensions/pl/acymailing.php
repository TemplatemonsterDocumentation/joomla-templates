<h3>AcyMailing</h3>

<p align="justify"><strong>AcyMailing</strong> jest składnikiem subskrypcji dla Joomla! AcyMailing jest niezawodnym newsletterem i rozszerzeniem e-mail marketingu dla Joomla. AcyMailing umożliwia sprawne zarządzanie nieograniczoną ilością subskrybentów, organizowanie ich w listach mailingowych i wysyłanie spersonalizowanych newsletterów. </p>
<p>Na swojej stronie internetowej można zobaczyć poniższy formularz subskrypcji:</p>
<figure class="img-polaroid">
    <img src="img/acymailing-1.jpg" alt="">
</figure>

<p>Należy przejść do <strong>Komponenty</strong> oraz <strong>AcyMailing</strong>, wtedy zostaniesz przekierowany do pulpitu nawigacyjnego AcyMailing.</p>
<figure class="img-polaroid">
    <img src="img/acymailing-4.jpg" alt="">
</figure>


<p align="justify"> Aby zobaczyć główne ustawienia tego rozszerzenia, należy przejść do <strong>Rozszerzenia</strong> oraz <strong>Moduły</strong>. Zatem należy kliknąć przycisk <strong>Nowe</strong>.</p>
