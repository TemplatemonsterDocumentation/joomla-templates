<h3>Plugin TM Facebook Page</h3>
 
<p>TM Facebook Page jest modułem, który umożliwia wyświetlanie własnej strony Facebook na stronie internetowej:</p>

<figure class="img-polaroid">
    <img src="img/facebook_feed-1.jpg" alt="">
</figure>

<p>Tutaj można znaleźć główne ustawienia modułu TM Facebook Page. Pole Facebook App ID jest wymagane. To jest identyfikator aplikacji Facebook. W celu uzyskania identyfikatora, należy otworzyć stronę internetową: <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a>
    i utworzyć aplikację na swojej stronie internetowej. Aby dowiedzieć się więcej na temat innych ustawień, należy odwiedzić oficjalną stronę internetową <a href="https://developers.facebook.com/docs/plugins/page-plugin"
                       rel="nofollow" target="_blank"></a>:</p>

