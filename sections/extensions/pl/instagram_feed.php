<h5>Co to jest Token Dostępu do Instagramu (Instagram Access Token) – Krótki opis</h5>
      <p align="justify">Uwierzytelnione wnioski o dopuszczenie potrzebują <strong>Token Dostępu do Instagramu</strong>.   Token dostępu to nieprzezroczysty ciąg znaków, który identyfikuje użytkownika, aplikację lub stronę.   Może być wykorzystywany przez aplikację do nawiązywania połączeń wykresu API i jest unikalny dla każdego użytkownika.</p>
      <blockquote>Token Dostępu do Instagramu ma zasadnicze znaczenie dla korzystania z większości aplikacji opartych na Instagram.</blockquote>
      <h5>Przewodnik "krok po kroku" dla uzyskania Tokena Dostępu do Instagramu</h5>
      <p>Trzeba wykonać następujące <strong>3 proste kroki</strong>, aby uzyskać Token Dostępu do Instagramu:</p>
      <ol class="marked-list">
        <li>Należy zalogować się do Instagramu jako deweloper.</li>
        <li>Zatem zarejestruj nowe ID Klienta.</li>
        <li>Uzyskaj Token Dostępu do Instagramu za pomocą ID oraz Tajemnicy Klienta.</li>
      </ol>
