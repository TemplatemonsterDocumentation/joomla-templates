<p>Tutaj można zobaczyć główne ustawienia TM Instagram. Należy wprowadzić nazwę profilu Instagram w polu nazwy użytkownika Instagram, wprowadzić żądaną liczbę obrazów w polu Liczba Zdjęć, ustawić ID aplikacji Instagram w polu o nazwie Twoje ID Klienta Instagram. Aby uzyskać identyfikator, należy przejść na link
<a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                 target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    i zarejestrować konto. Następnie należy przejść na link
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> i utworzyć aplikację. Zatem będzie można uzyskać dane aplikacji, gdzie można znaleźć 
