<p>Tutaj możesz zobaczyć główne ustawienia TM Instagram. Wprowadź nazwę profilu
    Instagram w polu nazwy użytkownika Instagram, wprowadź żądaną liczbę obrazów w polu
    Numbers of photos, ustaw ID aplikacji Instagram w polu z Twoim Instagram CLIENT ID.
    Aby otrzymać ID, należy kliknąć link <a
        href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
        target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    , gdzie należy zarejestrować konto. Następnie kliknij na ten link <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> i utwórz
    aplikację. Następnie otrzymasz dane Twojej aplikacji, gdzie zobaczysz CLIENT ID.</p>

