<h3>Pojedyncze artykuły (Articles Single)</h3>

<p><strong>Pojedyncze artykuły</strong> jest modułem, który umożliwia wyświetlanie jednego artykułu w żądanej pozycji: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">Jedynym nietypowym ustawieniem modułu jest <b>Niestandardowy link (Custom Link)</b>. To pozwala na wyświetlenie niestandardowego linku pod zawartością modułu. Można połączyć go z zewnętrznym adresem URL lub z pozycją menu swojej witryny Joomla:</p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">Jeśli chcesz zmienić wygląd sekcji &quot;<strong>JAK PRACUJEMY</strong>&quot; , należy przejść do <strong>Rozszerzenia</strong> &gt; <strong>Moduły</strong>. Zatem trzeba wybrać moduł &quot;<strong>Jak pracujemy</strong>&quot; , który jest należący do typu <strong>Artykuły - Pojedyncze</strong>. Żeby go znaleźć, można użyć skrótu Ctr+F. Należy naciśnąć kombinację klawiszy Ctrl + F i wpisać <strong>220</strong> [jest to ID modułu &quot;Jak pracujemy&quot; ]. Kiedy kliknąć link jego tematu, można zobaczyć okno konfiguracji modułu.</p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
 </p>
 <p>Moduł <strong>Pojedyncze artykuły</strong> posiada następujące ustawienia:</p>
 <p align="justify"><strong>Wybrać artykuł (Select Article.)</strong> To pole utrzymuje artykuł dla wyświetlania w tym układzie. Należy nacisnąć przycisk <strong>Wybrać/Zmienić</strong>, aby otworzyć okno wyboru artykułu, jak pokazano poniżej. </p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify"> Ten ekran jest podobny do <strong>Zarządzanie artykułami (Article Manager)</strong>. W celu znalezienia żądanego artykułu, można użyć <strong>Filtry pola</strong>, a zatem kliknąć na <strong>Tytuł</strong> artykułu, aby wybrać go. W tym momencie, modalne okno zostanie zamknięte, a tytuł pojawi się w <strong>polu Wybierz artykuł</strong>.     </p>
     <p align="justify">Teraz pojawiła się możliwość do edytowania artykułu. Aby edytować artykuł, należy kliknąć przycisk<strong> Edytować</strong>, a pojawi się okienko <strong>Edytować artykuł </strong>. Tutaj jest dostępna opcja edytowania zawartości artykułu oraz jego zdjęć i linków.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify"> <strong>Pojedynczy artykuł</strong> posiada następujące <strong>Opcje artykułu</strong> [Zakładka opcji (Options Tab), jak pokazano poniżej. Te opcje określają, w jaki sposób artykuł zostanie wyświetlony w układzie Front End witryny. Należy zauważyć, że te opcje obejmują &quot;<strong>Użyj globalnych wartości</strong>&quot;. Po wybraniu tej opcji, ustawienia z opcji Zarządzania artykułami zostaną wykorzystane.</p>
    <figure class="img-polaroid">
     <p><img src="img/articles-single-8.jpg" alt="">
     </p></figure>
