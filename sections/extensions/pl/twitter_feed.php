<h5>Znajdź swój identyfikator widżetu Twitter (Find your Twitter widget ID)</h5>
<p align="justify">Jeśli widżet Twitter'a, który chcesz włączyć, pyta o identyfikator widżetu Twitter, należy utworzyć nowy widżet ze swojej strony <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">ustawienia widżetu Twitter</a>.</p>
<p align="justify">Aby uzyskać dostęp do ustawień widgetu Twitter, trzeba zalogować się na konto Twitter, które chcesz używać ze swoim widżetem. Potem kliknij ikonę koła zębatego w prawym górnym rogu strony, a następnie kliknij <strong>Ustawienia</strong>. Na pasku bocznym ustawień trzeba wybrać zakładkę <strong>Widżety</strong>, znajdującą się na samym dole.</p>
<p align="justify">Na tej stronie należy znaleźć przycisk <strong>Utwórz nowy</strong> oraz nacisnąć go, aby utworzyć nowy widżet. Stąd będą oferowane opcje konfiguracyjne i podgląd widżetu. Zawsze można zmienić opcje konfiguracyjne widżetu później.</p>
<p align="justify">Po utworzeniu widżetu, przycisk <strong>Utwórz nowy</strong> zostanie zastąpiony przyciskiem <strong>Zapisz zmiany</strong>. Po utworzeniu widżetu można pobrać identyfikator widżetu z adresu URL. Spójrz na pasek adresu przeglądarki. Powinien on mieć adres URL, który wygląda mniej więcej tak:</p>
 <blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p>Identyfikator widżetu to 18-cyfrowy numer. W tym przypadku, <strong>512253083833733120</strong>. Należy pamiętać, aby ten numer był prywatny, żeby twój widget nie był używany przez innych bez twojej zgody.</p>
<p>Należy skopiować identyfikator i wkleić go w polu tekstowym, w którym ma zostać wyświetlony identyfikator widżetu. Po zapisaniu ustawień, nowy widżet Twitter powinien być funkcjonalny.</p>
