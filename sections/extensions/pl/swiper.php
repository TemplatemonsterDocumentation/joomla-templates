<h3>Swiper</h3>

<p>Swiper Module to wspaniały adaptacyjny slider, który stanowi integrację standardowego modułu Joomla Artykuły Newsflash oraz darmowego plugin'u jQuery Swiper. Utworzony dla IOS, ten plugin doskonale sprawdza się w najnowszym systemie Android, Windows Phone 8 i desktopowych przeglądarkach PC:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Swiper Module zawiera różne ustawienia i można go znaleźć w panelu administratora Joomla, na karcie <b>Rozszerzenia
        > Zarządzanie Modułami</b>. Wszystkie główne opcje 
plugin'u jQuery Swiper można zmienić w ustawieniach modułu. </p>

<p>Aby zobaczyć jego opis, należy umieścić kursor myszy na ustawieniu. Aby dowiedzieć się więcej na temat jQuery Swiper, zapraszamy do oficjalnej strony internetowej <a
        href="http://idangero.us/swiper/" target="_blank"></a>.</p>
<p>Wszystkie niezbędne ustawienia karuzeli można znaleźć w ustawieniach modułu:</p>

<ol class="index-list">
    <li>Ustawienia odtwarzania;</li>
    <li>Ustawienia wysokości;</li>
    <li>Widoczne elementy;</li>
    <li>Ustawienia animacji;</li>
    <li>Nawigacja i sterowanie;</li>
    <li>Ustawienia miniaturek.</li>
</ol>

