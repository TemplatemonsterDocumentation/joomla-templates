<h3>Swiper</h3>

<p align="justify"><strong>Moduł Swiper</strong> jest wspaniałym slajderem przystosowanym do adaptacji, który jest integracją standardowego modułu Newsflash artykułów Joomla, a także bezpłatną wtyczką jQuery Swiper. Utworzona dla IOS, ta wtyczka działa doskonale w najnowszych przeglądarkach Android, Windows Phone 8 i komputerach stacjonarnych.</p>
<p align="justify">Sprawdźmy, jakie rodzaje slajdów można dodać do slajdera witryny przy użyciu tego modułu. Przede wszystkim, może to być zwykły animowany slajd ze zdjęciem i napisem na nim: </p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">Ale może to być również slajd z wideo w tle lub konfigurowalny slajd z obrazem paralaksy. Tak naprawdę, to jest ogromna ilość opcji do wyboru. Nauczmy się jak z nich korzystać.</p>

