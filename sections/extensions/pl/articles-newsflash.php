<p align="justify">Potem trzeba wybrać moduł <strong>&quot;Co oferujemy&quot; </strong>(na przykład) , który należy do typu<strong> Newsflash [Zaawansowane] (Newsflash [Advanced])</strong>. Można użyć kombinację klawiszy Ctrl + F, aby go znaleźć. Należy kliknąć link tytułu, a pojawi się okno jego konfiguracji.</p>
