<h3 align="justify">Newsflash artykułów [Zaawansowane] (Articles Newsflash [Advanced])</h3>

<p align="justify">Jest to udoskonalona wersja domyślnego <strong>modułu Joomla Newsflash Artykułów (Joomla Articles Newsflash Module)</strong>. W uzupełnieniu do funkcji domyślnego modułu, posiada dodatkowe opcje konfiguracyjne i szablony modułu na podstawie Bootstrap. Aby dodać jeszcze więcej dla udoskonalenia twojego projektowania stron internetowych na podstawie Joomla, to jest to moduł znacznie wyższej jakości dla przedstawienia twoich materiałów. Łatwy w użyciu, łatwo aktualizować, zawiera niektóre wielkie i potężne opcje dostosowywania.</p>

