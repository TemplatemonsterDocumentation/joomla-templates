<p align="justify">Jeżeli trzeba zmodyfikować sekcję &quot;<strong>CO OFERUJEMY</strong>&quot; (na przykład) na swojej stronie internetowej, należy wybrać ją z listy modułów. Następnie, przejść do <strong>Rozszerzenia</strong> &gt; <strong>Moduły</strong>.</p>
<p>
  <figure class="img-polaroid"> <img src="img/newsflash-adv-6.jpg" alt="" /></figure>
