<h3>Wtyczka katalogu ciasteczek Joomla (Joomla Cookie Directive Plugin)</h3>

<p><strong>Wtyczka katalogu ciasteczek Joomla</strong> ma możliwość wyświetlania banera akceptacji ciasteczek na górze strony, aby ostrzec o witrynie używającej ciasteczka.</p>

<figure class="img-polaroid">
    <p><img src="img/cookies-1.jpg" alt="">
    </p></figure>
    <p align="justify">W celu konfiguracji wtyczki <strong>katalogu ciasteczek Joomla</strong>, należy przejść do <strong>Rozszerzenia</strong> &gt; <strong>Wtyczki</strong>. Aby znaleźć potrzebną wtyczkę wśród innych, należy wprowadzić &quot;cook/.../&quot; w polu wyszukiwania.</p>
    <p align="justify">Należy kliknąć link tytułu &quot;<strong>system - EUCookieDirectiveLite</strong>&quot; a pojawią się główne ustawienia wtyczki <strong>katalogu ciasteczek Joomla</strong>.</p>

<p>Ten ekran umożliwia edytowanie szczegółów i opcji dotyczących wtyczek. </p>

<p>
  <figure class="img-polaroid">
    <img src="img/cookies-2.jpg" alt="">
  </figure>
</p>
