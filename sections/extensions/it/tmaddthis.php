<h3>TM Addthis</h3>

<p>TM Addthis - plugin che ti permette di inserire pulsanti 'condividi' per i social
    network.</p>

<figure class="img-polaroid"><img src="img/addthis-1.jpg" alt=""></figure>

<p>Qui puoi trovare le impostazioni principali di TM Addthis plugin. Inserisci l'ID del
    tuo profilo nel campo 'profile ID'. Per poter ottenere l'ID del profilo dovrai
    registrare un account e creare il profilo sulla pagina seguente <a
        href="https://www.addthis.com/settings/publisher" target="_blank"
        rel="nofollow">https://www.addthis.com/settings/publisher</a>.</p>

