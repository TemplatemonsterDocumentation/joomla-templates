<h3>TM Addthis</h3>

<p align="justify"><strong>AddThis</strong> è la più grande piattaforma di condivisione di contenuti al mondo. Esso fornisce strumenti che rendono più facile condividere contenuti attraverso il web sociale, e fornisce agli editori con un aumento del traffico e paralitici in profondità.<strong> AddThis</strong> può essere integrato in un sito web con l'uso di un web widget. Una volta aggiunto il widget, i visitatori del sito possono contrassegnare una voce utilizzando una varietà di servizi, come Facebook, MySpace, Google Bookmarks, Pinterest e Twitter.</p>
<p><strong>TM Addthis </strong>è un plugin per la visualizzazione di pulsanti sociali di condivisione:</p>

<p align="justify">
  <figure class="img-polaroid"><img src="img/addthis-5.jpg" alt=""></figure>
  <br />
Aggiungendo i nostri pulsanti per Joomla per il tuo sito Joomla è un ottimo modo per ottenere più del vostro contenuti diffusi on-line. Quanto più il contenuto è condiviso, più traffico si guida al tuo sito. pulsanti AddThis sono facili da aggiungere, e anche fornire analisi dettagliate che vi aiuterà a diventare un editore più efficace.</p>
<p><strong>CARATTERISTICHE</strong></p>
<ul>
  <li>Scegliere dove i pulsanti appaiono sulla tua pagina;</li>
  <li>Controllare il numero di pulsanti che si desidera visualizzare;</li>
  <li>Includono la quota conteggi per incoraggiare la condivisione;</li>
  <li>Personalizza pulsanti di condivisione per ogni utente o prenderli da soli.</li>
</ul>
<p align="justify">Per personalizzare il plugin andare a <strong>Estensioni</strong> &gt; <strong>Plugins</strong>. Questo vi porterà alla <strong>Plugins Manager</strong>. Tipo &quot;Add&quot; nel campo di ricerca e vedrete la <strong>Contenuto - TM AddThis</strong>.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-2.jpg" alt=""></figure>
</p>
<p>Prima di tutto bisogna <strong>abilitare</strong> il plugin. Premere il rosso &quot;X&quot; pulsante per attivarlo. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-3.jpg" alt=""></figure>
</p>
<p>Una volta che il plugin è <strong>abilitato</strong>, si vedrà un messaggio di conferma.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-4.jpg" alt=""></figure>
</p>
<p>Per ottenere la finestra delle impostazioni principali della stampa plugin del <strong>Contenuto - TM AddThis</strong> link del titolo e ti verrà portato al seguente finestra:</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-6.jpg" alt=""></figure>
</p>
<p align="justify">Qui potete vedere la principale <strong>TM Addthis impostazioni</strong>. Impostare il<strong> categorie da escludere</strong> nel campo corrispondente. Nel campo <strong>Profilo ID</strong> inserisci il tuo ID profilo che si può ottenere dopo la registrazione e la creazione di un conto presso la seguente pagina: <a
        href="https://www.addthis.com" target="_blank"
        rel="nofollow">https://www.addthis.com</a>:</p>

<p>
  <figure class="img-polaroid"><img src="img/addthis-7.jpg" alt=""></figure>
</p>
<p>In sostanza, si dovrebbe ottenere un nuovo <strong>Profilo ID</strong> per cambiare <strong>AddThis</strong> condividere le icone sociali:</p>
<p>Dopo aver registrato, è necessario aggiungere <strong>condividere</strong> pulsanti. </p>
<p>Per fare questo comunicato &quot;<strong>AddThis</strong>&quot; sulla barra degli strumenti del profilo.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-8.jpg" alt=""></figure>
</p>
<p>Quindi è necessario premere il &quot;<strong>Iniziare</strong>&quot; nella sezione Azioni Pulsanti.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-9.jpg" alt=""></figure>
</p>
<p>Attivare <strong>In linea </strong>Condividi Bottoni.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-10.jpg" alt=""></figure>
</p>
<p align="justify">Puoi scegliere <strong>Condividi Contatori</strong> tipo. È inoltre in grado di selezionare i propri social network e personalizzare l'ordine con il <strong>Seleziona il tuo</strong> opzione.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-11.jpg" alt=""></figure>
</p>
<p align="justify">Modificare il design delle icone <strong>Design</strong> sezione. Personalizzare <strong>Stile</strong>, <strong>Pulsante Dimensione</strong> e <strong>Colori</strong> delle icone. Impostato <strong>Angoli Arrotondati</strong> parametri e <strong>nomi nascondere rete</strong> se necessario.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-12.jpg" alt=""></figure>
</p>
<p>Ogni volta che si è fatto con  <strong>Condividi Bottoni</strong> personalizzazione, stampa <strong>Salva&amp;Continua</strong> per passare alla fase successiva.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-13.jpg" alt=""></figure>
</p>
<p>Copiare il profilo ID nel &quot;<strong>1</strong>&quot; sezione. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-14.jpg" alt=""></figure>
</p>
<p>È necessario inserire al <strong>Profilo ID</strong> campo <strong>Contenuto - TM AddThis</strong> finestra delle impostazioni per rendere il nuovo <strong>Condividi Bottoni </strong> apparirà sul tuo sito.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-15.jpg" alt=""></figure>
</p>
<p>Hai e l'opzione di escludere le categorie [pages] in cui non si desidera posizionare il plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-16.jpg" alt=""></figure>
</p>
<p>Assicurarsi di avere <strong>abilitato </strong>il plugin.</p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-17.jpg" alt=""></figure>
</p>
<p>Salvare le modifiche e aggiornare il <strong>Condividi Bottoni</strong> pagina. </p>
<p>
  <figure class="img-polaroid"><img src="img/addthis-18.jpg" alt=""></figure>
