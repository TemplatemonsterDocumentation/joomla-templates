<h3>Image Swoop</h3>

<p>Il modulo "Image Swoop" è uno slider sorprendente e dinamico,sviluppato come
    combinazione tra il modulo predefinito Joomla “Articles Newsflash” e il plugin
    gratuito e open source jQuery “Camera Slideshow”. Funzione perfettamente su desktop
    e dispositivi mobiliiOs o Android. Compatibile con tutti i moderni browser a partire
    da IE8.</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>Il modulo "Image Swoop" possiede varie opzioni di configurazone, alle quali è
    possibile accedere aprendo il modulo Image Swoop da <b>Estensioni>Gestione Moduli
        (Extensions > Module Manager)</b> del pannello di amministrazione Joomla. Tutte
    le principali opzioni del plugin jQuery di "Camera Slideshow" possono essere
    configurate nell’area delle impostazioni modulo.</p>

<p>Muovere il cursore del mouse sopra il titolo dell’opzione per vederne la descrizione.
    Per scoprire di più sulle opzioni del plugin jQuery Camera Slideshow visitate il <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank">sito
        ufficiale</a></p>

