<h3>Articles Single</h3>

<p><strong>Articles Single</strong> è un modulo che permette di visualizzare un solo articolo nella posizione desiderata: </p>

<figure class="img-polaroid">
    <img src="img/articles-single-1.jpg" alt="">
</figure>

<p align="justify">L'unica impostazione insolita del modulo è <b>Link Personalizzato</b>. Esso consente di visualizzare un collegamento personalizzato sotto il contenuto del modulo. È possibile collegarlo a un URL esterno o per una voce di menu del vostro sito Joomla:</p>

<p align="justify">
  <figure class="img-polaroid">
    <img src="img/articles-single-2.jpg" alt="">
  </figure>
</p>
<p align="justify">Se si desidera cambiare il design della &quot;<strong>COME LAVORIAMO</strong>&quot; sezione, navigare <strong>Estensioni</strong> &gt; <strong>Moduli</strong>. Quindi scegliere &quot;<strong>Come lavoriamo</strong>&quot; modulo, che è appartengono <strong>Articoli - Singolo</strong> tipo.</p>
 <p>
   <figure class="img-polaroid">
     <img src="img/articles-single-4.jpg" alt="">
   </figure>
 </p>
 <p>Il <strong>Single Article</strong> modulo ha le seguenti impostazioni:</p>
 <p align="justify"><strong>Seleziona Articolo.</strong> Questo campo contiene l'articolo per essere mostrato in questo layout. premi il <strong>Scegli/Cambia</strong> per aprire la finestra di selezione articolo, come illustrato di seguito.</p>
 <figure class="img-polaroid">
     <p><img src="img/articles-single-5.jpg" alt="">
     </p>
</figure>
<p align="justify"> Questa schermata è simile alla <strong>Articolo Manager</strong>. È possibile utilizzare il <strong>Campi filtro</strong> per trovare l'articolo desiderato e poi clicca sul articolo di <strong>Titolo</strong> per selezionarla. A quel punto, la finestra modale si chiude e il titolo verrà visualizzato nella <strong>Selezionare il campo Articolo</strong>.</p>
     <p align="justify">Ora avete l'opportunità di modificare l'articolo. Per modificare l'articolo clicca il<strong> Modifica</strong> pulsante e vedrete la <strong>
Modifica Articolo </strong>finestra. Qui si ha la possibilità di modificare il contenuto dell'articolo e la sua immagine e collegamenti.</p>
<figure class="img-polaroid">
     <p><img src="img/articles-single-7.jpg" alt="">
     </p>
</figure>
     <p align="justify">Il <strong>Articolo Singolo</strong> ha le seguenti <strong>Opzioni Articolo</strong> [Opzioni Linguetta], come mostrato di seguito. Queste opzioni determinano come l'articolo mostrerà sul layout Front End del sito. Si noti che queste opzioni includono &quot;<strong>Usa globali</strong>&quot;. Se questa opzione è selezionata, verrà utilizzata l'impostazione dalle opzioni articolo Manager.</p>
    <figure class="img-polaroid">
     <p><img src="img/articles-single-8.jpg" alt="">
     </p></figure>
