<h3>Articles Single</h3>

<p>Singoli Articoli è un modulo che permette di collocare un singolo articolo nella
    posizione desiderata.</p>

    <figure class="img-polaroid"><img src="img/articles-single-1.jpg" alt=""></figure>


<p>L’unica opzione degna di approfondimento in questo modulo è <b>Personalizza Link
        (Custom Link)</b>. Permette di visualizzare il link da personalizzare sotto il
    contenuto del modulo. Può essere collegato a un URL esternoo ad alcuni elementi del
    menù Joomla.</p>
