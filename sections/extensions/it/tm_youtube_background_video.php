<h3>TM YouTube background video module</h3>

<p><strong>TM YouTube background video</strong> è un modulo per l'aggiunta di uno sfondo video di youtube al tuo sito web.</p>
<figure class="img-polaroid">
    <img src="img/background-video.jpg" alt="">
</figure>
<h5>
  <p>Al fine di configurare il modulo video di sfondo TM YouTube, effettuare le seguenti operazioni:</p></h5>
<ol class="marked-list">
<li><p>Navigare verso <strong>Estensioni</strong> &gt; <strong>Moduli</strong> linguetta:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Successivo è necessario selezionare la <strong>TM YouTube background video</strong> Modulo da modificare.</p>
      </li>
<div class="alert alert-warning">
 <div align="justify"><strong>Mancia:</strong> se si desidera trovare tutte <strong>TM YouTube background video</strong> moduli, usa i filtri per semplificare il processo. Premi &quot;<strong>Strumento di ricerca</strong>&quot; e selezionare il &quot;<strong>TM YouTube background video</strong>&quot; digitare il &quot;<strong>- Seleziona tipo -</strong>&quot; elenco a discesa filtro. </div>
  <figure class="img-polaroid">
<p> <img src="img/background-video-1.jpg" alt=""></p></figure></div>
<div class="alert alert-info">
  <p>È possibile fare clic &quot;<strong>Nuovo</strong>&quot; Pulsante per creare un nuovo modulo.</p>
<figure class="img-polaroid">
    <p> <img src="img/background-video-3.jpg" alt="">
</p></figure></div>
      <li>Per aprire le impostazioni del modulo di modifica necessario, fare clic sul suo titolo. [Modifichiamo <strong>Home youtube</strong> modulo.]</li>
<li>
  <div align="justify">Il <strong>Home youtube </strong>interfaccia di editing del modulo si apre di fronte a voi. </div>
  <figure class="img-polaroid">
    <p> <img src="img/background-video-2.jpg" alt=""></p></figure>
<li>Premi il <strong>Articolo</strong> per aprire la finestra modale selezione degli articoli, come illustrato di seguito. È possibile utilizzare il filtro per semplificare il processo di ricerca.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>

<div class="alert alert-warning">
  <p>Per aggiungere un nuovo utilizzo articolo <strong>Soddisfare</strong> &gt; <strong>Articoli</strong> &gt; <strong>Aggiungi Nuovo Articolo</strong>.</p>
  <figure class="img-polaroid">
    <img src="img/articles-single-9.jpg" alt="">
  </figure>
</div>
<li>
    <p>Qui si possono trovare <strong>Scheda Opzioni</strong>, dove sarà in grado di cambiare:</p>
  <ol class="marked-list">
      <li>
        <p><strong> Preparare Contenuti</strong> – opzionalmente preparare il contenuto con il contenuto Joomla Plugin. </p>
      </li>
      <li>
        <p><strong>Inserisci un URL di YouTube *</strong> – permette di specificare URL personalizzato per il video da YouTube che deve essere visibile sulla pagina.</p>
      </li>
      <li>
        <p><strong>Inizia il video</strong> – il video parte dai secondi specificati.</p>
      </li>
      <li>
        <p><strong>Muto</strong> – modificare video in modalità silenziosa o rumorosa.</p>
      </li>
      <li><strong>Autoplay</strong> – il video si avvia automaticamente, una volta che la pagina web viene caricata.</li>
      <li><strong>Mostra play/pause pulsante</strong> – you can either show or hide the buttons.</li>
      <li> <strong>Play/Pause pulsante testo</strong> – specificare il testo i pulsanti.</li>
      <li>
        <p><strong>Immagine mobile</strong> – mostrare questa immagine su dispositivi mobili, invece di un video. [Per caricare le immagini vanno al <strong>Soddisfare </strong>&gt; <strong>Media</strong> sezione.]</p>
        <figure class="img-polaroid">
    <p> <img src="img/background-video-4.jpg" alt=""></p></figure>
      </li>
  </ol>
  <li>
    <p>Al fine di modificare il video, semplicemente <strong>sostituire l'URL del video</strong> con precedentemente copiato <strong>collegamento video youtube</strong>.</p>
  </li>
  <li> </p>
<div align="justify">Scorrere la pagina su e fare clic sul verde <strong>Salva</strong> pulsante in alto a sinistra dello schermo per applicare le modifiche:</div>
<figure class="img-polaroid">
<p><img src="img/contact-form-12.png" alt=""></p></figure></li>
  <li>
    <p>Torna al tuo sito web e aggiornare la pagina con il testo che è stato modificato. Ora possiamo vedere che la modifica è stata eseguita correttamente.</p>
  </li>
  </ol>


<p align="justify" class="alert alert-warning">Nei nostri modelli <strong>Video di sfondo TM Youtube</strong> contenuto è presentato caricando determinato modulo o posizione, per esempio, "{loadmodule articles_single, About us}". Al fine di evitare l'errore 404 sul tuo sito web, si prega di assicurarsi che il nome del modulo caricato identico a quello, specifico per il carico. In questo caso, il modulo "Su di noi" dovrebbe essere pubblicato. Il nome del modulo non deve contenere più di uno spazio.
    È possibile saperne di più su loadmodule/loadposition al <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">sito ufficiale</a>.
</p>
