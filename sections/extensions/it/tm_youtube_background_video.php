<p class="alert alert-warning">
Nella maggior parte in nostri modelli, il modulo contenuti TM Youtube video di sfondo è rappresentato come un modulo di connessione che ha delle posizione che possono essere impostate e/o modificate, ad esempio, "{loadmodule articles_single, About us}". Per evitare l'errore 404 on-line, assicurati che il nome del plugin coincide con quello indicato al momento della connessione. In questo caso, il modulo "About us" deve essere nelle condizioni published e non deve contenere  più di uno spazio nel nome.
    È possibile ottenere informazioni dettagliate, su loadmodule/loadposition su <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">questo sito</a>.
</p>
