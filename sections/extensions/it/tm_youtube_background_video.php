<h3>Modulo TM Youtube background video</h3>

<p>TM Youtube background video - modulo per aggiungere video di sfondo sul sito youtube.</p>

<h6>Sono disponibili nel modulo tutte le impostazioni e le opzioni necessarie:</h6>

<ol class="index-list">
    <li>Un link al video da youtube;</li>
    <li>Impostazioni di riproduzione video;</li>
    <li>Impostazioni per cellulari;</li>
    <li>Impostare il tasto play/stop per il video.</li>
</ol>
