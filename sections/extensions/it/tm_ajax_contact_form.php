<h3>Modulo Contatti TM Ajax</h3>


<p>Il Modulo Contatti TM Ajax è un modulo contatti per il motore Joomla. Questo ti
    permette di aggiungere un modulo contatti su qualunque pagina Joomla.</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>

<p> L'invio dei
    dati è effettuato utilizzando richieste AJAX per cui la pagina non viene ricaricata.
    Le varie impostazioni ti permettono di aggiungere sistemi di protezione contro i bot
    (reCaptcha), definire i campi del modulo e le loro etichette, impostare l'indirizzo
    email del destinatario, aggiungere indirizzi per invio in CC, e definire messaggi di
    errore e di operazione riuscita. Puoi leggere l'intera lista delle impostazioni qui
    di seguito.</p>


