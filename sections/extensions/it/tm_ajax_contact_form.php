<h3>Modulo TM Ajax Contact Form</h3>

<p>Modulo TM Ajax Contact Form - questo modulo di contatto per il motore di Joomla.
 Con questo modulo è possibile aggiungere un modulo di contatto per ogni pagina del tuo sito di Joomla:</p>

<figure class="img-polaroid">
    <img src="img/contact-form-1.png" alt="">
</figure>

<p>I dati vengono trasmessi tramite richieste AJAX, in modo che la pagina non viene riavviata.
   Impostazioni sito consentono di attivare la protezione contro i bot (Captcha), aggiungere i campi richiesti, i loro nomi e specificare l'indirizzo e-mail per recapitare i messaggi, così come il testo dei messaggi inviati con successo e anche dei messaggio inviati con errore. È possibile trovare un elenco completo delle impostazioni riportate di seguito:
</p>

<figure class="img-polaroid">
    <img src="img/contact-form-2.png" alt="">
</figure>
<p>Le impostazioni di questo modulo consente di gestire tutte le funzioni del modulo di contatto. È possibile includere qualsiasi tipo di campo (di testo, e-mail, caselle di controllo, il testo della sezione). A causa di questo, il modulo di contatto può essere personalizzato in base alle proprie esigenze:</p>
