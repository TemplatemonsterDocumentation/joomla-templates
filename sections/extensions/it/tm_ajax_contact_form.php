<h3>TM Ajax Contact Form Module</h3>

<p align="justify"><strong>TM Ajax Contact Form Module</strong> è un modulo modulo per motore Joomla.
Questo modulo permette di aggiungere modulo di contatto per qualsiasi pagina del tuo sito Joomla.</p>
<p align="justify"><strong>Contact form </strong>è fondamentalmente una serie di domande compilati sulla pagina web per il visitatore che viene inviato automaticamente al tuo indirizzo email quando viene compilato. Di solito un modulo di contatto include campi per la compilazione nome, indirizzo, telefono, e-mail e il tipo di commento. Una volta che i visitatori del tuo sito web hanno compilato i campi del modulo di contatto, si clicca sul pulsante Invia [Invio] per inviare le informazioni.</p>
