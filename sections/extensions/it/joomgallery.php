<h3>JoomGallery</h3>

<p align="justify"><strong>JoomGallery</strong> è un componente che permette di creare una galleria fotografica completa sulla base di Joomla! Sentitevi liberi di controllare più dettagli al <a href="http://www.en.joomgallery.net/documentation/" target="_blank"
       rel="nofollow">sito ufficiale</a>. </p>

<figure class="img-polaroid">
  <img src="img/gallery-6.jpg" alt="">
</p></figure>
  <h5><p>Categoria Creazione</p></h5>

<p>Vai a <strong>Componenti</strong> &gt; <strong>JoomGallery</strong> &gt; <strong>Manager di Categoria</strong>. </p>

  <figure class="img-polaroid">
    <img src="img/gallery-7.jpg" alt="">
    </p>
