<h5><p>Proviamo <strong>Immagine Upload</strong>. </p></h5>
<figure class="img-polaroid">
  <img src="img/gallery-15.jpg" alt="">
</p></figure>
<ol class="marked-list">
  <li>
    <div align="justify">Premi <strong>Navigare</strong> pulsante nella <strong>Selezione immagine</strong> sezione e selezionare i file multimediali presenti sul computer.</div>
  </li>
  <li>
    <div align="justify">Specificare <strong>Categoria</strong> per queste immagini [è possibile caricare gruppo di immagini per ogni categoria in questo modo], <strong>Titolo Generico</strong> per queste immagini [per esempio, se si carica 3 file con titolo generico &quot;test&quot;, essi appariranno nella vostra immagine manager come test_1, test_2 e test_3]. </div>
  </li>
  <li>
    <div align="justify">Specificare <strong>Descrizione Generico</strong> e <strong>Autore</strong> se necessario. Impostare il <strong>Accesso</strong> Tipo: pubblico, super utenti, registrati, speciale.</div>
  </li>
  <li>
    <div align="justify">Usa il <strong>File Immagine Speciale</strong> opzione. Questa opzione permette l'upload di file di immagini speciali. Unicamente scegliere questa opzione nel caso in cui il caricamento è costituito esclusivamente da .png animato o trasparente o file .gif. Tenete a mente che i file non vengono ridimensionate e, pertanto, vengono visualizzati a schermo intero in vista di dettaglio.</div>
  </li>
  <li>
    <div align="justify"><strong>Modalità di Debug</strong>. Con attivato <strong>modalità di debug</strong> verranno mostrate tutte le fasi di lavorazione. In caso di errori sono sempre visualizzati.</div>
  </li>
</ol>
<div class="alert alert-danger">
  <div align="justify">
    <p>Cliccate su<strong> Caricare</strong> pulsante in basso dopo il processo di configurazione è completata.</p>
</div></div>
<h5><p>Modifica oggetto Galleria esistente</p></h5>
<div align="justify">Vai a <strong>Componenti</strong> > <strong>JoomGallery</strong> > <strong>Immagine Manager</strong> sezione. Qui potete trovare l'elenco delle immagini che sono state già caricate, in modo da poter modificare quelle esistenti: </div>
<p>
  <figure class="img-polaroid">
    <img src="img/gallery-16.jpg" alt="">
    
  </figure>
</p>
<p>Nel caso in cui, è necessario<strong> modificare</strong> loro, è necessario eseguire le seguenti: </p>
<ol class="marked-list">
  <li>Aprire qualsiasi elemento dall'elenco.</li>
  <li>Modificare<strong> Titolo</strong>, <strong>Categoria</strong>, <strong>Descrizione</strong>, <strong>Colpi</strong>, ecc. nel<strong> Immagine</strong> etichetta. Scorrere verso il basso per controllare il <strong>Thumbnail anteprima</strong> e <strong>anteprima immagine</strong>. </li>
  <li>
    <div align="justify">Usa <strong>Nascosto</strong> opzione. Con nasconde questa immagine non verrà visualizzata nella visualizzazione per categorie o riconosciuto dalla ricerca o le toplists più, ma sarà ancora disponibile tramite collegamento diretto. Se non si vuole che sia disponibile si prega di annullare la pubblicazione invece. Nascondendo le immagini è possibile usarli come categoria miniature senza mostrare loro nella categoria corrispondente, per esempio.</div>
  </li>
  <li>
    <div align="justify"><strong>In primo piano </strong>opzione. Qui è possibile contrassegnare un'immagine come 'vetrina'. Ad esempio, i JoomImages modulo può essere configurato per visualizzare solo le immagini in primo piano in una posizione modulo.</div>
  </li>

