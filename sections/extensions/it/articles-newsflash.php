<h3>Articles Newsflash (Advanced)</h3>

<p>Il modulo Articles Newsflash (Advanced) è una versione migliorata del componente di sottoscrizione
    per Joomla al rispetto del modulo standard Joomla Articles Newsflash. Oltre alle funzioni originali
    del modulo che offre le opzioni per i modelli che sono creati sulla base di Bootstrap:</p>

<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Le funzionalità aggiuntive</h4>

<p><b>Number of columns (La quantità delle colonne)</b> - fornisce la possibilità di visualizzare degli 
    articoli in più di una colonna.</p>

<p><b>Custom Link (Il link dell'utilizzatore)</b> - fornisce la possibilità di configurare un link
    sotto i contenuti del modulo stesso. Il link può essere puntato verso una risorse e/o pagina esterna
    del sito web di Joomla.</p>

<p><b>Pretext (Anteprima del testo)</b> - esso consente di visualizzare il testo personalizzato
    prima del contenuto del modulo.</p>

<p><b>Published Date (La Data di Pubblicazione)</b> - visualizza la data di pubblicazione del articolo. </p>

<p><b>Created by (Autore)</b> - visualizza gli articoli dell'autore.</p>

<p><b>Show intro images (Visualizzare l'immagine dell'articolo) </b> - permette di visualizzare
    le immagini degli articoli.</p>

<p><b>Intro image align (Allineamento dell'immagine dell'articolo) </b> - permette di allineare
    l'immagine dell'articolo.</p>

