<h3>Swiper</h3>

<p>Modulo Swiper - questo è un grande cursore reattivo, che è l'integrazione del
    modulo standard Joomla Articles Newsflash del plugin gratuito jQuery Swiper. Progettato principalmente per IOS, questo plugin funziona perfettamente e anche sull'ultimo Android, Windows Phone 8 e nei browser moderni su diversi PC:</p>

<figure class="img-polaroid">
    <img src="img/swiper.png" alt="">
</figure>

<p>Modulo Swiper contieni una serie di impostatsioni e si trova nel menù <b>Extensions
        (Estensione) > Module Manager (Modulo Manager)</b> admin panel di Joomla. Tutte
    le opzioni principali del plugin jQuery Swiper si cambiano nelle impostazioni del modulo. </p>

<p>Posizionare il cursore del mouse su impostazioni, in modo da vedere la sua descrizione. Maggiorni dettagli
    è le informazioni del plugin jQuery Swiper puoi trovare su <a
        href="http://idangero.us/swiper/" target="_blank">sito
        ufficiale</a>.</p>
<p>Tutte le impostazioni necessarie sono disponibili nelle impostazioni del modulo carosello:</p>

<ol class="index-list">
    <li>Impostazioni di riproduzione;</li>
    <li>Regolazione in altezza;</li>
    <li>Elementi visibili;</li>
    <li>Impostazioni di animazione;</li>
    <li>Navigazione e controllo;</li>
    <li>Opzioni di anteprima.</li>
</ol>

