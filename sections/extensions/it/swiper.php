<h3>Swiper</h3>

<p align="justify"><strong>Swiper Modulo</strong> è un meraviglioso dispositivo di scorrimento adattiva che è un'integrazione di modulo Newsflash e senza plugin per jQuery Swiper articoli standard di Joomla. Creato per IOS, questo plugin funziona perfettamente nella più recente di Android, Phone 8 e desktop browser per PC di Windows.</p>
<p align="justify">Cerchiamo di controllare quali tipi di scivoli possiamo aggiungere al cursore sito utilizzando questo modulo. Prima di tutto, potrebbe essere una diapositiva regolare animata con un'immagine e una didascalia su di esso:</p>

<figure class="img-polaroid">
    <img src="img/swiper-1.jpg" alt="">
</figure>
<p align="justify">Ma può essere anche una diapositiva con il video di sfondo o una diapositiva configurabile con lo sfondo dell'immagine di parallasse. In realtà un vasto di opzioni tra cui scegliere. Andiamo a imparare ad usarli.</p>

