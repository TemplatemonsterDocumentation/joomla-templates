<p>On <strong>Opzioni avanzate</strong> – Si può dare la <strong>modulo un suffisso di classe</strong> se desiderio.</p>
<p align="justify">Poi si sceglie la pagina dove posizionare il modulo. Si consiglia di utilizzare il <strong>Menu Assegnazione </strong>scheda per questo scopo. </p>
<p align="justify">In questa scheda v'è un elenco di voci di menu. Si può scegliere tra in tutte le pagine, senza pagine, solo sulle pagine selezionate e Su tutte le pagine tranne quelli selezionati dalla lista. Se vengono selezionate le ultime due opzioni un elenco mostrerà con tutte le voci di menu, come mostrato nello screenshot qui sopra. Questo consente di assegnare i moduli per alcuni, ma non tutte le pagine, e selezionando il link del menu che si desidera che il modulo associato è possibile personalizzare su quali pagine moduli apparire/non appaiono.</p>
