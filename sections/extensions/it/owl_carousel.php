<h3>Owl Carousel</h3>

<p>Owl Carousel rappresenta modulo immagini giostra, progettato sulla base di
    modulo standard Joomla Articles Newsflash e plugin jQuery Owl Carousel:</p>

<figure class="img-polaroid">
    <img src="img/owl-carousel.png" alt="">
</figure>

<h6>Sono disponibili nelle impostazioni del modulo carosello tutte le opzioni necessarie: </h6>

<ol class="index-list">
    <li>La larghezza dell'elemento;</li>
    <li>Elementi visibili;</li>
    <li>Impostazioni di animazione;</li>
    <li>Navigazione e Controllo.</li>
</ol>


