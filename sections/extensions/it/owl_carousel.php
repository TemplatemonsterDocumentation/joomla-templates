<h3>Owl Carousel</h3>
<p><strong>Owl Carousel </strong>modulo è una combinazione di default di Joomla articoli modulo Newsflash e plug-Gufo Carousel jQuery.</p>
<figure class="img-polaroid">
  <img src="img/owl-carousel-1.jpg" alt="">
</figure>
<h5>Come Accedere</h5>
<p>Per 'aggiungere' una nuova <strong>Owl Carousel</strong> modulo, passare alla Gestione moduli:</p>
<p>Seleziona <strong>Estensioni</strong> &gt; <strong>Moduli</strong>.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-2.jpg" alt="">
</figure>
<p>Click in '<strong>Nuovo</strong>' tasto e cliccare su <strong>Owl Carousel</strong> nella finestra popup modale.</p>
<figure class="img-polaroid">
    <img src="img/owl-carousel-3.jpg" alt="">
</figure>
<h5>Dettagli</h5>
<ol class="marked-list">
  <li><strong>Titolo:</strong> Modulo deve avere un titolo [nel nostro caso è <strong>Carousel</strong>].</li>
</ol>
<h5>Modulo</h5>
<p>The <strong>Owl Carousel </strong> Permette di mostrare un numero fisso di articoli da una categoria o di una serie di categorie specifiche.</p>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Categoria</strong>: Selezionare gli articoli da una Categoria o di un insieme di categorie specifiche. Se nessuna selezione mostrerà tutte le categorie come predefinito.</div>
  </li>
  <li><strong>Immagini</strong>: [Mostra/Nascondi]. Immagini Visualizza Articolo.</li>
  <li><strong>Titolo dell'articolo</strong>: Mostra/Nascondi Titolo dell'articolo.</li>
  <li><strong>Titoli collegati</strong>: [Use Global/No/Yes]. Collegare i titoli Articolo agli articoli.</li>
  <li> <strong>Data di Pubblicazione</strong>: Visualizzazione data di pubblicazione di un articolo.</li>
  <li><strong>Livello Header</strong>: [h1/h2/h3/h4/h5]. Selezionare il livello di intestazione HTML desiderato per l'articolo titoli.</li>
  <li><strong>Mostra le immagini intro</strong>: Selezionare se si desidera visualizzare le immagini intro articolo.</li>
  <li><strong>Intro allineamento immagine</strong>: [Mone/sinistra/destra] Selezionare l'allineamento intro image. impostazioni articolo sostituzioni.</li>
  <li>
    <div align="justify"><strong>'Leggi di più...' Link:</strong> [Mostra/Nascondi]. Se impostato su mostra, il 'Leggi di più...' collegamento mostrerà se il testo principale è stato previsto un articolo.</div>
  </li>
  <li><strong>Numero di articoli</strong>: Il numero di articoli da visualizzare all'interno di questo modulo.</li>
  <li>
    <div align="justify"><strong>Ordine Risultati</strong>: [Data pubblicazione/Data di creazione/Ordinazione/a caso]. Selezionare l'ordine in cui si desidera i risultati della query presentato.</div>
  </li>
  <li><strong>Pretext</strong>: È possibile inserire il testo che si desidera visualizzare prima del contenuto del modulo. Non supporta alcun tag HTML.</li>
</ol>
<h5>Dettagli comuni</h5>
<ol class="marked-list">
  <li>
    <div align="justify"><strong>Mostra Titolo</strong>. [Mostra/Nascondi] Scegliere se mostrare o nascondere il titolo moduli nel front-end. Il titolo sarà quello nel campo del modulo di cui sopra.</div>
  </li>
  <li>
    <div align="justify"><strong>Modulo Posizione</strong>. Scegliere la posizione del modulo che si desidera questo modulo da visualizzare in. Una posizione del modulo personalizzato può essere immesso al consumo con il plugin posizione del carico o il pulsante di posizione può essere premuto per selezionare una posizione modulo dal modello.</div>
  </li>
  <li><strong>Stato</strong>. [Pubblicato/Inedito/Trashed] Lo stato pubblicato dell'elemento.</li>

  <li><strong>Accesso</strong>. Il livello di accesso di visualizzazione per questo articolo.</li>

  <li>
    <div align="justify"><strong>Modulo Ordinazione</strong>. Questo dimostra una discesa di ogni modulo nella posizione che il modulo corrente è in. Questo è l'ordine che i moduli verranno visualizzati quando visualizzato sul nell'estremità anteriore così come nella gestione dei moduli.</div>
  </li>

  <li>
    <div align="justify"><strong>Inizio Pubblicazione</strong>. Data e ora di iniziare a pubblicare. Utilizzare questo campo se si vuole inserire il contenuto in anticipo e poi lo hanno pubblicato automaticamente in un tempo futuro.</div>
  </li>

  <li>
    <div align="justify"><strong>Finire Pubblicazione</strong>. Data e ora per finire pubblicazione. Utilizzare questo campo se si vuole avere il contenuto cambia automaticamente allo stato Inedito in un tempo futuro [per esempio, quando non è più applicabile].</div>
  </li>

  <li><strong>Lingua</strong>. Articolo lingua.</li>

  <li>
    <div align="justify"><strong>Nota</strong>. Nota Articolo. Questo è normalmente per l'utilizzo del amministratore del sito [ad esempio, per documentare le informazioni su questo articolo] e non mostra nel front-end del sito.</div>
  </li>
</ol>
