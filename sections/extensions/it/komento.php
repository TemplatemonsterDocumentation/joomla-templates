<h3>Komento Comments</h3>

<p><strong>Komento</strong> è un commento un'estensione avanzate di Joomla che permette ai visitatori del tuo sito di commentare gli articoli e blog.</p>
<figure class="img-polaroid"><img src="img/komento-1.jpg" alt=""></figure>

<p>Komento Comments può essere valutato ai sensi <b>Сomponenti > Komento</b> scheda del tuo pannello di amministrazione di Joomla.</p>
<p>
  <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
</p>
<h5>
  <p>Come gestire le integrazioni dei componenti Komento</p></h5>
<ol class="marked-list">
  <li>
    <p>Accedi al tuo pannello di amministrazione di Joomla, vai a <strong>Сomponenti</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrazioni</strong>. </p>
  </li>
<figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  <li><p>Clicca su <strong>Joomla Articoli</strong>. </p></li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p align="justify">Nella scheda Generale è possibile <strong>Abilitare</strong> o <strong>Disattivare</strong> Commenti. È inoltre possibile modificare <strong>Moderazione</strong> e 
altre impostazioni.</p>
  </li>

    <figure class="img-polaroid"><img src="img/komento-5.jpg" alt=""></figure>

  <li><p>Abbiamo <strong>disabilitato</strong> [No] commenti [controllare lo screenshot qui sotto].</p></li>

    <figure class="img-polaroid"><img src="img/komento-6.jpg" alt=""></figure>
  
 <li> <p>Il blocco di commenti verrà disabilitato sul sito web.</p></li>
  <p><strong>Abilitato</strong>:</p>
 <figure class="img-polaroid"><img src="img/komento-8.jpg" alt=""></figure>
  <p><strong>Disabilitato</strong>:</p>
<figure class="img-polaroid"><img src="img/komento-7.jpg" alt=""></figure>
  <li>
    <p align="justify">Abbiamo <strong>abilitato </strong>commenti indietro. È possibile assegnare Komento a tutte le categorie o selezionare particolari categorie.</p>
  </li>
  <li>Abbiamo assegnato commenti a &ldquo;<strong>Azienda</strong>&rdquo;.</li>
  <figure class="img-polaroid"><img src="img/komento-9.jpg" alt=""></figure>
  <li>
    <p align="justify">Puoi anche <strong>disattivare</strong> commenti per un particolare articolo. Aprire l'articolo del <strong>Gestione articoli [Content &gt; Articoli]</strong> e inserire il seguente codice al corpo dell'articolo:</p>
    <pre>{KomentoDisable}</pre>
  </li>
  <figure class="img-polaroid"><img src="img/komento-10.jpg" alt=""></figure>
  <li>
    <p>Salva I Cambiamenti. Commenti non appaiono su questo articolo.</p>
  </li>
</ol>
<h5><p>Come modificare Komento &quot;Termini e Condizioni&quot;</p></h5>
  <p align="justify">Komento <strong>Termini e Condizioni</strong> testo viene visualizzato sulle pagine singolo articolo. I visitatori del sito devono essere d'accordo con questi termini, al fine di procedere alla presentazione dei loro feedback se questa opzione è attivata. Si prega di fare riferimento alla schermata qui sotto:</p>
<figure class="img-polaroid"><img src="img/komento-11.jpg" alt=""></figure>
</h5>
<p>Si prega di effettuare le seguenti operazioni per modificare il testo:</p>
<ol class="marked-list">
  <li>
    <p>Aprire il pannello di amministrazione di Joomla e spostarsi <strong>Componenti</strong> &gt; <strong>Komento</strong> &gt; <strong>Integrazioni</strong> menu:</p>
    <figure class="img-polaroid"><img src="img/komento-3.jpg" alt=""></figure>
  </li>
  <li>
    <p>Apri &ldquo;<strong>Joomla Articolo</strong>&rdquo; componente. </p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-4.jpg" alt=""></figure>
  <li>
    <p>Poi passa a <strong>disposizione</strong> sezione e passare a<strong> commento Modulo</strong> etichetta:</p>
  </li>
   <figure class="img-polaroid"><img src="img/komento-12.jpg" alt=""></figure>
  <li>
    <p>In primo luogo, si prega di selezionare gruppo aveva bisogno di utenti in fondo alla pagina, a Abilita sezione Termini e Condizioni.</p>
  Per impostazione predefinita, è impostato su Guest, si può scegliere qualsiasi altro gruppo di utenti.
  <p>Permettiamo <strong>Termini e Condizioni</strong> per tutti i tipi degli utenti.</p>
  </li>
  <figure class="img-polaroid"><img src="img/komento-13.jpg" alt=""></figure>
  <li>
    <p>Poi si può cambiare il testo casella di controllo nella <strong>Termini e condizioni campo di testo</strong>. </p>
  Si prega di inserire il testo desiderato qui e salvare le modifiche premendo <strong>Salva</strong> pulsante nell'angolo in alto a sinistra.
  </li>
  <figure class="img-polaroid"><img src="img/komento-14.jpg" alt=""></figure>

<p>Il testo è stato cambiato con successo:</p>
<figure class="img-polaroid"><img src="img/komento-15.jpg" alt=""></figure>
</ol>
