<h3>CarouFredSel</h3>

<p>CarouFredSel è un carosello di immagini sviluppato come combinazione del modulo
    predefinito Joomla Articles Newsflash eil plugin jQueyCarouFredSel.</p>

<figure class="img-polaroid"><img src="img/caroufredsel-1.jpg" alt=""></figure>

<p>Tutte le opzioni necessarie del carosello sono disponibili
    all’internodelleimpostazioni del modulo.</p>

<ol class="index-list">
    <li>Direzione carosello (Carousel direction)</li>
    <li>Dimensione elementi (Item width)</li>
    <li>Elementi visibili (Visible items)</li>
    <li>Opzioni animazione (Animation options)</li>
    <li>Navigazione e controlli (Navigation and controls)</li>
</ol>


<p>Il plugin CarouFredSel ha molte altre <a
        href="http://caroufredsel.dev7studios.com/configuration.php" target="_blank">opzioni
        di configurazione</a>. Per aggiungere le vostre opzionimodifica il file <b>\modules\mod_caroufredsel\tmpl\default.php</b>.
</p>
