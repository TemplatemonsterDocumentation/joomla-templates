<h5>Trova il tuo widget di Twitter ID</h5>
<p align="justify">Se un widget di Twitter si sta tentando di abilitare chiede un widget di Twitter ID, è necessario creare un nuovo widget dal <a href="https://twitter.com/settings/widgets" onclick="__gaTracker('send', 'event', 'outbound-article', 'https://twitter.com/settings/widgets', 'Twitter widget settings');">impostazioni dei widget di Twitter</a> pagina.</p>
<p align="justify">Per accedere alle impostazioni di widget di Twitter, accedere al conto Twitter che si desidera utilizzare con il widget di. Quindi fare clic l'ingranaggio nell'angolo in alto a destra e fare clic su <strong>Impostazioni</strong>. Dalla barra laterale le impostazioni, scegliere il <strong>Widgets</strong> scheda nella parte inferiore.</p>
<p align="justify">In questa pagina, individuare il <strong>Crea nuovo</strong> e premere il tasto per creare un nuovo widget. Da qui, vi verrà offerto opzioni di configurazione e un'anteprima del widget. È sempre possibile modificare le opzioni di configurazione del widget in seguito.</p>
<p align="justify">Dopo aver creato il widget, il <strong>Crea nuovo</strong> pulsante verrà sostituito da un <strong>Salva I Cambiamenti</strong> pulsante. Ora che è stato creato il tuo widget, si può afferrare l'ID widget dalla URL. Guardate barra degli indirizzi del browser. Esso dovrebbe avere un URL che sembra qualcosa di simile:</p>
 <blockquote><p>https://twitter.com/settings/widgets/<strong>512253083833733120</strong>/edit?focus_textarea=1&amp;notice=WIDGET_CREATED</p> </blockquote>
<p>Il tuo widget di ID è il numero 18 cifre. In questo caso, <strong>512253083833733120</strong>. Ricordarsi di mantenere questo numero privato, quindi il tuo widget non verrà utilizzato da altri senza il vostro permesso.</p>
<p>Copiare il vostro ID e incollarlo nel campo di testo che chiede il vostro widget di ID. Dopo aver salvato le impostazioni, il nuovo widget di Twitter dovrebbe essere funzionale.</p>
