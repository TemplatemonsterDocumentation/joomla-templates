<h3>GoogleMap Plugin</h3>

<p><strong>GoogleMap</strong> is a plugin that allows  displaying Google map on your website:</p>

<figure class="img-polaroid">
    <img src="img/googlemap-1.jpg" alt="">
</figure>

<p align="justify"><strong>Per personalizzare il plugin andare a Estensioni &gt; Plugins. Questo vi porterà alla Plugin Mameger. Tipo &quot;Google&quot; nel campo di ricerca e fare clic sul sistema - Google Maps link del titolo.</strong></p>
<figure class="img-polaroid">
    <img src="img/googlemap-3.jpg" alt="">
</figure>
<p>Questo vi porterà alla pagina di configurazione principale del plugin GoogleMap. Sentitevi liberi di controllare più dettagli al<a
        href="http://tech.reumer.net/Google-Maps/Documentation-of-plugin-Googlemap/"
        target="_blank" rel="nofollow">sito ufficiale</a>.</p>
<p>
  <figure class="img-polaroid">
    <img src="img/googlemap-2.jpg" alt="">
  </figure>
</p>
