<h3>Plugin TM Facebook Page</h3>
 
<p>TM Facebook Page - modulo per visualizzare la tua pagina di Facebook sul tuo sito:</p>

<figure class="img-polaroid">
    <img src="img/facebook_feed-1.jpg" alt="">
</figure>

<p>Qui di seguito troverai il modulo e le sue impostazioni di base TM Facebook Page. Obbligatorio per
    riempire il campo Facebook App ID. Questo è un ID applicazione Facebook. Per poter
    ottenere l'ID, andare su <a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a>
    e crea un app per il tuo sito. È possibile ottenere informazioni dettagliate su altre impostazioni su <a href="https://developers.facebook.com/docs/plugins/page-plugin"
                       rel="nofollow" target="_blank">sito ufficiale</a>:</p>

