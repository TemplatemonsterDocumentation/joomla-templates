      <li> <strong>Bootstrap Dimensione</strong>. Un'opzione per specificare quante colonne utilizzerà il modulo.</li>
      <li> <strong>Colonne Compensazione</strong>. Aumentare il margine sinistro di una colonna selezionato numero di colonne.</li>
      <li> <strong>Header Tag</strong>. Il tag HTML per il modulo di header/titolo.</li>
      <li> <strong>Header Classe</strong>. La classe CSS per il modulo di header/titolo.</li>
      <li> <strong>Modulo Stile</strong>. Utilizzare questa opzione per modificare il modello di stile per la sua posizione.</li>
    </ol>
   