<h3>TM Facebook Page Plugin</h3>
 
<p><strong>TM Facebook Pagina</strong> è un modulo che permette di visualizzare la vostra pagina di Facebook sul sito:</p>

<figure class="img-polaroid">
    <p><img src="img/facebook_feed-1.jpg" alt="">
    </p></figure>
    <p>Per configurare il<strong>TM Facebook Pagina Plugin</strong> Modulo per navigare <strong>Estensioni</strong> &gt; <strong>Moduli</strong>. </p>
<figure class="img-polaroid">
    <p><img src="img/facebook_feed-3.jpg" alt="">
    </p></figure>
    <p align="justify">Premere &quot;<strong>Strumento di Ricerca</strong>&quot; e selezionare il <strong>TM Facebook Pagina Plugin </strong>nel &quot;- Seleziona Tipo -&quot; filter menu a tendina. Si vedrà il <strong>Facebook Box</strong> Modulo.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-4.jpg" alt="">
</p></figure>
    <p>Clicca sul modulo di collegamento titolo per essere portato alla finestra delle impostazioni del modulo.</p>
 <figure class="img-polaroid">
    <p><img src="img/facebook_feed-5.jpg" alt=""></p></figure>
   <p>Nel <strong>Modulo</strong> scheda è possibile impostare le seguenti opzioni:</p>
    <ol class="marked-list">
      <li> <strong>Facebook App ID</strong> *.</li>
      <li> <strong>Facebook Pagina URL</strong>.</li>
      <li> <strong>Il nome della tua azienda</strong>.</li>
      <li> <strong>Larghezza</strong>. La larghezza in pixel del widget.</li>
      <li> <strong>Altezza</strong>. L'altezza dei pixel del widget. </li>
      <li> <strong>Visualizza Amico&rsquo;s Faccias</strong>. L'attivazione di questa opzione consente foto del profilo da mostrare nel modulo.</li>
      <li> <strong>Mostra Pagina Messaggi</strong>. Mostra prima i messaggi di linea temporale della pagina.</li>
      <li> <strong>Nascondi la Foto di Copertina</strong>. Nascondi la foto copertina nell'intestazione.</li>
    </ol>
<p align="justify">Il campo <strong>Facebook App ID</strong> è obbligatorio. Si tratta di una Facebook App Identifier. Al fine di ottenere l'identificatore, si prega di aprire il sito web:<a
        href="https://developers.facebook.com/" rel="nofollow" target="_blank">https://developers.facebook.com/</a> e creare un app per il tuo sito. </p>
<p align="justify">Basta seguire le semplici istruzioni, e avrete ottenuto l'identificatore in pochissimo tempo.</p>
<h5>Step-by-Step Guida</h5>
        <h5>1. Login per Facebook</h5>
        <p>Prima di creare un account di sviluppatore, effettuare il login al proprio account Facebook.</p>
<a href="https://www.facebook.com/login.php">Login per Facebook</a>
        <h5>2. Developer Account</h5>
        <p align="justify">Avrete bisogno di Facebook account di sviluppatore per iniziare. Se non si dispone di uno aggiornare il vostro account personale di Facebook per un account sviluppatore Facebook ora. Ignorare questo passaggio, se si dispone già di un account di sviluppatore.</p>
<a href="https://developers.facebook.com/async/onboarding/dialog/" rel="dialog">Crea Developer Account</a>
<h5>3. Crea nuovo Facebook app</h5>
        <p>Scegli <a href="https://developers.facebook.com/apps/"><strong>Apps</strong></a> nella navigazione di intestazione e selezionare <strong>Aggiungere una nuova app</strong> oppure utilizzare il pulsante qui sotto.</p>
<a href="https://developers.facebook.com/apps/async/create/platform-setup/dialog/" rel="dialog">Crea nuovo Facebook App</a>
   <h5>4. Scegli Piattaforma</h5>
        <p align="justify">Scegli la piattaforma che si desidera aggiungere. Se la vostra applicazione è su più piattaforme, sarete in grado di aggiungere al vostro app Facebook in seguito.</p>
        <figure class="img-polaroid">
    <p><img src="img/facebook_feed-6.jpg" alt="">
</p></figure>
    <h5>5. Scegli un Nome</h5>
    <p align="justify">Scegliere un nome per la vostra applicazione e selezionare <strong>Crea Nuovo Facebook App ID</strong>. Scegli <strong>categoria</strong> per la tua app. Se si sta creando una versione di prova di un app Facebook esistente, selezionare l'applicazione che sta testando.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-7.jpg" alt="">
</p></figure>
    <h5>6. Segui &quot;Avvio rapido&quot; Passi</h5>
    <p align="justify">La procedura guidata di avvio rapido vi aiuterà a configurare il Facebook app per la piattaforma selezionata. È possibile tornare alla procedura guidata di avvio rapido in un secondo momento se continuare la configurazione o aggiungere una nuova piattaforma per la vostra applicazione.</p>

<h5>7. App ID</h5>
    <p align="justify">La vostra applicazione avrà un ID applicazione univoco. Si utilizzerà questo ID ogni volta che si utilizza uno dei nostri <a href="https://developers.facebook.com/docs/apis-and-sdks">SDKs</a> o <a href="https://developers.facebook.com/docs/sharing/webmasters/">Open Graph tags per la condivisione</a>. Puoi trovare il tuo ID applicazione nella vostra <a href="https://developers.facebook.com/apps/">app's dashboard</a>.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-9.jpg" alt="">
</p></figure>
    <h5>8. Proteggi la tua App Segreto</h5>
    <p align="justify">Facebook genera automaticamente un segreto di app associato con l'applicazione. Il segreto applicazione autentica le richieste fatte da l'applicazione per server di Facebook e dovrebbe essere trattato come una password.</p>
<p align="justify">Non uscita l'applicazione segreta di Facebook nella tua pagina web markup; fare attenzione quando commettere file contenenti questo valore in un software di controllo del codice sorgente, ad esempio un Git pubblico o repository Subversion.</p>
<h5>9. Contatto Email</h5>
    <p align="justify">È necessario aggiungere un contatto e-mail per ogni app. Vai al tuo <a href="https://developers.facebook.com/apps/">app's dashboard</a> e selezionare la voce di navigazione <strong>Impostazioni</strong> nella barra laterale sinistra e fornire un contatto e-mail valido.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-10.jpg" alt="">
</p></figure>
    <h5>10. App Dettagli</h5>
    <p>Selezionare il Dettagli App nella barra laterale sinistra per visualizzare e aggiungere ulteriori informazioni sulla vostra applicazione.</p>
    <p align="justify">Se si desidera che la vostra applicazione per essere elencati nel centro di app, attivare l'impostazione Elencati piattaforma App Center per il tuo sito o il sito mobile. Aggiungere icone, immagini promozionali e video. Assicurati di seguire la <a href="https://developers.facebook.com/docs/games/appcenter/guidelines">App Center Guidelines</a> essere approvato della messa in vendita in App Center.</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-11.jpg" alt="">
</p></figure>
<h5>11. Disattivare la Modalità di Sviluppo</h5>
    <p align="justify">Nuove applicazioni di Facebook esistono in modalità di sviluppo di default. Questi limiti azioni pubblicate ed oggetti come amico codifica su Facebook linee temporali di proprietà di uno sviluppatore dell'applicazione correlati.</p>
<p align="justify">Una volta che l'applicazione è pronta per l'uso pubblico è possibile passare dalla modalità di sviluppo al modo pubblico per consentire la vostra applicazione di interagire con gli account di Facebook non connessi con la vostra applicazione.</p>
   <figure class="img-polaroid">
    <p><img src="img/facebook_feed-12.jpg" alt="">
</p></figure>
    <p>È anche possibile impostare le seguenti opzioni per il modulo al <strong>Modulo</strong> tab:</p>
    <figure class="img-polaroid">
    <p><img src="img/facebook_feed-13.jpg" alt="">
</p></figure>
<ol class="marked-list">
      <li> <strong>Mostra Titolo</strong>. È possibile mostrare o nascondere il titolo del modulo in mostra. Effetto dipenderà dallo stile di cromo nel modello.</li>
      <li>
        <div align="justify"> <strong>Posizione</strong>. È possibile selezionare una posizione di modulo dalla lista delle posizioni predefinite o immettere la propria posizione modulo digitando il nome nel campo e premendo Invio.</div>
      </li>
      <li><strong>Stato</strong>. Se pubblicato, questo modulo verrà visualizzato sul tuo sito frontend o di backend a seconda del modulo.</li>
      <li> Data per la pubblicazione [<strong>Inizio Pubblicazione</strong>/<strong>Finire Pubblicazione</strong>] Una data opzionale Start / Finish Publishing modulo.</li>
      <li> <strong>Accesso</strong>. Il gruppo di livello di accesso che è autorizzato a visualizzare questa voce.</li>
      <li> <strong>Ordinazione</strong>. Selezionare l'ordinamento.</li>
      <li> <strong>Lingua</strong>. Assegnare una lingua a questo modulo.</li>
    </ol>
<p align="justify">Il <strong>Assegnazione Menu</strong> &amp; <strong>Autorizzazioni Modulo</strong> le schede sono standard per Joomla Moduli. È possibile impostare le voci di menu per il modulo a comparire e definire le autorizzazioni.</p>
