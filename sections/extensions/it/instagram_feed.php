<p>Qui puoi trovare le impostazioni principali di TM Instagram. Inserisci il nome del
    tuo profilo Instagram nel campo nome utente Instagram (username), inserisci il
    numero di immagini desiderato nel campo 'Number of photos', imposta l'ID della tua
    applicazione Instagram nel campo 'Your Instagram CLIENT ID'. Per ottenere l'ID,
    segui il link <a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                     target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    dove dovrai registrare un account. Poi segui questo link <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> e crea
    l'applicazione. Fatto ci√≤ otterrai i tuoi dati applicazione, dove vedrai il CLIENT
    ID.</p>

