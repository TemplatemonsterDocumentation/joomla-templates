<h3>TM Instagram</h3>
 
<p>TM Instagram - modulo per la visualizzazione di widget Instagram:</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Di seguito troverai le impostazioni di base del modulo TM Instagram. Nel nome utente Instagram
    inserisci il nome del tuo profilo Instagram, Numero di foto sul campo, il numero delle
    immagini che appaiono nella casella di widget di tuo Instagram CLIENT ID e seleziona
    il tuo ID Instagram applicazione. Per ottenere l'ID, vai al
    link <a href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
                 target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    e registrati. Dopodichè vai avanti e fai il clic sul collegamento <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> e crea
    l'applicazione. Riceverai i dati di applicazione, è sarà anche indicato
    un numero di identificaizone (CLIENT ID):</p>
 
