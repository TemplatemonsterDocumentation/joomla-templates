<h3>TM Ajax Style Switcher Module</h3>

<p align="justify"><strong>TM Ajax Style Switcher Module</strong> è un modulo per la commutazione colori tramite front-end. Il modulo applica automaticamente combinazioni di colori dal templates\themexxx\css\ la cartella del tema corrente.</p>
<figure class="img-polaroid">
    <img src="img/style_switcher_1.jpg" alt="">
</figure>
<figure class="img-polaroid">
    <img src="img/style_switcher_2.jpg" alt="">
</figure>
<p align="justify" class="alert alert-warning"><strong>Nota</strong>: sul modello Demo Live il modello funziona in modalità anteprima utilizzando i cookie. Sul tuo sito web il modulo funziona in modalità full e sarà disponibile per gli utenti autorizzati con diritti di categoria di utente SuperAdmin.</p>
<h5>Come gestire modulo switcher stile ajax TM</h5>
<p align="justify">Per configurare il <strong>TM Ajax Style Switcher Module</strong> vai in <strong>Estensioni </strong>&gt;<strong>Moduli</strong>. </p>
<figure class="img-polaroid">
  <img src="img/style_switcher_3.jpg" alt="">
</figure>

  <p align="justify"> Premi &quot;<strong>Strumento di Ricerca</strong>&quot; e selezionare il <strong>TM Ajax Style Switcher Module</strong>digitare in &quot;- Seleziona il Tipo -&quot; filtrare nell'elenco a discesa. Si vedrà il <strong>Ajax Style Switcher Module</strong>.</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_4.jpg" alt="">
</figure>
  <p align="justify">Clicca sul modulo di collegamento titolo per essere portato alla finestra delle impostazioni del modulo. Non ci sono impostazioni specifiche per il modulo nel pannello di amministrazione, solo le impostazioni standard per tutti i moduli:</p>
<figure class="img-polaroid">
  <img src="img/style_switcher_5.jpg" alt="">
</figure>
  <p align="justify">Assicurarsi che il modulo è <strong>pubblicato</strong>.</p>
  <h5><p>Per attivare lo stile switcher, si prega di seguire le istruzioni di seguito elencati:</p></h5>
  <ol class="marked-list">
    <li>
      <p>Aggiungere un nuovo utente di Joomla via <strong>Users</strong> &gt; <strong>Gestire</strong>.</p>
  
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_7.jpg" alt="">
</figure>
    <li><strong>Abilitare</strong> e	<strong>attivare</strong> nuovo user.</li>  
    <figure class="img-polaroid">
  <img src="img/style_switcher_6.jpg" alt="">
</figure>

    <li>Premere Il nuovo collegamento di accesso utente per essere portato a finestra delle impostazioni.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_9.jpg" alt="">
</figure>
    <li>Assicurati di assegnare l'utente a &lsquo;<strong>Super Users</strong>&rsquo; gruppo. Navigare verso <strong>Assegnato Users</strong> scheda e spuntare &quot;<strong>Super Users</strong>&quot;.</li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_11.jpg" alt="">
</figure>
    <li>
      <p>Accedi al tuo sito web tramite <strong>Login o Registrati</strong> modulo al front-end:</p>
    </li>
    <figure class="img-polaroid">
  <img src="img/style_switcher_12.jpg" alt="">
</figure>
    <li>
      <p>Style Switcher è stato attivato correttamente. Aggiorna il tuo sito per vedere box strumento di personalizzazione:</p>
</li>
     <figure class="img-polaroid">
  <img src="img/style_switcher_13.jpg" alt="">
</figure>
  </ol>
