<p class="alert alert-warning">Nota che nella demo live del modello il modulo funziona in modalità anteprima,
    utilizzando i cookie. Sul sito web vero e proprio, il modulo funziona invece in modalità completa ed è disponibile
    solo agli utenti con permesso SuperAdmin.</p>