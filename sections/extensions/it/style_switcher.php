</figure>

<p class="alert alert-warning">Si prega di notare: una versione demo del modulo modello viene eseguito in modalità demo,
    con l'utilizzando dei cookie. Sul tuo sito web modulo funziona in modalità full ed è disponibile solo per gli utenti
    autorizzati con i diritti alle categorie di utenti SuperAdmin.</p>
