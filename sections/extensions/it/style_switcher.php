<h3>Modulo TM Ajax Style Switcher </h3>

<p>Modulo <strong>TM Ajax Style Switcher</strong> – questo modulo, permette le combinazione dei colori lato frontend
    del sito. Modulo applica automaticamente la combinazione dei colori della cartella color_schemes\css\ del tema attivo.</p>

<p>Questo modulo non ha impostazioni speciali nel pannello di amministrazione, solo le impostazioni standard per tutti i moduli:</p>

