<h3>TM Ajax Style Switcher Module</h3>

<p><strong>TM Ajax Style Switcher Module</strong> – è un modulo che consente di cambiare lo schema colore attraverso il
    front-end. Il modulo applica automaticamente i moduli colore dalla color_schemes\css\ cartella del tema in uso.</p>

<p>Il modulo non ha impostazioni specifiche nel pannello admin. Ci sono solo le configurazioni standard dei moduli.</p>

