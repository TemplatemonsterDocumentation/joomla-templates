<h3>Joomla Cookie Directive Plugin</h3>

<p><strong>Joomla Cookie Directive Plugin</strong> in grado di visualizzare un banner biscotto accettazione nella parte superiore del sito per mettere in guardia circa il sito utilizzando i cookie.</p>

<figure class="img-polaroid">
    <p><img src="img/cookies-1.jpg" alt="">
    </p></figure>
    <p align="justify">Per configurare il <strong>Joomla Cookie Directive</strong> plugin di navigare a <strong>Estensioni</strong> &gt; <strong>Plugins</strong>. Per trovare il plugin necessario tra l'altro, tipo &quot;cook/.../&quot; nel campo di ricerca.</p>
    <p align="justify">Clicca il &quot;<strong>system - EUCookieDirectiveLite</strong>&quot; link del titolo e vedrete il principale <strong>Joomla Cookie Directive</strong> plugin impostazioni.</p>

<p>Questa schermata consente di modificare i dettagli e le opzioni del plugin.</p>

<p>
  <figure class="img-polaroid">
