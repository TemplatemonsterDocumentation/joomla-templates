<h3>TM Parallax Module</h3>

<p><strong>TM Parallax </strong>è un modulo che utilizza lo script RD Parallax per implementare la funzionalità di parallasse nel modello.</p>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-20.jpg" alt=""></p></figure>
<p align="justify">Il modulo sarà ora disponibile presso il Gestore modulo si trova sotto il <strong>Estensioni</strong> scheda nel menu in alto del l'interfaccia di amministrazione. È possibile creare moduli aggiuntivi cliccando sul <strong>Nuovo</strong> pulsante. Selezionare i moduli già effettuate al fine di configurarli. Non dimenticare di pubblicarli nel corretto posizionamento e sulle pagine giuste.</p>

<h5><p>Per configurare il<strong>TM Parallax </strong>modulo, effettuare le seguenti operazioni:</p></h5>
<li><p>Navigare verso <strong>Estensioni</strong> &gt; <strong>Moduli</strong> linguetta:</p></li>
<figure class="img-polaroid">
<p><img src="img/contact-form-7.png" alt=""></p></figure>
      <li>
        <p>Successivo è necessario selezionare la<strong> TM Parallax </strong>modulo da modificare.</p>
      </li>
    <div class="alert alert-warning">
 <div align="justify"><strong>Mancia:</strong> se si desidera trovare tutte <strong>TM Parallax </strong>moduli, usa i filtri per semplificare il processo. Premi &quot;<strong>Strumento di ricerca</strong>&quot; e selezionare il &quot;<strong>Articoli - Singolo</strong>&quot; digitare il &quot;<strong>- Seleziona Tipo-</strong>&quot; filtrare nell'elenco a discesa.</div>
  <figure class="img-polaroid">
<p> <img src="img/tm-parallax-1.jpg" alt=""></p></figure></div>
<ol class="marked-list">
      <li>Per aprire le impostazioni del modulo di modifica necessaria, fare clic sul suo titolo. [Modifichiamo  <strong>Home parallax</strong> modulo.]</li>
<li>
  <div align="justify">Il<strong> TM Parallax </strong>interfaccia di editing del modulo si apre di fronte a voi.            
  </div>
  <figure class="img-polaroid">
    <p> <img src="img/tm-parallax-2.jpg" alt=""></p></figure>
<li>Premi il <strong>Articolo</strong> per aprire la finestra modale selezione degli articoli, come illustrato di seguito.</li>
<figure class="img-polaroid">
<p> <img src="img/tm-parallax-3.jpg" alt=""></p></figure>
<div class="alert alert-warning">
    <div align="justify">Nei nostri modelli <strong>TM Parallax Modulo</strong> contenuto è presentato caricando un certo modulo, per esempio: "{loadmodule articles_single, Home parallax article}". Al fine di evitare l'errore 404 sul tuo sito web, si prega di assicurarsi che il nome del modulo caricato identico a quello, specifico per il carico. In questo caso, la "<strong>Home parallax articolo</strong>" modulo dovrebbe essere pubblicato. Il nome del modulo non deve contenere più di uno spazio. </div>
          È possibile saperne di più su loadmodule/loadposition in <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">sito ufficiale</a>.
  </p>
    </div>
  <p align="justify">Per modificare il <strong>Home parallax articolo</strong> modulo, individuate <strong>Estensioni</strong> &gt; <strong>Moduli</strong> scheda. Ricerca  <strong>Home parallax articolo</strong> utilizzando il campo di ricerca. Aprire  <strong>Home parallax articolo</strong> impostazioni del modulo, fare clic sul suo titolo.</p>
 <figure class="img-polaroid"> <p> <img src="img/tm-parallax-4.jpg" alt=""></p></figure>
  <p align="justify">Ora avete l'opportunità di modificare l'articolo. Per modificare l'articolo clicca il<strong> Modifica </strong>tasto e ti andare a vedere la finestra Modifica articolo. Qui si ha la possibilità di modificare il contenuto dell'articolo e la sua immagine e link.</p>
  <div class="alert alert-warning">
  <p>Per aggiungere un nuovo utilizzo articolo <strong>Soddisfare</strong> &gt; <strong>Articoli</strong> &gt; <strong>Aggiungi Nuovo Articolo</strong>.</p>
