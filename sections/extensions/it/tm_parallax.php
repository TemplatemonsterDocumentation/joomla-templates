</figure>

<p class="alert alert-warning">
Nella maggior parte dei nostri modelli è presente il modulo TM Parallax che ha il collegamento con delle posizioni diverse, ad esempio, "{loadmodule articles_single, About us}". Per evitare errori 404 sul tuo sito web, assicurati che il nome del plugin coincide con quello indicato al momento della connessione. In questo caso "About us" deve essere nelle condizioni published e non deve contenere più di uno spazio nel titolo.
    È possibile ottenere informazioni dettagliate su loadmodule/loadposition su <a href="https://docs.joomla.org/How_do_you_put_a_module_inside_an_article%3F" target="_blank">questo sito</a>.
</p>
