<h3>Modulo TM Parallax</h3>

<p>TM Parallax - questo modulo, che utilizza il RD Parallax script per la realizzazione dell'effetto di parallasse nel modello.</p>

<h6>Sono disponibili nel modulo le impostazioni con tutte le opzioni necessarie:</h6>

<ol class="index-list">
    <li>Impostazioni immagini;</li>
    <li>Impostazioni velocità di effetto parallasse;</li>
    <li>Impostazioni della direzione di effetto parallasse.</li>
</ol>
