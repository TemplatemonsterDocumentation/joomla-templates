<h5>Creazione di Categorie</h5>
<ol class="marked-list">
  <li>
    <p>Vai a <strong>Componenti</strong> &gt; <strong>Kunena Forum</strong> &gt; <strong>Categorie</strong>.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-19.jpg" alt=""></figure>
  <li>
    <p>Click <strong>Nuova Categoria</strong> pulsante nell'angolo in alto a sinistra.</p>
  </li>
  <figure class="img-polaroid"><img src="img/forum-20.jpg" alt=""></figure>
  <li>
    <p>Inserire le impostazioni di categoria generali sotto il <strong> Nome categoria e descrizione </strong>etichetta:</p>
     <figure class="img-polaroid"><img src="img/forum-21.jpg" alt=""></figure>
   <ol class="marked-list">
       <li>
         <p align="justify">Per creare una sezione, scegliete <strong>Livello superiore</strong> come genitore.</p>
       </li>
       <li>
         <p>Inserisci categoria <strong>Nome</strong> e <strong>Alias</strong>.</p>
       </li>
       <li>
         <p>Assicurarsi categoria è <strong>pubblicato</strong>.</p>
       </li>
       <li>
         <p>Compilare la categoria <strong>descrizione</strong> e <strong>titolo</strong> ai campi corrispondenti.</p>
       </li>
       <li>
         <p>Puoi aggiungere <strong>classe CSS personalizzato</strong> per la categoria.</p>
       </li>
     </ol>
  <p>&nbsp;</p>
  <li>
    
    <p>Click il<strong> Сategoria di autorizzazioni</strong> scheda per definire i permessi per le categorie:</p>
    <figure class="img-polaroid"><img src="img/forum-22.jpg" alt=""></figure>
    <p>Potete vedere due opzioni disponibili qui:</p>
    
    <ol class="marked-list">
      <li>
        <p><strong>Tipo di controllo di accesso</strong> da utilizzare in questa categoria.</p>
      </li>
      <li>
        <p align="justify">Il <strong>Gruppo Livello di accesso</strong> che è autorizzato a visualizzare questa categoria. È possibile definire i propri livelli di accesso in<strong> Users</strong> &gt; <strong>Livelli di Accesso</strong>. </p></li>
    </ol>
</li>
<p>&nbsp;</p>
  <li>
    <p><strong>Categoria Impostazioni</strong> etichetta:</p>
    <figure class="img-polaroid"><img src="img/forum-23.jpg" alt=""></figure>
    <ol class="marked-list">
      <li>
        <p align="justify"><strong>Bloccato</strong>. Impostare a Sì se si desidera bloccare la categoria. Moderatori e gli amministratori sono gli unici che possono creare nuove risposte alla categoria bloccato.</p>
      </li>
      <li>
        <p align="justify"><strong>Messaggi Review</strong>. Impostare su Sì se si desidera i messaggi da rivedere dai moderatori prima di pubblicarli in questa categoria.</p>
      </li>
      <li>
        <p align="justify"><strong>Permetti messaggi anonimi </strong>opzione consente di abilitare/disabilitare messaggi anonimi per la categoria.</p>
      </li>
      <li>
        <p align="justify"><strong>Per impostazione predefinita rispondere ai messaggi in opzione</strong>. Se i messaggi anonimi sono stati abilitati, questa opzione consente di selezionare la scelta di default per l'utente. Gli utenti normali possono poi modificare i propri post di essere anonimi, ma solo i moderatori hanno la capacità di modificare i messaggi anonimi.</p>
      </li>
      <li>
        <p>Puoi <strong>Consenti Sondaggi</strong> per la categoria che si crea.</p></li></ol>
