<h3>Forum Kunena</h3>

<p>Kunena è un componente forum per Joomla. Esso consente di implementare un forum
    completamente funzionale basato sul CMS Joomla.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>


<p>Kunena componente si può accedere attraverso il pannello di amministrazione di Joomla
    sotto il menu <b>Componenti -> Kunena</b>.</p>


<p>Potete saperne di più sull'uso componente Kunena e la configurazione sul <a
        href="http://www.kunena.org/docs/Main_Page" target="_blank">sito ufficiale</a>.
</p>
