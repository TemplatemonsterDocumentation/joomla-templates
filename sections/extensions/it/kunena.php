<h3>Kunena Forum</h3>

<p align="justify"><strong>Kunena Forum</strong> è un forum di discussione per il sito Joomla. È flessibile e facile da lavorare. Il layout di default del forum per gli utenti non registrati e registrati è diverso, così come le opzioni disponibili.</p>
<p><strong>Kunena</strong> è un componente per Joomla forum. Consente l'implementazione di un forum completamente funzionale basato su Joomla!</p>
<p>Questa funzione si trova sotto '<strong>Pagine</strong>' voce di menu al front end del sito.</p>
<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>
<p>Impostazioni generali del <strong>Kunena</strong> e il suo contenuto può essere controllato tramite il pannello di amministrazione del sito.</p>
<p>Scroll down the sidebar to Extensions and select <strong>Kunena Forum</strong> there:</p>
<figure class="img-polaroid"><img src="img/forum-2.jpg" alt=""></figure>

<p>Ci saranno diverse schede a disposizione per lavorare con:</p>
<figure class="img-polaroid"><img src="img/forum-3.jpg" alt=""></figure>
<p align="justify"><strong>Manager di categoria</strong>. In mezzo ci saranno tutte le categorie attualmente disponibili nel tuo forum. Selezionare qualsiasi per modificare, se necessario. È possibile modificare la sua sezione genitore, il nome, alias, descrizione, intestazione, e altre impostazioni:</p>
<figure class="img-polaroid"><img src="img/forum-4.jpg" alt=""></figure>
<p align="justify">Questa sarà l'area che contiene il membro discussione. È possibile creare quante discussioni, se necessario. Ogni discussione deve essere assegnato alla sezione generale [viene utilizzato come intestazione per argomenti del forum].</p>
<figure class="img-polaroid"><img src="img/forum-5.jpg" alt=""></figure>
<p align="justify"><strong>Categoria di Autorizzazioni</strong>scheda specifica il livello di accesso della categoria specifica - gruppi di utenti di visualizzare e modificare esso.</p>
<figure class="img-polaroid"><img src="img/forum-6.jpg" alt=""></figure>
<p align="justify"><strong>Categoria Impostazioni</strong> permette il controllo delle impostazioni generali di messaggi di categoria.</p>
<figure class="img-polaroid"><img src="img/forum-7.jpg" alt=""></figure>
<p align="justify"><strong>Users</strong> sezione. Qui puoi trovare gli utenti del forum corrente e gestire la loro impostazioni degli account, la modifica delle autorizzazioni e forum di visione:</p>
<figure class="img-polaroid"><img src="img/forum-8.jpg" alt=""></figure>
<p align="justify">Premi il<strong> username  link</strong> e vedrete la finestra dove è possibile selezionare il <strong>avatar</strong> e <strong>testo della firma</strong> se necessario, sotto la <strong>Impostazioni di Base</strong>. </p>
<figure class="img-polaroid"><img src="img/forum-9.jpg" alt=""></figure>

<p align="justify"><strong>Moderazione</strong> scheda permette di specificare se l'utente è moderatore o no, selezionando le categorie a moderata pure. Clic <strong>Salva &amp; Chiudi</strong> pulsante sulla parte superiore una volta che hai finito con le impostazioni utente.</p>
<figure class="img-polaroid"><img src="img/forum-10.jpg" alt=""></figure>
<p><strong>Allegati</strong>. Qui è possibile lavorare con i file caricati.</p>
<figure class="img-polaroid"><img src="img/forum-11.jpg" alt=""></figure>
<p><strong>Emozioni</strong>. Tutte le emozioni disponibili si trovano qui.</p>
<figure class="img-polaroid"><img src="img/forum-12.jpg" alt=""></figure>
<p>Usa <strong>Nuovo Sorriso</strong> zona per ottenere alcuni nuovi, se necessario:</p>
<figure class="img-polaroid"><img src="img/forum-13.jpg" alt=""></figure>
<p align="justify">&lsquo;<strong>Ranks</strong>&rsquo;. Qui sotto potete vedere tutti i ranghi disponibili per l'utente si cam aggiungere nuovo rango, se si vuole. Ogni rango può essere modificato manualmente così:</p>
<figure class="img-polaroid"><img src="img/forum-14.jpg" alt=""></figure>
<p><strong>Templates</strong>. Selezionare il modello da applicare Forum per:</p>
<figure class="img-polaroid"><img src="img/forum-15.jpg" alt=""></figure>
<p align="justify"><strong>Configurazione</strong>. Questa scheda permette di controllare le impostazioni del Forum in dettaglio. Titoli, indirizzo e-mail, SEO, cache e altre impostazioni possono essere gestite sotto la <strong>Generale</strong> etichetta. Con il <strong>Frontend</strong> sezione è possibile modificare il layout del Forum. Impostazioni generali per gli utenti sono gestiti con la <strong>Users</strong> sezione. <strong>Security</strong> scheda controlla i permessi per gli ospiti e gli utenti registrati che utilizzano il Forum. <strong>Avatars</strong> scheda è quello di consentire / impedire agli utenti il ​​caricamento di propri avatar, e specifica le impostazioni dell'immagine. Usando il <strong>Uploads</strong> 
è possibile consentire / impedire il caricamento degli allegati ai messaggi, specificare le impostazioni di questi file.</p>
<figure class="img-polaroid"><img src="img/forum-16.jpg" alt=""></figure>
