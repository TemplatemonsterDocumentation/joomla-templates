<h3>Forum Kunena</h3>

<p>Kunena - componente del forum per Joomla. Esso permette di realizzare completamente
    un forum con funzionalità complete sulla base di Joomla! CMS:</p>

<figure class="img-polaroid"><img src="img/forum-1.jpg" alt=""></figure>

<p>Componente Kunena è disponibile nel pannello di gestione di Joomla nel menu <b>Components
        (Componenti) -> Kunena</b>.</p>

<p>Le informazioni dettagliate sulle funzionalità e la possibilità d'utilizzo del componente Kunena è possibile
    trovare su <a href="http://www.kunena.org/docs/Main_Page" target="_blank">sito
        ufficiale</a>:</p>

