<h3>Установка шаблона</h3>

<p>Используйте этот метод, если у Вас уже есть веб-сайт Joomla и Вы лишь хотите поменять его внешний вид, установив новый
    шаблон.</p>

<p>При помощи данного способа, Вы можете установить шаблон Joomla и все необходимые расширения.</p>

<!-- Extensions manager -->
<h6>В админ панели Joomla перейдите в меню <b>"Extensions
        (Расширения) > Extensions Manager  (Менеджер расширений)"</b>:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Во вкладке Package File (Файл шаблона) нажмите
    кнопку "Choose File" (Выбрать файл) и выберите файл "<b>theme###.zip</b>" из папки шаблона
    "joomla".</h6>

<p>Нажмите на кнопку
    <strong>Upload & Install</strong> (Загрузить и установить)
    для установки шаблона Joomla:
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>После завершения загрузки, активируйте установленный шаблон.
    В верхнем меню выберите "<b>Extensions (Расширения) > Template Manager (Менеджер
    шаблонов)</b>".</h6>

<p>Выберите новый шаблон и нажмите кнопку <b>”Make Default” (По умолчанию)</b> в левом верхнем углу. Посмотрите, как теперь выглядит ваш веб-сайт:</p>

<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>
