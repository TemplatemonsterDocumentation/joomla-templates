<h3>Instalação do Template</h3>

<p>Eu já tenho site com base em Joomla e só quero alterar a aparência dele instalando um novo template.</p>

<p>Usando esse método você terá um template do Joomla, todas as extensões necessárias instaladas.</p>

<!-- Extensions manager -->
<h6>Abra o painel de administração do Joomla e vá para
    a janela <b>"Extensions > Extensions Manager"</b>usando o menu superior.</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>


<!-- Upload theme zip -->
<h6>Usando o botão de Arquivo de Pacote &quot;Escolher
    Arquivo&quot; procure o arquivo "<b>theme###.zip</b>"no diretório "joomla" do seu
    pacote do template</h6>

<p>Clique no botão &quot;Enviar e Instalar&quot; (em Inglês, &quot;Upload &amp; Install&quot;)
    <strong>Upload & Install</strong>
    para instalar o template do Joomla.
</p>
<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>


<!-- Activate template -->
<h6>Quando o upload estiver concluído, você precisa
    ativar o template instalado. No menu superior, selecione "<b>Extensões &gt;
        Gerenciador de Template&quot; (em Inglês, &quot;Extensions > Template
        Manager</b>")</h6>
<p>Selecione o seu mais novo template instalado e clique no botão "<b>Padronizar&quot;
        (em Inglês, &quot;Make Default</b>") no canto superior esquerdo. Agora você pode
    visualizar o seu site para ver as alterações.</p>
<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>

