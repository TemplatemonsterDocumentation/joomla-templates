<h3>Instalação de Módulos</h3>

<p>Os temas do Joomla são fornecidos com extensões adicionais: módulos, plugins e
    componentes que são utilizados para implementar várias funções e recursos. Todas as
    extensões adicionais podem ser instaladas em alguns cliques utilizando o pacote
    fornecido.</p>

<p>O arquivo do pacote está localizado no diretório 'fontes' do pacote do seu template e
    chamado de 'pkg_bundle.zip'. Pode ser facilmente instalado como qualquer outra
    extensão ou tema através do <b>Painel de Controle do Joomla &gt; Gerenciador de
        Extensões</b>. </p>

<ol class="index-list">
    <li>Abra o painel de administração do Joomla. No menu superior selecione '<b>Extensões</b>'.
        Na caixa suspensa selecione '<b>Gerenciador de Extensões</b>';
    </li>
    <li>Certifique-se de que a seção 'Instalar' está selecionada na coluna da
        esquerda;
    </li>
    <li>Clique no botão 'Navegar/Escolher arquivo' e selecione o arquivo
        'sources/pkg_bundle.zip';
    </li>
    <li>Clique no botão 'Fazer Upload e instalar' para instalar todos os módulos.</li>
</ol>

<figure class="img-polaroid"><img alt="" src="img/extensions_manager.jpg"></figure>

<p>Se caso o seu tema contém algumas extensões que não têm sido incluídas no '<b>pkg_bundle.zip</b>'
    você precisa instalar cada um separadamente. Isso poder ser feito exatamente como
    instalação do 'sources/pkg_bundle.zip' através da interface '<b>Gerenciador de
        Extensões</b>'.</p>

<p>O nome dos arquivos das extensões geralmente começam com '<b>mod_</b>', '<b>com_</b>'
    ou '<b>plg_</b>' então você irá reconhecê-los facilmente. </p>