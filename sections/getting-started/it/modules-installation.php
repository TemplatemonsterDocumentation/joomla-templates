<h3>Moduli di Istallazione</h3>

<p>I temi Joomla sono fornite con estensioni addizionali: moduli, plugin e componenti
    che sono usati per implementare varie funzionalità e caratteristiche. Tutte le
    estensioni addizionali possono essere istallate in un paio di click usando il
    pacchetto fornito.</p>

<h6>L'archivio è situato nella directory 'sorgenti' nella vostra template del pacchetto
    chiamata 'pkg_bundle.zip'. Può essere facilmente istallata come ogni altra
    estensione o tema attraverso il <b>pannello amministratore Joomla &gt; Gestore
        estensioni </b>. </h6>
<ol class="index-list">
    <li>Apri il pannello amministratore di Joomla. Dal menù superiore seleziona '<b>Estensioni</b>'.
        Dalle opzioni che si aprono seleziona '<b>Gestore Estensioni</b>';
    </li>
    <li>Assicurati che la sezione 'Installa' sia selezionata nella colonna di
        sinistra;
    </li>
    <li>Clicca il bottone 'Browse/Scegli File' e seleziona 'l'archivio
        sources/pkg_bundle.zip';
    </li>
    <li>Clicca il bottone 'Upload ed installa' per installare tutti i moduli.</li>
</ol>

<figure class="img-polaroid"><img alt="" src="img/extensions_manager.jpg"></figure>

<p>Nel caso che il tuo tema contenga alcune estensioni che non siano state incluse in
    '<b>pkg_bundle.zip</b>' necessiterai di installarle separatamente. Questo può essere
    fatto esattamente come per l'istallazione 'sources/pkg_bundle.zip' attraverso
    l'interfaccia del '<b>Gestore Estensioni</b>'.</p>

<p>I nomi delle estensioni dei file archivio normalmente iniziano con '<b>mod_</b>',
    '<b>com_</b>' or '<b>plg_</b>' quindi li riconoscerai facilmente. </p>