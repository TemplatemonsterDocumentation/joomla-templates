<h3>Installazione dei moduli</h3>

<p>Gli argomenti includono ulteriori estensioni di Joomla: moduli, plugin e componenti che
    utilizzano per estendere la funzionalità del sito. Qualunque ulteriore proroga è possibile
    con facilità da installare utilizzando un file speciale.</p>

<h6>Questo file si trova nella cartella 'sources' del modello e si chiama 'pkg_bundle.zip'. Questo file è
    molto facile da installare come qualsiasi altra estensione o il tema attraverso <b>pannello di Joomla Admin Extension Manager</b>. </h6>
<ol class="index-list">
    <li>Aprire il pannello di amministrazione di Joomla. Nel pannello di controllo, selezionare <b>”Extensions”</b> (Estensione). Nell'elenco a discesa visualizzata e fare clic sul pulsante <b>”Extensions Manager”</b> (Gestione estensioni).
    </li>
    <li>Scegli l'opzione “Install” (Installazione) nella colonna a sinistra;</li>
    <li>Premi sul pulsante “Browse/Choose file” (Sfoglia/Seleziona file) e individua il file
        'sources/pkg_bundle.zip';
    </li>
    <li>Premi sul pulsante “Upload and install” (Scaricare e installare), per installare
        tutti i moduli:
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>Se il modello contiene una estensione che non è inclusa nel file '<b>pkg_bundle.zip</b>', questi file dovranno 
    essere installati separatamente. L'installazione viene eseguita nello stesso modo come nel caso con
    'sources/pkg_bundle.zip', con l'aiuto di “<b>Extensions Manager</b>” (Gestione estensioni).
</p>

<p>I nomi di file con estensioni di solito iniziano con '<b>mod_</b>', '<b>com_</b>' oppure puoi sempre
    '<b>plg_</b>', esplorarli. </p>
