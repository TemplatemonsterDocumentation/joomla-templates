<h3>Installazione Modello</h3>

<p>Possiedo già un sito Web in Joomlae vorrei solamente modificarne l’aspetto, installandoun nuovo modello.</p>

<p>Seguendo questa procedura potrete installare un modello Joomla e le relative estensioni richieste.</p>

<!-- Extensions manager -->
<h6>Aprite il pannello di amministrazione di Joomlae
    andate sulla schermata <b>"Estensioni>Gestione Estensioni"("Extensions > Extensions
        Manager")</b> utilizzando il menù in alto.</h6>
<figure class="img-polaroid"><img src="img/template-install-1.jpg" alt=""></figure>


<!-- Upload theme zip -->
<h6>Utilizzando il pulsante “Scegli File”(Choose
    File)dal Pacchetto per aprire ilfile "<b>theme###.zip</b>" nella directory “joomla”
    del vostro modello.</h6>


<p>Clicca il pulsante
    <strong>Carica & Installa (Upload &amp; Install)</strong>
    per installare il modello Joomla.
</p>
<figure class="img-polaroid"><img src="img/template-install-2.jpg" alt=""></figure>

<!-- Activate template -->
<h6>Quando il caricamento sarà completato dovrete
    attivare il modello installato. Dal menù in alto selezionate "<b>Estensioni>Gestione
        Modello" (Extensions > Template Manager)</b>"</h6>


<p>Selezionate il vostro modello appena installatoe cliccate sul tasto "<b>Imposta
        comePredefinito (Make Default)</b>" nell’angolo in alto a sinistra. Adesso è
    possibile vedere le modifiche sul proprio sito.</p>
<figure class="img-polaroid"><img src="img/template-install-3.jpg" alt=""></figure>