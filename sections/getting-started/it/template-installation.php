<h3>Installazione del modello</h3>

<p>Utilizzare questo metodo se si dispone già di un sito web Joomla e si desidera solo cambiare il suo aspetto, stabilendo un nuovo
    modello.</p>

<p>Con questo metodo, è possibile installare template Joomla e qualsiasi espansione necessaria.</p>

<!-- Extensions manager -->
<h6>Nel pannello di amministrazione di Joomla, vai a <b>"Extensions
        (Estensione) > Extensions Manager  (Gestione estensioni)"</b>:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Nella scheda File Package (File del modello) premi
    il pulsante "Choose File" (Scegliere il file) e scegli il file "<b>theme###.zip</b>" dalla cartella del modello
    "joomla".</h6>

<p>Premere il pulsante
    <strong>Upload & Install</strong> (Scaricare e installare)
    installare template Joomla:
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>Dopo aver completato il download, attivare il modello specificato.
    Nel menu in alto "<b>Extensions (Estensioni) > Template Manager (Gestione
    dei modelli)</b>".</h6>

<p>Selezionare il nuovo modello, e quindi fare clic su <b>”Make Default” (Per impostazione predefinita)</b> nell'angolo superiore sinistro. Guarda come ora viene visualizzato il tuo sito web:</p>

<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>
