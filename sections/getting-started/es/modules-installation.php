<h3>Instalación de módulos</h3>

<p> Los temas para Joomla están provistas de extensiones adicionales: módulos, plugins y componentes que se utilizan para implementar diversas características Puede instalar todas las extensiones adicionales con la ayuda de un paquete de paquete proporcionado.</p>

<h6>Puede encontrar este archivo en la carpeta 'sources' de la plantilla. Su nombre es 'pkg_bundle.zip'. Simplemente se instala como cualquier otra extensión o un tema, a partir de <b>Gestor de extensiones de su Panel de Administración de Joomla</b>. </h6>
<ol class="index-list">
    <li>Abra el panel de administración de Joomla. Seleccione <b>”Extensions”</b>. En la lista desplegable, seleccione el botón <b>”Extensions Manager” (Gestor de extensiones)</b> .
    </li>
    <li>Seleccione la opción "Instalar" ("Install") en la columna de la izquierda;</li>
    <li>Haga clic en "Navegar/Seleccionar archivo" (“Browse/Choose file”) y seleccione el archivo 'sources/pkg_bundle.zip';
    </li>
    <li>Haga clic en "Subir e instalar" (“Upload and install”), para instalar todos los módulos:
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>Si el tema utiliza extensiones, las cuales no están incluidos en '<b>pkg_bundle.zip</b>', es necesario instalar por separado. Esto se hace exactamente de la misma manera que la instalación de 'sources/pkg_bundle.zip' desde la interfaz de “<b>Gestor de extensiones (Extensions Manager)</b>”.
</p>

<p>Los nombres de las extensiones de archivos por lo general comienzan con '<b>mod_</b>', '<b>com_</b>' o
    '<b>plg_</b>', facil de reconocer. </p>
