<h3> Instalación de módulos </h3>

<p> Los temas de Joomla cuentan con extensiones adicionales: módulos, plugins
    (complementos) y componentes que se utilizan para implementar varias funcionalidades
    y características. Todas las extensiones adicionales pueden instalarse en un par de
    clicks usando el bundle (paquete) proporcionado.</p>

<h6> El archivo bundle (paquete) está ubicado en el directorio de las fuentes de su
    paquete de plantillas y se llama 'pkg_bundle.zip'. Puede ser instalado fácilmente
    como cualquier otra extensión o tema a través de <b> Panel de administración de
        Joomla > Gestor de extensiones </b>. </h6>
<ol class="index-list">
    <li> Abra el panel de administración de Joomla. Del menú principal seleccione'<b>Extensiones</b>'.
        Del menú desplegable, seleccione '<b> Gestor de extensiones </b>';
    </li>
    <li> Asegúrese de que la sección Instalar este seleccionada en la columna
        izquierda;
    </li>
    <li> Haga click en el botón 'Examinar/Escoger Archivo' y seleccione
        'sources/pkg_bundle.zip' archivo;
    </li>
    <li> Haga click en el botón 'Subir e Instalar' para instalar todos los módulos.</li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p> En caso de que su tema contenga algunas extensiones que no han sido incluidas en
    '<b>pkg_bundle.zip</b>' tiene que instalar cada una por separado. Esto puede hacerse
    exactamente como la 'sources/pkg_bundle.zip' instalación a través de la interfaz del
    '<b> Gestor de extensiones.</b> p>

<p> Los nombres de los archivos de extensiones suelen comenzar con '<b>mod_</b>', '<b>com_</b>'
    or '<b>plg_</b>' entonces los reconocerá fácilmente. </p>