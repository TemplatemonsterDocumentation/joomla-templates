<h3>Instalación de plantilla</h3>

<p>Ya tengo un sitio web basado en Joomla y sólo quiero modificar su apariencia mediante la instalación de
    nueva plantilla.</p>

<p>Por medio de este método obtendrá una plantilla Joomla, todas las extensiones necesarias instaladas.</p>
<!-- Extensions manager -->

<h6>Abra el panel de administración de Joomla y haga
    clic en <b>"Extensions > Extensions Manager" (Extensiones > Gestor de
        Extensiones)</b> en el menú superior.</h6>

<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Utilizando el botón "Choose File/Elegir fichero"
    del fichero de paquete, busque el fichero "theme###.zip" en el directorio "joomla"
    de su paquete de la plantilla.
</h6>

<p>Haga clic en el botón Subir e Instalar para instalar su plantilla Joomla.</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>


<!-- Activate template -->
<h6>Si ha subido todo, Usted tiene que activar la
    plantilla instalada. En el menú superior haga clic en "Extensions > Template
    Manager" (Extensiones > Gestor de plantillas).</h6>

<p>Seleccione la plantilla instalada y haga clic en el botón "Make Default/Hacer
    predeterminada" en la esquina superior a la izquierda. Ahora Usted puede abrir su
    sitio web para ver los cambios.
</p>

