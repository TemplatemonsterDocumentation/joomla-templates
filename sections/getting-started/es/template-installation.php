<h3>Instalación de plantilla</h3>

<p>"Ya tengo un sitio web basado en Joomla y justo
querer cambiar su aspecto mediante la instalación de una nueva plantilla."</p>

<p>Utilizando este método, se puede instalar una plantilla de Joomla y todas las extensiones necesarias</p>

<!-- Extensions manager -->
<h6>En su panel de administración de Joomla, vaya a la pestaña <b>"Extensiones > Gestor de Extensiones" ("Extensions > Extensions Manager")</b>:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>En la pestaña de Package File, pulse el botón 
"Elegir fichero" ("Choose File") y elija "<b>theme###.zip</b>" el archivo de la "joomla"  de su paquete de la plantilla.</h6>

<p>Haga clic en el botón para instalar su plantilla Joomla
    <strong>Subir e Instalar (Upload & Install)</strong>:
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>Cuando termine la carga, por favor active la plantilla instalada.
    En el menú superior, vaya a la pestaña "<b>Extensiones > Gestor de la plantilla (Extensions > Template Manager)</b>" .</h6>

<p>Seleccione la plantilla que acaba de instalar y haga clic en el Botón <b>"Usar por Defecto" (”Make Default”)</b> en la parte superior izquierda. Usted ya puede abrir su sitio web para ver los cambios:</p>

<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>
