<h3>Installation de modèles</h3>

<p>J'ai déjà un site basé sur Joomla et je veux simplement en changer l'apparence en installant un nouveau modèle.</p>

<p>En utilisant cette méthode, vous installerez un modèle pour Joomla et toutes les extensions requises.</p>

<!-- Extensions manager -->
<h6>ouvrez le panneau d'administration
    de Joomla et allez sur la page <b>« Extensions > Gestionnaire d'extensions »
        (« Extensions > Extensions Manager »)</b> en utilisant le menu en haut.</h6>
<figure class="img-polaroid"><img src="img/template-install-1.jpg" alt=""></figure>

<!-- Upload theme zip -->
<h6>en utilisant le bouton « Choisir
    fichier » (« Choose File ») à côté de « Fichier de paquet » (« Package File »),
    sélectionnez le fichier « <b>theme###.zip</b> » dans le répertoire « joomla » de
    votre paquet de modèle.</h6>
<figure class="img-polaroid"><img src="img/template-install-2.jpg" alt=""></figure>

<p>Cliquez sur le bouton « Envoyer et installer » (
    <strong>Upload & Install</strong>
    ) pour installer le modèle pour Joomla.
</p>

<!-- Activate template -->
<h6> une fois l'envoi terminé, vous
    devrez activer le modèle installé. Dans le menu du haut, sélectionnez « <b>Extensions
        > Gestionnaire de modèles</b> » (« <b>Extensions > Template Manager</b> »).</h6>
<figure class="img-polaroid"><img src="img/template-install-3.jpg" alt=""></figure>

<p>Sélectionnez le modèle que vous venez d'installer et cliquez sur le bouton « <b>Utiliser
        par défaut</b> » (« <b>Make Default</b> ») dans le coin en haut à gauche. Vous
    pouvez maintenant voir les changements sur votre site web.</p>