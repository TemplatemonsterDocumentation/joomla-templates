<h3>Installation de modèle</h3>

<p>"J'ai déjà un site basé sur Joomla et je veux simplement en changer l'apparence en installant 
   un nouveau modèle."</p>

<p>En utilisant cette méthode, vous installerez un modèle pour Joomla et toutes les extensions requises.</p>

<!-- Extensions manager -->
<h6>Ouvrez le panneau d'administration de Joomla et allez sur la page <b>"Extensions (Extensions) > Extensions Manager (Gestionnaire d'extensions)"</b>:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Sur l'onglet Package File (Fichier de paquet)cliquez sur le bouton
   "Choose File" (Choisir fichier) et sélectionnez le fichier "<b>theme###.zip</b>" de votre paquet de modèle
    "joomla".</h6>

<p>Cliquez sur le bouton 
    <strong>Upload & Install (Envoyer et installer)</strong> pour installer le modèle pour Joomla:
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>Une fois l'envoi terminé, vous devrez activer le modèle installé.
    Dans le menu du haut sélectionnez "<b>Extensions (Extensions) > Template Manager ( Gestionnaire 
	de modèles)</b>".</h6>

<p>Sélectionnez le modèle que vous venez d'installer et cliquez sur le bouton <b>”Make Default” (Utiliser par défaut)</b> dans le coin en haut à gauche. Vous pouvez maintenant voir les changements sur votre site web:</p>

<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>
