<h3>Installation de modules</h3>

<p>Les thèmes pour Joomla sont fournis avec des extensions supplémentaires : modules,
    plugins, et composants qui sont utilisés pour implémenter diverses fonctionnalités.
    Toutes les extensions supplémentaires peuvent être installées en quelques clics en
    utilisant le paquet fourni.</p>

<h6>L'archive du paquet se trouve dans le dossier « sources » du paquet de votre
    template, et se nomme « pkg_bundle.zip. » Il s'installe simplement comme n'importe
    quelle autre extension ou thème, à partir de <b>Panneau d'administration de Joomla
        &gt; Gestionaire d'extensions (Joomla admin panel &gt; Extensions manager)</b>.
</h6>

<ol class="index-list">
    <li>Ouvres le panneau d'administration de Joomla. Sélectionnez « <b>Extensions</b> »
        dans le menu du haut. Dans le menu déroulant, sélectionnez « <b>Gestionnaire
            d'extensions</b> » (« <b>Extensions Manager</b> ») ;
    </li>
    <li>Assurez-vous que l'option « installer » (« install ») est sélectionnée dans la
        colonne de gauche ;
    </li>
    <li>Cliquez sur le bouton « Parcourir/Choisir un fichier » (« Browse/Choose file »)
        et sélectionnez l'archive « sources/pkg_bundle.zip ; »
    </li>
    <li>Cliquez sur le bouton « Envoyer et installer » (« Upload and install ») pour
        installer tous les modules.
    </li>
</ol>

