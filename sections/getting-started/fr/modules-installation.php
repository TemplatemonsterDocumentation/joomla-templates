<h3>Installation de modules</h3>

<p> Les thèmes pour Joomla sont fournis avec des extensions supplémentaires: modules, plugins et composants, qui sont utilisés pour implémenter diverses fonctionnalités. Toutes les extensions supplémentaires peuvent être installées facilement en utilisant une archive spéciale.</p>

<h6>Cette archive se trouve dans le dossier 'sources' du paquet de votre template et se nomme 'pkg_bundle.zip'. Il s'installe simplement, comme n'importe quelle autre extension ou thème, à partir de <b>Gestionaire d'extensions de Panneau d'administration de Joomla</b>. </h6>
<ol class="index-list">
    <li>Ouvrez le panneau d'administration de Joomla. Sélectionnez <b>”Extensions” ("Extensions")</b>. Dans la liste déroulante sélectionnez <b>”Extensions Manager” ("Gestionnaire d'extensions")</b>.
    </li>
    <li>Choisissez l'option “Install” ("Installer") dans la colonne de gauche;</li>
    <li>Cliquez sur le bouton “Browse/Choose file” ("Parcourir/Choisir un fichier"") et sélectionnez l'archive 'sources/pkg_bundle.zip';
    </li>
    <li>Cliquez sur le bouton “Upload and install” ("Envoyer et installer"), pour installer tous les modules:
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>Si votre thème utilise des extensions, qui ne sont pas comprises dans '<b>pkg_bundle.zip</b>', il vous faudra les installer séparément. Cela se fait exactement de la même manière que pour l'installation de 'sources/pkg_bundle.zip', à partir de l'interface du “<b>Extensions Manager</b>” (Gestionnaire d'extensions).
</p>

<p>Les noms des archives d'extensions commencent en général par '<b>mod_</b>', '<b>com_</b>' ou
    '<b>plg_</b>', afin de les reconnaître facilement. </p>
