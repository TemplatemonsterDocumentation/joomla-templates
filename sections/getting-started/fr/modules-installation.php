<p>Si votre th&egrave;me utilise des extensions qui ne sont pas comprises dans &laquo;&nbsp;<b>pkg_bundle.zip</b>,&nbsp;&raquo;
    il vous faudra les installer s&eacute;par&eacute;ment. Cela se fait exactement de la
    m&ecirc;me mani&egrave;re que pour l'installation de &laquo;&nbsp;sources/pkg_bundle.zip,&nbsp;&raquo; &agrave;
    partir de l'interface du &laquo;&nbsp;<b>Gestionnaire d'extensions</b>.&nbsp;&raquo;
</p>

<p>Le nom de fichier des archives d'extensions commence en g&eacute;n&eacute;ral
    par &laquo;&nbsp;<b>mod_</b>,&nbsp;&raquo; &laquo;&nbsp;<b>com_</b>,&nbsp;&raquo;
    ou &laquo;&nbsp;<b>plg_</b>&nbsp;&raquo; afin de les reconna&icirc;tre facilement.
</p>