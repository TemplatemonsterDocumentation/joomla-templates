<h3>Template installation</h3>

<p>If you  already have a Joomla based website and  
want to change its design, you need to use the template installation method.</p>

<p>This method lets you to  install a <strong>Joomla template</strong> and all the necessary<strong> extensions</strong>.</p>

<!-- Extensions manager -->
<h6>In your admin panel, navigate to <b>"Extensions >  Manage"</b> tab:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.png" alt="">
</figure>

<!-- Upload theme zip -->
<h6 align="justify">In &quot;Upload Package File&quot; tab, press the 
"Choose File" button and choose "<b>theme###.zip</b>" file from the "joomla" folder of your template package. You'll see the installation confirmation massage.</h6>
<p>To install the Joomla template,  press the 
    <strong>Upload & Install</strong> button.</p>
<p align="justify">Next you need to install  all the necessary extensions. The scheme of actions is similar to the previous one. </p>
<p align="justify">Press the 
  &quot;Choose File&quot; button.  Go to your  template package folder and then, to the &quot;sources&quot; folder. There you'll see the extensions' archive files. Choose one and press &quot;Open&quot; button. After this, press the  
<strong>Upload &amp; Install</strong> one. You'll see the installation confirmation massage. </p>
<p align="justify">You need to install each extension one by one.</p>
<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>When installation process is complete, please activate the  template.
    In the top menu, navigate to "<b>Extensions > Templates</b>" tab.</h6>

<p>Select the new template and press the <b>&quot;Default&quot;</b> button. Now you can check the new appearance of your website:</p>

<figure class="img-polaroid">
    <img src="img/template-settings-7.png" alt="">
</figure>
