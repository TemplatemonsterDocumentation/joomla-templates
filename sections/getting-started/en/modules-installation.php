<h3>Modules installation</h3>

<p> Joomla templates contain additional extensions: modules, plugins and components that are used for expanding your website functionality. You can install all the additional extensions with the help of a bundle package provided.</p>

<h6>You can find this archive in the ‘sources’ folder of the template. It’s name is 'pkg_bundle.zip'. It can be easily installed, as any other extension or theme, with the help of <b>Extensions manager of your Joomla admin panel</b>. </h6>
<ol class="index-list">
    <li>Please open your Joomla admin panel. From the top menu select <b>”Extensions”</b>. From the dropdown select the <b>”Extensions Manager”</b> button.
    </li>
    <li>Please select the “Install” option in the left column;</li>
    <li>Press the “Browse/Choose file” button and find the 'sources/pkg_bundle.zip' archive;
    </li>
    <li>Press the “Upload and install” button, in order to install all modules:
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>If your theme contains some extensions that are not included to '<b>pkg_bundle.zip</b>', you should install them separately. This should be done just like installing 'sources/pkg_bundle.zip' with the help of “<b>Extensions Manager</b>”.
</p>

<p>The file names of extensions archives usually start with '<b>mod_</b>', '<b>com_</b>' or
    '<b>plg_</b>', so you will easily recognize them. </p>
