<h3>Template installation</h3>

<p>"Ich habe schon eine Joomla-basierte Website und möchte nun das Aussehen durch die Installation von einem neuem Template verändern"</p>

<p>Dank dieser Methode bekommen Sie ein installiertes Joomla-Template und alle benötigen Erweiterungen.</p>

<!-- Extensions manager -->
<h6>Öffnen Sie Joomla Admin Panel und gehen Sie zum <b>"Extensions > Extensions Manager"</b> Tab:</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Im Package File Tab, klicken Sie auf den 
"Choose File" Knopf und wählen Sie die "<b>theme###.zip</b>" Datei aus dem "joomla" Ordner Ihres Template-Pakets .</h6>

<p>Drücken Sie auf den Button 
    <strong> Hochladen u. installieren (Upload & Install)</strong>  um das Joomla Template zu installieren:
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6>Nach dem Upload aktivieren Sie das installierte Template. Im Hauptmenü wählen Sie "<b>Extensions > Template Manager</b>" .</h6>

<p>Wählen Ihre neu installierte Template aus und klicken Sie auf den Button <b>" Zum Standard machen" (Make Default)</b> in der obere linke Ecke. Schauen Sie sich nun Ihre Webseite an :</p>

<figure class="img-polaroid">
    <img src="img/template-install-3.jpg" alt="">
</figure>
