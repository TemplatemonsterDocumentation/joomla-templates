<h3>Template Installation</h3>

<p> Ich habe schon eine Joomla-basierte Website und möchte nun ihre Darstellung bei Installation von neuem
    Template verändern.</p>

<p>Dank dieser Methode bekommen Sie ein installiertes Joomla-Template und alle benötigen Erweiterungen.</p>

<h6> Öffnen Sie Ihr Joomla Administration Panel und
    gehen Sie auf <b>"Extensions/Erweiterungen > Extensions Manager/Erweiterungs
        Manager"</b> via Hauptmenü.
</h6>

<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6> Mit Hilfe vom Button "Wähle eine Datei" (Choose
    File) der Paket Datei finden Sie die Datei "<b>theme###.zip</b>" im "joomla"
    Verzeichnis Ihres Template-Pakets
</h6>

<p>Drücken Sie auf den Button
    <strong> Hochladen u. installieren</strong>
    (Upload & Install), um das Joomla Template zu installieren.
</p>

<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>

<!-- Activate template -->
<h6> Nach dem Upload aktivieren Sie das installierte Template. Im Hauptmenü wählen Sie
    When uploading is complete you need to activate installed template. Im Hauptmenü
    wählen Sie "<b>Extensions/Erweiterungen > Template Manager</b>"
</h6>

<p>Wählen Ihre neu installierte Template aus und klicken Sie auf den Button "<b> Zum
        Standard machen</b>" (Make Default) in der obere linke Ecke. Jetzt schauen Sie Ihre
    Webseite an, damit die Änderungen zu überprüfen</p>

