<h3>Modulen-Installation</h3>

<p>Joomla Themen kommen mit zusätzlichen Dateierweiterungen: Modulen, Plug-ins und
    Komponenten, die zur Implementierung verschiedener Funktionalitäten und
    Besonderheiten verwendet werden. Alle sonstige Dateierweiterungen können in ein paar
    Klicks mit dem bereitgestellten Angebotspaket installiert werden.</p>

<h6> Das Archiv des Angebotspakets liegt im 'sources' Verzeichnis ihres Vorlagenpakets.
    Es heißt 'pkg_bundle.zip'. Sie können es wie jede andere Dateierweiterung oder Thema
    durch <b>Joomla admin panel > Extensions manager installiert werden </b>. </h6>
<ol class="index-list">
    <li> Öffenen Sie das Joomla Administrationsfeld. Von dem obigen Menü wählen sie '<b>Extensions</b>'.
        Vom Aufklappmenü wählen Sie'<b>Extensions Manager</b>';
    </li>
    <li> Bitte vergewissern Sie sich, dass das 'Install' Teil von der linken Kolonne
        gewählt ist;
    </li>
    <li>Klicken Sie den 'Browse/Choose file' Knopf und wählen Sie
        'sources/pkg_bundle.zip' Archiv;
    </li>
    <li>Klicken Sie den 'Upload and install' Knopf, um alle Modulen zu installieren.
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>Falles Ihr Thema einige Dateierweiterungen enthält, die in '<b>pkg_bundle.zip</b>'
    nicht enthalten sind, müssen Sie jede einzelne installieren. Dies wird, wie die
    'sources/pkg_bundle.zip' Installation durch das '<b>Extensions Manager</b>'
    Interface.</p>

<p> Die Archiven der Dateierweiterungen-Dateinamen fangen normalerweise mit '<b>mod_</b>',
    '<b>com_</b>' or '<b>plg_</b>' an, damit Sie sie einfach merken können. </p>