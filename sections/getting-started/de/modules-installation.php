<h3>Modulinstallation</h3>

<p>Joomla Themen kommen mit zusätzlichen Dateierweiterungen: Modulen, Plug-ins und Komponenten, die zur Implementierung verschiedener Funktionalitäten und Besonderheiten verwendet werden. Alle sonstige Dateierweiterungen können in ein paar Klicks mit dem bereitgestellten Angebotspaket installiert werden.</p>

<h6>Das Archiv des Angebotspakets liegt im 'sources' Verzeichnis ihres Vorlagenpakets. Es heißt 'pkg_bundle.zip'. Sie können es wie jede andere Dateierweiterung oder Thema durch <b>Joomla admin panel > Extensions manager</b> installert werden. </h6>
<ol class="index-list">
    <li>Öffenen Sie das Joomla Administrationsbereich. Oben aus dem Menü wählen sie <b>”Extensions”</b>. Aus dem Dropdownmenü wählen Sie <b>”Extensions Manager”</b> .
    </li>
    <li>itte vergewissern Sie sich, dass die 'Install' Option in der linken Kolonne gewählt ist;</li>
    <li>Klicken Sie den 'Browse/Choose file' Knopf und wählen Sie 'sources/pkg_bundle.zip' Archiv;
    </li>
    <li>Klicken Sie den 'Upload and install' Knopf, um alle Module zu installieren:
    </li>
</ol>
<figure class="img-polaroid"><img src="img/extensions_manager.jpg" alt=""/></figure>
<p>Falles Ihr Thema einige Dateierweiterungen enthält, die in '<b>pkg_bundle.zip</b>',  nicht enthalten sind, müssen Sie jede einzelne installieren. Das kann man, wie auch die 'sources/pkg_bundle.zip' Installation durch das “<b>Extensions Manager</b>”  Interface machen.
</p>

<p>Die Namen der Dateierweiterungen fangen normalerweise mit '<b>mod_</b>', '<b>com_</b>' oder
    '<b>plg_</b>' an, sodass Sie sie leicht finden können. </p>
