<h3>Modüllerin Yüklenmesi</h3>

<p>Joomla temaları ekstra eklentilerle birlikte sunulur:farklı fonksiyonları ve
    özellikleri yerleştirmek için moduller,eklenti, ve araçlar.Tüm ekstra eklentiler
    sağlanan paketi kullanarak birkaç tıkla yüklenebilir.</p>

<h6>Paket arşivi şablon paketinizin 'sources' dosyasında bulunur ve ismi
    'pkg_bundle.zip'tir. Kolayca bir başka eklenti yada tema aracılığıyla da
    yüklenebilir. <b>Joomla admin panel &gt; Eklentiler Yöneticisi</b>. </h6>

<ol class="index-list">
    <li>Joomla idare panelini açın. Yukarıdaki menüden '<b>Eklentiler</b>'i seçin.
        Alttaki menüden '<b>Eklenti Yöneticisi</b>'ni seçin;
    </li>
    <li>'Yükleme' bölümünun solda seçili olduğundan emin olun;</li>
    <li>'Dosyayı Seç/Göz At" butonuna basın ve 'sources/pkg_bundle.zip' arşivini
        seçin;
    </li>
    <li>Tüm modülleri yükleme için 'Yükleme ve kurma" tuşuna basın.</li>
</ol>

<figure class="img-polaroid">
    <img alt="" src="img/extensions_manager.jpg">
</figure>

<p>Temanızın '<b>pkg_bundle.zip</b>'e ekli olmayan bazı eklentiler içermesi halinde
    hepsini teker teker kurmanız gerekmektedir.Bu '<b>Eklenti Yöneticisi</b>'
    arayüzünden 'sources/pkg_bundle.zip' şeklinde yüklenebilir.</p>

<p>Eklenti arşivleri dosya isimleri genellikle '<b>mod_</b>', '<b>com_</b>' or
    '<b>plg_</b>'le başlar,böylece bunları kolayca ayırt edebilirsiniz. </p>