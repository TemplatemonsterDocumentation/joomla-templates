<h3>Şablon Kurulumu</h3>

<p>Zaten Joomla tabanlı bir web siteniz var ve sadece yeni bir şablon yükleyerek değiştirmek istiyorsanız.</p>

<p>Bu yöntemi kullanarak Joomla şablonunuzun tüm gerekli uzantıları yüklenecektir.</p>

<!-- Extensions manager -->
<h6>Adım Joomla Yönetim panelinizi açın ve üsr menüyü
    kullanarak <b>"Eklentiler > Eklenti yöneticisi" - (Extensions > Extensions
        Manager)</b>sekmesine tıklayın.</h6>
<figure class="img-polaroid">
    <img src="img/template-install-1.jpg" alt="">
</figure>

<!-- Upload theme zip -->
<h6>Adım Paket dosyanızı seçmek için "Gözat" (Choose File)
    butonuna tıklayarak Joomla dizininizdeki "<b>theme###.zip</b>" şablon paketinizi
    seçin.</h6>
<p>Joomla şablonunu yüklemek için
    <strong>Yükle & Kur (Upload &amp; Install)</strong>
    butonuna basın.
</p>
<figure class="img-polaroid">
    <img src="img/template-install-2.jpg" alt="">
</figure>


<!-- Activate template -->
<h6>Adım Yükleme işleminiz tamamlandığında yüklenen şablonu
    etkinleştirmeniz gerekecektir. Üst menüden "<b>Eklentiler > Şablon Yöneticisini
        seçin</b>" - (Extensions &gt; Template Manager)</h6>
<p>Yüklenen yeni şablonu seçin ve sol üst köşedeki düğmeyi "<b>Varsayılan</b>" (<b>Make
        Default</b>) butonuna tıklayın. Şimdi değişiklikleri görmek için web sitenizi
    görüntüleyebilirsiniz.</p>
