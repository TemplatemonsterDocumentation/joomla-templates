<h2 class="item2">Vorbereitung</h2>

<h6> Bevor Sie Ihre Joomla Website einrichten, sorgen Sie dafür, dass Sie alles Notwendige vorbereitet haben. Führen Sie die folgenden Vorbereitungsschritte aus </h6>


<h4>Erforderliche Software</h4>

<p> Damit Sie sich mit dem Joomla Template wohl fühlen, empfehlen wir Ihnen alle
    notwendige Anwendungen herunterzuladen. Auf der Vorschauseite vom Template finden
    Sie die Liste der erforderlichen Software. <br> Je nach Template-Typ unterscheiden
    sich die Anforderungen, deswegen geben wir hier eine allgemeine Übersicht:</p>

<ol class="index-list">
	<li> Zunächst brauchen Sie die richtige Anwendung, um das passwortgeschützte
        sources_#########.zip Arhiv zu entpacken. Nehmen Sie WinZip 9+ (Windows) oder
        Stuffit Expander 10+ (Mac).
    </li>    
	<li> Wenn Sie Grafiken und Bilder vom Template-Design oder .PSD Quelldatein
        bearbeiten möchten, wird Sie dafür die Adobe Photoshop Anweisung benötigen.
    </li>
	<li>Zur Bearbeitung von Quellcode-Dateien der Vorlage verwenden Sie einen
        Code-Editor, wie zum Beispiel Adobe Dreamweaver, Notepad++, Sublime Text usw.
    </li>  
	<li> Zum Upload der Dateien auf den Hosting Server benötigen Sie einen FTP-Manager
        wie Total Commander, FileZilla, CuteFTP usw.
    </li></ol>


<h4>Hosting</h4>

<p>Weil Joomla CMS eine PHP/MySQL-basierte Anwendung ist, sollen Sie eine
    Hosting-Umgebung vorbereiten, damit Sie Joomla verwenden können.</p>
<p>Wenn Sie schon einen funktionierenden Hosting haben, beachten Sie bitte, ob es <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank"> den Joomla
        Anforderungen entspricht</a> und für Joomla Website angepasst ist.</p>
<p> Außerdem können Sie Joomla lokal auf Ihrem Computer mit lokalen Server benutzen. Um
    einen lokalen Server zu erstellen, verwenden Sie bitte die LocalHost-Anwendungen wie
    <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> usw. Alle diese Programme kann man leicht
    installieren und zur Joomla Betreibung verwenden. </p>
