<h2 class="item2">Préparation</h2>

<h6>Avant de procéder à l'installation de votre site web Joomla, veuillez vous assurer d'être
    complètement préparé. Veuillez compléter les étapes de préparation suivantes :</h6>


<h4>Logiciel d'édition</h4>

<p>Pour être confortable en travaillant avec un modèle Joomla, nous vous recommandons de
    télécharger toutes les applications requises. Vous pouvez consulter la liste des
    logiciels requis sur la page de démo live du modèle. <br>Du fait que les exigences
    peuvent varier pour différents modèles, nous allons vous dire ici ce qu'il faut
    avoir en général :</p>

<ol class="index-list">
    <li>Tout d'abord vous aurez besoin des bonnes applications pour extraire l'archive
        protégée par mot de passe sources_#########.zip. Vous pouvez utiliser WinZip9+
        (Windows) et Stuffit Expander 10+ (Mac).
    </li>
    <li>Vous aurez peut-être aussi besoin de l'application Adobe Photoshop pour éditer
        les fichiers source PSD. Elle est également nécessaire si vous voulez modifier
        les graphiques et images du modèle.
    </li>
    <li>Pour éditer les fichiers source du code du modèle, vous aurez besoin d'un
        éditeur de code tel qu'Adobe Dreamweaver, Notepad++, Sublime Text, etc.
    </li>
    <li>Pour envoyer les fichiers sur le serveur d'hébergement, il est possible que vous
        ayez besoin d'un gestionnaire FTP tel que Total Commander, FileZilla, CuteFTP,
        etc.
    </li>
</ol>

<h4>Hébergement</h4>

<p>Étant donné que Joomla CMS est une application basée sur PHP/MySQL, vous aurez besoin
    de préparer un environnement d'hébergement pour supporter Joomla.</p>

<p>Si vous avez un service d'hébergement en ligne, veuillez vous assurer qu'il fournisse
    les <a href="http://www.joomla.org/technical-requirements.html" target="_blank">logiciels
        requis par Joomla</a> et qu'il soit prêt à être utilisé pour des sites web
    Joomla.</p>

<p>Vous pouvez également faire tourner Joomla en local sur votre ordinateur en utilisant
    le serveur local. Pour créer un server d'hébergement local, veuillez utiliser les
    applications localhost telles que <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b>, etc.
    Celles-ci peuvent facilement être installées comme n'importe quel autre programme et
    être utilisées pour faire tourner Joomla. </p>

