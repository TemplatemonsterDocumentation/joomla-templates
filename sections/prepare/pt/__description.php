<h2 class="item2">Preparar</h2>

<h6>Antes que você comece a configurar o seu site Joomla, por favor, certifique-se de que
    você estará totalmente preparado. Por favor, complete as seguintes etapas de
    preparação:</h6>


<h4>Software de edição</h4>

<p>Para se sentir confortável ao mexer no template Joomla, nós recomendamos que você
    baixe todos os aplicativos necessários. Você pode ver a lista do software necessário
    na página de amostra do template ao vivo. <br>
    Os requerimentos podem variar para diferentes templates, então nós lhe avisaremos
    aqui o que é necessário no geral:</p>

<ol class="index-list">
    <li>Primeiro de tudo você precisa dos aplicativos corretos para extrair o arquivo
        protegido por senha sources_#########.zip. Você pode usar o WinZip 9+ (Windows)
        e o Stuffit Expander 10+ (Mac).
    </li>
    <li>Você pode usar também o Adobe Photoshop. Ele é utilizado para editar arquivos
        com a extensão .PSD e é necessário se você quiser editar os gráficos e imagens
        do template.
    </li>
    <li>Para editar os arquivos de código fonte você precisa de algum editor de código
        como o Adobe Dreamweaver, Notepad++, Sublime Text etc.
    </li>
    <li>Para enviar os arquivos ao servidor de hospedagem você pode precisar de um
        Gerenciador de FTP como o Total Commander, FileZilla, CuteFTP etc.
    </li>
</ol>

<h4>Hospedagem</h4>

<p>Como o Joomla CMS é um aplicativo com base em PHP/MySQL, você precisa preparar um
    ambiente de hospedagem para rodar o Joomla.</p>

<p>Se você possuir hospedagem, por favor, certifique-se de que os <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">requerimentos
        do software Joomla</a> são aceitos e de que está pronto para ser usado com sites
    Joomla.</p>

<p>Caso contrário, você pode rodar o Joomla localmente no seu computador usando um
    servidor local. Para criar um servidor de hospedagem local, por favor, utilize os
    aplicativos da localhost como o <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> etc.
    Qualquer um desses podem ser facilmente instalados como qualquer outro programa e
    usado para rodar o Joomla. </p>

