<h2 class="item2">Preparación</h2>

<p>Antes de la creación de su sitio web de Joomla, por favor asegúrese de que está
    totalmente preparado. Por favor, realice los siguientes pasos de preparación:</p>


<h4>Software de edición</h4>

<p>Para que Usted se sienta cómodo trabajando con la plantilla Joomla, le recomendamos
    descargar todas las aplicaciones requeridas. Usted puede encontrar la lista de
    software necesario en la página de vista previa de la plantilla. Cada plantilla
    tiene su lista especial y aquí vamos a mostrar lo que Usted necesita en general:</p>

<ol class="index-list">
    <li>En primer lugar Usted necesita las aplicaciones adecuadas para descomprimir el
        archivo sources_#########.zip protegido por la contraseña. Usted puede utilizar
        WinZip 9+ (Windows) y Stuffit Expander 10+ (Mac)
    </li>
    <li>También Usted va a necesitar la aplicación Adobe Photoshop. Se utilízala para la
        modificación de ficheros .PSD y es necesario si desea modificar imágenes y
        gráficos de diseño de plantilla.
    </li>
    <li>Para modificar los ficheros de código de origen de la plantilla Usted va a
        necesitar algún editor de código como Adobe Dreamweaver, Notepad++, Sublime Text
        etc.
    </li>
    <li>Para subir sus ficheros al servidor de hosting es posible que necesite un
        administrador de FTP como Total Commander, FileZilla, CuteFTP, etc.
    </li>
</ol>

<h4>Hosting</h4>

<p>Como CMS Joomla es una aplicación basada en PHP/MySQL, Usted tiene que preparar un
    entorno de hosting para ejecutar Joomla.</p>

<p>If you have a live hosting please make sure it matches Joomla software requirements
    and is ready to be used for Joomla websites.
    Si Usted tiene algún hosting existente, por favor asegúrese de que cumpla <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">los
        requisitos del software Joomla</a> y se puede utilizarlo para los sitios web de
    Joomla.</p>

<p>Por otra parte, Usted puede ejecutar Joomla localmente en su ordenador con el
    servidor local. Para crear un servidor de hosting local, utilice las aplicaciones
    como WAMP, AppServ, MAMP etc. Se puede instalar fácilmente cualquiera de ellas y
    utilizarlas para ejecutar Joomla. </p>

