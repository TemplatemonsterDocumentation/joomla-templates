<h2 class="item2">Przygotowanie</h2>

<h6>Zanim zaczniesz ustawiać swoja stronę internetową Joomla, upewnij się, że jesteś
    całkowicie przygotowany. Wykonaj następujące kroki:</h6>


<h4>Programy do edycji</h4>

<p>Aby czuc sie pewnie pracując z szablonem Joomla, zalecamy pobrać wszystkie potrzebne
    aplikacje. Możesz sprawdzić listę wymaganego oprogramowania na stronie szablonu live
    demo. <br>Wymagania mogą się różnić. w zależności od szablonu, więc podajemy co jest
    generalnie wymagane:</p>

<ol class="index-list">
    <li>Przede wszystkim, potrzebujesz odpowiednie aplikacje do wypakowania
        zabezpieczonego hasłem archiwum sources_#########.zip. Możesz użyć WinZip 9+
        (Windows) lub Stuffit Expander 10+ (Mac).
    </li>
    <li>Możesz również potrzebować aplikacji Adobe Photoshop. Używana jest do edycji
        plików źródłowych .PSD, jest niezbędna, jeśli chcesz edytować grafiki i obrazy w
        szablonie.
    </li>
    <li>Aby edytować kod źródłowy szablonu, potrzebny jest edytor, jak Adobe
        Dreamweaver, Notepad++, Sublime Text itp.
    </li>
    <li>Aby przesłać pliki na serwer hostingowy, możesz potrzebować FTP Manager, jak
        Total Commander, FileZilla, CuteFTP itp.
    </li>
</ol>

<h4>Hosting</h4>

<p>Jako, że Joomla CMS jest aplikacja bazowaną na PHP/MySQL, potrzebujesz przygotować
    hosting do uruchomienia aplikacji Joomla.</p>

<p>Jeśli masz hosting, upewnij się, że pasuje do <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">wymagań
        Joomla</a> i jest gotowe do użycia dla stron Joomla.</p>

<p>W innym wypadku, możesz uruchomić Joomla lokalnie na swoim komputerze, używając
    lokalnego serwera. Aby utworzyć lokalny serwer hostingowy, użyj aplikacji typu
    localhost, jak <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> itp. Każda z nich może być z
    łatwością zainstalowana jak zwykły program, i użyta do stron Joomla. </p>

