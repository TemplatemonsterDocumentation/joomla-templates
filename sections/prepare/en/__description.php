<h2 class="item2">Preparation</h2>

<h6> Before you proceed to setting up your Joomla website, make sure you are fully prepared. Please complete the following preparation steps:</h6>


<h4>Software required</h4>

<p>To feel comfortable working with the Joomla template, we recommend downloading all the applications required. You can see the list of required software at the template Live Demo page. 
Requirements may vary for different templates, so we'll tell you here what is needed in general:</p>

<ol class="index-list">
    <li> First of all, you need the right applications to extract the password-protected sources_#########.zip archive. You can use WinZip 9+ (Windows) and Stuffit Expander 10+ (Mac).    </li>
    <li> You may also need the Adobe Photoshop software. It is used to edit .PSD source files and it is necessary, if you want to edit template design graphics and images.    </li>
    <li> To edit template source code files you need some code editor, for instance, Adobe Dreamweaver, Notepad++, Sublime Text, etc. </li>
    <li>To upload files to the hosting server, you may need an FTP Manager like Total Commander, FileZilla, CuteFTP, etc.    </li>
</ol>


<h4>Hosting</h4>

<p> As Joomla CMS is a PHP/MySQL based application, you need to prepare a hosting environment to run Joomla.</p>

<p> If you have a live hosting server, please make sure that it matches <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">Joomla technical requirements</a> and is ready to be used for a Joomla website.</p>

<p> Moreover, you can run Joomla locally on your computer using local server. To create a local hosting server, please use localhost applications, such as <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b>, etc. Any of those can be easily installed as any other software and be used to run Joomla.</p>

