<h2 class="item2">Hazırlamak</h2>

<h6>Joomla web sitesi yapımına başlamadan önce tam olarak hazır olduğunuzdan emin olun.
    Lütfen, aşşağıdaki hazırlık adımlarını tamamlayın:</h6>


<h4>Düzenleme Yazılımı</h4>

<p>Joomla yazılımı ile rahat çalışabildiğinizi hissetmeniz açısından tüm gerekli
    uygulamaları önceden indirmenizi öneririz. Bu şablon için canlı demo sayfasına girip
    gereken yazılımların listesini görebilirsiniz. <br>Biz genel olarak gerekli olanları
    buradan söyleyeceğiz, gereksinimleri farklı olan şablonlar için bunlar değişiklik
    gösterebilir:</p>

<ol class="index-list">
    <li>İlk olarak şifre korumalı sources_#########.zip dosyasını ayıklamak için doğru
        uygulamalar gerekmektedir. Kullanabileceğiniz yardımcı uygulamalar WinZip 9+
        (Windows) ve Stuffit Expander 10+ (Mac).
    </li>
    <li>Ayrıca Adobe Photoshop uygulaması gerekebilir. Şablon tasarımınızın grafik ve
        görüntülerini düzenlemek istiyorsanız .PDS kaynak dosyalarını düzenlemek için
        kullanılır.
    </li>
    <li>Şablonun kaynak kodu dosyalarını düzenleyebilmek için Adobe Dreamweawer,
        Notepad++, Sublie Text vb bazı kod editörleri gerekmektedir.
    </li>
    <li>Dosya barındırma sunucunuza (Hosting) yükleme yapabilmek için Total Commander,
        FileZilla, CuteFTP vb gibi FTP yöneticileri gerekebilir.
    </li>
</ol>

<h4>Hosting</h4>

<p>Joomla CMS - PHP/MySQL tabanlı bir uygulama olduğu için, Joomla ile çalışabilmek için
    bir barındırma (Hosting) ortamı hazırlamak gerekmektedir.</p>

<p>Eğer canlı bir hosting'iniz varsa uyumlu olduğundan emin olun <a
        href="http://www.joomlatr.org/joomla-dokumanlar/joomla-indirme-kurulum/joomla-teknik-gereksinimleri.html"
        target="_blank">Joomla yazılım gereksinimleri </a> ve Joomla web sitesini
    kullanmak üzere hazır olduğundan.</p>

<p>Aksi takdirde bilgisayarınızdaki yerel sunucunuzu kullanarak Joomla'yı local olarak
    çalıştırabilirsiniz. Yerel bir barındırma sunucusunu oluşturabilmek için localhost
    uygulamalarını kullanabilirsiniz <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> vb.
    Kolayca herhangi bir program yüklenebilir ve Joomla çalıştırmak için kullanılabilir.
</p>

